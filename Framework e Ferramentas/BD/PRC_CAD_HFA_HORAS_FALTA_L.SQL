 
--PRC_CAD_HFA_HORAS_FALTA_L
create or replace procedure PRC_CAD_HFA_HORAS_FALTA_L 
(
     pCAD_HFA_ID IN TB_CAD_HFA_HORAS_FALTA.CAD_HFA_ID%type DEFAULT NULL,
     pCAD_HTR_ID IN TB_CAD_HFA_HORAS_FALTA.CAD_HTR_ID%type DEFAULT NULL,
     pCAD_HFA_MES_COMPETENCIA IN TB_CAD_HFA_HORAS_FALTA.CAD_HFA_MES_COMPETENCIA%type DEFAULT NULL,
     pCAD_HFA_ANO_COMPETENCIA IN TB_CAD_HFA_HORAS_FALTA.CAD_HFA_ANO_COMPETENCIA%type DEFAULT NULL,
     pCAD_HFA_MES_PAGTO IN TB_CAD_HFA_HORAS_FALTA.CAD_HFA_MES_PAGTO%type DEFAULT NULL,
     pCAD_HFA_ANO_PAGTO IN TB_CAD_HFA_HORAS_FALTA.CAD_HFA_ANO_PAGTO%type DEFAULT NULL,
     pCAD_HFA_QT_HR_FALTA_SEMANA IN TB_CAD_HFA_HORAS_FALTA.CAD_HFA_QT_HR_FALTA_SEMANA%type DEFAULT NULL,
     pCAD_HFA_QT_HR_FALTA_FIMSEMA IN TB_CAD_HFA_HORAS_FALTA.CAD_HFA_QT_HR_FALTA_FIMSEMA%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_HFA_HORAS_FALTA.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_HFA_DT_ULTIMA_ATUALIZACAO IN TB_CAD_HFA_HORAS_FALTA.CAD_HFA_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_HFA_HORAS_FALTA_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_HFA_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HFA_ID = ' || pCAD_HFA_ID; END IF;
IF pCAD_HTR_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HTR_ID = ' || pCAD_HTR_ID; END IF;
IF pCAD_HFA_MES_COMPETENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HFA_MES_COMPETENCIA = ' || pCAD_HFA_MES_COMPETENCIA; END IF;
IF pCAD_HFA_ANO_COMPETENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HFA_ANO_COMPETENCIA = ' || pCAD_HFA_ANO_COMPETENCIA; END IF;
IF pCAD_HFA_MES_PAGTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HFA_MES_PAGTO = ' || pCAD_HFA_MES_PAGTO; END IF;
IF pCAD_HFA_ANO_PAGTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HFA_ANO_PAGTO = ' || pCAD_HFA_ANO_PAGTO; END IF;
IF pCAD_HFA_QT_HR_FALTA_SEMANA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HFA_QT_HR_FALTA_SEMANA = ' || pCAD_HFA_QT_HR_FALTA_SEMANA; END IF;
IF pCAD_HFA_QT_HR_FALTA_FIMSEMA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HFA_QT_HR_FALTA_FIMSEMA = ' || pCAD_HFA_QT_HR_FALTA_FIMSEMA; END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pCAD_HFA_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HFA_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_HFA_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       CAD_HFA_ID,
       CAD_HTR_ID,
       CAD_HFA_MES_COMPETENCIA,
       CAD_HFA_ANO_COMPETENCIA,
       CAD_HFA_MES_PAGTO,
       CAD_HFA_ANO_PAGTO,
       CAD_HFA_QT_HR_FALTA_SEMANA,
       CAD_HFA_QT_HR_FALTA_FIMSEMA,
       SEG_USU_ID_USUARIO,
       CAD_HFA_DT_ULTIMA_ATUALIZACAO
FROM TB_CAD_HFA_HORAS_FALTA
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_HFA_HORAS_FALTA_L;
