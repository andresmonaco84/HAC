create or replace procedure PRC_ASS_PPI_PLAPRODUTOINT_U
  (
     pCAD_CNV_ID_CONVENIO IN TB_ASS_PPI_PLAPRODUTOINT.CAD_CNV_ID_CONVENIO%type,
     pCAD_PLA_ID_PLANO IN TB_ASS_PPI_PLAPRODUTOINT.CAD_PLA_ID_PLANO%type,
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_ASS_PPI_PLAPRODUTOINT.CAD_LAT_ID_LOCAL_ATENDIMENTO%type,
     pCAD_PRD_ID IN TB_ASS_PPI_PLAPRODUTOINT.CAD_PRD_ID%type,
     pASS_PPI_FL_EXIGE_GUIA IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_FL_EXIGE_GUIA%type,
     pASS_PPI_FL_EXIGE_SENHA IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_FL_EXIGE_SENHA%type,
     pASS_PPI_FL_AUTORIZATEL IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_FL_AUTORIZATEL%type,
     pASS_PPI_FL_AUTORIZAFAX IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_FL_AUTORIZAFAX%type,
     pASS_PPI_FL_AUTORIZAPES IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_FL_AUTORIZAPES%type,
     pASS_PPI_FL_AUTORIZAWEB IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_FL_AUTORIZAWEB%type,
     pASS_PPI_FL_COBERTURA IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_FL_COBERTURA%type,
     pASS_PPI_FL_STATUS IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_FL_STATUS%type,
     pSEG_USU_ID_USUARIO IN TB_ASS_PPI_PLAPRODUTOINT.SEG_USU_ID_USUARIO%type,
     pASS_PPI_DT_ULTIMA_ATUALIZACAO IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_DT_ULTIMA_ATUALIZACAO%type,
     pASS_PPI_QT_MAXIMA_LANC_DIA IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_QT_MAXIMA_LANC_DIA%TYPE DEFAULT NULL,
     pASS_PPI_DS_MSG_EXIGENCIA IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_DS_MSG_EXIGENCIA%type default NULL
  )
  is
  /********************************************************************
  *    Procedure: PRC_ASS_PPI_PLAPRODUTOINT_U
  *
  *    Data Criacao:   19/11/2009    Por: Fabíola
  *    Data Alteracao: 28/12/2010    Por: André S. Monaco
  *         Alteracao: Inclusão do campo ASS_PPI_DS_MSG_EXIGENCIA
  *
  *    Funcao: Atualiza exigência do convênio
  *
  *******************************************************************/
  begin
    UPDATE TB_ASS_PPI_PLAPRODUTOINT
    SET
        ASS_PPI_FL_EXIGE_GUIA = pASS_PPI_FL_EXIGE_GUIA,
        ASS_PPI_FL_EXIGE_SENHA = pASS_PPI_FL_EXIGE_SENHA,
        ASS_PPI_FL_AUTORIZATEL = pASS_PPI_FL_AUTORIZATEL,
        ASS_PPI_FL_AUTORIZAFAX = pASS_PPI_FL_AUTORIZAFAX,
        ASS_PPI_FL_AUTORIZAPES = pASS_PPI_FL_AUTORIZAPES,
        ASS_PPI_FL_AUTORIZAWEB = pASS_PPI_FL_AUTORIZAWEB,
        ASS_PPI_FL_COBERTURA = pASS_PPI_FL_COBERTURA,
        ASS_PPI_FL_STATUS = pASS_PPI_FL_STATUS,
        SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO,
        ASS_PPI_DT_ULTIMA_ATUALIZACAO = pASS_PPI_DT_ULTIMA_ATUALIZACAO,
        ASS_PPI_QT_MAXIMA_LANC_DIA = pASS_PPI_QT_MAXIMA_LANC_DIA,
        ASS_PPI_DS_MSG_EXIGENCIA = pASS_PPI_DS_MSG_EXIGENCIA
    WHERE
        CAD_CNV_ID_CONVENIO = pCAD_CNV_ID_CONVENIO
    AND CAD_LAT_ID_LOCAL_ATENDIMENTO = pCAD_LAT_ID_LOCAL_ATENDIMENTO
    AND CAD_PLA_ID_PLANO = pCAD_PLA_ID_PLANO
    AND CAD_PRD_ID = pCAD_PRD_ID;
  end PRC_ASS_PPI_PLAPRODUTOINT_U;