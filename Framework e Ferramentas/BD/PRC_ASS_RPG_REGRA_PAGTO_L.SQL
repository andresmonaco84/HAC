--PRC_ASS_RPG_REGRA_PAGTO_L
create or replace procedure PRC_ASS_RPG_REGRA_PAGTO_L 
(
     pASS_CPR_ID IN TB_ASS_RPG_REGRA_PAGTO.ASS_CPR_ID%type DEFAULT NULL,
     pCAD_REP_ID IN TB_ASS_RPG_REGRA_PAGTO.CAD_REP_ID%type DEFAULT NULL,
     pCAD_REP_DT_INICIO_VIGENCIA IN TB_ASS_RPG_REGRA_PAGTO.CAD_REP_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pCAD_REP_DT_FIM_VIGENCIA IN TB_ASS_RPG_REGRA_PAGTO.CAD_REP_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_RPG_REGRA_PAGTO.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_REP_DT_ULTIMA_ATUALIZACAO IN TB_ASS_RPG_REGRA_PAGTO.CAD_REP_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pASS_RPG_ID IN TB_ASS_RPG_REGRA_PAGTO.ASS_RPG_ID%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ASS_RPG_REGRA_PAGTO_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pASS_CPR_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CPR_ID = ' || pASS_CPR_ID; END IF;
IF pCAD_REP_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_REP_ID = ' || pCAD_REP_ID; END IF;
IF pCAD_REP_DT_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_REP_DT_INICIO_VIGENCIA = ' || CHR(39) || pCAD_REP_DT_INICIO_VIGENCIA || CHR(39); END IF;
IF pCAD_REP_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_REP_DT_FIM_VIGENCIA = ' || CHR(39) || pCAD_REP_DT_FIM_VIGENCIA || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pCAD_REP_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_REP_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_REP_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pASS_RPG_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_RPG_ID = ' || pASS_RPG_ID; END IF;
 
   V_SELECT := '
SELECT	
*
FROM TB_ASS_RPG_REGRA_PAGTO
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ASS_RPG_REGRA_PAGTO_L;