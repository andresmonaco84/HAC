--PRC_CAD_TAN_TIPO_ANESTESIA_L
create or replace procedure PRC_CAD_TAN_TIPO_ANESTESIA_L 
(
     pCAD_TAN_TP_ANESTESIA IN TB_CAD_TAN_TIPO_ANESTESIA.CAD_TAN_TP_ANESTESIA%type DEFAULT NULL,
     pCAD_TAN_DS_ANESTESIA IN TB_CAD_TAN_TIPO_ANESTESIA.CAD_TAN_DS_ANESTESIA%type DEFAULT NULL,
     pCAD_TAN_FL_STATUS IN TB_CAD_TAN_TIPO_ANESTESIA.CAD_TAN_FL_STATUS%type DEFAULT NULL,
     pCAD_TAN_DT_ULTIMA_ATUALIZACAO IN TB_CAD_TAN_TIPO_ANESTESIA.CAD_TAN_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_TAN_TIPO_ANESTESIA.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_TAN_TIPO_ANESTESIA_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_TAN_TP_ANESTESIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TAN_TP_ANESTESIA = ' || pCAD_TAN_TP_ANESTESIA; END IF;
IF pCAD_TAN_DS_ANESTESIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TAN_DS_ANESTESIA = ' || CHR(39) || pCAD_TAN_DS_ANESTESIA || CHR(39); END IF;
IF pCAD_TAN_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TAN_FL_STATUS = ' || CHR(39) || pCAD_TAN_FL_STATUS || CHR(39); END IF;
IF pCAD_TAN_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TAN_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_TAN_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       CAD_TAN_TP_ANESTESIA,
       CAD_TAN_DS_ANESTESIA,
       CAD_TAN_FL_STATUS,
       CAD_TAN_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO
FROM TB_CAD_TAN_TIPO_ANESTESIA
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_TAN_TIPO_ANESTESIA_L;
