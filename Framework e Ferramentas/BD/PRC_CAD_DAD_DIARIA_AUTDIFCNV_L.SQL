 
--PRC_CAD_DAD_DIARIA_AUTDIFCNV_L
create or replace procedure PRC_CAD_DAD_DIARIA_AUTDIFCNV_L 
(
     pCAD_DAD_ID IN TB_CAD_DAD_DIARIA_AUTDIFCNV.CAD_DAD_ID%type DEFAULT NULL,
     pCAD_CNV_ID_CONVENIO IN TB_CAD_DAD_DIARIA_AUTDIFCNV.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pCAD_CNV_ID_CONVENIO_DES IN TB_CAD_DAD_DIARIA_AUTDIFCNV.CAD_CNV_ID_CONVENIO_DES%type DEFAULT NULL,
     pCAD_PLA_ID_PLANO IN TB_CAD_DAD_DIARIA_AUTDIFCNV.CAD_PLA_ID_PLANO%type DEFAULT NULL,
     pCAD_SPL_ID IN TB_CAD_DAD_DIARIA_AUTDIFCNV.CAD_SPL_ID%type DEFAULT NULL,
     pTIS_TAC_CD_TIPO_ACOMOD_ORI IN TB_CAD_DAD_DIARIA_AUTDIFCNV.TIS_TAC_CD_TIPO_ACOMOD_ORI%type DEFAULT NULL,
     pCAD_PRD_ID_ORI IN TB_CAD_DAD_DIARIA_AUTDIFCNV.CAD_PRD_ID_ORI%type DEFAULT NULL,
     pTIS_TAC_CD_TIPO_ACOMOD_DES IN TB_CAD_DAD_DIARIA_AUTDIFCNV.TIS_TAC_CD_TIPO_ACOMOD_DES%type DEFAULT NULL,
     pCAD_PRD_ID_DES IN TB_CAD_DAD_DIARIA_AUTDIFCNV.CAD_PRD_ID_DES%type DEFAULT NULL,
     pCAD_SET_ID IN TB_CAD_DAD_DIARIA_AUTDIFCNV.CAD_SET_ID%type DEFAULT NULL,
     pCAD_DAD_DT_INICIO_VIGENCIA IN TB_CAD_DAD_DIARIA_AUTDIFCNV.CAD_DAD_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pCAD_DAD_DT_FIM_VIGENCIA IN TB_CAD_DAD_DIARIA_AUTDIFCNV.CAD_DAD_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_DAD_DIARIA_AUTDIFCNV.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_DAD_DT_ULTIMA_ATUALIZACAO IN TB_CAD_DAD_DIARIA_AUTDIFCNV.CAD_DAD_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pCAD_DAD_FL_CONSIDERAOBITO IN TB_CAD_DAD_DIARIA_AUTDIFCNV.CAD_DAD_FL_CONSIDERAOBITO%type DEFAULT NULL,
     pCAD_DAD_HR_OBITO_MAIOR IN TB_CAD_DAD_DIARIA_AUTDIFCNV.CAD_DAD_HR_OBITO_MAIOR%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_DAD_DIARIA_AUTDIFCNV_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
v_cursor PKG_CURSOR.t_cursor;
begin
OPEN v_cursor FOR
SELECT	
       CAD_DAD_ID,
       CAD_CNV_ID_CONVENIO,
       CAD_CNV_ID_CONVENIO_DES,
       CAD_PLA_ID_PLANO,
       CAD_SPL_ID,
       TIS_TAC_CD_TIPO_ACOMOD_ORI,
       CAD_PRD_ID_ORI,
       TIS_TAC_CD_TIPO_ACOMOD_DES,
       CAD_PRD_ID_DES,
       CAD_SET_ID,
       CAD_DAD_DT_INICIO_VIGENCIA,
       CAD_DAD_DT_FIM_VIGENCIA,
       SEG_USU_ID_USUARIO,
       CAD_DAD_DT_ULTIMA_ATUALIZACAO,
       CAD_DAD_FL_CONSIDERAOBITO,
       CAD_DAD_HR_OBITO_MAIOR
FROM TB_CAD_DAD_DIARIA_AUTDIFCNV
WHERE
        (pCAD_DAD_ID is null OR CAD_DAD_ID = pCAD_DAD_ID) AND 
        (pCAD_CNV_ID_CONVENIO is null OR CAD_CNV_ID_CONVENIO = pCAD_CNV_ID_CONVENIO) AND 
        (pCAD_CNV_ID_CONVENIO_DES is null OR CAD_CNV_ID_CONVENIO_DES = pCAD_CNV_ID_CONVENIO_DES) AND 
        (pCAD_PLA_ID_PLANO is null OR CAD_PLA_ID_PLANO = pCAD_PLA_ID_PLANO) AND 
        (pCAD_SPL_ID is null OR CAD_SPL_ID = pCAD_SPL_ID) AND 
        (pTIS_TAC_CD_TIPO_ACOMOD_ORI is null OR TIS_TAC_CD_TIPO_ACOMOD_ORI = pTIS_TAC_CD_TIPO_ACOMOD_ORI) AND 
        (pCAD_PRD_ID_ORI is null OR CAD_PRD_ID_ORI = pCAD_PRD_ID_ORI) AND 
        (pTIS_TAC_CD_TIPO_ACOMOD_DES is null OR TIS_TAC_CD_TIPO_ACOMOD_DES = pTIS_TAC_CD_TIPO_ACOMOD_DES) AND 
        (pCAD_PRD_ID_DES is null OR CAD_PRD_ID_DES = pCAD_PRD_ID_DES) AND 
        (pCAD_SET_ID is null OR CAD_SET_ID = pCAD_SET_ID) AND 
        (pCAD_DAD_DT_INICIO_VIGENCIA is null OR CAD_DAD_DT_INICIO_VIGENCIA = pCAD_DAD_DT_INICIO_VIGENCIA) AND 
        (pCAD_DAD_DT_FIM_VIGENCIA is null OR CAD_DAD_DT_FIM_VIGENCIA = pCAD_DAD_DT_FIM_VIGENCIA) AND 
        (pSEG_USU_ID_USUARIO is null OR SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO) AND 
        (pCAD_DAD_DT_ULTIMA_ATUALIZACAO is null OR CAD_DAD_DT_ULTIMA_ATUALIZACAO = pCAD_DAD_DT_ULTIMA_ATUALIZACAO) AND 
        (pCAD_DAD_FL_CONSIDERAOBITO is null OR CAD_DAD_FL_CONSIDERAOBITO = pCAD_DAD_FL_CONSIDERAOBITO) AND 
        (pCAD_DAD_HR_OBITO_MAIOR is null OR CAD_DAD_HR_OBITO_MAIOR = pCAD_DAD_HR_OBITO_MAIOR);          
io_cursor := v_cursor;
end PRC_CAD_DAD_DIARIA_AUTDIFCNV_L;
