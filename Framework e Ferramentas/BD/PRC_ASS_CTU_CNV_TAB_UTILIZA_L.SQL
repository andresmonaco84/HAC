--PRC_ASS_CTU_CNV_TAB_UTILIZA_L
create or replace procedure PRC_ASS_CTU_CNV_TAB_UTILIZA_L 
(
     pASS_CTU_ID IN TB_ASS_CTU_CNV_TAB_UTILIZA.ASS_CTU_ID%type DEFAULT NULL,
     pCAD_CNV_ID_CONVENIO IN TB_ASS_CTU_CNV_TAB_UTILIZA.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pTIS_MED_CD_TABELAMEDICA IN TB_ASS_CTU_CNV_TAB_UTILIZA.TIS_MED_CD_TABELAMEDICA%type DEFAULT NULL,
     pASS_CTU_DT_INICIO_VIGENCIA IN TB_ASS_CTU_CNV_TAB_UTILIZA.ASS_CTU_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_CTU_CNV_TAB_UTILIZA.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pASS_CTU_DT_FIM_VIGENCIA IN TB_ASS_CTU_CNV_TAB_UTILIZA.ASS_CTU_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pASS_CTU_DT_ULTIMA_ATUALIZACAO IN TB_ASS_CTU_CNV_TAB_UTILIZA.ASS_CTU_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pCAD_TAP_TP_ATRIBUTO IN TB_ASS_CTU_CNV_TAB_UTILIZA.CAD_TAP_TP_ATRIBUTO%type DEFAULT NULL,
     pTIS_MED_CD_TABELAMEDICA_COBRA IN TB_ASS_CTU_CNV_TAB_UTILIZA.TIS_MED_CD_TABELAMEDICA_COBRA%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ASS_CTU_CNV_TAB_UTILIZA_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pASS_CTU_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CTU_ID = ' || pASS_CTU_ID; END IF;
IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
IF pTIS_MED_CD_TABELAMEDICA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_MED_CD_TABELAMEDICA = ' || CHR(39) || pTIS_MED_CD_TABELAMEDICA || CHR(39); END IF;
IF pASS_CTU_DT_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CTU_DT_INICIO_VIGENCIA = ' || CHR(39) || pASS_CTU_DT_INICIO_VIGENCIA || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pASS_CTU_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CTU_DT_FIM_VIGENCIA = ' || CHR(39) || pASS_CTU_DT_FIM_VIGENCIA || CHR(39); END IF;
IF pASS_CTU_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CTU_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pASS_CTU_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pCAD_TAP_TP_ATRIBUTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TAP_TP_ATRIBUTO = ' || CHR(39) || pCAD_TAP_TP_ATRIBUTO || CHR(39); END IF;
IF pTIS_MED_CD_TABELAMEDICA_COBRA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_MED_CD_TABELAMEDICA_COBRA = ' || CHR(39) || pTIS_MED_CD_TABELAMEDICA_COBRA || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       ASS_CTU_ID,
       CAD_CNV_ID_CONVENIO,
       TIS_MED_CD_TABELAMEDICA,
       ASS_CTU_DT_INICIO_VIGENCIA,
       SEG_USU_ID_USUARIO,
       ASS_CTU_DT_FIM_VIGENCIA,
       ASS_CTU_DT_ULTIMA_ATUALIZACAO,
       CAD_TAP_TP_ATRIBUTO,
       TIS_MED_CD_TABELAMEDICA_COBRA
FROM TB_ASS_CTU_CNV_TAB_UTILIZA
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ASS_CTU_CNV_TAB_UTILIZA_L;
