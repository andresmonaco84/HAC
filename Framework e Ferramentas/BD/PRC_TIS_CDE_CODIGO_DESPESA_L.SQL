CREATE OR REPLACE PROCEDURE "PRC_TIS_CDE_CODIGO_DESPESA_L"
  (
     pTIS_CDE_CD_CODIGO_DESPESA IN TB_TIS_CDE_CODIGO_DESPESA.TIS_CDE_CD_CODIGO_DESPESA%type DEFAULT NULL,
     pTIS_CDE_DS_DESCRICAO_DESPESA IN TB_TIS_CDE_CODIGO_DESPESA.TIS_CDE_DS_DESCRICAO_DESPESA%type DEFAULT NULL,
     pTIS_CBE_DT_INI_VIGENCIA IN TB_TIS_CDE_CODIGO_DESPESA.TIS_CBE_DT_INI_VIGENCIA%type DEFAULT NULL,
     pTIS_CBE_DT_FIM_VIGENCIA IN TB_TIS_CDE_CODIGO_DESPESA.TIS_CBE_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pTIS_CBE_FL_STATUS IN TB_TIS_CDE_CODIGO_DESPESA.TIS_CBE_FL_STATUS%type DEFAULT NULL,
     pTIS_CBE_DT_ULTIMA_ATUALIZACAO IN TB_TIS_CDE_CODIGO_DESPESA.TIS_CBE_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_TIS_CDE_CODIGO_DESPESA.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pTIS_CDE_CD_CODIGO_DESPESA_V3 IN TB_TIS_CDE_CODIGO_DESPESA.TIS_CDE_CD_CODIGO_DESPESA_V3%type DEFAULT NULL,
     pTIS_CDE_DS_OBSERVACAO IN TB_TIS_CDE_CODIGO_DESPESA.TIS_CDE_DS_OBSERVACAO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
  )
  is
  /********************************************************************
  *    Procedure: PRC_TIS_CDE_CODIGO_DESPESA_L
  *
  *    Data Criacao:  data da  criação   Por: Nome do Analista
  *    Data Alteracao: data da alteração  Por: Nome do Analista
  *
  *    Funcao: Descrição da funcionalidade da Stored Procedure
  *
  *    Data Alteração:  21/03/2014  Por: Andréa Cazuca
  *    Alteração:  Inclusão dos campos TIS_CDE_CDE_CODIGO_DESPESA_V3 E TIS_CDE_DS_OBSERVACAO
  *
  *******************************************************************/
  v_cursor PKG_CURSOR.t_cursor;
  begin
    OPEN v_cursor FOR
    SELECT
       TIS_CDE_CD_CODIGO_DESPESA,
       TIS_CDE_DS_DESCRICAO_DESPESA,
       TIS_CBE_DT_INI_VIGENCIA,
       TIS_CBE_DT_FIM_VIGENCIA,
       TIS_CBE_FL_STATUS,
       TIS_CBE_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       TIS_CDE_CD_CODIGO_DESPESA_V3,
       TIS_CDE_DS_OBSERVACAO
    FROM TB_TIS_CDE_CODIGO_DESPESA
    WHERE
        (pTIS_CDE_CD_CODIGO_DESPESA is null OR TIS_CDE_CD_CODIGO_DESPESA = pTIS_CDE_CD_CODIGO_DESPESA) AND
        (pTIS_CDE_DS_DESCRICAO_DESPESA is null OR TIS_CDE_DS_DESCRICAO_DESPESA = pTIS_CDE_DS_DESCRICAO_DESPESA) AND
        (pTIS_CBE_DT_INI_VIGENCIA is null OR TIS_CBE_DT_INI_VIGENCIA = pTIS_CBE_DT_INI_VIGENCIA) AND
        (pTIS_CBE_DT_FIM_VIGENCIA is null OR TIS_CBE_DT_FIM_VIGENCIA = pTIS_CBE_DT_FIM_VIGENCIA) AND
        (pTIS_CBE_FL_STATUS is null OR TIS_CBE_FL_STATUS = pTIS_CBE_FL_STATUS) AND
        (pTIS_CBE_DT_ULTIMA_ATUALIZACAO is null OR TIS_CBE_DT_ULTIMA_ATUALIZACAO = pTIS_CBE_DT_ULTIMA_ATUALIZACAO) AND
        (pSEG_USU_ID_USUARIO is null OR SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO) AND
        (pTIS_CDE_CD_CODIGO_DESPESA_V3 is null OR TIS_CDE_CD_CODIGO_DESPESA_V3 = pTIS_CDE_CD_CODIGO_DESPESA_V3) AND
        (pTIS_CDE_DS_OBSERVACAO is null OR pTIS_CDE_DS_OBSERVACAO = pTIS_CDE_DS_OBSERVACAO);
    io_cursor := v_cursor;
  end PRC_TIS_CDE_CODIGO_DESPESA_L;
 