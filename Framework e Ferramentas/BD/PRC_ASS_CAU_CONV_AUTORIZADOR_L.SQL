create or replace procedure PRC_ASS_CAU_CONV_AUTORIZADOR_L
(
     pASS_CAU_ID IN TB_ASS_CAU_CONV_AUTORIZADOR.ASS_CAU_ID%type DEFAULT NULL,
     pCAD_CNV_ID_CONVENIO IN TB_ASS_CAU_CONV_AUTORIZADOR.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pCAD_AUT_ID IN TB_ASS_CAU_CONV_AUTORIZADOR.CAD_AUT_ID%type DEFAULT NULL,
     pASS_CAU_DT_INI_VIGENCIA IN TB_ASS_CAU_CONV_AUTORIZADOR.ASS_CAU_DT_INI_VIGENCIA%type DEFAULT NULL,
     pASS_CAU_DT_FIM_VIGENCIA IN TB_ASS_CAU_CONV_AUTORIZADOR.ASS_CAU_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pASS_CAU_QT_MAX_AUTORIZACAO IN TB_ASS_CAU_CONV_AUTORIZADOR.ASS_CAU_QT_MAX_AUTORIZACAO%type DEFAULT NULL,
     pASS_CAU_FL_STATUS IN TB_ASS_CAU_CONV_AUTORIZADOR.ASS_CAU_FL_STATUS%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_ASS_CAU_CONV_AUTORIZADOR.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pASS_CAU_DT_CRIACAO IN TB_ASS_CAU_CONV_AUTORIZADOR.ASS_CAU_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ATUALIZA IN TB_ASS_CAU_CONV_AUTORIZADOR.SEG_USU_ID_USUARIO_ATUALIZACAO%type DEFAULT NULL,
     pASS_CAU_DT_ULTIMA_ATUALIZACAO IN TB_ASS_CAU_CONV_AUTORIZADOR.ASS_CAU_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
)
is
/********************************************************************
*    Procedure: PRC_ASS_CAU_CONV_AUTORIZADOR_L
*
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pASS_CAU_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CAU_ID = ' || pASS_CAU_ID; END IF;
IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
IF pCAD_AUT_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_AUT_ID = ' || pCAD_AUT_ID; END IF;
IF pASS_CAU_DT_INI_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CAU_DT_INI_VIGENCIA = ' || CHR(39) || pASS_CAU_DT_INI_VIGENCIA || CHR(39); END IF;
IF pASS_CAU_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CAU_DT_FIM_VIGENCIA = ' || CHR(39) || pASS_CAU_DT_FIM_VIGENCIA || CHR(39); END IF;
IF pASS_CAU_QT_MAX_AUTORIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CAU_QT_MAX_AUTORIZACAO = ' || pASS_CAU_QT_MAX_AUTORIZACAO; END IF;
IF pASS_CAU_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CAU_FL_STATUS = ' || CHR(39) || pASS_CAU_FL_STATUS || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; END IF;
IF pASS_CAU_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CAU_DT_CRIACAO = ' || CHR(39) || pASS_CAU_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_ATUALIZA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ATUALIZACAO = ' || pSEG_USU_ID_USUARIO_ATUALIZA; END IF;
IF pASS_CAU_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CAU_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pASS_CAU_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;

   V_SELECT := '
SELECT
       ASS_CAU_ID,
       CAD_CNV_ID_CONVENIO,
       CAD_AUT_ID,
       ASS_CAU_DT_INI_VIGENCIA,
       ASS_CAU_DT_FIM_VIGENCIA,
       ASS_CAU_QT_MAX_AUTORIZACAO,
       ASS_CAU_FL_STATUS,
       SEG_USU_ID_USUARIO_CRIACAO,
       ASS_CAU_DT_CRIACAO,
       SEG_USU_ID_USUARIO_ATUALIZACAO,
       ASS_CAU_DT_ULTIMA_ATUALIZACAO
FROM TB_ASS_CAU_CONV_AUTORIZADOR
WHERE null is null  '    ;

OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ASS_CAU_CONV_AUTORIZADOR_L;
/
