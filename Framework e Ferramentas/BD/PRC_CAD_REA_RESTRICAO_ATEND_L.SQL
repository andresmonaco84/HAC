 
--PRC_CAD_REA_RESTRICAO_ATEND_L
create or replace procedure PRC_CAD_REA_RESTRICAO_ATEND_L 
(
     pCAD_PAC_ID_PACIENTE IN TB_CAD_REA_RESTRICAO_ATEND.CAD_PAC_ID_PACIENTE%type DEFAULT NULL,
     pCAD_REA_DT_INICIO_VIGENCIA IN TB_CAD_REA_RESTRICAO_ATEND.CAD_REA_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pCAD_REA_DT_FIM_VIGENCIA IN TB_CAD_REA_RESTRICAO_ATEND.CAD_REA_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_CAD_REA_RESTRICAO_ATEND.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pCAD_REA_DT_CRIACAO IN TB_CAD_REA_RESTRICAO_ATEND.CAD_REA_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ATUALIZ IN TB_CAD_REA_RESTRICAO_ATEND.SEG_USU_ID_USUARIO_ATUALIZ%type DEFAULT NULL,
     pCAD_REA_DT_ULTIMA_ATUALIZ IN TB_CAD_REA_RESTRICAO_ATEND.CAD_REA_DT_ULTIMA_ATUALIZ%type DEFAULT NULL,
     pCAD_PES_ID_PESSOA_RESPONSAVEL IN TB_CAD_REA_RESTRICAO_ATEND.CAD_PES_ID_PESSOA_RESPONSAVEL%type DEFAULT NULL,
     pATD_ATE_ID IN TB_CAD_REA_RESTRICAO_ATEND.ATD_ATE_ID%type DEFAULT NULL,
     pFAT_CCP_ID IN TB_CAD_REA_RESTRICAO_ATEND.FAT_CCP_ID%type DEFAULT NULL,
     pCAD_REA_DS_OBSERVACAO IN TB_CAD_REA_RESTRICAO_ATEND.CAD_REA_DS_OBSERVACAO%type DEFAULT NULL,
     pFAT_NOF_ID IN TB_CAD_REA_RESTRICAO_ATEND.FAT_NOF_ID%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_REA_RESTRICAO_ATEND_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_PAC_ID_PACIENTE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PAC_ID_PACIENTE = ' || pCAD_PAC_ID_PACIENTE; END IF;
IF pCAD_REA_DT_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_REA_DT_INICIO_VIGENCIA = ' || CHR(39) || pCAD_REA_DT_INICIO_VIGENCIA || CHR(39); END IF;
IF pCAD_REA_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_REA_DT_FIM_VIGENCIA = ' || CHR(39) || pCAD_REA_DT_FIM_VIGENCIA || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; END IF;
IF pCAD_REA_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_REA_DT_CRIACAO = ' || CHR(39) || pCAD_REA_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ATUALIZ = ' || pSEG_USU_ID_USUARIO_ATUALIZ; END IF;
IF pCAD_REA_DT_ULTIMA_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_REA_DT_ULTIMA_ATUALIZ = ' || CHR(39) || pCAD_REA_DT_ULTIMA_ATUALIZ || CHR(39); END IF;
IF pCAD_PES_ID_PESSOA_RESPONSAVEL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PES_ID_PESSOA_RESPONSAVEL = ' || pCAD_PES_ID_PESSOA_RESPONSAVEL; END IF;
IF pATD_ATE_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_ATE_ID = ' || pATD_ATE_ID; END IF;
IF pFAT_CCP_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_CCP_ID = ' || pFAT_CCP_ID; END IF;
IF pCAD_REA_DS_OBSERVACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_REA_DS_OBSERVACAO = ' || CHR(39) || pCAD_REA_DS_OBSERVACAO || CHR(39); END IF;
IF pFAT_NOF_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_NOF_ID = ' || pFAT_NOF_ID; END IF;
 
   V_SELECT := '
SELECT	
       CAD_PAC_ID_PACIENTE,
       CAD_REA_DT_INICIO_VIGENCIA,
       CAD_REA_DT_FIM_VIGENCIA,
       SEG_USU_ID_USUARIO_CRIACAO,
       CAD_REA_DT_CRIACAO,
       SEG_USU_ID_USUARIO_ATUALIZ,
       CAD_REA_DT_ULTIMA_ATUALIZ,
       CAD_PES_ID_PESSOA_RESPONSAVEL,
       ATD_ATE_ID,
       FAT_CCP_ID,
       CAD_REA_DS_OBSERVACAO,
       FAT_NOF_ID
FROM TB_CAD_REA_RESTRICAO_ATEND
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_REA_RESTRICAO_ATEND_L;
