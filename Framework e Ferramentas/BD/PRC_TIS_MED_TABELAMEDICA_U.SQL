CREATE OR REPLACE PROCEDURE "PRC_TIS_MED_TABELAMEDICA_U" (
     pTIS_MED_CD_TABELAMEDICA IN TB_TIS_MED_TABELAMEDICA.TIS_MED_CD_TABELAMEDICA%type,
     pTIS_MED_DS_TABELAMEDICA IN TB_TIS_MED_TABELAMEDICA.TIS_MED_DS_TABELAMEDICA%type,
     pTIS_MED_DT_INICIO_VIGENCIA IN TB_TIS_MED_TABELAMEDICA.TIS_MED_DT_INICIO_VIGENCIA%type default NULL,
     pTIS_MED_DT_FIM_VIGENCIA IN TB_TIS_MED_TABELAMEDICA.TIS_MED_DT_FIM_VIGENCIA%type default NULL,
     pTIS_MED_FL_STATUS IN TB_TIS_MED_TABELAMEDICA.TIS_MED_FL_STATUS%type default NULL,
     pTIS_MED_FL_UTILIZA_LANCAMENTO IN TB_TIS_MED_TABELAMEDICA.TIS_MED_FL_UTILIZA_LANCAMENTO%type default NULL,
     pTIS_MED_DT_ULTIMA_ATUALIZACAO IN TB_TIS_MED_TABELAMEDICA.TIS_MED_DT_ULTIMA_ATUALIZACAO%type default NULL,
     pSEG_USU_ID_USUARIO IN TB_TIS_MED_TABELAMEDICA.SEG_USU_ID_USUARIO%type default NULL,
  pTIS_MED_FL_UTILIZA_REPASSE IN TB_TIS_MED_TABELAMEDICA.TIS_MED_FL_UTILIZA_REPASSE%type,
   pTIS_MED_DT_CRIACAO IN TB_TIS_MED_TABELAMEDICA.TIS_MED_DT_CRIACAO%TYPE DEFAULT NULL,     
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_TIS_MED_TABELAMEDICA.SEG_USU_ID_USUARIO_CRIACAO%TYPE DEFAULT NULL
  )
  is
  /********************************************************************
  *    Procedure: {nome da procedure}
  *
  *    Data Criacao:  14/04/2007   Por: Silmara
  *    Data Alteracao: 25/05/2007  Por: Silmara
  *    Alterac?o:Foi alterado os parametros para gravar em maiusculo
  *    Funcao: Altera  tabela(s) medica(s)- TISS
  *
  *******************************************************************/
  begin
    UPDATE TB_TIS_MED_TABELAMEDICA
    SET
        TIS_MED_DS_TABELAMEDICA = UPPER(pTIS_MED_DS_TABELAMEDICA),
        TIS_MED_DT_INICIO_VIGENCIA = pTIS_MED_DT_INICIO_VIGENCIA,
        TIS_MED_DT_FIM_VIGENCIA = pTIS_MED_DT_FIM_VIGENCIA,
        TIS_MED_FL_STATUS = pTIS_MED_FL_STATUS,
        TIS_MED_FL_UTILIZA_LANCAMENTO = pTIS_MED_FL_UTILIZA_LANCAMENTO,
        TIS_MED_FL_UTILIZA_REPASSE = pTIS_MED_FL_UTILIZA_REPASSE,
        SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO,
        TIS_MED_DT_ULTIMA_ATUALIZACAO = SYSDATE
    WHERE
        TIS_MED_CD_TABELAMEDICA = pTIS_MED_CD_TABELAMEDICA;
  end PRC_TIS_MED_TABELAMEDICA_U;
 