 
  --PRC_ASS_PRP_PROFISSIONAL_PROD_S
  create or replace procedure PRC_ASS_PRP_PROF_PROD_S
  (
     pASS_PRP_ID IN TB_ASS_PRP_PROFISSIONAL_PROD.ASS_PRP_ID%type DEFAULT NULL,
     pCAD_PRO_ID_PROFISSIONAL IN TB_ASS_PRP_PROFISSIONAL_PROD.CAD_PRO_ID_PROFISSIONAL%type DEFAULT NULL,
     pCAD_PRD_ID IN TB_ASS_PRP_PROFISSIONAL_PROD.CAD_PRD_ID%type DEFAULT NULL,
     pASS_PRP_FL_ATIVO_OK IN TB_ASS_PRP_PROFISSIONAL_PROD.ASS_PRP_FL_ATIVO_OK%type DEFAULT NULL,
     pASS_PRP_DT_ULTIMA_ATUALIZACAO IN TB_ASS_PRP_PROFISSIONAL_PROD.ASS_PRP_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_PRP_PROFISSIONAL_PROD.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_UNI_ID_UNIDADE IN TB_ASS_PRP_PROFISSIONAL_PROD.CAD_UNI_ID_UNIDADE%type DEFAULT NULL,
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_ASS_PRP_PROFISSIONAL_PROD.CAD_LAT_ID_LOCAL_ATENDIMENTO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
  ) 
  is
  /********************************************************************
  *    Procedure: PRC_ASS_PRP_PROFISSIONAL_PROD_S
  * 
  *    Data Criacao: 	data da  criação   Por: Nome do Analista
  *    Data Alteracao:	data da alteração  Por: Nome do Analista
  *
  *    Funcao: Descrição da funcionalidade da Stored Procedure
  *
  *******************************************************************/
  v_cursor PKG_CURSOR.t_cursor;
  begin
    OPEN v_cursor FOR
    SELECT	
       ASS_PRP_ID,
       CAD_PRO_ID_PROFISSIONAL,
       CAD_PRD_ID,
       ASS_PRP_FL_ATIVO_OK,
       ASS_PRP_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       CAD_UNI_ID_UNIDADE,
       CAD_LAT_ID_LOCAL_ATENDIMENTO
    FROM TB_ASS_PRP_PROFISSIONAL_PROD
    WHERE
        (pASS_PRP_ID is null OR ASS_PRP_ID = pASS_PRP_ID) AND 
        (pCAD_PRO_ID_PROFISSIONAL is null OR CAD_PRO_ID_PROFISSIONAL = pCAD_PRO_ID_PROFISSIONAL) AND 
        (pCAD_PRD_ID is null OR CAD_PRD_ID = pCAD_PRD_ID) AND 
        (pASS_PRP_FL_ATIVO_OK is null OR ASS_PRP_FL_ATIVO_OK = pASS_PRP_FL_ATIVO_OK) AND 
        (pASS_PRP_DT_ULTIMA_ATUALIZACAO is null OR ASS_PRP_DT_ULTIMA_ATUALIZACAO = pASS_PRP_DT_ULTIMA_ATUALIZACAO) AND 
        (pSEG_USU_ID_USUARIO is null OR SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO) AND 
        (pCAD_UNI_ID_UNIDADE is null OR CAD_UNI_ID_UNIDADE = pCAD_UNI_ID_UNIDADE) AND 
        (pCAD_LAT_ID_LOCAL_ATENDIMENTO is null OR CAD_LAT_ID_LOCAL_ATENDIMENTO = pCAD_LAT_ID_LOCAL_ATENDIMENTO);          
    io_cursor := v_cursor;
  end PRC_ASS_PRP_PROF_PROD_S;
