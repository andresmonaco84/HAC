--PRC_ATS_TXT_PADRAO_LAUDO_S
create or replace procedure PRC_ATS_TXT_PADRAO_LAUDO_S 
(
     pATS_TPL_ID IN TB_ATS_TPL_TEXTO_PADRAO_LAUDO.ATS_TPL_ID%type DEFAULT NULL,
     pATS_TPL_CD_CODIGO IN TB_ATS_TPL_TEXTO_PADRAO_LAUDO.ATS_TPL_CD_CODIGO%type DEFAULT NULL,
     pATS_TPL_DS_DESCRICAO IN TB_ATS_TPL_TEXTO_PADRAO_LAUDO.ATS_TPL_DS_DESCRICAO%type DEFAULT NULL,
     pATS_TPL_FL_STATUS IN TB_ATS_TPL_TEXTO_PADRAO_LAUDO.ATS_TPL_FL_STATUS%type DEFAULT NULL,
     pATS_TPL_DT_ULTIMA_ATUALIZACAO IN TB_ATS_TPL_TEXTO_PADRAO_LAUDO.ATS_TPL_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ATS_TPL_TEXTO_PADRAO_LAUDO.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ATS_TXT_PADRAO_LAUDO_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pATS_TPL_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_TPL_ID = ' || pATS_TPL_ID; END IF;
IF pATS_TPL_CD_CODIGO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_TPL_CD_CODIGO = ' || CHR(39) || pATS_TPL_CD_CODIGO || CHR(39); END IF;
IF pATS_TPL_DS_DESCRICAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_TPL_DS_DESCRICAO = ' || CHR(39) || pATS_TPL_DS_DESCRICAO || CHR(39); END IF;
IF pATS_TPL_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_TPL_FL_STATUS = ' || CHR(39) || pATS_TPL_FL_STATUS || CHR(39); END IF;
IF pATS_TPL_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_TPL_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pATS_TPL_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       ATS_TPL_ID,
       ATS_TPL_CD_CODIGO,
       ATS_TPL_DS_DESCRICAO,
       ATS_TPL_FL_STATUS,
       ATS_TPL_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO
FROM TB_ATS_TPL_TEXTO_PADRAO_LAUDO
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ATS_TXT_PADRAO_LAUDO_S;
