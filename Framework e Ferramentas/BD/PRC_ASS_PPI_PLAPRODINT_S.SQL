create or replace procedure PRC_ASS_PPI_PLAPRODINT_S
  (
     pCAD_CNV_ID_CONVENIO IN TB_ASS_PPI_PLAPRODUTOINT.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pCAD_PLA_ID_PLANO IN TB_ASS_PPI_PLAPRODUTOINT.CAD_PLA_ID_PLANO%type DEFAULT NULL,
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_ASS_PPI_PLAPRODUTOINT.CAD_LAT_ID_LOCAL_ATENDIMENTO%type DEFAULT NULL,
     pCAD_PRD_ID IN TB_ASS_PPI_PLAPRODUTOINT.CAD_PRD_ID%type DEFAULT NULL,
     pASS_PPI_FL_EXIGE_GUIA IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_FL_EXIGE_GUIA%type DEFAULT NULL,
     pASS_PPI_FL_EXIGE_SENHA IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_FL_EXIGE_SENHA%type DEFAULT NULL,
     pASS_PPI_FL_AUTORIZATEL IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_FL_AUTORIZATEL%type DEFAULT NULL,
     pASS_PPI_FL_AUTORIZAFAX IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_FL_AUTORIZAFAX%type DEFAULT NULL,
     pASS_PPI_FL_AUTORIZAPES IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_FL_AUTORIZAPES%type DEFAULT NULL,
     pASS_PPI_FL_AUTORIZAWEB IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_FL_AUTORIZAWEB%type DEFAULT NULL,
     pASS_PPI_FL_COBERTURA IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_FL_COBERTURA%type DEFAULT NULL,
     pASS_PPI_FL_STATUS IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_FL_STATUS%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_PPI_PLAPRODUTOINT.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pASS_PPI_DT_ULTIMA_ATUALIZACAO IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pASS_PPI_QT_MAXIMA_LANC_DIA IN TB_ASS_PPI_PLAPRODUTOINT.ASS_PPI_QT_MAXIMA_LANC_DIA%TYPE DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
  )
  is
  /********************************************************************
  *    Procedure: PRC_ASS_PPI_PLAPRODINT_S
  *
  *    Data Criacao:   data da  criação   Por: Nome do Analista
  *    Data Alteracao:  data da alteração  Por: Nome do Analista
  *
  *    Funcao: Descrição da funcionalidade da Stored Procedure
  *
  *******************************************************************/
  v_cursor PKG_CURSOR.t_cursor;
  begin
    OPEN v_cursor FOR
     SELECT
       PPI.CAD_CNV_ID_CONVENIO,
       PPI.CAD_PLA_ID_PLANO,
       PPI.CAD_LAT_ID_LOCAL_ATENDIMENTO,
       PPI.CAD_PRD_ID,
       PPI.ASS_PPI_FL_EXIGE_GUIA,
       PPI.ASS_PPI_FL_EXIGE_SENHA,
       PPI.ASS_PPI_FL_AUTORIZATEL,
       PPI.ASS_PPI_FL_AUTORIZAFAX,
       PPI.ASS_PPI_FL_AUTORIZAPES,
       PPI.ASS_PPI_FL_AUTORIZAWEB,
       PPI.ASS_PPI_FL_COBERTURA,
       PPI.ASS_PPI_FL_STATUS,
       PPI.SEG_USU_ID_USUARIO,
       PPI.ASS_PPI_DT_ULTIMA_ATUALIZACAO,
       PPI.ASS_PPI_QT_MAXIMA_LANC_DIA,
       PRD.CAD_PRD_CD_CODIGO,
       PRD.CAD_PRD_DS_DESCRICAO
    FROM TB_ASS_PPI_PLAPRODUTOINT PPI,
         TB_CAD_PRD_PRODUTO PRD
    WHERE
        (pCAD_CNV_ID_CONVENIO is null OR PPI.CAD_CNV_ID_CONVENIO = pCAD_CNV_ID_CONVENIO) AND
        (pCAD_PLA_ID_PLANO is null OR PPI.CAD_PLA_ID_PLANO = pCAD_PLA_ID_PLANO) AND
        (pCAD_LAT_ID_LOCAL_ATENDIMENTO is null OR PPI.CAD_LAT_ID_LOCAL_ATENDIMENTO = pCAD_LAT_ID_LOCAL_ATENDIMENTO) AND
        (pCAD_PRD_ID is null OR PPI.CAD_PRD_ID = pCAD_PRD_ID) AND
        (pASS_PPI_FL_EXIGE_GUIA is null OR PPI.ASS_PPI_FL_EXIGE_GUIA = pASS_PPI_FL_EXIGE_GUIA) AND
        (pASS_PPI_FL_EXIGE_SENHA is null OR PPI.ASS_PPI_FL_EXIGE_SENHA = pASS_PPI_FL_EXIGE_SENHA) AND
        (pASS_PPI_FL_AUTORIZATEL is null OR PPI.ASS_PPI_FL_AUTORIZATEL = pASS_PPI_FL_AUTORIZATEL) AND
        (pASS_PPI_FL_AUTORIZAFAX is null OR PPI.ASS_PPI_FL_AUTORIZAFAX = pASS_PPI_FL_AUTORIZAFAX) AND
        (pASS_PPI_FL_AUTORIZAPES is null OR PPI.ASS_PPI_FL_AUTORIZAPES = pASS_PPI_FL_AUTORIZAPES) AND
        (pASS_PPI_FL_AUTORIZAWEB is null OR PPI.ASS_PPI_FL_AUTORIZAWEB = pASS_PPI_FL_AUTORIZAWEB) AND
        (pASS_PPI_FL_COBERTURA is null OR PPI.ASS_PPI_FL_COBERTURA = pASS_PPI_FL_COBERTURA) AND
        (pASS_PPI_FL_STATUS is null OR PPI.ASS_PPI_FL_STATUS = pASS_PPI_FL_STATUS) AND
        (pSEG_USU_ID_USUARIO is null OR PPI.SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO) AND
        (pASS_PPI_DT_ULTIMA_ATUALIZACAO is null OR PPI.ASS_PPI_DT_ULTIMA_ATUALIZACAO = pASS_PPI_DT_ULTIMA_ATUALIZACAO) AND
        (pASS_PPI_QT_MAXIMA_LANC_DIA is null OR ASS_PPI_QT_MAXIMA_LANC_DIA = pASS_PPI_QT_MAXIMA_LANC_DIA) AND
        (PPI.CAD_PRD_ID = PRD.CAD_PRD_ID);
    io_cursor := v_cursor;
  end PRC_ASS_PPI_PLAPRODINT_S;
