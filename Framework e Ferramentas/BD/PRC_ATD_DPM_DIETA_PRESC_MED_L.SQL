create or replace procedure PRC_ATD_DPM_DIETA_PRESC_MED_L
(
     pATD_DPM_ID IN TB_ATD_DPM_DIETA_PRESC_MED.ATD_DPM_ID%type DEFAULT NULL,
     pATD_PME_ID IN TB_ATD_DPM_DIETA_PRESC_MED.ATD_PME_ID%type DEFAULT NULL,
     pATD_DPM_DS_DIETA IN TB_ATD_DPM_DIETA_PRESC_MED.ATD_DPM_DS_DIETA%type DEFAULT NULL,
     pATD_DPM_DS_OBSERVACAO IN TB_ATD_DPM_DIETA_PRESC_MED.ATD_DPM_DS_OBSERVACAO%type DEFAULT NULL,
     pATD_DPM_DT_HORA_CRIACAO IN TB_ATD_DPM_DIETA_PRESC_MED.ATD_DPM_DT_HORA_CRIACAO%type DEFAULT NULL,
     pATD_DPM_ID_USU_CRIACAO IN TB_ATD_DPM_DIETA_PRESC_MED.ATD_DPM_ID_USU_CRIACAO%type DEFAULT NULL,
     pATD_DPM_DT_ULT_ATUALIZ IN TB_ATD_DPM_DIETA_PRESC_MED.ATD_DPM_DT_ULT_ATUALIZ%type DEFAULT NULL,
     pATD_DPM_ID_USU_ULT_ATUALIZ IN TB_ATD_DPM_DIETA_PRESC_MED.ATD_DPM_ID_USU_ULT_ATUALIZ%type DEFAULT NULL,
          pATD_DPM_STATUS IN TB_ATD_DPM_DIETA_PRESC_MED.ATD_DPM_STATUS%type DEFAULT NULL,
               pATD_DPM_FL_IMPRESSO IN TB_ATD_DPM_DIETA_PRESC_MED.ATD_DPM_FL_IMPRESSO%type DEFAULT NULL,
                   pCAD_MTMD_ID IN TB_ATD_DPM_DIETA_PRESC_MED.CAD_MTMD_ID%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
)
is
/********************************************************************
*    Procedure: PRC_ATD_DPM_DIETA_PRESC_MED_L
*
*    Data Criacao:   data da  cria??o   Por: Nome do Analista
*    Data Alteracao:  data da altera??o  Por: Nome do Analista
*
*    Funcao: Descri??o da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pATD_DPM_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_DPM_ID = ' || pATD_DPM_ID; END IF;
IF pATD_PME_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PME_ID = ' || pATD_PME_ID; END IF;
IF pATD_DPM_DS_DIETA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_DPM_DS_DIETA = ' || CHR(39) || pATD_DPM_DS_DIETA || CHR(39); END IF;
IF pATD_DPM_DS_OBSERVACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_DPM_DS_OBSERVACAO = ' || CHR(39) || pATD_DPM_DS_OBSERVACAO || CHR(39); END IF;
IF pATD_DPM_DT_HORA_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_DPM_DT_HORA_CRIACAO = ' || CHR(39) || pATD_DPM_DT_HORA_CRIACAO || CHR(39); END IF;
IF pATD_DPM_ID_USU_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_DPM_ID_USU_CRIACAO = ' || pATD_DPM_ID_USU_CRIACAO; END IF;
IF pATD_DPM_DT_ULT_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_DPM_DT_ULT_ATUALIZ = ' || CHR(39) || pATD_DPM_DT_ULT_ATUALIZ || CHR(39); END IF;
IF pATD_DPM_ID_USU_ULT_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_DPM_ID_USU_ULT_ATUALIZ = ' || pATD_DPM_ID_USU_ULT_ATUALIZ; END IF;
IF pATD_DPM_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_DPM_STATUS = ' || CHR(39) || pATD_DPM_STATUS || CHR(39); END IF;
IF pATD_DPM_FL_IMPRESSO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_DPM_FL_IMPRESSO = ' || CHR(39) || pATD_DPM_FL_IMPRESSO || CHR(39); END IF;
IF pCAD_MTMD_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_MTMD_ID = ' || pCAD_MTMD_ID; END IF;

   V_SELECT := '
SELECT
       ATD_DPM_ID,
       ATD_PME_ID,
       ATD_DPM_DS_DIETA,
       ATD_DPM_DS_OBSERVACAO,
       ATD_DPM_DT_HORA_CRIACAO,
       ATD_DPM_ID_USU_CRIACAO,
       ATD_DPM_DT_ULT_ATUALIZ,
       ATD_DPM_ID_USU_ULT_ATUALIZ,
       ATD_DPM_STATUS,
       ATD_DPM_FL_IMPRESSO,
       CAD_MTMD_ID
FROM TB_ATD_DPM_DIETA_PRESC_MED
WHERE null is null  '    ;

OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ATD_DPM_DIETA_PRESC_MED_L;
