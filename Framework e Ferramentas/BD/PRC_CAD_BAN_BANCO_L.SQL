--PRC_CAD_BAN_BANCO_L
create or replace procedure PRC_CAD_BAN_BANCO_L 
(
     pCAD_BAN_ID IN TB_CAD_BAN_BANCO.CAD_BAN_ID%type DEFAULT NULL,
     pCAD_BAN_CD_BANCO IN TB_CAD_BAN_BANCO.CAD_BAN_CD_BANCO%type DEFAULT NULL,
     pCAD_BAN_NM_BANCO IN TB_CAD_BAN_BANCO.CAD_BAN_NM_BANCO%type DEFAULT NULL,
     pCAD_BAN_CD_BANCO_LEG IN TB_CAD_BAN_BANCO.CAD_BAN_CD_BANCO_LEG%type DEFAULT NULL,
     pCAD_BAN_DT_CRIACAO IN TB_CAD_BAN_BANCO.CAD_BAN_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_CAD_BAN_BANCO.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pCAD_BAN_DT_ULTIMA_ATUALIZACAO IN TB_CAD_BAN_BANCO.CAD_BAN_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ATUALIZ IN TB_CAD_BAN_BANCO.SEG_USU_ID_USUARIO_ATUALIZ%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_BAN_BANCO_L
* 
*    Data Criacao: 	12/8/2011   Por: Pedro
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(3000);
  V_SELECT  varchar2(3000);
begin
  V_WHERE := NULL;
  IF pCAD_BAN_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_BAN_ID = ' || pCAD_BAN_ID; END IF;
IF pCAD_BAN_CD_BANCO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_BAN_CD_BANCO = ' || CHR(39) || pCAD_BAN_CD_BANCO || CHR(39); END IF;
IF pCAD_BAN_NM_BANCO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_BAN_NM_BANCO = ' || CHR(39) || pCAD_BAN_NM_BANCO || CHR(39); END IF;
IF pCAD_BAN_CD_BANCO_LEG IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_BAN_CD_BANCO_LEG = ' || CHR(39) || pCAD_BAN_CD_BANCO_LEG || CHR(39); END IF;
IF pCAD_BAN_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_BAN_DT_CRIACAO = ' || CHR(39) || pCAD_BAN_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; END IF;
IF pCAD_BAN_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_BAN_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_BAN_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ATUALIZ = ' || pSEG_USU_ID_USUARIO_ATUALIZ; END IF;
 
   V_SELECT := '
SELECT	
       CAD_BAN_ID,
       CAD_BAN_CD_BANCO,
       CAD_BAN_NM_BANCO,
       CAD_BAN_CD_BANCO_LEG,
       CAD_BAN_DT_CRIACAO,
       SEG_USU_ID_USUARIO_CRIACAO,
       CAD_BAN_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO_ATUALIZ
FROM TB_CAD_BAN_BANCO
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_BAN_BANCO_L;
/
