create or replace procedure PRC_ASS_CAU_CONV_AUTORIZADOR_U
(
     pASS_CAU_ID IN TB_ASS_CAU_CONV_AUTORIZADOR.ASS_CAU_ID%type,
     pCAD_CNV_ID_CONVENIO IN TB_ASS_CAU_CONV_AUTORIZADOR.CAD_CNV_ID_CONVENIO%type,
