     pASS_CAU_DT_INI_VIGENCIA IN TB_ASS_CAU_CONV_AUTORIZADOR.ASS_CAU_DT_INI_VIGENCIA%type,
     pASS_CAU_DT_FIM_VIGENCIA IN TB_ASS_CAU_CONV_AUTORIZADOR.ASS_CAU_DT_FIM_VIGENCIA%type default NULL,
     pASS_CAU_QT_MAX_AUTORIZACAO IN TB_ASS_CAU_CONV_AUTORIZADOR.ASS_CAU_QT_MAX_AUTORIZACAO%type,
