     pSEG_USU_ID_USUARIO_ATUALIZA IN TB_ASS_CAU_CONV_AUTORIZADOR.SEG_USU_ID_USUARIO_ATUALIZACAO%type default NULL,
     pASS_CAU_DT_ULTIMA_ATUALIZACAO IN TB_ASS_CAU_CONV_AUTORIZADOR.ASS_CAU_DT_ULTIMA_ATUALIZACAO%type default NULL
)
is
/********************************************************************
*    Procedure: PRC_ASS_CAU_CONV_AUTORIZADOR_U
*
*    Data Criacao:   data da  criação   Por: Nome do Analista
*    Data Alteracao:  data da alteração  Por: Nome do Analista
