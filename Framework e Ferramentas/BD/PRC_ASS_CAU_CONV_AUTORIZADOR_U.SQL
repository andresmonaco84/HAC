begin
UPDATE TB_ASS_CAU_CONV_AUTORIZADOR
SET
        CAD_CNV_ID_CONVENIO = pCAD_CNV_ID_CONVENIO,
        CAD_AUT_ID = pCAD_AUT_ID,
        ASS_CAU_DT_INI_VIGENCIA = pASS_CAU_DT_INI_VIGENCIA,
        ASS_CAU_DT_FIM_VIGENCIA = pASS_CAU_DT_FIM_VIGENCIA,
        ASS_CAU_QT_MAX_AUTORIZACAO = pASS_CAU_QT_MAX_AUTORIZACAO,
