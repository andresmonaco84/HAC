CREATE OR REPLACE PROCEDURE PRC_ASS_CPA_CNV_PCT_S
(
   pASS_CPA_ID IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_ID%type DEFAULT NULL,
   pCAD_CNV_ID_CONVENIO IN TB_ASS_CPA_CONVENIO_PACOTE.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
   pCAD_PLA_ID_PLANO IN TB_ASS_CPA_CONVENIO_PACOTE.CAD_PLA_ID_PLANO%type DEFAULT NULL,
   pCAD_PRD_ID IN TB_ASS_CPA_CONVENIO_PACOTE.CAD_PRD_ID%type DEFAULT NULL,
   pASS_CPA_FL_STATUS IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_FL_STATUS%type DEFAULT NULL,
   pASS_CPA_DT_INICIO_VIGENCIA IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_DT_INICIO_VIGENCIA%type DEFAULT NULL,
   pASS_CPA_DT_FIM_VIGENCIA IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_DT_FIM_VIGENCIA%type DEFAULT NULL,
   pASS_CPA_FL_LANC_PAC_ABERTO IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_FL_LANC_PAC_ABERTO%type DEFAULT NULL,
   pASS_CPA_DS_OBSERVACAO IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_DS_OBSERVACAO%type DEFAULT NULL,
   pASS_CPA_DT_ULTIMA_ATUALIZACAO IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
   pSEG_USU_ID_USUARIO IN TB_ASS_CPA_CONVENIO_PACOTE.SEG_USU_ID_USUARIO%type DEFAULT NULL,
   pVIGENCIA IN VARCHAR2 DEFAULT NULL, -- 0 = TODOS; 1 = VIGENTES; -1 = NAO VIGENTES
   io_cursor OUT PKG_CURSOR.t_cursor
)
is
/********************************************************************
*    Procedure: PRC_ASS_CPA_CNV_PCT_S
*
*    Data Criacao:   25/11/2009   Por: Fabíola
*    Data Alteracao: 06/2010   Por: André S. Monaco
*			    Alteracao: Adição dos campos CAD_PRD_CD_CODIGO e CAD_PLA_CD_PLANO_HAC
*    Data Alteracao: 04/02/2014   Por: André
*         Alteracao: Passei a query para o padrão de string e 
*                    adição de cod/descrição do pacote associado na query 
*                    (CAD_PRD_CD_CODIGO_ASSOCIADO, CAD_PRD_DS_DESCRICAO_ASSOCIADO)
*
*    Funcao: Lista Convenio x Pacote
*
*******************************************************************/
v_cursor PKG_CURSOR.t_cursor;
V_WHERE  varchar2(5000);
V_SELECT  varchar2(5000);
begin

IF pASS_CPA_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPA.ASS_CPA_ID = ' || pASS_CPA_ID; END IF;
IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPA.CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
IF pCAD_PLA_ID_PLANO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPA.CAD_PLA_ID_PLANO = ' || pCAD_PLA_ID_PLANO; END IF;
IF pCAD_PRD_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPA.CAD_PRD_ID = ' || pCAD_PRD_ID; END IF;
IF pASS_CPA_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPA.ASS_CPA_FL_STATUS = ' || CHR(39) || pASS_CPA_FL_STATUS || CHR(39); END IF;
IF pASS_CPA_DT_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPA.ASS_CPA_DT_INICIO_VIGENCIA = ' || CHR(39) || pASS_CPA_DT_INICIO_VIGENCIA || CHR(39); END IF;
IF pASS_CPA_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPA.ASS_CPA_DT_FIM_VIGENCIA = ' || CHR(39) || pASS_CPA_DT_FIM_VIGENCIA || CHR(39); END IF;
IF pASS_CPA_FL_LANC_PAC_ABERTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPA.ASS_CPA_FL_LANC_PAC_ABERTO = ' || CHR(39) || pASS_CPA_FL_LANC_PAC_ABERTO || CHR(39); END IF;
IF pASS_CPA_DS_OBSERVACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPA.ASS_CPA_DS_OBSERVACAO = ' || CHR(39) || pASS_CPA_DS_OBSERVACAO || CHR(39); END IF;
IF pASS_CPA_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPA.ASS_CPA_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pASS_CPA_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPA.SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF (NVL(pVIGENCIA, '0') = '1') THEN
   V_WHERE := V_WHERE || ' AND (CPA.ASS_CPA_FL_STATUS = ''A'' AND (FNC_VALIDAR_VIGENCIA(CPA.ASS_CPA_DT_INICIO_VIGENCIA, CPA.ASS_CPA_DT_FIM_VIGENCIA) = 1 OR CPA.ASS_CPA_DT_INICIO_VIGENCIA > TRUNC(SYSDATE))) ';
ELSIF (NVL(pVIGENCIA, '0') = '-1') THEN
   V_WHERE := V_WHERE || ' AND (CPA.ASS_CPA_FL_STATUS = ''I'' OR (FNC_VALIDAR_VIGENCIA(CPA.ASS_CPA_DT_INICIO_VIGENCIA, CPA.ASS_CPA_DT_FIM_VIGENCIA) = 0 AND CPA.ASS_CPA_DT_INICIO_VIGENCIA < TRUNC(SYSDATE))) ';
END IF;

V_SELECT := 'SELECT CPA.ASS_CPA_ID,
                    CPA.CAD_CNV_ID_CONVENIO,
                    CPA.CAD_PLA_ID_PLANO,
                    CPA.CAD_PRD_ID,
                    CPA.ASS_CPA_FL_STATUS,
                    CPA.ASS_CPA_DT_INICIO_VIGENCIA,
                    CPA.ASS_CPA_DT_FIM_VIGENCIA,
                    CPA.ASS_CPA_FL_LANC_PAC_ABERTO,
                    CPA.ASS_CPA_DS_OBSERVACAO,
                    CPA.ASS_CPA_DT_ULTIMA_ATUALIZACAO,
                    CPA.SEG_USU_ID_USUARIO,
                    CPA.ASS_CPA_VL_PACOTE_ESPECIAL,
                    CPA.CAD_PRD_ID_PACOTE_ASSOCIADO,
                    PRD.CAD_PRD_CD_CODIGO,
                    PRD.CAD_PRD_DS_DESCRICAO,
                    PRD.CAD_PRD_VL_PRODUTO,
                    MED.TIS_MED_DS_TABELAMEDICA,
                    PLA.CAD_PLA_CD_PLANO_HAC,
                    PRD_ASS.CAD_PRD_CD_CODIGO CAD_PRD_CD_CODIGO_ASSOCIADO,
                    PRD_ASS.CAD_PRD_DS_DESCRICAO CAD_PRD_DS_DESCRICAO_ASSOCIADO
               FROM TB_ASS_CPA_CONVENIO_PACOTE CPA,
                    TB_CAD_PRD_PRODUTO PRD,
                    TB_TIS_MED_TABELAMEDICA MED,
                    TB_CAD_PLA_PLANO PLA,
                    TB_CAD_PRD_PRODUTO PRD_ASS
              WHERE (CPA.CAD_PRD_ID = PRD.CAD_PRD_ID) AND
                    (CPA.CAD_PRD_ID_PACOTE_ASSOCIADO = PRD_ASS.CAD_PRD_ID) AND
                    (MED.TIS_MED_CD_TABELAMEDICA = PRD.TIS_MED_CD_TABELAMEDICA) AND
                    (PLA.CAD_PLA_ID_PLANO (+)= CPA.CAD_PLA_ID_PLANO) ';      
OPEN v_cursor FOR
V_SELECT || V_WHERE ;
io_cursor := v_cursor;
end PRC_ASS_CPA_CNV_PCT_S;
 
