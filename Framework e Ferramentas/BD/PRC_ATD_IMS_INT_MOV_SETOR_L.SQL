create or replace procedure PRC_ATD_IMS_INT_MOV_SETOR_L 
(
     pATD_ATE_ID IN TB_ATD_IMS_INT_MOV_SETOR.ATD_ATE_ID%type DEFAULT NULL,
     pATD_IMS_DT_ENTRADA IN TB_ATD_IMS_INT_MOV_SETOR.ATD_IMS_DT_ENTRADA%type DEFAULT NULL,
     pATD_IMS_HR_ENTRADA IN TB_ATD_IMS_INT_MOV_SETOR.ATD_IMS_HR_ENTRADA%type DEFAULT NULL,
     pATD_IMS_DT_SAIDA IN TB_ATD_IMS_INT_MOV_SETOR.ATD_IMS_DT_SAIDA%type DEFAULT NULL,
     pATD_IMS_HR_SAIDA IN TB_ATD_IMS_INT_MOV_SETOR.ATD_IMS_HR_SAIDA%type DEFAULT NULL,
     pCAD_SET_ID_SETOR IN TB_ATD_IMS_INT_MOV_SETOR.CAD_SET_ID_SETOR%type DEFAULT NULL,
     pATD_IMS_DT_ULTIMA_ATUALIZACAO IN TB_ATD_IMS_INT_MOV_SETOR.ATD_IMS_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ATD_IMS_INT_MOV_SETOR.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pATD_IMS_DS_OBSERVACAO IN TB_ATD_IMS_INT_MOV_SETOR.ATD_IMS_DS_OBSERVACAO%type DEFAULT NULL,
     pATD_IMS_ID IN TB_ATD_IMS_INT_MOV_SETOR.ATD_IMS_ID%type DEFAULT NULL,
     pATD_IMS_FL_STATUS IN TB_ATD_IMS_INT_MOV_SETOR.ATD_IMS_FL_STATUS%type DEFAULT NULL,
     pTIS_TAC_CD_TIPO_ACOMOD_AUT IN TB_ATD_IMS_INT_MOV_SETOR.TIS_TAC_CD_TIPO_ACOMOD_AUT%TYPE DEFAULT NULL,
     pATD_IMS_DT_INI_ACOMOD_AUT IN TB_ATD_IMS_INT_MOV_SETOR.ATD_IMS_DT_INI_ACOMOD_AUT%TYPE DEFAULT NULL,
     pATD_IMS_HR_INI_ACOMOD_AUT IN TB_ATD_IMS_INT_MOV_SETOR.ATD_IMS_HR_INI_ACOMOD_AUT%TYPE DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ATD_IMS_INT_MOV_SETOR_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*    Data Alteracao:    14/05/2011       Por: Fabiola Lopes
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*    Alteracao: Inclusão dos campos TIS_TAC_CD_TIPO_ACOMOD_AUT, ATD_IMS_DT_INI_ACOMOD_AUT e ATD_IMS_HR_INI_ACOMOD_AUT
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
   IF PATD_ATE_ID IS NOT NULL THEN
    V_WHERE := V_WHERE || ' AND ATD_ATE_ID = ' || PATD_ATE_ID;
  END IF;
  IF PATD_IMS_DT_ENTRADA IS NOT NULL THEN
    V_WHERE := V_WHERE || ' AND ATD_IMS_DT_ENTRADA = ' || CHR(39) ||
               PATD_IMS_DT_ENTRADA || CHR(39);
  END IF;
  IF PATD_IMS_HR_ENTRADA IS NOT NULL THEN
    V_WHERE := V_WHERE || ' AND ATD_IMS_HR_ENTRADA = ' ||
               PATD_IMS_HR_ENTRADA;
  END IF;
  IF PATD_IMS_DT_SAIDA IS NOT NULL THEN
    V_WHERE := V_WHERE || ' AND ATD_IMS_DT_SAIDA = ' || CHR(39) ||
               PATD_IMS_DT_SAIDA || CHR(39);
  END IF;
  IF PATD_IMS_HR_SAIDA IS NOT NULL THEN
    V_WHERE := V_WHERE || ' AND ATD_IMS_HR_SAIDA = ' || PATD_IMS_HR_SAIDA;
  END IF;
  IF PCAD_SET_ID_SETOR IS NOT NULL THEN
    V_WHERE := V_WHERE || ' AND CAD_SET_ID_SETOR = ' || PCAD_SET_ID_SETOR;
  END IF;
  IF PATD_IMS_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN
    V_WHERE := V_WHERE || ' AND ATD_IMS_DT_ULTIMA_ATUALIZACAO = ' ||
               CHR(39) || PATD_IMS_DT_ULTIMA_ATUALIZACAO || CHR(39);
  END IF;
  IF PSEG_USU_ID_USUARIO IS NOT NULL THEN
    V_WHERE := V_WHERE || ' AND SEG_USU_ID_USUARIO = ' ||
               PSEG_USU_ID_USUARIO;
  END IF;
  IF PATD_IMS_DS_OBSERVACAO IS NOT NULL THEN
    V_WHERE := V_WHERE || ' AND ATD_IMS_DS_OBSERVACAO = ' || CHR(39) ||
               PATD_IMS_DS_OBSERVACAO || CHR(39);
  END IF;
  IF PATD_IMS_ID IS NOT NULL THEN
    V_WHERE := V_WHERE || ' AND ATD_IMS_ID = ' || PATD_IMS_ID;
  END IF;
  IF PATD_IMS_FL_STATUS IS NOT NULL THEN
    V_WHERE := V_WHERE || ' AND ATD_IMS_FL_STATUS = ' || CHR(39) ||
               PATD_IMS_FL_STATUS || CHR(39);
  END IF;

  IF PTIS_TAC_CD_TIPO_ACOMOD_AUT IS NOT NULL THEN
    V_WHERE := V_WHERE || ' AND TIS_TAC_CD_TIPO_ACOMOD_AUT = ' ||
               PTIS_TAC_CD_TIPO_ACOMOD_AUT;
  END IF;
  IF PATD_IMS_DT_INI_ACOMOD_AUT IS NOT NULL THEN
    V_WHERE := V_WHERE || ' AND ATD_IMS_DT_INI_ACOMOD_AUT = ' ||
               PATD_IMS_DT_INI_ACOMOD_AUT;
  END IF;
  IF PATD_IMS_HR_INI_ACOMOD_AUT IS NOT NULL THEN
    V_WHERE := V_WHERE || ' AND ATD_IMS_HR_INI_ACOMOD_AUT = ' ||
               PATD_IMS_HR_INI_ACOMOD_AUT;
  END IF;
 
   V_SELECT := '
SELECT	
       ATD_ATE_ID,
       ATD_IMS_DT_ENTRADA,
       ATD_IMS_HR_ENTRADA,
       ATD_IMS_DT_SAIDA,
       ATD_IMS_HR_SAIDA,
       CAD_SET_ID_SETOR,
       ATD_IMS_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       ATD_IMS_DS_OBSERVACAO,
       ATD_IMS_ID,
       ATD_IMS_FL_STATUS,
       TIS_TAC_CD_TIPO_ACOMOD_AUT,
       ATD_IMS_DT_INI_ACOMOD_AUT,
       ATD_IMS_HR_INI_ACOMOD_AUT
FROM TB_ATD_IMS_INT_MOV_SETOR
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ATD_IMS_INT_MOV_SETOR_L;