CREATE OR REPLACE PROCEDURE "PRC_CAD_CLC_CLINICA_CREDENC_S"
  (
     pCAD_CLC_ID IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_CLC_ID%type DEFAULT NULL,
     pCAD_PES_NR_CNPJ_CPF IN TB_CAD_PES_PESSOA.CAD_PES_NR_CNPJ_CPF%type DEFAULT NULL,
     pCAD_PES_NM_RAZAOSOCIAL IN TB_CAD_PES_PESSOA.CAD_PES_NM_RAZAOSOCIAL%type DEFAULT NULL,
     pCAD_PES_NM_PESSOA IN TB_CAD_PES_PESSOA.CAD_PES_NM_PESSOA%type DEFAULT NULL,
     pCAD_CLC_CD_CLINICA IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_CLC_CD_CLINICA%TYPE DEFAULT NULL,
     pCAD_CLC_FL_STATUS IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_CLC_FL_STATUS%TYPE DEFAULT NULL,
     pCAD_CLC_DS_RESUMIDA IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_CLC_DS_RESUMIDA%type default NULL,
     pCAD_CLC_FL_UTILIZA_LIBERACAO IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_CLC_FL_UTILIZA_LIBERACAO%type default NULL,
     pCAD_CLC_DS_DESCRICAO IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_CLC_DS_DESCRICAO%type default NULL,
     pCAD_CLC_CT_EMPRESA IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_CLC_CT_EMPRESA%type default NULL,     
     io_cursor OUT PKG_CURSOR.t_cursor
  )
  is
  /********************************************************************
  *    Procedure: PRC_CAD_CLC_CLINICA_CREDENC_S
  *
  *    Data Criacao:  22/01/2008      Por: Fabiola Lopes
  *    Data Alteracao: 24/01/2008  Por: Davi S. M. dos Reis
  *
  *    Funcao: Listar as clinicas credenciadas
  *
  *    Data Alteracao: 24/01/2008  Por: Davi S. M. dos Reis
  *    Alteração: Inclusao dos campos de pessoa
  *
  *    Data Alteracao: 29/04/2008  Por: Andréa Cazuca
  *    Alteração: Inclusão da pesquisa pela descrição resumida
  *
  *    Data Alteracao: 04/08/2011  Por: Davi S. M. dos Reis
  *    Alteração: Inclusao dos campos CAD_CLC_FL_UTILIZA_LIBERACAO e CAD_CLC_DS_DESCRICAO
  *
  *******************************************************************/
  v_cursor PKG_CURSOR.t_cursor;
  begin
    OPEN v_cursor FOR
    SELECT
       CLC.CAD_CLC_ID,
       CLC.CAD_CLC_DT_INI_VIGENCIA,
       CLC.CAD_CLC_DT_FIM_VIGENCIA,
       CLC.CAD_PES_ID_PESSOA,
       CLC.CAD_CLC_CD_CLINICA,
       CLC.CAD_CLC_FL_STATUS,
       CLC.CAD_CLC_DT_ULTIMA_ATUALIZACAO,
       CLC.SEG_USU_ID_USUARIO,
       CLC.CAD_CLC_DS_RESUMIDA,
       CLC.CAD_CLC_FL_UTILIZA_LIBERACAO,
       CLC.CAD_CLC_DS_DESCRICAO,
       CLC.CAD_CLC_CT_EMPRESA,
       PES.*
    FROM TB_CAD_CLC_CLINICA_CREDENCIADA CLC,
         TB_CAD_PES_PESSOA PES
    WHERE
        CLC.CAD_PES_ID_PESSOA = PES.CAD_PES_ID_PESSOA
    AND (pCAD_CLC_ID is null OR CLC.CAD_CLC_ID = pCAD_CLC_ID)
    AND (pCAD_PES_NR_CNPJ_CPF is null OR PES.CAD_PES_NR_CNPJ_CPF = pCAD_PES_NR_CNPJ_CPF)
    AND (pCAD_PES_NM_RAZAOSOCIAL is null OR PES.CAD_PES_NM_RAZAOSOCIAL LIKE UPPER(pCAD_PES_NM_RAZAOSOCIAL))
    AND (pCAD_PES_NM_PESSOA is null OR PES.CAD_PES_NM_PESSOA LIKE UPPER(pCAD_PES_NM_PESSOA))
    AND (pCAD_CLC_CD_CLINICA is null OR CLC.CAD_CLC_CD_CLINICA = pCAD_CLC_CD_CLINICA)
    AND (pCAD_CLC_FL_STATUS is null OR CLC.CAD_CLC_FL_STATUS = pCAD_CLC_FL_STATUS)
    AND (pCAD_CLC_DS_RESUMIDA is null OR CLC.CAD_CLC_DS_RESUMIDA LIKE UPPER(pCAD_CLC_DS_RESUMIDA))
    AND (pCAD_CLC_FL_UTILIZA_LIBERACAO is null OR CLC.CAD_CLC_FL_UTILIZA_LIBERACAO = pCAD_CLC_FL_UTILIZA_LIBERACAO)
    AND (pCAD_CLC_DS_DESCRICAO is null OR CLC.CAD_CLC_DS_DESCRICAO = pCAD_CLC_DS_DESCRICAO)
    AND (pCAD_CLC_CT_EMPRESA is null OR CLC.CAD_CLC_CT_EMPRESA = pCAD_CLC_CT_EMPRESA);

    io_cursor := v_cursor;

  end PRC_CAD_CLC_CLINICA_CREDENC_S;
 