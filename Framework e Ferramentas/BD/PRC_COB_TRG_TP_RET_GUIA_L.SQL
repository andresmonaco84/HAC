 
--PRC_COB_TRG_TP_RET_GUIA_L
create or replace procedure PRC_COB_TRG_TP_RET_GUIA_L 
(
     pCAD_CNV_ID_CONVENIO IN TB_COB_TRG_TIPO_RETENCAO_GUIA.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pCOB_TRG_TP_RETENCAO IN TB_COB_TRG_TIPO_RETENCAO_GUIA.COB_TRG_TP_RETENCAO%type DEFAULT NULL,
     pCOB_TRG_DT_INI_VIGENCIA IN TB_COB_TRG_TIPO_RETENCAO_GUIA.COB_TRG_DT_INI_VIGENCIA%type DEFAULT NULL,
     pCOB_TRG_DT_FIM_VIGENCIA IN TB_COB_TRG_TIPO_RETENCAO_GUIA.COB_TRG_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_COB_TRG_TIPO_RETENCAO_GUIA.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pCOB_TRG_DT_CRIACAO IN TB_COB_TRG_TIPO_RETENCAO_GUIA.COB_TRG_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ATUALIZ IN TB_COB_TRG_TIPO_RETENCAO_GUIA.SEG_USU_ID_USUARIO_ATUALIZ%type DEFAULT NULL,
     pCOB_TRG_DT_ULTIMA_ATUALIZ IN TB_COB_TRG_TIPO_RETENCAO_GUIA.COB_TRG_DT_ULTIMA_ATUALIZ%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_COB_TRG_TP_RET_GUIA_L
* 
*    Data Criacao: 	13/06/2012  Por: Pedro
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
IF pCOB_TRG_TP_RETENCAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_TRG_TP_RETENCAO = ' || CHR(39) || pCOB_TRG_TP_RETENCAO || CHR(39); END IF;
IF pCOB_TRG_DT_INI_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_TRG_DT_INI_VIGENCIA = ' || CHR(39) || pCOB_TRG_DT_INI_VIGENCIA || CHR(39); END IF;
IF pCOB_TRG_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_TRG_DT_FIM_VIGENCIA = ' || CHR(39) || pCOB_TRG_DT_FIM_VIGENCIA || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; END IF;
IF pCOB_TRG_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_TRG_DT_CRIACAO = ' || CHR(39) || pCOB_TRG_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ATUALIZ = ' || pSEG_USU_ID_USUARIO_ATUALIZ; END IF;
IF pCOB_TRG_DT_ULTIMA_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_TRG_DT_ULTIMA_ATUALIZ = ' || CHR(39) || pCOB_TRG_DT_ULTIMA_ATUALIZ || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       CAD_CNV_ID_CONVENIO,
       COB_TRG_TP_RETENCAO,
       COB_TRG_DT_INI_VIGENCIA,
       COB_TRG_DT_FIM_VIGENCIA,
       SEG_USU_ID_USUARIO_CRIACAO,
       COB_TRG_DT_CRIACAO,
       SEG_USU_ID_USUARIO_ATUALIZ,
       COB_TRG_DT_ULTIMA_ATUALIZ
FROM TB_COB_TRG_TIPO_RETENCAO_GUIA
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_COB_TRG_TP_RET_GUIA_L;
