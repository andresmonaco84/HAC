  SELECT EST_DIA.MTMD_MOV_DATA,
       EST_DIA.CAD_UNI_ID_UNIDADE,
       UNI.CAD_UNI_DS_UNIDADE,
       SUM(DECODE(EST_DIA.CAD_MTMD_GRUPO_ID,1,EST_DIA.VLR_SAIDA,0))   DROG_MED,    --1 
