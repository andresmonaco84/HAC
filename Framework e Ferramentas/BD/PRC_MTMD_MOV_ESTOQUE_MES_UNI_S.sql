/********************************************************************
*    Procedure: PRC_MTMD_MOV_ESTOQUE_MES_UNI_S
*
*    Data Criacao: 	 01/2011     Por: RICARDO COSTA  
*    Data Alteração: 24/03/2011  Por: André Souza Monaco
*         Alteração: Adição do parâmetro pRETIRAR_SANTOS
*
*    Funcao: Relatorio de fechamento 29 de consumo  (por grupo/unidade)
*******************************************************************/
  dDataIni DATE;
  dDataFim DATE;
  sMes     VARCHAR2(2);
  v_cursor PKG_CURSOR.t_cursor;
begin
  IF (  LENGTH(TO_CHAR(pMTMD_MOV_MES)) = 1 ) THEN
     sMes := '0'||TO_CHAR(pMTMD_MOV_MES);  
  ELSE
     sMes := TO_CHAR(pMTMD_MOV_MES);     
  END IF;
  dDataIni := TO_DATE( '01'||TO_CHAR(sMes)||TO_CHAR(pMTMD_MOV_ANO)||' 0000','DDMMYYYY HH24MI');
  dDatafIM := TO_DATE( TO_CHAR(LAST_DAY(dDataIni),'DDMMYYYY')||' 0000','DDMMYYYY HH24MI');
