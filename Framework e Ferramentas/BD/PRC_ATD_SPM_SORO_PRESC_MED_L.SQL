create or replace procedure PRC_ATD_SPM_SORO_PRESC_MED_L
(
     pATD_SPM_ID IN TB_ATD_SPM_SORO_PRESC_MED.ATD_SPM_ID%type DEFAULT NULL,
     pATD_PME_ID IN TB_ATD_SPM_SORO_PRESC_MED.ATD_PME_ID%type DEFAULT NULL,
     pATD_SPM_DS_DESCRICAO IN TB_ATD_SPM_SORO_PRESC_MED.ATD_SPM_DS_DESCRICAO%type DEFAULT NULL,
     pATD_SPM_DS_VIA IN TB_ATD_SPM_SORO_PRESC_MED.ATD_SPM_DS_VIA%type DEFAULT NULL,
     pATD_SPM_DS_INTERVALO IN TB_ATD_SPM_SORO_PRESC_MED.ATD_SPM_DS_INTERVALO%type DEFAULT NULL,
     pATD_SPM_DS_OBSERVACAO IN TB_ATD_SPM_SORO_PRESC_MED.ATD_SPM_DS_OBSERVACAO%type DEFAULT NULL,
     pATD_SPM_DT_HORA_CRIACAO IN TB_ATD_SPM_SORO_PRESC_MED.ATD_SPM_DT_HORA_CRIACAO%type DEFAULT NULL,
     pATD_SPM_ID_USU_CRIACAO IN TB_ATD_SPM_SORO_PRESC_MED.ATD_SPM_ID_USU_CRIACAO%type DEFAULT NULL,
     pATD_SPM_DT_ULT_ATUALIZ IN TB_ATD_SPM_SORO_PRESC_MED.ATD_SPM_DT_ULT_ATUALIZ%type DEFAULT NULL,
     pATD_SPM_ID_USU_ULT_ATUALIZ IN TB_ATD_SPM_SORO_PRESC_MED.ATD_SPM_ID_USU_ULT_ATUALIZ%type DEFAULT NULL,
  pATD_SPM_CD_VIA IN TB_ATD_SPM_SORO_PRESC_MED.ATD_SPM_CD_VIA%type default NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
)
is
/********************************************************************
*    Procedure: PRC_ATD_SPM_SORO_PRESC_MED_L
*
*    Data Criacao: 	data da  cria??o   Por: Nome do Analista
*    Data Alteracao:	data da altera??o  Por: Nome do Analista
*
*    Funcao: Descri??o da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pATD_SPM_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_SPM_ID = ' || pATD_SPM_ID; END IF;
IF pATD_PME_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PME_ID = ' || pATD_PME_ID; END IF;
IF pATD_SPM_DS_DESCRICAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_SPM_DS_DESCRICAO = ' || CHR(39) || pATD_SPM_DS_DESCRICAO || CHR(39); END IF;
IF pATD_SPM_DS_VIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_SPM_DS_VIA = ' || CHR(39) || pATD_SPM_DS_VIA || CHR(39); END IF;
IF pATD_SPM_DS_INTERVALO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_SPM_DS_INTERVALO = ' || CHR(39) || pATD_SPM_DS_INTERVALO || CHR(39); END IF;
IF pATD_SPM_DS_OBSERVACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_SPM_DS_OBSERVACAO = ' || CHR(39) || pATD_SPM_DS_OBSERVACAO || CHR(39); END IF;
IF pATD_SPM_DT_HORA_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_SPM_DT_HORA_CRIACAO = ' || CHR(39) || pATD_SPM_DT_HORA_CRIACAO || CHR(39); END IF;
IF pATD_SPM_ID_USU_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_SPM_ID_USU_CRIACAO = ' || pATD_SPM_ID_USU_CRIACAO; END IF;
IF pATD_SPM_DT_ULT_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_SPM_DT_ULT_ATUALIZ = ' || CHR(39) || pATD_SPM_DT_ULT_ATUALIZ || CHR(39); END IF;
IF pATD_SPM_ID_USU_ULT_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_SPM_ID_USU_ULT_ATUALIZ = ' || pATD_SPM_ID_USU_ULT_ATUALIZ; END IF;
IF pATD_SPM_CD_VIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_SPM_CD_VIA = ' || CHR(39) || pATD_SPM_CD_VIA || CHR(39); END IF;
   V_SELECT := '
SELECT
       ATD_SPM_ID,
       ATD_PME_ID,
       ATD_SPM_DS_DESCRICAO,
       ATD_SPM_DS_VIA,
       ATD_SPM_DS_INTERVALO,
       ATD_SPM_DS_OBSERVACAO,
       ATD_SPM_DT_HORA_CRIACAO,
       ATD_SPM_ID_USU_CRIACAO,
       ATD_SPM_DT_ULT_ATUALIZ,
       ATD_SPM_ID_USU_ULT_ATUALIZ,
       ATD_SPM_CD_VIA
FROM TB_ATD_SPM_SORO_PRESC_MED
WHERE null is null  '    ;

OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ATD_SPM_SORO_PRESC_MED_L;
