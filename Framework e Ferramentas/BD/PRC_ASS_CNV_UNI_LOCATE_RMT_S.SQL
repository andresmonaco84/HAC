  --PRC_ASS_CNV_UNI_LOCATE_RMT_S
  create or replace procedure PRC_ASS_CNV_UNI_LOCATE_RMT_S 
  (
     pASS_CUL_ID IN TB_ASS_CUL_CONV_UNI_LOCATEND.ASS_CUL_ID%type DEFAULT NULL,
     pASS_CUL_DT_INI_VIGENCIA IN TB_ASS_CUL_CONV_UNI_LOCATEND.ASS_CUL_DT_INI_VIGENCIA%type DEFAULT NULL,
     pASS_CUL_DS_MOT_FIM_VIGENCIA IN TB_ASS_CUL_CONV_UNI_LOCATEND.ASS_CUL_DS_MOTIVO_FIM_VIGENCIA%type DEFAULT NULL,
     pASS_CUL_DT_ULTIMA_ATUALIZACAO IN TB_ASS_CUL_CONV_UNI_LOCATEND.ASS_CUL_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_ASS_CUL_CONV_UNI_LOCATEND.CAD_LAT_ID_LOCAL_ATENDIMENTO%type DEFAULT NULL,
     pASS_CUL_DT_FIM_VIGENCIA IN TB_ASS_CUL_CONV_UNI_LOCATEND.ASS_CUL_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_CUL_CONV_UNI_LOCATEND.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pASS_CNU_ID IN TB_ASS_CUL_CONV_UNI_LOCATEND.ASS_CNU_ID%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
  ) 
  is
  /********************************************************************
  *    Procedure: PRC_ASS_CNV_UNI_LOCATE_RMT_S
  * 
  *    Data Criacao: 	data da  criação   Por: Nome do Analista
  *    Data Alteracao:	data da alteração  Por: Nome do Analista
  *
  *    Funcao: Descrição da funcionalidade da Stored Procedure
  *
  *******************************************************************/
  v_cursor PKG_CURSOR.t_cursor;
  begin
    OPEN v_cursor FOR
    SELECT	
       ASS_CUL_ID,
       ASS_CUL_DT_INI_VIGENCIA,
       ASS_CUL_DS_MOTIVO_FIM_VIGENCIA,
       ASS_CUL_DT_ULTIMA_ATUALIZACAO,
       CAD_LAT_ID_LOCAL_ATENDIMENTO,
       ASS_CUL_DT_FIM_VIGENCIA,
       SEG_USU_ID_USUARIO,
       ASS_CNU_ID
    FROM TB_ASS_CUL_CONV_UNI_LOCATEND
    WHERE
        (pASS_CUL_ID is null OR ASS_CUL_ID = pASS_CUL_ID) AND 
        (pASS_CUL_DT_INI_VIGENCIA is null OR ASS_CUL_DT_INI_VIGENCIA = pASS_CUL_DT_INI_VIGENCIA) AND 
        (pASS_CUL_DS_MOT_FIM_VIGENCIA is null OR ASS_CUL_DS_MOTIVO_FIM_VIGENCIA = pASS_CUL_DS_MOT_FIM_VIGENCIA) AND 
        (pASS_CUL_DT_ULTIMA_ATUALIZACAO is null OR ASS_CUL_DT_ULTIMA_ATUALIZACAO = pASS_CUL_DT_ULTIMA_ATUALIZACAO) AND 
        (pCAD_LAT_ID_LOCAL_ATENDIMENTO is null OR CAD_LAT_ID_LOCAL_ATENDIMENTO = pCAD_LAT_ID_LOCAL_ATENDIMENTO) AND 
        (pASS_CUL_DT_FIM_VIGENCIA is null OR ASS_CUL_DT_FIM_VIGENCIA = pASS_CUL_DT_FIM_VIGENCIA) AND 
        (pSEG_USU_ID_USUARIO is null OR SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO) AND 
        (pASS_CNU_ID is null OR ASS_CNU_ID = pASS_CNU_ID);          
    io_cursor := v_cursor;
  end PRC_ASS_CNV_UNI_LOCATE_RMT_S;
