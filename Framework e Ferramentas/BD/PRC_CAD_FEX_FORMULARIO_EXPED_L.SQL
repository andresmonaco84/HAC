 
--PRC_CAD_FEX_FORMULARIO_EXPED_L
create or replace procedure PRC_CAD_FEX_FORMULARIO_EXPED_L 
(
     pCAD_FEX_ID IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_ID%type DEFAULT NULL,
     pCAD_FEX_NOME_FANTASIA IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_NOME_FANTASIA%type DEFAULT NULL,
     pCAD_FEX_RAZAO_SOCIAL IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_RAZAO_SOCIAL%type DEFAULT NULL,
     pCAD_FEX_PESSOA_CONTATO IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_PESSOA_CONTATO%type DEFAULT NULL,
     pCAD_FEX_TELEFONE IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_TELEFONE%type DEFAULT NULL,
     pCAD_FEX_EMAIL IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_EMAIL%type DEFAULT NULL,
     pCAD_FEX_NUMERO_FORMULARIO IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_NUMERO_FORMULARIO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_CAD_FEX_FORMULARIO_EXPED.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pCAD_FEX_DT_CRIACAO IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ATUALIZ IN TB_CAD_FEX_FORMULARIO_EXPED.SEG_USU_ID_USUARIO_ATUALIZ%type DEFAULT NULL,
     pCAD_FEX_DT_ULTIMA_ATUALIZ IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_DT_ULTIMA_ATUALIZ%type DEFAULT NULL,
     pCAD_FEX_DS_OBSERVACAO IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_DS_OBSERVACAO%type DEFAULT NULL,
     pCAD_FEX_INICIO_VIGENCIA IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_INICIO_VIGENCIA%type DEFAULT NULL,
     pCAD_FEX_FIM_VIGENCIA IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_FIM_VIGENCIA%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_FEX_FORMULARIO_EXPED_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_FEX_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_FEX_ID = ' || pCAD_FEX_ID; END IF;
IF pCAD_FEX_NOME_FANTASIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_FEX_NOME_FANTASIA = ' || CHR(39) || pCAD_FEX_NOME_FANTASIA || CHR(39); END IF;
IF pCAD_FEX_RAZAO_SOCIAL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_FEX_RAZAO_SOCIAL = ' || CHR(39) || pCAD_FEX_RAZAO_SOCIAL || CHR(39); END IF;
IF pCAD_FEX_PESSOA_CONTATO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_FEX_PESSOA_CONTATO = ' || CHR(39) || pCAD_FEX_PESSOA_CONTATO || CHR(39); END IF;
IF pCAD_FEX_TELEFONE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_FEX_TELEFONE = ' || CHR(39) || pCAD_FEX_TELEFONE || CHR(39); END IF;
IF pCAD_FEX_EMAIL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_FEX_EMAIL = ' || CHR(39) || pCAD_FEX_EMAIL || CHR(39); END IF;
IF pCAD_FEX_NUMERO_FORMULARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_FEX_NUMERO_FORMULARIO = ' || pCAD_FEX_NUMERO_FORMULARIO; END IF;
IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; END IF;
IF pCAD_FEX_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_FEX_DT_CRIACAO = ' || CHR(39) || pCAD_FEX_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ATUALIZ = ' || pSEG_USU_ID_USUARIO_ATUALIZ; END IF;
IF pCAD_FEX_DT_ULTIMA_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_FEX_DT_ULTIMA_ATUALIZ = ' || CHR(39) || pCAD_FEX_DT_ULTIMA_ATUALIZ || CHR(39); END IF;
IF pCAD_FEX_DS_OBSERVACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_FEX_DS_OBSERVACAO = ' || CHR(39) || pCAD_FEX_DS_OBSERVACAO || CHR(39); END IF;
IF pCAD_FEX_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_FEX_INICIO_VIGENCIA = ' || CHR(39) || pCAD_FEX_INICIO_VIGENCIA || CHR(39); END IF;
IF pCAD_FEX_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_FEX_FIM_VIGENCIA = ' || CHR(39) || pCAD_FEX_FIM_VIGENCIA || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       CAD_FEX_ID,
       CAD_FEX_NOME_FANTASIA,
       CAD_FEX_RAZAO_SOCIAL,
       CAD_FEX_PESSOA_CONTATO,
       CAD_FEX_TELEFONE,
       CAD_FEX_EMAIL,
       CAD_FEX_NUMERO_FORMULARIO,
       SEG_USU_ID_USUARIO_CRIACAO,
       CAD_FEX_DT_CRIACAO,
       SEG_USU_ID_USUARIO_ATUALIZ,
       CAD_FEX_DT_ULTIMA_ATUALIZ,
       CAD_FEX_DS_OBSERVACAO,
       CAD_FEX_INICIO_VIGENCIA,
       CAD_FEX_FIM_VIGENCIA
FROM TB_CAD_FEX_FORMULARIO_EXPED
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_FEX_FORMULARIO_EXPED_L;
