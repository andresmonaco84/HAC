create or replace procedure PRC_CAD_AUT_AUTORIZADOR_L
(
     pCAD_AUT_ID IN TB_CAD_AUT_AUTORIZADOR.CAD_AUT_ID%type DEFAULT NULL,
     pCAD_AUT_DS_DESCRICAO IN TB_CAD_AUT_AUTORIZADOR.CAD_AUT_DS_DESCRICAO%type DEFAULT NULL,
     pCAD_AUT_FL_STATUS IN TB_CAD_AUT_AUTORIZADOR.CAD_AUT_FL_STATUS%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_CAD_AUT_AUTORIZADOR.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pCAD_AUT_DT_CRIACAO IN TB_CAD_AUT_AUTORIZADOR.CAD_AUT_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ATUALIZA IN TB_CAD_AUT_AUTORIZADOR.SEG_USU_ID_USUARIO_ATUALIZACAO%type DEFAULT NULL,
     pCAD_AUT_DT_ULTIMA_ATUALIZACAO IN TB_CAD_AUT_AUTORIZADOR.CAD_AUT_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_AUT_AUTORIZADOR_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_AUT_ID IS NOT NULL THEN 
    V_WHERE:= V_WHERE || ' AND CAD_AUT_ID = ' || pCAD_AUT_ID; END IF;
  IF pCAD_AUT_DS_DESCRICAO IS NOT NULL THEN 
    V_WHERE:= V_WHERE || ' AND CAD_AUT_DS_DESCRICAO = ' || CHR(39) || pCAD_AUT_DS_DESCRICAO || CHR(39); 
  END IF;
  IF pCAD_AUT_FL_STATUS IS NOT NULL THEN 
    V_WHERE:= V_WHERE || ' AND CAD_AUT_FL_STATUS = ' || CHR(39) || pCAD_AUT_FL_STATUS || CHR(39); 
  END IF;
  IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN 
    V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; 
  END IF;
  IF pCAD_AUT_DT_CRIACAO IS NOT NULL THEN 
    V_WHERE:= V_WHERE || ' AND CAD_AUT_DT_CRIACAO = ' || CHR(39) || pCAD_AUT_DT_CRIACAO || CHR(39); 
  END IF;
  IF pSEG_USU_ID_USUARIO_ATUALIZA IS NOT NULL THEN 
    V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ATUALIZACAO = ' || pSEG_USU_ID_USUARIO_ATUALIZA; 
  END IF;
  IF pCAD_AUT_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN 
    V_WHERE:= V_WHERE || ' AND CAD_AUT_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_AUT_DT_ULTIMA_ATUALIZACAO || CHR(39); 
  END IF;
 
   V_SELECT := '
SELECT	
       CAD_AUT_ID,
       CAD_AUT_DS_DESCRICAO,
       CAD_AUT_FL_STATUS,
       SEG_USU_ID_USUARIO_CRIACAO,
       CAD_AUT_DT_CRIACAO,
       SEG_USU_ID_USUARIO_ATUALIZACAO,
       CAD_AUT_DT_ULTIMA_ATUALIZACAO
FROM TB_CAD_AUT_AUTORIZADOR
WHERE null is null  '    ;
  dbms_output.put_line(V_SELECT || V_WHERE);     
OPEN v_cursor FOR
  
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_AUT_AUTORIZADOR_L;
/
