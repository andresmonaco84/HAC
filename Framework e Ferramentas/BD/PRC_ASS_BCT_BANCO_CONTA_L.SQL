 
--PRC_ASS_BCT_BANCO_CONTA_L
create or replace procedure PRC_ASS_BCT_BANCO_CONTA_L 
(
     pASS_BCT_ID IN TB_ASS_BCT_BANCO_CONTA.ASS_BCT_ID%type DEFAULT NULL,
     pCAD_BAN_ID IN TB_ASS_BCT_BANCO_CONTA.CAD_BAN_ID%type DEFAULT NULL,
     pASS_BCT_CD_AGENCIA IN TB_ASS_BCT_BANCO_CONTA.ASS_BCT_CD_AGENCIA%type DEFAULT NULL,
     pASS_BCT_NR_CONTA IN TB_ASS_BCT_BANCO_CONTA.ASS_BCT_NR_CONTA%type DEFAULT NULL,
     pASS_BCT_CD_CTA_CAIXA_RM IN TB_ASS_BCT_BANCO_CONTA.ASS_BCT_CD_CTA_CAIXA_RM%type DEFAULT NULL,
     pASS_BCT_FL_ATIVO IN TB_ASS_BCT_BANCO_CONTA.ASS_BCT_FL_ATIVO%type DEFAULT NULL,
     pASS_BCT_DV_AGENCIA IN TB_ASS_BCT_BANCO_CONTA.ASS_BCT_DV_AGENCIA%type DEFAULT NULL,
     pASS_BCT_DV_CONTA IN TB_ASS_BCT_BANCO_CONTA.ASS_BCT_DV_CONTA%type DEFAULT NULL,
     pASS_BCT_DT_CRIACAO IN TB_ASS_BCT_BANCO_CONTA.ASS_BCT_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_ASS_BCT_BANCO_CONTA.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pASS_BCT_DT_ULTIMA_ATUALIZACAO IN TB_ASS_BCT_BANCO_CONTA.ASS_BCT_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ATUALIZ IN TB_ASS_BCT_BANCO_CONTA.SEG_USU_ID_USUARIO_ATUALIZ%type DEFAULT NULL,
     pASS_BCT_CD_BANCO_LEG IN TB_ASS_BCT_BANCO_CONTA.ASS_BCT_CD_BANCO_LEG%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ASS_BCT_BANCO_CONTA_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pASS_BCT_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_BCT_ID = ' || pASS_BCT_ID; END IF;
IF pCAD_BAN_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_BAN_ID = ' || pCAD_BAN_ID; END IF;
IF pASS_BCT_CD_AGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_BCT_CD_AGENCIA = ' || pASS_BCT_CD_AGENCIA; END IF;
IF pASS_BCT_NR_CONTA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_BCT_NR_CONTA = ' || pASS_BCT_NR_CONTA; END IF;
IF pASS_BCT_CD_CTA_CAIXA_RM IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_BCT_CD_CTA_CAIXA_RM = ' || CHR(39) || pASS_BCT_CD_CTA_CAIXA_RM || CHR(39); END IF;
IF pASS_BCT_FL_ATIVO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_BCT_FL_ATIVO = ' || CHR(39) || pASS_BCT_FL_ATIVO || CHR(39); END IF;
IF pASS_BCT_DV_AGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_BCT_DV_AGENCIA = ' || pASS_BCT_DV_AGENCIA; END IF;
IF pASS_BCT_DV_CONTA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_BCT_DV_CONTA = ' || pASS_BCT_DV_CONTA; END IF;
IF pASS_BCT_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_BCT_DT_CRIACAO = ' || CHR(39) || pASS_BCT_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; END IF;
IF pASS_BCT_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_BCT_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pASS_BCT_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ATUALIZ = ' || pSEG_USU_ID_USUARIO_ATUALIZ; END IF;
IF pASS_BCT_CD_BANCO_LEG IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_BCT_CD_BANCO_LEG = ' || CHR(39) || pASS_BCT_CD_BANCO_LEG || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       ASS_BCT_ID,
       CAD_BAN_ID,
       ASS_BCT_CD_AGENCIA,
       ASS_BCT_NR_CONTA,
       ASS_BCT_CD_CTA_CAIXA_RM,
       ASS_BCT_FL_ATIVO,
       ASS_BCT_DV_AGENCIA,
       ASS_BCT_DV_CONTA,
       ASS_BCT_DT_CRIACAO,
       SEG_USU_ID_USUARIO_CRIACAO,
       ASS_BCT_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO_ATUALIZ,
       ASS_BCT_CD_BANCO_LEG
FROM TB_ASS_BCT_BANCO_CONTA
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ASS_BCT_BANCO_CONTA_L;
