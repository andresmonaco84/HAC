begin
UPDATE TB_FAT_COC_CONTA_CONSUMO
SET	   
        FAT_COC_TP_DESTINO_CONTA = pFAT_COC_TP_DESTINO_CONTA,
        FAT_COC_VL_TOTAL_ORIGINAL = pFAT_COC_VL_TOTAL_ORIGINAL,
        FAT_COC_PC_DESCONTO = pFAT_COC_PC_DESCONTO,
        FAT_COC_VL_DESCONTO = pFAT_COC_VL_DESCONTO,
