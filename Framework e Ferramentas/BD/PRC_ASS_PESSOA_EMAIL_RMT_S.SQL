--PRC_ASS_PESSOA_EMAIL_RMT_S
create or replace procedure PRC_ASS_PESSOA_EMAIL_RMT_S 
(
     pASS_PEM_ID_PESSOA_EMAIL IN TB_ASS_PEM_PESSOA_EMAIL.ASS_PEM_ID_PESSOA_EMAIL%type DEFAULT NULL,
     pASS_PEM_NM_FUNCAO IN TB_ASS_PEM_PESSOA_EMAIL.ASS_PEM_NM_FUNCAO%type DEFAULT NULL,
     pASS_PEM_NM_EMAIL IN TB_ASS_PEM_PESSOA_EMAIL.ASS_PEM_NM_EMAIL%type DEFAULT NULL,
     pASS_PEM_DT_ULTIMA_ATUALIZACAO IN TB_ASS_PEM_PESSOA_EMAIL.ASS_PEM_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_PEM_PESSOA_EMAIL.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_PES_ID_PESSOA IN TB_ASS_PEM_PESSOA_EMAIL.CAD_PES_ID_PESSOA%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ASS_PESSOA_EMAIL_RMT_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pASS_PEM_ID_PESSOA_EMAIL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PEM_ID_PESSOA_EMAIL = ' || pASS_PEM_ID_PESSOA_EMAIL; END IF;
IF pASS_PEM_NM_FUNCAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PEM_NM_FUNCAO = ' || CHR(39) || pASS_PEM_NM_FUNCAO || CHR(39); END IF;
IF pASS_PEM_NM_EMAIL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PEM_NM_EMAIL = ' || CHR(39) || pASS_PEM_NM_EMAIL || CHR(39); END IF;
IF pASS_PEM_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PEM_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pASS_PEM_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pCAD_PES_ID_PESSOA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PES_ID_PESSOA = ' || pCAD_PES_ID_PESSOA; END IF;
 
   V_SELECT := '
SELECT	
       ASS_PEM_ID_PESSOA_EMAIL,
       ASS_PEM_NM_FUNCAO,
       ASS_PEM_NM_EMAIL,
       ASS_PEM_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       CAD_PES_ID_PESSOA
FROM TB_ASS_PEM_PESSOA_EMAIL
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ASS_PESSOA_EMAIL_RMT_S;
