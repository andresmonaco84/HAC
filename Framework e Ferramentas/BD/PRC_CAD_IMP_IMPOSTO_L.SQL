 
--PRC_CAD_IMP_IMPOSTO_L
create or replace procedure PRC_CAD_IMP_IMPOSTO_L 
(
     pCAD_IMP_ID_IMPOSTO IN TB_CAD_IMP_IMPOSTO.CAD_IMP_ID_IMPOSTO%type DEFAULT NULL,
     pCAD_IMP_NM_IMPOSTO IN TB_CAD_IMP_IMPOSTO.CAD_IMP_NM_IMPOSTO%type DEFAULT NULL,
     pCAD_IMP_DS_IMPOSTO IN TB_CAD_IMP_IMPOSTO.CAD_IMP_DS_IMPOSTO%type DEFAULT NULL,
     pCAD_IMP_FL_ATIVO_OK IN TB_CAD_IMP_IMPOSTO.CAD_IMP_FL_ATIVO_OK%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_IMP_IMPOSTO.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_IMP_DT_ULTIMA_ATUALIZACAO IN TB_CAD_IMP_IMPOSTO.CAD_IMP_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_IMP_IMPOSTO_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_IMP_ID_IMPOSTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_IMP_ID_IMPOSTO = ' || pCAD_IMP_ID_IMPOSTO; END IF;
IF pCAD_IMP_NM_IMPOSTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_IMP_NM_IMPOSTO = ' || CHR(39) || pCAD_IMP_NM_IMPOSTO || CHR(39); END IF;
IF pCAD_IMP_DS_IMPOSTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_IMP_DS_IMPOSTO = ' || CHR(39) || pCAD_IMP_DS_IMPOSTO || CHR(39); END IF;
IF pCAD_IMP_FL_ATIVO_OK IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_IMP_FL_ATIVO_OK = ' || CHR(39) || pCAD_IMP_FL_ATIVO_OK || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pCAD_IMP_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_IMP_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_IMP_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       CAD_IMP_ID_IMPOSTO,
       CAD_IMP_NM_IMPOSTO,
       CAD_IMP_DS_IMPOSTO,
       CAD_IMP_FL_ATIVO_OK,
       SEG_USU_ID_USUARIO,
       CAD_IMP_DT_ULTIMA_ATUALIZACAO
FROM TB_CAD_IMP_IMPOSTO
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_IMP_IMPOSTO_L;
