create or replace procedure PRC_TIS_TRI_TP_REGINTERNACAO_U
  (
     pTIS_TRI_CD_TP_REGINTERNACAO IN TB_TIS_TRI_TP_REGINTERNACAO.TIS_TRI_CD_TP_REGINTERNACAO%type,
     pTIS_TRI_DS_TP_REGINTERNACAO IN TB_TIS_TRI_TP_REGINTERNACAO.TIS_TRI_DS_TP_REGINTERNACAO%type,
  pTIS_TRI_FL_STATUS     IN TB_TIS_TRI_TP_REGINTERNACAO.TIS_TRI_FL_STATUS%type,
    pTIS_TRI_DT_CRIACAO IN TB_TIS_TRI_TP_REGINTERNACAO.TIS_TRI_DT_CRIACAO%TYPE DEFAULT NULL,     
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_TIS_TRI_TP_REGINTERNACAO.SEG_USU_ID_USUARIO_CRIACAO%TYPE DEFAULT NULL,
     pTIS_TRI_DT_ULT_ATUALIZACAO IN TB_TIS_TRI_TP_REGINTERNACAO.TIS_TRI_DT_ULT_ATUALIZACAO%TYPE DEFAULT NULL,
     pSEG_USU_ID_USU_ULT_ATUALIZ IN TB_TIS_TRI_TP_REGINTERNACAO.SEG_USU_ID_USU_ULT_ATUALIZ%TYPE DEFAULT NULL
 
  )
  is
  /********************************************************************
  *    Procedure: PRC_TIS_TRI_TP_REGINTERNACAO_U
  *
  *    Data Criacao:    12/04/2007    Por: Fabiola R. Lopes
  *    Data Alteracao:   28/05/2007       Por: Silmara
  *    Alterac?o:Foi alterado os parametros para gravar em maiusculo
  *
  *    Funcao: Alterar os Tipos de Regime de Internacao
  *
  *******************************************************************/
  begin
    UPDATE TB_TIS_TRI_TP_REGINTERNACAO
    SET
        TIS_TRI_DS_TP_REGINTERNACAO = UPPER(pTIS_TRI_DS_TP_REGINTERNACAO),
        TIS_TRI_FL_STATUS = pTIS_TRI_FL_STATUS,
    
        TIS_TRI_DT_ULT_ATUALIZACAO = SYSDATE,
        SEG_USU_ID_USU_ULT_ATUALIZ = pSEG_USU_ID_USU_ULT_ATUALIZ
 
    WHERE
        TIS_TRI_CD_TP_REGINTERNACAO = pTIS_TRI_CD_TP_REGINTERNACAO;
  end PRC_TIS_TRI_TP_REGINTERNACAO_U;
 