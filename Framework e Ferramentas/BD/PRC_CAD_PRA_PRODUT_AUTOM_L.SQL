--PRC_CAD_PRA_PRODUT_AUTOM_L
create or replace procedure PRC_CAD_PRA_PRODUT_AUTOM_L 
(
     pCAD_PRA_ID IN TB_CAD_PRA_PRODUT_AUTOM.CAD_PRA_ID%type DEFAULT NULL,
     pCAD_PRD_ID IN TB_CAD_PRA_PRODUT_AUTOM.CAD_PRD_ID%type DEFAULT NULL,
     pCAD_PRD_ID_PRO_ASS IN TB_CAD_PRA_PRODUT_AUTOM.CAD_PRD_ID_PRO_ASS%type DEFAULT NULL,
     pCAD_PRA_DT_INICIO_VIGENCIA IN TB_CAD_PRA_PRODUT_AUTOM.CAD_PRA_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pCAD_PRA_DT_FIM_VIGENCIA IN TB_CAD_PRA_PRODUT_AUTOM.CAD_PRA_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pCAD_PRA_FL_STATUS IN TB_CAD_PRA_PRODUT_AUTOM.CAD_PRA_FL_STATUS%type DEFAULT NULL,
     pCAD_PRA_QT_PRO_ASS IN TB_CAD_PRA_PRODUT_AUTOM.CAD_PRA_QT_PRO_ASS%type DEFAULT NULL,
     pCAD_PRA_DT_ULTIMA_ATUALIZACAO IN TB_CAD_PRA_PRODUT_AUTOM.CAD_PRA_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_PRA_PRODUT_AUTOM.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_PRA_QT_MULT_ASS IN TB_CAD_PRA_PRODUT_AUTOM.CAD_PRA_QT_MULT_ASS%type DEFAULT NULL,
     pCAD_CNV_ID_CONVENIO IN TB_CAD_PRA_PRODUT_AUTOM.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_CAD_PRA_PRODUT_AUTOM.CAD_LAT_ID_LOCAL_ATENDIMENTO%type DEFAULT NULL,
	 pCAD_PRA_FL_HORAEXTRA IN TB_CAD_PRA_PRODUT_AUTOM.CAD_PRA_FL_HORAEXTRA%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_PRA_PRODUT_AUTOM_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_PRA_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRA_ID = ' || pCAD_PRA_ID; END IF;
IF pCAD_PRD_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_ID = ' || pCAD_PRD_ID; END IF;
IF pCAD_PRD_ID_PRO_ASS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_ID_PRO_ASS = ' || pCAD_PRD_ID_PRO_ASS; END IF;
IF pCAD_PRA_DT_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRA_DT_INICIO_VIGENCIA = ' || CHR(39) || pCAD_PRA_DT_INICIO_VIGENCIA || CHR(39); END IF;
IF pCAD_PRA_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRA_DT_FIM_VIGENCIA = ' || CHR(39) || pCAD_PRA_DT_FIM_VIGENCIA || CHR(39); END IF;
IF pCAD_PRA_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRA_FL_STATUS = ' || CHR(39) || pCAD_PRA_FL_STATUS || CHR(39); END IF;
IF pCAD_PRA_QT_PRO_ASS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRA_QT_PRO_ASS = ' || pCAD_PRA_QT_PRO_ASS; END IF;
IF pCAD_PRA_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRA_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_PRA_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pCAD_PRA_QT_MULT_ASS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRA_QT_MULT_ASS = ' || pCAD_PRA_QT_MULT_ASS; END IF;
IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
IF pCAD_LAT_ID_LOCAL_ATENDIMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_LAT_ID_LOCAL_ATENDIMENTO = ' || pCAD_LAT_ID_LOCAL_ATENDIMENTO; END IF;
IF pCAD_PRA_FL_HORAEXTRA IS NOT NULL THEN V_WHERE := V_WHERE || ' AND CAD_PRA_FL_HORAEXTRA = ' ||  CHR(39) || pCAD_PRA_FL_HORAEXTRA || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       CAD_PRA_ID,
       CAD_PRD_ID,
       CAD_PRD_ID_PRO_ASS,
       CAD_PRA_DT_INICIO_VIGENCIA,
       CAD_PRA_DT_FIM_VIGENCIA,
       CAD_PRA_FL_STATUS,
       CAD_PRA_QT_PRO_ASS,
       CAD_PRA_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       CAD_PRA_QT_MULT_ASS,
       CAD_CNV_ID_CONVENIO,
       CAD_LAT_ID_LOCAL_ATENDIMENTO,
	   CAD_PRA_FL_HORAEXTRA
FROM TB_CAD_PRA_PRODUT_AUTOM
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_PRA_PRODUT_AUTOM_L;
