 
--PRC_CAD_CUR_CONV_UNI_REFER_L
create or replace procedure PRC_CAD_CUR_CONV_UNI_REFER_L 
(
     pCAD_CNV_ID_CONVENIO IN TB_CAD_CUR_CONV_UNI_REFER.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pCAD_UNI_ID_UNIDADE IN TB_CAD_CUR_CONV_UNI_REFER.CAD_UNI_ID_UNIDADE%type DEFAULT NULL,
     pCAD_CUR_CD_REFERENCIADO IN TB_CAD_CUR_CONV_UNI_REFER.CAD_CUR_CD_REFERENCIADO%type DEFAULT NULL,
     pCAD_CUR_DT_ULTIMA_ATUALIZACAO IN TB_CAD_CUR_CONV_UNI_REFER.CAD_CUR_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_CUR_CONV_UNI_REFER.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_CUR_FL_STATUS IN TB_CAD_CUR_CONV_UNI_REFER.CAD_CUR_FL_STATUS%type DEFAULT NULL,
     pCAD_SET_ID IN TB_CAD_CUR_CONV_UNI_REFER.CAD_SET_ID%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_CUR_CONV_UNI_REFER_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
IF pCAD_UNI_ID_UNIDADE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_UNI_ID_UNIDADE = ' || pCAD_UNI_ID_UNIDADE; END IF;
IF pCAD_CUR_CD_REFERENCIADO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CUR_CD_REFERENCIADO = ' || CHR(39) || pCAD_CUR_CD_REFERENCIADO || CHR(39); END IF;
IF pCAD_CUR_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CUR_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_CUR_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pCAD_CUR_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CUR_FL_STATUS = ' || CHR(39) || pCAD_CUR_FL_STATUS || CHR(39); END IF;
IF pCAD_SET_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_SET_ID = ' || pCAD_SET_ID; END IF;
 
   V_SELECT := '
SELECT	
       CAD_CNV_ID_CONVENIO,
       CAD_UNI_ID_UNIDADE,
       CAD_CUR_CD_REFERENCIADO,
       CAD_CUR_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       CAD_CUR_FL_STATUS,
       CAD_SET_ID
FROM TB_CAD_CUR_CONV_UNI_REFER
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_CUR_CONV_UNI_REFER_L;
