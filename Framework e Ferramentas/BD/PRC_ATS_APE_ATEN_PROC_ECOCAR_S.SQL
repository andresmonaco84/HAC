begin
  V_WHERE := NULL;
  IF pATS_ATE_CD_INTLIB IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_ATE_CD_INTLIB = ' || pATS_ATE_CD_INTLIB; END IF;
