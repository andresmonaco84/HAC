CREATE OR REPLACE FUNCTION FNC_RETORNA_TEL_PAC(PCAD_PES_ID_PESSOA IN TB_CAD_PES_PESSOA.CAD_PES_ID_PESSOA%TYPE)
  RETURN VARCHAR IS
  RESULT VARCHAR(200);
BEGIN

  SELECT TEL.CAD_TEL_NR_NUM_TEL
    INTO RESULT
    FROM TB_CAD_TEL_TELEFONE TEL
   INNER JOIN TB_AUX_TTE_TP_TEL_ENDERECO TTE
      ON TTE.AUX_TTE_CD_TP_TEL_END = TEL.AUX_TTE_CD_TP_TEL_END
   WHERE ROWNUM < 2
     AND TEL.CAD_PES_ID_PESSOA = PCAD_PES_ID_PESSOA;

  RETURN(RESULT);
END FNC_RETORNA_TEL_PAC;
