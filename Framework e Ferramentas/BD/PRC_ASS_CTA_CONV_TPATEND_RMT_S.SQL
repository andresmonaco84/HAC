--PRC_ASS_CTA_CONV_TPATEND_RMT_S
create or replace procedure PRC_ASS_CTA_CONV_TPATEND_RMT_S 
(
     pASS_CTA_ID IN TB_ASS_CTA_CONV_TPATEND.ASS_CTA_ID%type DEFAULT NULL,
     pCAD_CNV_ID_CONVENIO IN TB_ASS_CTA_CONV_TPATEND.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pTIS_TAT_CD_TPATENDIMENTO IN TB_ASS_CTA_CONV_TPATEND.TIS_TAT_CD_TPATENDIMENTO%type DEFAULT NULL,
     pASS_CTA_DS_MOTIVO_FIM_VIGENC IN TB_ASS_CTA_CONV_TPATEND.ASS_CTA_DS_MOTIVO_FIM_VIGENCIA%type DEFAULT NULL,
     pASS_CTA_DT_ULTIMA_ATUALIZACAO IN TB_ASS_CTA_CONV_TPATEND.ASS_CTA_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pASS_CTA_DT_INI_VIGENCIA IN TB_ASS_CTA_CONV_TPATEND.ASS_CTA_DT_INI_VIGENCIA%type DEFAULT NULL,
     pASS_CTA_DT_FIM_VIGENCIA IN TB_ASS_CTA_CONV_TPATEND.ASS_CTA_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_CTA_CONV_TPATEND.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ASS_CTA_CONV_TPATEND_RMT_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pASS_CTA_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CTA_ID = ' || pASS_CTA_ID; END IF;
IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
IF pTIS_TAT_CD_TPATENDIMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_TAT_CD_TPATENDIMENTO = ' || CHR(39) || pTIS_TAT_CD_TPATENDIMENTO || CHR(39); END IF;
IF pASS_CTA_DS_MOTIVO_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CTA_DS_MOTIVO_FIM_VIGENCIA = ' || CHR(39) || pASS_CTA_DS_MOTIVO_FIM_VIGENCIA || CHR(39); END IF;
IF pASS_CTA_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CTA_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pASS_CTA_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pASS_CTA_DT_INI_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CTA_DT_INI_VIGENCIA = ' || CHR(39) || pASS_CTA_DT_INI_VIGENCIA || CHR(39); END IF;
IF pASS_CTA_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CTA_DT_FIM_VIGENCIA = ' || CHR(39) || pASS_CTA_DT_FIM_VIGENCIA || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       ASS_CTA_ID,
       CAD_CNV_ID_CONVENIO,
       TIS_TAT_CD_TPATENDIMENTO,
       ASS_CTA_DS_MOTIVO_FIM_VIGENCIA,
       ASS_CTA_DT_ULTIMA_ATUALIZACAO,
       ASS_CTA_DT_INI_VIGENCIA,
       ASS_CTA_DT_FIM_VIGENCIA,
       SEG_USU_ID_USUARIO
FROM TB_ASS_CTA_CONV_TPATEND
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ASS_CTA_CONV_TPATEND_RMT_S;
