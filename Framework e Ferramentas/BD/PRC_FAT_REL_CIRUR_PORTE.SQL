CREATE OR REPLACE PROCEDURE "PRC_FAT_REL_CIRUR_PORTE"
(
    pATD_ATE_DT_ATENDIMENTO_INI IN TB_ATD_ATE_ATENDIMENTO.ATD_ATE_DT_ATENDIMENTO%TYPE DEFAULT NULL,
    pATD_ATE_DT_ATENDIMENTO_FIM IN TB_ATD_ATE_ATENDIMENTO.ATD_ATE_DT_ATENDIMENTO%TYPE DEFAULT NULL,
    pCAD_UNI_ID_UNIDADE IN TB_ATD_ATE_ATENDIMENTO.CAD_UNI_ID_UNIDADE%TYPE DEFAULT NULL,
    pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_ATD_ATE_ATENDIMENTO.CAD_LAT_ID_LOCAL_ATENDIMENTO%TYPE DEFAULT NULL,
    pCAD_SET_ID IN TB_CAD_SET_SETOR.CAD_SET_ID%TYPE DEFAULT NULL,
    pCAD_CNV_ID_CONVENIO     IN TB_CAD_CNV_CONVENIO.CAD_CNV_ID_CONVENIO%TYPE DEFAULT NULL,
    pCAD_PLA_ID_PLANO        IN TB_CAD_PLA_PLANO.CAD_PLA_ID_PLANO%TYPE DEFAULT NULL,
    pTIS_TAT_CD_TPATENDIMENTO IN TB_TIS_TAT_TP_ATENDIMENTO.TIS_TAT_CD_TPATENDIMENTO%TYPE DEFAULT NULL,
    pTIS_CBO_CD_CBOS IN TB_TIS_CBO_CBOS.TIS_CBO_CD_CBOS%TYPE DEFAULT NULL,
    pCAD_PLA_CD_TIPOPLANO_GB IN TB_CAD_PLA_PLANO.CAD_PLA_CD_TIPOPLANO%TYPE DEFAULT NULL, --ACS
    pCAD_PLA_CD_TIPOPLANO_PL IN TB_CAD_PLA_PLANO.CAD_PLA_CD_TIPOPLANO%TYPE DEFAULT NULL, --ACS
    pCAD_PLA_CD_TIPOPLANO_FU IN TB_CAD_PLA_PLANO.CAD_PLA_CD_TIPOPLANO%TYPE DEFAULT NULL,
    pCAD_PLA_CD_TIPOPLANO_NP IN TB_CAD_PLA_PLANO.CAD_PLA_CD_TIPOPLANO%TYPE DEFAULT NULL,
    pCAD_PLA_CD_TIPOPLANO_PA IN TB_CAD_PLA_PLANO.CAD_PLA_CD_TIPOPLANO%TYPE DEFAULT NULL,
    pCAD_PLA_CD_TIPOPLANO_SP IN TB_CAD_PLA_PLANO.CAD_PLA_CD_TIPOPLANO%TYPE DEFAULT NULL,
    pFAT_CCP_MES_FAT IN TB_FAT_CCP_CONTA_CONS_PARC.FAT_CCP_MES_FAT%TYPE,
    pFAT_CCP_ANO_FAT IN TB_FAT_CCP_CONTA_CONS_PARC.FAT_CCP_ANO_FAT%TYPE,
    IO_CURSOR OUT PKG_CURSOR.T_CURSOR,
    TESTE OUT LONG
)
IS
/********************************************************************
*    PROCEDURE: PRC_FAT_REL_CIRUR_PORTE
*    DATA ALTERACAO: 15/12/2010  POR: PEDRO
*    ALTERAC?O:
*******************************************************************/
v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(3000);
  V_SELECT  varchar2(30000);
begin
  V_WHERE := NULL;
IF pCAD_UNI_ID_UNIDADE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CCI.CAD_UNI_ID_UNIDADE = ' || pCAD_UNI_ID_UNIDADE; END IF;
IF pCAD_LAT_ID_LOCAL_ATENDIMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CCI.CAD_LAT_ID_LOCAL_ATENDIMENTO = ' || pCAD_LAT_ID_LOCAL_ATENDIMENTO; END IF;
IF pCAD_SET_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATE.CAD_SET_ID = ' || pCAD_SET_ID; END IF;
IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE := V_WHERE || ' AND CCI.CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO;    END IF;
IF pCAD_PLA_ID_PLANO IS NOT NULL THEN    V_WHERE := V_WHERE || ' AND PAC.CAD_PLA_ID_PLANO = ' || pCAD_PLA_ID_PLANO;    END IF;
IF pFAT_CCP_MES_FAT IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CCI.FAT_CCI_MES_FECHAMENTO = ' || pFAT_CCP_MES_FAT; END IF;
IF pFAT_CCP_ANO_FAT IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CCI.FAT_CCI_ANO_FECHAMENTO = ' || pFAT_CCP_ANO_FAT; END IF;
IF pTIS_TAT_CD_TPATENDIMENTO IS NOT NULL THEN V_WHERE := V_WHERE || ' AND ATE.TIS_TAT_CD_TPATENDIMENTO = ' ||CHR(39)|| pTIS_TAT_CD_TPATENDIMENTO ||CHR(39); END IF;
IF pATD_ATE_DT_ATENDIMENTO_INI IS NOT NULL THEN V_WHERE := V_WHERE || ' AND CCI.FAT_CCI_DT_INICIO_CONSUMO >= ' ||CHR(39)|| pATD_ATE_DT_ATENDIMENTO_INI ||CHR(39);    END IF;
IF pATD_ATE_DT_ATENDIMENTO_FIM IS NOT NULL THEN V_WHERE := V_WHERE || ' AND CCI.FAT_CCI_DT_FIM_CONSUMO <= ' ||CHR(39)|| pATD_ATE_DT_ATENDIMENTO_FIM ||CHR(39);    END IF;
IF pTIS_CBO_CD_CBOS IS NOT NULL THEN V_WHERE := V_WHERE || ' AND ATE.TIS_CBO_CD_CBOS <= ' ||CHR(39)|| pTIS_CBO_CD_CBOS ||CHR(39);    END IF;
V_SELECT:=
'
 SELECT  DISTINCT
              CASE WHEN CCP.CAD_TPE_CD_CODIGO = '||chr(39)||'FU'||chr(39)||' THEN '||chr(39)||'FUNCIONARIO'||chr(39)||'
                   WHEN CCP.CAD_TPE_CD_CODIGO = '||chr(39)||'PA'||chr(39)||' THEN '||chr(39)||'PARTICULAR'||chr(39)||'
                   WHEN CCP.CAD_TPE_CD_CODIGO = '||chr(39)||'NP'||chr(39)||' THEN '||chr(39)||'NAO PAGANTE'||chr(39)||'
                   WHEN CCP.CAD_TPE_CD_CODIGO = '||chr(39)||'SP'||chr(39)||' THEN '||chr(39)||'SERVICO PRESTADO'||chr(39)||'
                   WHEN CCP.CAD_TPE_CD_CODIGO = '||chr(39)||'ACS'||chr(39)||' THEN '||chr(39)||'ANA COSTA SAUDE'||chr(39)||'
              END CAD_TPE_CD_CODIGO,
              UNI.CAD_UNI_DS_UNIDADE,
              CASE WHEN CCI.ATD_ATE_TP_PACIENTE = '||chr(39)||'I'||chr(39)||' THEN '||chr(39)||'INTERNO'||chr(39)||' ELSE '||chr(39)||'EXTERNO'||chr(39)||' END ATD_ATE_TP_PACIENTE,
              SETOR.CAD_SET_CD_SETOR,
              SETOR.CAD_SET_DS_SETOR,
              NVL(COUNT(CASE WHEN CCI.FAT_CCI_TP_PORTEANESTESICO IN (0,1,2) THEN CCI.FAT_CCI_ID ELSE NULL END) OVER (PARTITION BY CCP.CAD_TPE_CD_CODIGO, CCI.CAD_UNI_ID_UNIDADE,
               CCI.ATD_ATE_TP_PACIENTE, SETOR.CAD_SET_CD_SETOR),0) TOTAL_PEQUENO,
              NVL(COUNT(CASE WHEN CCI.FAT_CCI_TP_PORTEANESTESICO IN (3,4) THEN CCI.FAT_CCI_ID ELSE NULL END) OVER (PARTITION BY  CCP.CAD_TPE_CD_CODIGO,CCI.CAD_UNI_ID_UNIDADE,
               CCI.ATD_ATE_TP_PACIENTE, SETOR.CAD_SET_CD_SETOR),0) TOTAL_MEDIO,
              NVL(COUNT(CASE WHEN CCI.FAT_CCI_TP_PORTEANESTESICO IN (5,6,7,8) THEN CCI.FAT_CCI_ID ELSE NULL END) OVER (PARTITION BY CCP.CAD_TPE_CD_CODIGO, CCI.CAD_UNI_ID_UNIDADE,
                CCI.ATD_ATE_TP_PACIENTE, SETOR.CAD_SET_CD_SETOR),0) TOTAL_GRANDE
              FROM    TB_FAT_CCI_CONTA_CONSU_ITEM  CCI
              JOIN    TB_FAT_CCP_CONTA_CONS_PARC   CCP
              ON      CCP.FAT_CCP_ID             = CCI.FAT_CCP_ID
              AND     CCP.ATD_ATE_ID             = CCI.ATD_ATE_ID
              AND     CCP.CAD_PAC_ID_PACIENTE    = CCI.CAD_PAC_ID_PACIENTE
              AND     CCP.FAT_COC_ID             = CCI.FAT_COC_ID
              JOIN    TB_CAD_PRD_PRODUTO           PRD
              ON      PRD.CAD_PRD_ID             = CCI.CAD_PRD_ID_COBRADO
              JOIN    TB_ATD_ATE_ATENDIMENTO       ATE
              ON      ATE.ATD_ATE_ID             = CCI.ATD_ATE_ID
              JOIN    TB_CAD_UNI_UNIDADE           UNI
              ON      UNI.CAD_UNI_ID_UNIDADE     = CCI.CAD_UNI_ID_UNIDADE
              JOIN    TB_FAT_MCC_MOV_COM_CONSUMO   MCC
              ON      MCC.FAT_MCC_ID             = CCI.FAT_MCC_ID
              AND     MCC.ATD_ATE_ID             = CCI.ATD_ATE_ID
              JOIN    TB_CAD_SET_SETOR             SETOR
              ON      SETOR.CAD_SET_ID           = MCC.CAD_SET_ID
              WHERE   CCI.CAD_UNI_ID_UNIDADE IN (244,247)
              AND     CCI.FAT_CCI_FL_STATUS   = '||chr(39)||'A'||chr(39)||'
              AND     CCP.FAT_CCP_FL_STATUS   = '||chr(39)||'A'||chr(39)||'
              AND     CCP.FAT_CCP_FL_FATURADA = '||chr(39)||'S'||chr(39)||'
              AND     MCC.FAT_MCC_FL_STATUS   = '||chr(39)||'A'||chr(39)||'
              AND     CCI.FAT_CCI_TP_PORTEANESTESICO IS NOT NULL
              AND     PRD.CAD_PRD_FL_CIRURGIA = '||chr(39)||'S'||chr(39)||'
              AND     (TRIM(CCI.TIS_GPP_CD_GRAU_PART_PROF) = '||chr(39)||'0'||chr(39)||')
               AND     (CCI.CAD_TAP_TP_ATRIBUTO IN ('||chr(39)||'HM'||chr(39)||'))
               AND     CCI.ATD_ATE_TP_PACIENTE IN ('||chr(39)||'I'||chr(39)||','||chr(39)||'E'||chr(39)||')
            ' || V_WHERE || '
              AND    ('||chr(39)|| pCAD_PLA_CD_TIPOPLANO_GB ||chr(39)||' IS not NULL and CCP.CAD_TPE_CD_CODIGO = '||chr(39)||'ACS'||chr(39)||'
               OR '||chr(39)|| pCAD_PLA_CD_TIPOPLANO_PA ||chr(39)||' IS NOT NULL AND CCP.CAD_TPE_CD_CODIGO = '||chr(39)||'PA'||chr(39)||'
               OR '||chr(39)|| pCAD_PLA_CD_TIPOPLANO_SP ||chr(39)||' IS NOT NULL AND CCP.CAD_TPE_CD_CODIGO = '||chr(39)||'SP'||chr(39)||'
               OR '||chr(39)|| pCAD_PLA_CD_TIPOPLANO_FU ||chr(39)||' IS NOT NULL AND CCP.CAD_TPE_CD_CODIGO = '||chr(39)||'FU'||chr(39)||'
               OR '||chr(39)|| pCAD_PLA_CD_TIPOPLANO_NP ||chr(39)||' IS NOT NULL AND CCP.CAD_TPE_CD_CODIGO = '||chr(39)||'NP'||chr(39)||')
              ORDER BY CASE WHEN CCP.CAD_TPE_CD_CODIGO = '||chr(39)||'FU'||chr(39)||' THEN '||chr(39)||'FUNCIONARIO'||chr(39)||'
                   WHEN CCP.CAD_TPE_CD_CODIGO = '||chr(39)||'PA'||chr(39)||' THEN '||chr(39)||'PARTICULAR'||chr(39)||'
                   WHEN CCP.CAD_TPE_CD_CODIGO = '||chr(39)||'NP'||chr(39)||' THEN '||chr(39)||'NAO PAGANTE'||chr(39)||'
                   WHEN CCP.CAD_TPE_CD_CODIGO = '||chr(39)||'SP'||chr(39)||' THEN '||chr(39)||'SERVICO PRESTADO'||chr(39)||'
                   WHEN CCP.CAD_TPE_CD_CODIGO = '||chr(39)||'ACS'||chr(39)||' THEN '||chr(39)||'ANA COSTA SAUDE'||chr(39)||'
              END,UNI.CAD_UNI_DS_UNIDADE,
              CASE WHEN CCI.ATD_ATE_TP_PACIENTE = '||chr(39)||'I'||chr(39)||' THEN '||chr(39)||'INTERNO'||chr(39)||' ELSE '||chr(39)||'EXTERNO'||chr(39)||' END,
              SETOR.CAD_SET_DS_SETOR'
  ;
    TESTE :=  V_SELECT ;
  OPEN v_cursor FOR
   V_SELECT ;
    io_cursor := v_cursor;
END PRC_FAT_REL_CIRUR_PORTE;
 