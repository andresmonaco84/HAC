 
--PRC_CAD_GRP_GRADUACAO_PROF_L
create or replace procedure PRC_CAD_GRP_GRADUACAO_PROF_L 
(
     pCAD_GRP_ID IN TB_CAD_GRP_GRADUACAO_PROF.CAD_GRP_ID%type DEFAULT NULL,
     pCAD_GRP_TP_CURSO IN TB_CAD_GRP_GRADUACAO_PROF.CAD_GRP_TP_CURSO%type DEFAULT NULL,
     pCAD_GRP_NM_ENTIDADE_CURSO IN TB_CAD_GRP_GRADUACAO_PROF.CAD_GRP_NM_ENTIDADE_CURSO%type DEFAULT NULL,
     pCAD_GRP_NR_ANO_CONCLUSAO IN TB_CAD_GRP_GRADUACAO_PROF.CAD_GRP_NR_ANO_CONCLUSAO%type DEFAULT NULL,
     pCAD_GRP_DT_ULTIMA_ATUALIZACAO IN TB_CAD_GRP_GRADUACAO_PROF.CAD_GRP_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_GRP_GRADUACAO_PROF.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_PRO_ID_PROFISSIONAL IN TB_CAD_GRP_GRADUACAO_PROF.CAD_PRO_ID_PROFISSIONAL%type DEFAULT NULL,
     pCAD_PRO_FL_RECONHECIDA_MEC IN TB_CAD_GRP_GRADUACAO_PROF.CAD_PRO_FL_RECONHECIDA_MEC%type DEFAULT NULL,
     pTIS_CBO_CD_CBOS IN TB_CAD_GRP_GRADUACAO_PROF.TIS_CBO_CD_CBOS%type DEFAULT NULL,
     pCAD_PRO_FL_ESPECIALISTA IN TB_CAD_GRP_GRADUACAO_PROF.CAD_PRO_FL_ESPECIALISTA%type DEFAULT NULL,
     pCAD_GRP_DT_REVALID_CURSO IN TB_CAD_GRP_GRADUACAO_PROF.CAD_GRP_DT_REVALID_CURSO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_GRP_GRADUACAO_PROF_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_GRP_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_GRP_ID = ' || pCAD_GRP_ID; END IF;
IF pCAD_GRP_TP_CURSO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_GRP_TP_CURSO = ' || pCAD_GRP_TP_CURSO; END IF;
IF pCAD_GRP_NM_ENTIDADE_CURSO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_GRP_NM_ENTIDADE_CURSO = ' || CHR(39) || pCAD_GRP_NM_ENTIDADE_CURSO || CHR(39); END IF;
IF pCAD_GRP_NR_ANO_CONCLUSAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_GRP_NR_ANO_CONCLUSAO = ' || pCAD_GRP_NR_ANO_CONCLUSAO; END IF;
IF pCAD_GRP_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_GRP_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_GRP_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pCAD_PRO_ID_PROFISSIONAL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRO_ID_PROFISSIONAL = ' || pCAD_PRO_ID_PROFISSIONAL; END IF;
IF pCAD_PRO_FL_RECONHECIDA_MEC IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRO_FL_RECONHECIDA_MEC = ' || CHR(39) || pCAD_PRO_FL_RECONHECIDA_MEC || CHR(39); END IF;
IF pTIS_CBO_CD_CBOS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_CBO_CD_CBOS = ' || CHR(39) || pTIS_CBO_CD_CBOS || CHR(39); END IF;
IF pCAD_PRO_FL_ESPECIALISTA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRO_FL_ESPECIALISTA = ' || CHR(39) || pCAD_PRO_FL_ESPECIALISTA || CHR(39); END IF;
IF pCAD_GRP_DT_REVALID_CURSO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_GRP_DT_REVALID_CURSO = ' || CHR(39) || pCAD_GRP_DT_REVALID_CURSO || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       CAD_GRP_ID,
       CAD_GRP_TP_CURSO,
       CAD_GRP_NM_ENTIDADE_CURSO,
       CAD_GRP_NR_ANO_CONCLUSAO,
       CAD_GRP_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       CAD_PRO_ID_PROFISSIONAL,
       CAD_PRO_FL_RECONHECIDA_MEC,
       TIS_CBO_CD_CBOS,
       CAD_PRO_FL_ESPECIALISTA,
       CAD_GRP_DT_REVALID_CURSO
FROM TB_CAD_GRP_GRADUACAO_PROF
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_GRP_GRADUACAO_PROF_L;
