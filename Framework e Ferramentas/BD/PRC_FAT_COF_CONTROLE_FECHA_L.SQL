 
--PRC_FAT_COF_CONTROLE_FECHA_L
create or replace procedure PRC_FAT_COF_CONTROLE_FECHA_L 
(
     pCAD_CNV_ID_CONVENIO IN TB_FAT_COF_CONTROLE_FECHA.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pCAD_UNI_ID_UNIDADE IN TB_FAT_COF_CONTROLE_FECHA.CAD_UNI_ID_UNIDADE%type DEFAULT NULL,
     pFAT_COF_NR_MES_FATURAMENTO IN TB_FAT_COF_CONTROLE_FECHA.FAT_COF_NR_MES_FATURAMENTO%type DEFAULT NULL,
     pFAT_COF_NR_ANO_FATURAMENTO IN TB_FAT_COF_CONTROLE_FECHA.FAT_COF_NR_ANO_FATURAMENTO%type DEFAULT NULL,
     pFAT_COF_FL_SITUACAO_FECHA IN TB_FAT_COF_CONTROLE_FECHA.FAT_COF_FL_SITUACAO_FECHA%type DEFAULT NULL,
     pFAT_COF_DT_ULTIMA_ATUALIZACAO IN TB_FAT_COF_CONTROLE_FECHA.FAT_COF_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_FAT_COF_CONTROLE_FECHA.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pATD_ATE_TP_PACIENTE IN TB_FAT_COF_CONTROLE_FECHA.ATD_ATE_TP_PACIENTE%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_FAT_COF_CONTROLE_FECHA_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
IF pCAD_UNI_ID_UNIDADE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_UNI_ID_UNIDADE = ' || pCAD_UNI_ID_UNIDADE; END IF;
IF pFAT_COF_NR_MES_FATURAMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_COF_NR_MES_FATURAMENTO = ' || pFAT_COF_NR_MES_FATURAMENTO; END IF;
IF pFAT_COF_NR_ANO_FATURAMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_COF_NR_ANO_FATURAMENTO = ' || pFAT_COF_NR_ANO_FATURAMENTO; END IF;
IF pFAT_COF_FL_SITUACAO_FECHA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_COF_FL_SITUACAO_FECHA = ' || CHR(39) || pFAT_COF_FL_SITUACAO_FECHA || CHR(39); END IF;
IF pFAT_COF_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_COF_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pFAT_COF_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pATD_ATE_TP_PACIENTE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_ATE_TP_PACIENTE = ' || CHR(39) || pATD_ATE_TP_PACIENTE || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       CAD_CNV_ID_CONVENIO,
       CAD_UNI_ID_UNIDADE,
       FAT_COF_NR_MES_FATURAMENTO,
       FAT_COF_NR_ANO_FATURAMENTO,
       FAT_COF_FL_SITUACAO_FECHA,
       FAT_COF_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       ATD_ATE_TP_PACIENTE
FROM TB_FAT_COF_CONTROLE_FECHA
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_FAT_COF_CONTROLE_FECHA_L;
