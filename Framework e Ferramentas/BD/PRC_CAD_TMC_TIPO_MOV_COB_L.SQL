create or replace procedure PRC_CAD_TMC_TIPO_MOV_COB_L
(
     pCAD_TMC_ID IN TB_CAD_TMC_TIPO_MOV_COBRANCA.CAD_TMC_ID%type DEFAULT NULL,
     pCAD_TMC_DS_DESCRICAO IN TB_CAD_TMC_TIPO_MOV_COBRANCA.CAD_TMC_DS_DESCRICAO%type DEFAULT NULL,
     pCAD_TMC_DT_CRIACAO IN TB_CAD_TMC_TIPO_MOV_COBRANCA.CAD_TMC_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_CAD_TMC_TIPO_MOV_COBRANCA.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pCAD_TMC_DT_ULTIMA_ATUALIZACAO IN TB_CAD_TMC_TIPO_MOV_COBRANCA.CAD_TMC_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ATUALIZ IN TB_CAD_TMC_TIPO_MOV_COBRANCA.SEG_USU_ID_USUARIO_ATUALIZ%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
)
is
/********************************************************************
*    Procedure: PRC_CAD_TMC_TIPO_MOV_COB_L
*
*    Data Criacao: 	30/03/2012   Por: André
*
*    Funcao: lista tipo movimento cobrança
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_TMC_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TMC_ID = ' || pCAD_TMC_ID; END IF;
IF pCAD_TMC_DS_DESCRICAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TMC_DS_DESCRICAO = ' || CHR(39) || pCAD_TMC_DS_DESCRICAO || CHR(39); END IF;
IF pCAD_TMC_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TMC_DT_CRIACAO = ' || CHR(39) || pCAD_TMC_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; END IF;
IF pCAD_TMC_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TMC_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_TMC_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ATUALIZ = ' || pSEG_USU_ID_USUARIO_ATUALIZ; END IF;

   V_SELECT := '
SELECT
       CAD_TMC_ID,
       CAD_TMC_DS_DESCRICAO,
       CAD_TMC_DT_CRIACAO,
       SEG_USU_ID_USUARIO_CRIACAO,
       CAD_TMC_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO_ATUALIZ
FROM TB_CAD_TMC_TIPO_MOV_COBRANCA
WHERE null is null  '    ;

OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_TMC_TIPO_MOV_COB_L;
