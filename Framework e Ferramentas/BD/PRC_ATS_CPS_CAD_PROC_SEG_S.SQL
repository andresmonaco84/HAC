--PRC_ATS_CPS_CAD_PROC_SEG_S
create or replace procedure PRC_ATS_CPS_CAD_PROC_SEG_S 
(
     pCAD_PRD_ID IN TB_ATS_CPS_CAD_PROC_SEGMENTO.CAD_PRD_ID%type DEFAULT NULL,
     pATS_CPS_CD_SEGMENTO IN TB_ATS_CPS_CAD_PROC_SEGMENTO.ATS_CPS_CD_SEGMENTO%type DEFAULT NULL,
     pATS_CPS_DS_SEGMENTO IN TB_ATS_CPS_CAD_PROC_SEGMENTO.ATS_CPS_DS_SEGMENTO%type DEFAULT NULL,
     pATS_CPS_DT_ULTIMA_ATUALIZACAO IN TB_ATS_CPS_CAD_PROC_SEGMENTO.ATS_CPS_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ATS_CPS_CAD_PROC_SEGMENTO.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pATS_CPS_FL_STATUS IN TB_ATS_CPS_CAD_PROC_SEGMENTO.ATS_CPS_FL_STATUS%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ATS_CPS_CAD_PROC_SEG_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_PRD_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_ID = ' || pCAD_PRD_ID; END IF;
IF pATS_CPS_CD_SEGMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_CPS_CD_SEGMENTO = ' || CHR(39) || pATS_CPS_CD_SEGMENTO || CHR(39); END IF;
IF pATS_CPS_DS_SEGMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_CPS_DS_SEGMENTO = ' || CHR(39) || pATS_CPS_DS_SEGMENTO || CHR(39); END IF;
IF pATS_CPS_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_CPS_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pATS_CPS_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pATS_CPS_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_CPS_FL_STATUS = ' || CHR(39) || pATS_CPS_FL_STATUS || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       CAD_PRD_ID,
       ATS_CPS_CD_SEGMENTO,
       ATS_CPS_DS_SEGMENTO,
       ATS_CPS_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       ATS_CPS_FL_STATUS
FROM TB_ATS_CPS_CAD_PROC_SEGMENTO
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ATS_CPS_CAD_PROC_SEG_S;
