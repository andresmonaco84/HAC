--PRC_COB_RPG_RETENCAO_PAGTO_GUIA_L
create or replace procedure PRC_COB_RPG_RETENC_PAG_GUIA_L 
(
     pCOB_RPG_ID IN TB_COB_RPG_RETENCAO_PAGTO_GUIA.COB_RPG_ID%type DEFAULT NULL,
     pCOB_PAG_ID IN TB_COB_RPG_RETENCAO_PAGTO_GUIA.COB_PAG_ID%type DEFAULT NULL,
     pASS_CUI_ID IN TB_COB_RPG_RETENCAO_PAGTO_GUIA.ASS_CUI_ID%type DEFAULT NULL,
     pCOB_RPG_VL_RETENCAO IN TB_COB_RPG_RETENCAO_PAGTO_GUIA.COB_RPG_VL_RETENCAO%type DEFAULT NULL,
     pCOB_RPG_DT_CRIACAO IN TB_COB_RPG_RETENCAO_PAGTO_GUIA.COB_RPG_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_COB_RPG_RETENCAO_PAGTO_GUIA.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pCOB_RPG_DT_ULTIMA_ATUALIZACAO IN TB_COB_RPG_RETENCAO_PAGTO_GUIA.COB_RPG_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ATUALIZ IN TB_COB_RPG_RETENCAO_PAGTO_GUIA.SEG_USU_ID_USUARIO_ATUALIZ%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_COB_RPG_RETENCAO_PAGTO_GUIA_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCOB_RPG_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_RPG_ID = ' || pCOB_RPG_ID; END IF;
IF pCOB_PAG_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_PAG_ID = ' || pCOB_PAG_ID; END IF;
IF pASS_CUI_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CUI_ID = ' || pASS_CUI_ID; END IF;
IF pCOB_RPG_VL_RETENCAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_RPG_VL_RETENCAO = ' || pCOB_RPG_VL_RETENCAO; END IF;
IF pCOB_RPG_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_RPG_DT_CRIACAO = ' || CHR(39) || pCOB_RPG_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; END IF;
IF pCOB_RPG_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_RPG_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCOB_RPG_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ATUALIZ = ' || pSEG_USU_ID_USUARIO_ATUALIZ; END IF;
 
   V_SELECT := '
SELECT	
       COB_RPG_ID,
       COB_PAG_ID,
       ASS_CUI_ID,
       COB_RPG_VL_RETENCAO,
       COB_RPG_DT_CRIACAO,
       SEG_USU_ID_USUARIO_CRIACAO,
       COB_RPG_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO_ATUALIZ
FROM TB_COB_RPG_RETENCAO_PAGTO_GUIA
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_COB_RPG_RETENC_PAG_GUIA_L;
/
