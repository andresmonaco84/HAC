create or replace procedure PRC_CAD_CCR_CCONTA_RM_L
(
     pCAD_CCR_ID IN TB_CAD_CCR_CCONTA_RM.CAD_CCR_ID%type DEFAULT NULL,
     pCAD_CCR_CD_COLIGADA IN TB_CAD_CCR_CCONTA_RM.CAD_CCR_CD_COLIGADA%type DEFAULT NULL,
     pCAD_CCR_CD_CONTA IN TB_CAD_CCR_CCONTA_RM.CAD_CCR_CD_CONTA%type DEFAULT NULL,
     pCAD_CCR_CD_REDUZIDO IN TB_CAD_CCR_CCONTA_RM.CAD_CCR_CD_REDUZIDO%type DEFAULT NULL,
     pCAD_CCR_DS_DESCRICAO IN TB_CAD_CCR_CCONTA_RM.CAD_CCR_DS_DESCRICAO%type DEFAULT NULL,
     pCAD_CCR_FL_ANALIT_SINT IN TB_CAD_CCR_CCONTA_RM.CAD_CCR_FL_ANALIT_SINT%type DEFAULT NULL,
     pCAD_CCR_FL_RATEIO IN TB_CAD_CCR_CCONTA_RM.CAD_CCR_FL_RATEIO%type DEFAULT NULL,
     pCAD_CCR_FL_NATUREZA IN TB_CAD_CCR_CCONTA_RM.CAD_CCR_FL_NATUREZA%type DEFAULT NULL,
     pCAD_CCR_FL_STATUS IN TB_CAD_CCR_CCONTA_RM.CAD_CCR_FL_STATUS%type DEFAULT NULL,
     pSEG_USU_ID_INCLUSAO IN TB_CAD_CCR_CCONTA_RM.SEG_USU_ID_INCLUSAO%type DEFAULT NULL,
     pCAD_CCR_DT_INCLUSAO IN TB_CAD_CCR_CCONTA_RM.CAD_CCR_DT_INCLUSAO%type DEFAULT NULL,
     pSEG_USU_ID_ALTERACAO IN TB_CAD_CCR_CCONTA_RM.SEG_USU_ID_ALTERACAO%type DEFAULT NULL,
     pCAD_CCR_DT_ALTERACAO IN TB_CAD_CCR_CCONTA_RM.CAD_CCR_DT_ALTERACAO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
)
is
/********************************************************************
*    Procedure: PRC_CAD_CCR_CCONTA_RM_L
*
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  
  IF pCAD_CCR_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CCR_ID = ' || pCAD_CCR_ID; END IF;
  IF pCAD_CCR_CD_COLIGADA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CCR_CD_COLIGADA = ' || pCAD_CCR_CD_COLIGADA; END IF;
  IF pCAD_CCR_CD_CONTA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CCR_CD_CONTA = ' || CHR(39) || pCAD_CCR_CD_CONTA || CHR(39); END IF;
  IF pCAD_CCR_CD_REDUZIDO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CCR_CD_REDUZIDO = ' || CHR(39) || pCAD_CCR_CD_REDUZIDO || CHR(39); END IF;
  IF pCAD_CCR_DS_DESCRICAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CCR_DS_DESCRICAO = ' || CHR(39) || pCAD_CCR_DS_DESCRICAO || CHR(39); END IF;
  IF pCAD_CCR_FL_ANALIT_SINT IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CCR_FL_ANALIT_SINT = ' || pCAD_CCR_FL_ANALIT_SINT; END IF;
  IF pCAD_CCR_FL_RATEIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CCR_FL_RATEIO = ' || pCAD_CCR_FL_RATEIO; END IF;
  IF pCAD_CCR_FL_NATUREZA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CCR_FL_NATUREZA = ' || pCAD_CCR_FL_NATUREZA; END IF;
  IF pCAD_CCR_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CCR_FL_STATUS = ' || CHR(39) || pCAD_CCR_FL_STATUS || CHR(39); END IF;
  IF pSEG_USU_ID_INCLUSAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_INCLUSAO = ' || pSEG_USU_ID_INCLUSAO; END IF;
  IF pCAD_CCR_DT_INCLUSAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CCR_DT_INCLUSAO = ' || pCAD_CCR_DT_INCLUSAO; END IF;
  IF pSEG_USU_ID_ALTERACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_ALTERACAO = ' || pSEG_USU_ID_ALTERACAO; END IF;
  IF pCAD_CCR_DT_ALTERACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CCR_DT_ALTERACAO = ' || pCAD_CCR_DT_ALTERACAO; END IF;

   V_SELECT := '
SELECT
       CAD_CCR_ID,
       CAD_CCR_CD_COLIGADA,
       CAD_CCR_CD_CONTA,
       CAD_CCR_CD_REDUZIDO,
       CAD_CCR_DS_DESCRICAO,
       CAD_CCR_FL_ANALIT_SINT,
       CAD_CCR_FL_RATEIO,
       CAD_CCR_FL_NATUREZA,
       CAD_CCR_FL_STATUS,
       SEG_USU_ID_INCLUSAO,
       CAD_CCR_DT_INCLUSAO,
       SEG_USU_ID_ALTERACAO,
       CAD_CCR_DT_ALTERACAO
FROM TB_CAD_CCR_CCONTA_RM
WHERE null is null  '    ;

OPEN v_cursor FOR
  V_SELECT || V_WHERE || ' ORDER BY CAD_CCR_CD_CONTA' ;
  io_cursor := v_cursor;
end PRC_CAD_CCR_CCONTA_RM_L;
/
