 
--PRC_COB_BPC_BANCO_PAGTO_CONV_L
create or replace procedure PRC_COB_BPC_BANCO_PAGTO_CONV_L 
(
     pCOB_BPC_ID IN TB_COB_BPC_BANCO_PAGTO_CONV.COB_BPC_ID%type DEFAULT NULL,
     pASS_CNU_ID IN TB_COB_BPC_BANCO_PAGTO_CONV.ASS_CNU_ID%type DEFAULT NULL,
     pASS_BCT_ID IN TB_COB_BPC_BANCO_PAGTO_CONV.ASS_BCT_ID%type DEFAULT NULL,
     pCOB_BPC_DT_INI_VIGENCIA IN TB_COB_BPC_BANCO_PAGTO_CONV.COB_BPC_DT_INI_VIGENCIA%type DEFAULT NULL,
     pCOB_BPC_DT_FIM_VIGENCIA IN TB_COB_BPC_BANCO_PAGTO_CONV.COB_BPC_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pCOB_BPC_DT_CRIACAO IN TB_COB_BPC_BANCO_PAGTO_CONV.COB_BPC_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_COB_BPC_BANCO_PAGTO_CONV.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pCOB_BPC_DT_ULTIMA_ATUALIZACAO IN TB_COB_BPC_BANCO_PAGTO_CONV.COB_BPC_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ATUALIZ IN TB_COB_BPC_BANCO_PAGTO_CONV.SEG_USU_ID_USUARIO_ATUALIZ%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_COB_BPC_BANCO_PAGTO_CONV_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCOB_BPC_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_BPC_ID = ' || pCOB_BPC_ID; END IF;
IF pASS_CNU_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CNU_ID = ' || pASS_CNU_ID; END IF;
IF pASS_BCT_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_BCT_ID = ' || pASS_BCT_ID; END IF;
IF pCOB_BPC_DT_INI_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_BPC_DT_INI_VIGENCIA = ' || CHR(39) || pCOB_BPC_DT_INI_VIGENCIA || CHR(39); END IF;
IF pCOB_BPC_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_BPC_DT_FIM_VIGENCIA = ' || CHR(39) || pCOB_BPC_DT_FIM_VIGENCIA || CHR(39); END IF;
IF pCOB_BPC_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_BPC_DT_CRIACAO = ' || CHR(39) || pCOB_BPC_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; END IF;
IF pCOB_BPC_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_BPC_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCOB_BPC_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ATUALIZ = ' || pSEG_USU_ID_USUARIO_ATUALIZ; END IF;
 
   V_SELECT := '
SELECT	
       COB_BPC_ID,
       ASS_CNU_ID,
       ASS_BCT_ID,
       COB_BPC_DT_INI_VIGENCIA,
       COB_BPC_DT_FIM_VIGENCIA,
       COB_BPC_DT_CRIACAO,
       SEG_USU_ID_USUARIO_CRIACAO,
       COB_BPC_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO_ATUALIZ
FROM TB_COB_BPC_BANCO_PAGTO_CONV
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_COB_BPC_BANCO_PAGTO_CONV_L;
/
