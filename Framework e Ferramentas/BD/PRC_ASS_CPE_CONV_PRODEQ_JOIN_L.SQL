CREATE OR REPLACE PROCEDURE PRC_ASS_CPE_CONV_PRODEQ_JOIN_L
  (
     pASS_CPE_ID IN TB_ASS_CPE_CONV_PROD_EQUIVALE.ASS_CPE_ID%type DEFAULT NULL,
     pCAD_CNV_ID_CONVENIO IN TB_ASS_CPE_CONV_PROD_EQUIVALE.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_ASS_CPE_CONV_PROD_EQUIVALE.CAD_LAT_ID_LOCAL_ATENDIMENTO%type DEFAULT NULL,
     pCAD_PRD_ID_ORIGEM IN TB_ASS_CPE_CONV_PROD_EQUIVALE.CAD_PRD_ID_ORIGEM%type DEFAULT NULL,
     pCAD_PRD_ID_DESTINO IN TB_ASS_CPE_CONV_PROD_EQUIVALE.CAD_PRD_ID_DESTINO%type DEFAULT NULL,
     pCAD_CPE_DT_INICIO_VIGENCIA IN TB_ASS_CPE_CONV_PROD_EQUIVALE.CAD_CPE_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pCAD_CPE_DT_FIM_VIGENCIA IN TB_ASS_CPE_CONV_PROD_EQUIVALE.CAD_CPE_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pCAD_CPE_DT_ULTIMA_ATUALIZACAO IN TB_ASS_CPE_CONV_PROD_EQUIVALE.CAD_CPE_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_CPE_CONV_PROD_EQUIVALE.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCD_TABELAMEDICA_ORIGEM IN TB_CAD_PRD_PRODUTO.TIS_MED_CD_TABELAMEDICA%type DEFAULT NULL,
     pAUX_EPP_CD_ESPECPROC_ORIGEM IN TB_CAD_PRD_PRODUTO.AUX_EPP_CD_ESPECPROC%type DEFAULT NULL,
     --pAUX_GPC_CD_GRUPOPROC_ORIGEM IN TB_CAD_PRD_PRODUTO.AUX_GPC_CD_GRUPOPROC%type DEFAULT NULL,
     pCD_TABELAMEDICA_DESTINO IN TB_CAD_PRD_PRODUTO.TIS_MED_CD_TABELAMEDICA%type DEFAULT NULL,
     pAUX_EPP_CD_ESPECPROC_DESTINO IN TB_CAD_PRD_PRODUTO.AUX_EPP_CD_ESPECPROC%type DEFAULT NULL,
     --pAUX_GPC_CD_GRUPOPROC_DESTINO IN TB_CAD_PRD_PRODUTO.AUX_GPC_CD_GRUPOPROC%type DEFAULT NULL,
     pVIGENCIA IN VARCHAR2 DEFAULT NULL, -- 0 = TODOS; 1 = VIGENTES; -1 = NAO VIGENTES
     io_cursor OUT PKG_CURSOR.t_cursor
  )
  is
  /********************************************************************
  *    Procedure: PRC_ASS_CPE_CONV_PRODEQ_JOIN_L
  *
  *    Data Criacao:   ?            Por: ?
  *    Data Alteracao: 05/08/2013   Por: André
  *         Alteracao: Passei a query para o padrão de string e adição de parâmetros dos joins
  *
  *    Funcao: Descrição da funcionalidade da Stored Procedure
  *******************************************************************/
  v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
  begin
  
  V_WHERE := NULL;
  
  IF pASS_CPE_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPE.ASS_CPE_ID = ' || pASS_CPE_ID; END IF;
  IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPE.CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
  IF pCAD_LAT_ID_LOCAL_ATENDIMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPE.CAD_LAT_ID_LOCAL_ATENDIMENTO = ' || pCAD_LAT_ID_LOCAL_ATENDIMENTO; END IF;
  IF pCAD_PRD_ID_ORIGEM IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPE.CAD_PRD_ID_ORIGEM = ' || pCAD_PRD_ID_ORIGEM; END IF;
  IF pCAD_PRD_ID_DESTINO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPE.CAD_PRD_ID_DESTINO = ' || pCAD_PRD_ID_DESTINO; END IF;
  IF pCAD_CPE_DT_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPE.CAD_CPE_DT_INICIO_VIGENCIA = ' || CHR(39) || pCAD_CPE_DT_INICIO_VIGENCIA || CHR(39); END IF;
  IF pCAD_CPE_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPE.CAD_CPE_DT_FIM_VIGENCIA = ' || CHR(39) || pCAD_CPE_DT_FIM_VIGENCIA || CHR(39); END IF;
  IF pCAD_CPE_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPE.CAD_CPE_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_CPE_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
  IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CPE.SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
  IF pCD_TABELAMEDICA_ORIGEM IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND PRD_ORIGEM.TIS_MED_CD_TABELAMEDICA = ' || CHR(39) || pCD_TABELAMEDICA_ORIGEM || CHR(39); END IF;
  IF pAUX_EPP_CD_ESPECPROC_ORIGEM IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND PRD_ORIGEM.AUX_EPP_CD_ESPECPROC = ' || CHR(39) || pAUX_EPP_CD_ESPECPROC_ORIGEM || CHR(39); END IF;
  IF pCD_TABELAMEDICA_DESTINO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND PRD_DESTINO.TIS_MED_CD_TABELAMEDICA = ' || CHR(39) || pCD_TABELAMEDICA_DESTINO || CHR(39); END IF;
  IF pAUX_EPP_CD_ESPECPROC_DESTINO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND PRD_DESTINO.AUX_EPP_CD_ESPECPROC = ' || CHR(39) || pAUX_EPP_CD_ESPECPROC_DESTINO || CHR(39); END IF;
  IF (NVL(pVIGENCIA, '0') = '1') THEN
     V_WHERE := V_WHERE || ' AND (FNC_VALIDAR_VIGENCIA(CAD_CPE_DT_INICIO_VIGENCIA, CAD_CPE_DT_FIM_VIGENCIA) = 1 OR CAD_CPE_DT_INICIO_VIGENCIA > TRUNC(SYSDATE)) ';
  ELSIF (NVL(pVIGENCIA, '0') = '-1') THEN
     V_WHERE := V_WHERE || ' AND (FNC_VALIDAR_VIGENCIA(CAD_CPE_DT_INICIO_VIGENCIA, CAD_CPE_DT_FIM_VIGENCIA) = 0 AND CAD_CPE_DT_INICIO_VIGENCIA < TRUNC(SYSDATE)) ';
  END IF;
  
  V_SELECT := 'SELECT CPE.ASS_CPE_ID,
                       CPE.CAD_CNV_ID_CONVENIO,
                       CPE.CAD_LAT_ID_LOCAL_ATENDIMENTO,
                       CPE.CAD_PRD_ID_ORIGEM,
                       CPE.CAD_PRD_ID_DESTINO,
                       CPE.CAD_CPE_DT_INICIO_VIGENCIA,
                       CPE.CAD_CPE_DT_FIM_VIGENCIA,
                       CPE.CAD_CPE_DT_ULTIMA_ATUALIZACAO,
                       CPE.SEG_USU_ID_USUARIO,
                       PRD_ORIGEM.CAD_PRD_CD_CODIGO CAD_PRD_CD_CODIGO_ORIGEM,
                       PRD_ORIGEM.CAD_PRD_DS_DESCRICAO CAD_PRD_DS_DESCRICAO_ORIGEM,
                       PRD_DESTINO.CAD_PRD_CD_CODIGO CAD_PRD_CD_CODIGO_DESTINO,
                       PRD_DESTINO.CAD_PRD_DS_DESCRICAO CAD_PRD_DS_DESCRICAO_DESTINO,
                       CPE.CAD_CNV_ID_CONVENIO CAD_CNV_ID_CONVENIO,
                       LOC.CAD_LAT_DS_LOCAL_ATENDIMENTO
                  FROM TB_ASS_CPE_CONV_PROD_EQUIVALE CPE,
                       TB_CAD_PRD_PRODUTO PRD_ORIGEM,
                       TB_CAD_PRD_PRODUTO PRD_DESTINO,
                       TB_CAD_LAT_LOCAL_ATENDIMENTO LOC
                  WHERE (CPE.CAD_PRD_ID_ORIGEM = PRD_ORIGEM.CAD_PRD_ID) AND
                        (CPE.CAD_PRD_ID_DESTINO = PRD_DESTINO.CAD_PRD_ID) AND
                        (CPE.CAD_LAT_ID_LOCAL_ATENDIMENTO = LOC.CAD_LAT_ID_LOCAL_ATENDIMENTO(+)) ';
  OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;

end PRC_ASS_CPE_CONV_PRODEQ_JOIN_L;
 
