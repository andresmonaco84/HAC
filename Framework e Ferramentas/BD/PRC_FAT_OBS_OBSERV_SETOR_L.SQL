
--PRC_FAT_OBS_OBSERV_SETOR_L
create or replace procedure PRC_FAT_OBS_OBSERV_SETOR_L 
(
     pFAT_OBS_ID IN TB_FAT_OBS_OBSERV_SETOR.FAT_OBS_ID%type DEFAULT NULL,
     pFAT_OBS_DS_OBSERVACAO IN TB_FAT_OBS_OBSERV_SETOR.FAT_OBS_DS_OBSERVACAO%type DEFAULT NULL,
     pCAD_SET_ID_SETOR IN TB_FAT_OBS_OBSERV_SETOR.CAD_SET_ID_SETOR%type DEFAULT NULL,
     pFAT_OBS_FL_STATUS IN TB_FAT_OBS_OBSERV_SETOR.FAT_OBS_FL_STATUS%type DEFAULT NULL,
     pFAT_OBS_DT_ULTIMA_ATUALIZACAO IN TB_FAT_OBS_OBSERV_SETOR.FAT_OBS_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_FAT_OBS_OBSERV_SETOR.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_FAT_OBS_OBSERV_SETOR_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pFAT_OBS_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_OBS_ID = ' || pFAT_OBS_ID; END IF;
IF pFAT_OBS_DS_OBSERVACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_OBS_DS_OBSERVACAO = ' || CHR(39) || pFAT_OBS_DS_OBSERVACAO || CHR(39); END IF;
IF pCAD_SET_ID_SETOR IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_SET_ID_SETOR = ' || pCAD_SET_ID_SETOR; END IF;
IF pFAT_OBS_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_OBS_FL_STATUS = ' || CHR(39) || pFAT_OBS_FL_STATUS || CHR(39); END IF;
IF pFAT_OBS_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_OBS_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pFAT_OBS_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       FAT_OBS_ID,
       FAT_OBS_DS_OBSERVACAO,
       CAD_SET_ID_SETOR,
       FAT_OBS_FL_STATUS,
       FAT_OBS_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO
FROM TB_FAT_OBS_OBSERV_SETOR
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_FAT_OBS_OBSERV_SETOR_L;
