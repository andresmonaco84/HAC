--PRC_CAD_CMM_CARACT_MATMED_L
create or replace procedure PRC_CAD_CMM_CARACT_MATMED_L 
(
     pCAD_CMM_CD_CARACMATMED IN TB_CAD_CMM_CARACT_MATMED.CAD_CMM_CD_CARACMATMED%type DEFAULT NULL,
     pCAD_CMM_DS_CARACMATMED IN TB_CAD_CMM_CARACT_MATMED.CAD_CMM_DS_CARACMATMED%type DEFAULT NULL,
     pCAD_CMM_FL_STATUS IN TB_CAD_CMM_CARACT_MATMED.CAD_CMM_FL_STATUS%type DEFAULT NULL,
     pCAD_CMM_DT_ULTIMA_ATUALIZACAO IN TB_CAD_CMM_CARACT_MATMED.CAD_CMM_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_CMM_CARACT_MATMED.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_CMM_CARACT_MATMED_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_CMM_CD_CARACMATMED IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CMM_CD_CARACMATMED = ' || pCAD_CMM_CD_CARACMATMED; END IF;
IF pCAD_CMM_DS_CARACMATMED IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CMM_DS_CARACMATMED = ' || CHR(39) || pCAD_CMM_DS_CARACMATMED || CHR(39); END IF;
IF pCAD_CMM_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CMM_FL_STATUS = ' || CHR(39) || pCAD_CMM_FL_STATUS || CHR(39); END IF;
IF pCAD_CMM_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CMM_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_CMM_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       CAD_CMM_CD_CARACMATMED,
       CAD_CMM_DS_CARACMATMED,
       CAD_CMM_FL_STATUS,
       CAD_CMM_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO
FROM TB_CAD_CMM_CARACT_MATMED
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_CMM_CARACT_MATMED_L;
