  --PRC_ASS_CNV_UNIDADE_RMT_S
  create or replace procedure PRC_ASS_CNV_UNIDADE_RMT_S 
  (
     pASS_CNU_ID IN TB_ASS_CNU_CONVENIO_UNIDADE.ASS_CNU_ID%type DEFAULT NULL,
     pASS_CNU_DT_INI_VIGENCIA IN TB_ASS_CNU_CONVENIO_UNIDADE.ASS_CNU_DT_INI_VIGENCIA%type DEFAULT NULL,
     pASS_CNU_DT_FIM_VIGENCIA IN TB_ASS_CNU_CONVENIO_UNIDADE.ASS_CNU_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pASS_CNU_DS_MOT_FIM_VIGENCIA IN TB_ASS_CNU_CONVENIO_UNIDADE.ASS_CNU_DS_MOTIVO_FIM_VIGENCIA%type DEFAULT NULL,
     pASS_CNU_DT_ULTIMA_ATUALIZACAO IN TB_ASS_CNU_CONVENIO_UNIDADE.ASS_CNU_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pCAD_CNV_ID_CONVENIO IN TB_ASS_CNU_CONVENIO_UNIDADE.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pCAD_UNI_ID_UNIDADE IN TB_ASS_CNU_CONVENIO_UNIDADE.CAD_UNI_ID_UNIDADE%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_CNU_CONVENIO_UNIDADE.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
  ) 
  is
  /********************************************************************
  *    Procedure: PRC_ASS_CNV_UNIDADE_RMT_S
  * 
  *    Data Criacao: 	data da  criação   Por: Nome do Analista
  *    Data Alteracao:	data da alteração  Por: Nome do Analista
  *
  *    Funcao: Descrição da funcionalidade da Stored Procedure
  *
  *******************************************************************/
  v_cursor PKG_CURSOR.t_cursor;
  begin
    OPEN v_cursor FOR
    SELECT	
       ASS_CNU_ID,
       ASS_CNU_DT_INI_VIGENCIA,
       ASS_CNU_DT_FIM_VIGENCIA,
       ASS_CNU_DS_MOTIVO_FIM_VIGENCIA,
       ASS_CNU_DT_ULTIMA_ATUALIZACAO,
       CAD_CNV_ID_CONVENIO,
       CAD_UNI_ID_UNIDADE,
       SEG_USU_ID_USUARIO
    FROM TB_ASS_CNU_CONVENIO_UNIDADE
    WHERE
        (pASS_CNU_ID is null OR ASS_CNU_ID = pASS_CNU_ID) AND 
        (pASS_CNU_DT_INI_VIGENCIA is null OR ASS_CNU_DT_INI_VIGENCIA = pASS_CNU_DT_INI_VIGENCIA) AND 
        (pASS_CNU_DT_FIM_VIGENCIA is null OR ASS_CNU_DT_FIM_VIGENCIA = pASS_CNU_DT_FIM_VIGENCIA) AND 
        (pASS_CNU_DS_MOT_FIM_VIGENCIA is null OR ASS_CNU_DS_MOTIVO_FIM_VIGENCIA = pASS_CNU_DS_MOT_FIM_VIGENCIA) AND 
        (pASS_CNU_DT_ULTIMA_ATUALIZACAO is null OR ASS_CNU_DT_ULTIMA_ATUALIZACAO = pASS_CNU_DT_ULTIMA_ATUALIZACAO) AND 
        (pCAD_CNV_ID_CONVENIO is null OR CAD_CNV_ID_CONVENIO = pCAD_CNV_ID_CONVENIO) AND 
        (pCAD_UNI_ID_UNIDADE is null OR CAD_UNI_ID_UNIDADE = pCAD_UNI_ID_UNIDADE) AND 
        (pSEG_USU_ID_USUARIO is null OR SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO);          
    io_cursor := v_cursor;
  end PRC_ASS_CNV_UNIDADE_RMT_S;
