            AND   PES.CAD_PES_DT_NASCIMENTO              = pASS_PMD_DT_NASCIMENTO -- TO_DATE('29111964','DDMMYYYY')
            AND   PES.CAD_PES_TP_SEXO                    = pASS_PMD_TP_SEXO
