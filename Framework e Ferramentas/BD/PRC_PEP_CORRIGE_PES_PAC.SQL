   ( ppID, 
     ppTIPO_CORRECAO,      ppCAD_PES_ID_PESSOA_ERRADA,  ppCAD_PES_ID_PESSOA_CERTA,
     ppATD_ATE_ID,         ppCAD_PAC_ID_PACIENTE_CERTO, ppCAD_PAC_ID_PACIENTE_ERRADO,
     SYSDATE,              ppROTINA,                    ppSEG_USU_ID_USUARIO,
