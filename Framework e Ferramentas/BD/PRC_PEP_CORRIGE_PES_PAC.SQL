FUNCTION GERA_MD5(sNome IN VARCHAR, dData IN DATE, sSexo IN CHAR) RETURN VARCHAR2 IS
nome VARCHAR2(100);
data VARCHAR2(8);
BEGIN
     BEGIN
          SELECT TRIM(REPLACE(sNome,' ','')) , TO_CHAR(dData,'DDMMYYYY')
          INTO   nome,                         data
          FROM DUAL;
     END;
     RETURN MD5(TRIM(nome||data||sSexo));
END GERA_MD5;
