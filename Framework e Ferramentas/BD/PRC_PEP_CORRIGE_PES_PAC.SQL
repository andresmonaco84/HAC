            SELECT  CAD_PES_ID_PESSOA,
                    CAD_PES_NM_PESSOA,      LENGTH(CAD_PES_NM_PESSOA) TAMA, 
                    CAD_PES_DT_NASCIMENTO,  CAD_PES_TP_SEXO,  
