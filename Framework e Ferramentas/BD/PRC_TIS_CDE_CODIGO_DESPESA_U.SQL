CREATE OR REPLACE PROCEDURE "PRC_TIS_CDE_CODIGO_DESPESA_U"
  (
     pTIS_CDE_CD_CODIGO_DESPESA     IN TB_TIS_CDE_CODIGO_DESPESA.TIS_CDE_CD_CODIGO_DESPESA%type,
     pTIS_CDE_DS_DESCRICAO_DESPESA  IN TB_TIS_CDE_CODIGO_DESPESA.TIS_CDE_DS_DESCRICAO_DESPESA%type,
     pTIS_CBE_DT_INI_VIGENCIA       IN TB_TIS_CDE_CODIGO_DESPESA.TIS_CBE_DT_INI_VIGENCIA%type,
     pTIS_CBE_DT_FIM_VIGENCIA       IN TB_TIS_CDE_CODIGO_DESPESA.TIS_CBE_DT_FIM_VIGENCIA%type default NULL,
     pTIS_CBE_FL_STATUS             IN TB_TIS_CDE_CODIGO_DESPESA.TIS_CBE_FL_STATUS%type,
     pTIS_CBE_DT_ULTIMA_ATUALIZACAO IN TB_TIS_CDE_CODIGO_DESPESA.TIS_CBE_DT_ULTIMA_ATUALIZACAO%type,
     pSEG_USU_ID_USUARIO            IN TB_TIS_CDE_CODIGO_DESPESA.SEG_USU_ID_USUARIO%type,
     pTIS_CDE_CD_CODIGO_DESPESA_V3  IN TB_TIS_CDE_CODIGO_DESPESA.TIS_CDE_CD_CODIGO_DESPESA_V3%type default NULL,
     pTIS_CDE_DS_OBSERVACAO         IN TB_TIS_CDE_CODIGO_DESPESA.TIS_CDE_DS_OBSERVACAO%type default NULL
  )
  is
  /********************************************************************
  *    Procedure: PRC_TIS_CDE_CODIGO_DESPESA_U
  *
  *    Data Criacao:  data da  criação   Por: Nome do Analista
  *    Data Alteracao: data da alteração  Por: Nome do Analista
  *
  *    Funcao: Descrição da funcionalidade da Stored Procedure
  *
  *    Data Alteração:  21/03/2014  Por: Andréa Cazuca
  *    Alteração:  Inclusão dos campos TIS_CDE_CDE_CODIGO_DESPESA_V3 E TIS_CDE_DS_OBSERVACAO
  *
  *******************************************************************/
  begin
    UPDATE TB_TIS_CDE_CODIGO_DESPESA
    SET
        TIS_CDE_DS_DESCRICAO_DESPESA = UPPER(pTIS_CDE_DS_DESCRICAO_DESPESA),
        TIS_CBE_DT_INI_VIGENCIA = pTIS_CBE_DT_INI_VIGENCIA,
        TIS_CBE_DT_FIM_VIGENCIA = pTIS_CBE_DT_FIM_VIGENCIA,
        TIS_CBE_FL_STATUS = pTIS_CBE_FL_STATUS,
        TIS_CBE_DT_ULTIMA_ATUALIZACAO = pTIS_CBE_DT_ULTIMA_ATUALIZACAO,
        SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO,
        TIS_CDE_CD_CODIGO_DESPESA_V3 = pTIS_CDE_CD_CODIGO_DESPESA_V3,
        TIS_CDE_DS_OBSERVACAO = UPPER(pTIS_CDE_DS_OBSERVACAO)
    WHERE
        TIS_CDE_CD_CODIGO_DESPESA = pTIS_CDE_CD_CODIGO_DESPESA;
  end PRC_TIS_CDE_CODIGO_DESPESA_U;
 