CREATE OR REPLACE PROCEDURE PRC_CAD_CTS_FECHA_PERIODO_D(PCAD_CTS_ANO_FECHAMENTO IN TB_CAD_CTS_FECHA_PERIODO.CAD_CTS_ANO_FECHAMENTO%TYPE,
                                                        PCAD_CTS_MES_FECHAMENTO IN TB_CAD_CTS_FECHA_PERIODO.CAD_CTS_MES_FECHAMENTO%TYPE,
                                                        PCAD_CTS_STATUS         IN TB_CAD_CTS_FECHA_PERIODO.CAD_CTS_STATUS%TYPE) IS
  /********************************************************************
  *    PROCEDURE: PRC_CAD_CTS_FECHA_PERIODO_D
  * 
  *    DATA CRIACAO:  DATA DA  CRIAÇÃO   POR: NOME DO ANALISTA
  *    DATA ALTERACAO:  DATA DA ALTERAÇÃO  POR: NOME DO ANALISTA
  *
  *    FUNCAO: DESCRIÇÃO DA FUNCIONALIDADE DA STORED PROCEDURE
  *
  *******************************************************************/
BEGIN
  DELETE TB_CAD_CTS_FECHA_PERIODO
   WHERE CAD_CTS_ANO_FECHAMENTO = PCAD_CTS_ANO_FECHAMENTO
     AND CAD_CTS_MES_FECHAMENTO = PCAD_CTS_MES_FECHAMENTO
     AND CAD_CTS_STATUS = PCAD_CTS_STATUS;
END PRC_CAD_CTS_FECHA_PERIODO_D;
