 
--PRC_ASS_PRE_PROF_ESP_EXAME_L
create or replace procedure PRC_ASS_PRE_PROF_ESP_EXAME_L 
(
     pASS_PRE_ID IN TB_ASS_PRE_PROF_ESP_EXAME.ASS_PRE_ID%type DEFAULT NULL,
     pCAD_PRO_ID_PROFISSIONAL IN TB_ASS_PRE_PROF_ESP_EXAME.CAD_PRO_ID_PROFISSIONAL%type DEFAULT NULL,
     pAUX_EPP_CD_ESPECPROC IN TB_ASS_PRE_PROF_ESP_EXAME.AUX_EPP_CD_ESPECPROC%type DEFAULT NULL,
     pAUX_GPC_GRUPOPROC IN TB_ASS_PRE_PROF_ESP_EXAME.AUX_GPC_GRUPOPROC%type DEFAULT NULL,
     pASS_PRE_DT_ULTIMA_ATUALIZACAO IN TB_ASS_PRE_PROF_ESP_EXAME.ASS_PRE_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_PRE_PROF_ESP_EXAME.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pTIS_MED_CD_TABELAMEDICA IN TB_ASS_PRE_PROF_ESP_EXAME.TIS_MED_CD_TABELAMEDICA%type DEFAULT NULL,
     pASS_PRE_FL_ATIVO IN TB_ASS_PRE_PROF_ESP_EXAME.ASS_PRE_FL_ATIVO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ASS_PRE_PROF_ESP_EXAME_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pASS_PRE_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PRE_ID = ' || pASS_PRE_ID; END IF;
IF pCAD_PRO_ID_PROFISSIONAL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRO_ID_PROFISSIONAL = ' || pCAD_PRO_ID_PROFISSIONAL; END IF;
IF pAUX_EPP_CD_ESPECPROC IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AUX_EPP_CD_ESPECPROC = ' || CHR(39) || pAUX_EPP_CD_ESPECPROC || CHR(39); END IF;
IF pAUX_GPC_GRUPOPROC IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AUX_GPC_GRUPOPROC = ' || CHR(39) || pAUX_GPC_GRUPOPROC || CHR(39); END IF;
IF pASS_PRE_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PRE_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pASS_PRE_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pTIS_MED_CD_TABELAMEDICA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_MED_CD_TABELAMEDICA = ' || CHR(39) || pTIS_MED_CD_TABELAMEDICA || CHR(39); END IF;
IF pASS_PRE_FL_ATIVO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PRE_FL_ATIVO = ' || CHR(39) || pASS_PRE_FL_ATIVO || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       ASS_PRE_ID,
       CAD_PRO_ID_PROFISSIONAL,
       AUX_EPP_CD_ESPECPROC,
       AUX_GPC_GRUPOPROC,
       ASS_PRE_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       TIS_MED_CD_TABELAMEDICA,
       ASS_PRE_FL_ATIVO
FROM TB_ASS_PRE_PROF_ESP_EXAME
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ASS_PRE_PROF_ESP_EXAME_L;
