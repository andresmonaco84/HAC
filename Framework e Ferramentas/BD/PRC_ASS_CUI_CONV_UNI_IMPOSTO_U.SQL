create or replace procedure PRC_ASS_CUI_CONV_UNI_IMPOSTO_U
  (
     pASS_CUI_ID IN TB_ASS_CUI_CONV_UNI_IMPOSTO.ASS_CUI_ID%type,
     pASS_CUI_DT_INI_VIGENCIA IN TB_ASS_CUI_CONV_UNI_IMPOSTO.ASS_CUI_DT_INI_VIGENCIA%type,
     pASS_CUI_DT_FIM_VIGENCIA IN TB_ASS_CUI_CONV_UNI_IMPOSTO.ASS_CUI_DT_FIM_VIGENCIA%type default NULL,
     pASS_CNU_ID IN TB_ASS_CUI_CONV_UNI_IMPOSTO.ASS_CNU_ID%type,
     pASS_CUI_PC_IMPOSTO IN TB_ASS_CUI_CONV_UNI_IMPOSTO.ASS_CUI_PC_IMPOSTO%type,
     pASS_CUI_DS_MOTIVO_FIM_VIG IN TB_ASS_CUI_CONV_UNI_IMPOSTO.ASS_CUI_DS_MOTIVO_FIM_VIGENCIA%type default NULL,
     pCAD_IMP_ID_IMPOSTO IN TB_ASS_CUI_CONV_UNI_IMPOSTO.CAD_IMP_ID_IMPOSTO%type,
     pASS_CUI_VL_MIN_RECOLHIM IN TB_ASS_CUI_CONV_UNI_IMPOSTO.ASS_CUI_VL_MIN_RECOLHIM%type,
     pSEG_USU_ID_USUARIO IN TB_ASS_CUI_CONV_UNI_IMPOSTO.SEG_USU_ID_USUARIO%type,
     pASS_CUI_DT_ULTIMA_ATUALIZACAO IN TB_ASS_CUI_CONV_UNI_IMPOSTO.ASS_CUI_DT_ULTIMA_ATUALIZACAO%TYPE DEFAULT NULL
  )
  is
  /********************************************************************
  *    Procedure: PRC_ASS_CUI_CONV_UNI_IMPOSTO_U
  *
  *    Data Criacao: 	28/05/2007         Por: Fabiola R. Lopes
  *    Data Alteracao:			   Por:
  *
  *    Funcao: Alterar a Associacao do Convenio Unidade com Imposto
  *
  *******************************************************************/
  begin
    UPDATE TB_ASS_CUI_CONV_UNI_IMPOSTO
    SET
        ASS_CUI_DT_INI_VIGENCIA = pASS_CUI_DT_INI_VIGENCIA,
        ASS_CUI_DT_FIM_VIGENCIA = pASS_CUI_DT_FIM_VIGENCIA,
        ASS_CNU_ID = pASS_CNU_ID,
        ASS_CUI_PC_IMPOSTO = pASS_CUI_PC_IMPOSTO,
        ASS_CUI_DS_MOTIVO_FIM_VIGENCIA = UPPER(pASS_CUI_DS_MOTIVO_FIM_VIG),
        ASS_CUI_DT_ULTIMA_ATUALIZACAO = SYSDATE,
        CAD_IMP_ID_IMPOSTO = pCAD_IMP_ID_IMPOSTO,
        ASS_CUI_VL_MIN_RECOLHIM = pASS_CUI_VL_MIN_RECOLHIM,
        SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO
    WHERE
        ASS_CUI_ID = pASS_CUI_ID;
  end PRC_ASS_CUI_CONV_UNI_IMPOSTO_U;
