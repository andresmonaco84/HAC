
--PRC_SEG_FUN_FUNCIONALID_R_L
create or replace procedure PRC_SEG_FUN_FUNCIONALID_R_L 
(
     pSEG_FUN_ID_FUNCIONALIDADE IN TB_SEG_FUN_FUNCIONALIDADE.SEG_FUN_ID_FUNCIONALIDADE%type DEFAULT NULL,
     pSEG_FUN_DS_DESCRICAO IN TB_SEG_FUN_FUNCIONALIDADE.SEG_FUN_DS_DESCRICAO%type DEFAULT NULL,
     pSEG_FUN_FL_ITEM_MENU_OK IN TB_SEG_FUN_FUNCIONALIDADE.SEG_FUN_FL_ITEM_MENU_OK%type DEFAULT NULL,
     pSEG_FUN_ID_FUNCIONALIDADE_PAI IN TB_SEG_FUN_FUNCIONALIDADE.SEG_FUN_ID_FUNCIONALIDADE_PAI%type DEFAULT NULL,
     pSEG_FUN_DS_NOME_PAGINA IN TB_SEG_FUN_FUNCIONALIDADE.SEG_FUN_DS_NOME_PAGINA%type DEFAULT NULL,
     pSEG_FUN_NM_NOME IN TB_SEG_FUN_FUNCIONALIDADE.SEG_FUN_NM_NOME%type DEFAULT NULL,
     pSEG_MOD_ID_MODULO IN TB_SEG_FUN_FUNCIONALIDADE.SEG_MOD_ID_MODULO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_SEG_FUN_FUNCIONALID_R_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pSEG_FUN_ID_FUNCIONALIDADE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_FUN_ID_FUNCIONALIDADE = ' || pSEG_FUN_ID_FUNCIONALIDADE; END IF;
IF pSEG_FUN_DS_DESCRICAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_FUN_DS_DESCRICAO = ' || CHR(39) || pSEG_FUN_DS_DESCRICAO || CHR(39); END IF;
IF pSEG_FUN_FL_ITEM_MENU_OK IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_FUN_FL_ITEM_MENU_OK = ' || CHR(39) || pSEG_FUN_FL_ITEM_MENU_OK || CHR(39); END IF;
IF pSEG_FUN_ID_FUNCIONALIDADE_PAI IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_FUN_ID_FUNCIONALIDADE_PAI = ' || pSEG_FUN_ID_FUNCIONALIDADE_PAI; END IF;
IF pSEG_FUN_DS_NOME_PAGINA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_FUN_DS_NOME_PAGINA = ' || CHR(39) || pSEG_FUN_DS_NOME_PAGINA || CHR(39); END IF;
IF pSEG_FUN_NM_NOME IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_FUN_NM_NOME = ' || CHR(39) || pSEG_FUN_NM_NOME || CHR(39); END IF;
IF pSEG_MOD_ID_MODULO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_MOD_ID_MODULO = ' || pSEG_MOD_ID_MODULO; END IF;
 
   V_SELECT := '
SELECT	
       SEG_FUN_ID_FUNCIONALIDADE,
       SEG_FUN_DS_DESCRICAO,
       SEG_FUN_FL_ITEM_MENU_OK,
       SEG_FUN_ID_FUNCIONALIDADE_PAI,
       SEG_FUN_DS_NOME_PAGINA,
       SEG_FUN_NM_NOME,
       SEG_MOD_ID_MODULO
FROM TB_SEG_FUN_FUNCIONALIDADE
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_SEG_FUN_FUNCIONALID_R_L;
