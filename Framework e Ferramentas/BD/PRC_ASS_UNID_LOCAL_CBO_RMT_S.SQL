  --PRC_ASS_UNID_LOCAL_CBO_RMT_S
  create or replace procedure PRC_ASS_UNID_LOCAL_CBO_RMT_S 
  (
     pTIS_CBO_CD_CBOS IN TB_ASS_ULC_UNID_LOCAL_CBOS.TIS_CBO_CD_CBOS%type DEFAULT NULL,
     pASS_ULC_QTD_ATEND_HORA IN TB_ASS_ULC_UNID_LOCAL_CBOS.ASS_ULC_QTD_ATEND_HORA%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_ULC_UNID_LOCAL_CBOS.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pASS_ULC_DT_ULTIMA_ATUALIZACAO IN TB_ASS_ULC_UNID_LOCAL_CBOS.ASS_ULC_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pASS_ULC_FL_STATUS IN TB_ASS_ULC_UNID_LOCAL_CBOS.ASS_ULC_FL_STATUS%type DEFAULT NULL,
     pCAD_UNI_ID_UNIDADE IN TB_ASS_ULC_UNID_LOCAL_CBOS.CAD_UNI_ID_UNIDADE%type DEFAULT NULL,
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_ASS_ULC_UNID_LOCAL_CBOS.CAD_LAT_ID_LOCAL_ATENDIMENTO%type DEFAULT NULL--,
    -- pASS_ULC_CD_PROGRAMA_ACS IN TB_ASS_ULC_UNID_LOCAL_CBOS.ASS_ULC_CD_PROGRAMA_ACS%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
  ) 
  is
  /********************************************************************
  *    Procedure: PRC_ASS_UNID_LOCAL_CBO_RMT_S
  * 
  *    Data Criacao: 	data da  criação   Por: Nome do Analista
  *    Data Alteracao:	data da alteração  Por: Nome do Analista
  *
  *    Funcao: Descrição da funcionalidade da Stored Procedure
  *
  *******************************************************************/
  v_cursor PKG_CURSOR.t_cursor;
  begin
    OPEN v_cursor FOR
    SELECT	
       TIS_CBO_CD_CBOS,
       ASS_ULC_QTD_ATEND_HORA,
       SEG_USU_ID_USUARIO,
       ASS_ULC_DT_ULTIMA_ATUALIZACAO,
       ASS_ULC_FL_STATUS,
       CAD_UNI_ID_UNIDADE,
       CAD_LAT_ID_LOCAL_ATENDIMENTO--,
     --  ASS_ULC_CD_PROGRAMA_ACS
       
    FROM TB_ASS_ULC_UNID_LOCAL_CBOS
    WHERE
        (pTIS_CBO_CD_CBOS is null OR TIS_CBO_CD_CBOS = pTIS_CBO_CD_CBOS) AND 
        (pASS_ULC_QTD_ATEND_HORA is null OR ASS_ULC_QTD_ATEND_HORA = pASS_ULC_QTD_ATEND_HORA) AND 
        (pSEG_USU_ID_USUARIO is null OR SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO) AND 
        (pASS_ULC_DT_ULTIMA_ATUALIZACAO is null OR ASS_ULC_DT_ULTIMA_ATUALIZACAO = pASS_ULC_DT_ULTIMA_ATUALIZACAO) AND 
        (pASS_ULC_FL_STATUS is null OR ASS_ULC_FL_STATUS = pASS_ULC_FL_STATUS) AND 
        (pCAD_UNI_ID_UNIDADE is null OR CAD_UNI_ID_UNIDADE = pCAD_UNI_ID_UNIDADE) AND 
        (pCAD_LAT_ID_LOCAL_ATENDIMENTO is null OR CAD_LAT_ID_LOCAL_ATENDIMENTO = pCAD_LAT_ID_LOCAL_ATENDIMENTO)-- AND
      --  (pASS_ULC_CD_PROGRAMA_ACS IS NULL or ASS_ULC_CD_PROGRAMA_ACS = pASS_ULC_CD_PROGRAMA_ACS)
      ;          
    io_cursor := v_cursor;
  end PRC_ASS_UNID_LOCAL_CBO_RMT_S;
