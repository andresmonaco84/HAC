--PRC_ASS_PAT_PACIEATEND_R_L
create or replace procedure PRC_ASS_PAT_PACIEATEND_R_L 
(
     pATD_ATE_ID IN TB_ASS_PAT_PACIEATEND.ATD_ATE_ID%type DEFAULT NULL,
     pCAD_PAC_ID_PACIENTE IN TB_ASS_PAT_PACIEATEND.CAD_PAC_ID_PACIENTE%type DEFAULT NULL,
     pASS_PAT_DT_ENTRADA IN TB_ASS_PAT_PACIEATEND.ASS_PAT_DT_ENTRADA%type DEFAULT NULL,
     pASS_PAT_HR_ENTRADA IN TB_ASS_PAT_PACIEATEND.ASS_PAT_HR_ENTRADA%type DEFAULT NULL,
     pASS_PAT_DT_SAIDA IN TB_ASS_PAT_PACIEATEND.ASS_PAT_DT_SAIDA%type DEFAULT NULL,
     pASS_PAT_HR_SAIDA IN TB_ASS_PAT_PACIEATEND.ASS_PAT_HR_SAIDA%type DEFAULT NULL,
     pASS_PAT_FL_STATUS IN TB_ASS_PAT_PACIEATEND.ASS_PAT_FL_STATUS%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_PAT_PACIEATEND.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pASS_PAT_DT_ULT_ATUALIZACAO IN TB_ASS_PAT_PACIEATEND.ASS_PAT_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pASS_PAT_NR_CONTA IN TB_ASS_PAT_PACIEATEND.ASS_PAT_NR_CONTA%type DEFAULT NULL,
     pCAD_CNV_ID_CONVENIO IN TB_ASS_PAT_PACIEATEND.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pCAD_PLA_ID_PLANO IN TB_ASS_PAT_PACIEATEND.CAD_PLA_ID_PLANO%type DEFAULT NULL,
     pCAD_PAC_CD_CREDENCIAL IN TB_ASS_PAT_PACIEATEND.CAD_PAC_CD_CREDENCIAL%type DEFAULT NULL,
     pASS_PAT_DS_OBSERVACAO IN TB_ASS_PAT_PACIEATEND.ASS_PAT_DS_OBSERVACAO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ASS_PAT_PACIEATEND_R_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pATD_ATE_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_ATE_ID = ' || pATD_ATE_ID; END IF;
IF pCAD_PAC_ID_PACIENTE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PAC_ID_PACIENTE = ' || pCAD_PAC_ID_PACIENTE; END IF;
IF pASS_PAT_DT_ENTRADA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PAT_DT_ENTRADA = ' || CHR(39) || pASS_PAT_DT_ENTRADA || CHR(39); END IF;
IF pASS_PAT_HR_ENTRADA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PAT_HR_ENTRADA = ' || pASS_PAT_HR_ENTRADA; END IF;
IF pASS_PAT_DT_SAIDA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PAT_DT_SAIDA = ' || CHR(39) || pASS_PAT_DT_SAIDA || CHR(39); END IF;
IF pASS_PAT_HR_SAIDA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PAT_HR_SAIDA = ' || pASS_PAT_HR_SAIDA; END IF;
IF pASS_PAT_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PAT_FL_STATUS = ' || CHR(39) || pASS_PAT_FL_STATUS || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pASS_PAT_DT_ULT_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PAT_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pASS_PAT_DT_ULT_ATUALIZACAO || CHR(39); END IF;
IF pASS_PAT_NR_CONTA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PAT_NR_CONTA = ' || pASS_PAT_NR_CONTA; END IF;
IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
IF pCAD_PLA_ID_PLANO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PLA_ID_PLANO = ' || pCAD_PLA_ID_PLANO; END IF;
IF pCAD_PAC_CD_CREDENCIAL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PAC_CD_CREDENCIAL = ' || CHR(39) || pCAD_PAC_CD_CREDENCIAL || CHR(39); END IF;
IF pASS_PAT_DS_OBSERVACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PAT_DS_OBSERVACAO = ' || CHR(39) || pASS_PAT_DS_OBSERVACAO || CHR(39); END IF;
V_WHERE := V_WHERE || ' order by ASS_PAT_DT_ENTRADA, ASS_PAT_HR_ENTRADA ';
   V_SELECT := '
SELECT	
       ATD_ATE_ID,
       CAD_PAC_ID_PACIENTE,
       ASS_PAT_DT_ENTRADA,
       ASS_PAT_HR_ENTRADA,
       ASS_PAT_DT_SAIDA,
       ASS_PAT_HR_SAIDA,
       ASS_PAT_FL_STATUS,
       SEG_USU_ID_USUARIO,
       ASS_PAT_DT_ULTIMA_ATUALIZACAO,
       ASS_PAT_NR_CONTA,
       CAD_CNV_ID_CONVENIO,
       CAD_PLA_ID_PLANO,
       CAD_PAC_CD_CREDENCIAL,
       ASS_PAT_DS_OBSERVACAO
FROM TB_ASS_PAT_PACIEATEND
WHERE null is null  ' ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ASS_PAT_PACIEATEND_R_L;
