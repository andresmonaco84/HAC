--PRC_CAD_DOC_DOCUMENTO_RMT_S
create or replace procedure PRC_CAD_DOC_DOCUMENTO_RMT_S 
(
     pCAD_DOC_ID_DOCUMENTO IN TB_CAD_DOC_DOCUMENTO.CAD_DOC_ID_DOCUMENTO%type DEFAULT NULL,
     pCAD_DOC_SG_DOCUMENTO IN TB_CAD_DOC_DOCUMENTO.CAD_DOC_SG_DOCUMENTO%type DEFAULT NULL,
     pCAD_DOC_DS_DOCUMENTO IN TB_CAD_DOC_DOCUMENTO.CAD_DOC_DS_DOCUMENTO%type DEFAULT NULL,
     pCAD_DOC_DS_OBSERVACAO IN TB_CAD_DOC_DOCUMENTO.CAD_DOC_DS_OBSERVACAO%type DEFAULT NULL,
     pCAD_DOC_FL_ATIVO_OK IN TB_CAD_DOC_DOCUMENTO.CAD_DOC_FL_ATIVO_OK%type DEFAULT NULL,
     pCAD_DOC_DT_ULTIMA_ATUALIZACAO IN TB_CAD_DOC_DOCUMENTO.CAD_DOC_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_DOC_DOCUMENTO.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_DOC_DOCUMENTO_RMT_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_DOC_ID_DOCUMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_DOC_ID_DOCUMENTO = ' || pCAD_DOC_ID_DOCUMENTO; END IF;
IF pCAD_DOC_SG_DOCUMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_DOC_SG_DOCUMENTO = ' || CHR(39) || pCAD_DOC_SG_DOCUMENTO || CHR(39); END IF;
IF pCAD_DOC_DS_DOCUMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_DOC_DS_DOCUMENTO = ' || CHR(39) || pCAD_DOC_DS_DOCUMENTO || CHR(39); END IF;
IF pCAD_DOC_DS_OBSERVACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_DOC_DS_OBSERVACAO = ' || CHR(39) || pCAD_DOC_DS_OBSERVACAO || CHR(39); END IF;
IF pCAD_DOC_FL_ATIVO_OK IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_DOC_FL_ATIVO_OK = ' || CHR(39) || pCAD_DOC_FL_ATIVO_OK || CHR(39); END IF;
IF pCAD_DOC_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_DOC_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_DOC_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       CAD_DOC_ID_DOCUMENTO,
       CAD_DOC_SG_DOCUMENTO,
       CAD_DOC_DS_DOCUMENTO,
       CAD_DOC_DS_OBSERVACAO,
       CAD_DOC_FL_ATIVO_OK,
       CAD_DOC_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO
FROM TB_CAD_DOC_DOCUMENTO
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_DOC_DOCUMENTO_RMT_S;
