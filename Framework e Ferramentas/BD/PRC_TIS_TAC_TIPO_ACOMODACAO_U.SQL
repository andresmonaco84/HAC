create or replace procedure PRC_TIS_TAC_TIPO_ACOMODACAO_U
  (
     pTIS_TAC_CD_TIPO_ACOMODACAO IN TB_TIS_TAC_TIPO_ACOMODACAO.TIS_TAC_CD_TIPO_ACOMODACAO%type,
     pTIS_TAC_DS_TIPO_ACOMODACAO IN TB_TIS_TAC_TIPO_ACOMODACAO.TIS_TAC_DS_TIPO_ACOMODACAO%type,
  pTIS_TAC_PC_ACOMODACAO_HM  IN TB_TIS_TAC_TIPO_ACOMODACAO.TIS_TAC_PC_ACOMODACAO_HM%type default null,
 -- pTIS_TAC_DT_ULTIMA_ATUALIZACAO IN TB_TIS_TAC_TIPO_ACOMODACAO.TIS_TAC_DT_ULTIMA_ATUALIZACAO%type,
  pSEG_USU_ID_USUARIO   IN TB_TIS_TAC_TIPO_ACOMODACAO.SEG_USU_ID_USUARIO%type,
  pTIS_TAC_FL_PERM_DIF_CLASSE IN TB_TIS_TAC_TIPO_ACOMODACAO.TIS_TAC_FL_PERM_DIF_CLASSE%type default null,
  pTIS_TAC_FL_UTILIZA_REPASSE IN TB_TIS_TAC_TIPO_ACOMODACAO.TIS_TAC_FL_UTILIZA_REPASSE%type default null,
  pTIS_TAC_FL_STATUS    IN TB_TIS_TAC_TIPO_ACOMODACAO.TIS_TAC_FL_STATUS%type default null
  )
  is
  /********************************************************************
  *    Procedure: PRC_TIS_TAC_TIPO_ACOMODACAO_U
  *
  *    Data Criacao:  12/04/2007   Por: Cristiane Gomes da Silva
  *    Data Alteracao: 02/05/2007   Por: Cristiane Gomes da Silva
  *
  *    Data Alteracao: 25/05/2007   Por: Silmara
  *    Alteração: Foi alterado os parâmetros para gravar em maiúsculo
  *
  *    Funcao: Altera Tipo de Acomodação - TISS
  *
  *******************************************************************/
  begin
    UPDATE TB_TIS_TAC_TIPO_ACOMODACAO
    SET
        TIS_TAC_DS_TIPO_ACOMODACAO = UPPER(pTIS_TAC_DS_TIPO_ACOMODACAO),
  TIS_TAC_PC_ACOMODACAO_HM = pTIS_TAC_PC_ACOMODACAO_HM,
  TIS_TAC_DT_ULTIMA_ATUALIZACAO = SYSDATE,
  SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO,
  TIS_TAC_FL_PERM_DIF_CLASSE = pTIS_TAC_FL_PERM_DIF_CLASSE,
  TIS_TAC_FL_UTILIZA_REPASSE = pTIS_TAC_FL_UTILIZA_REPASSE,
  TIS_TAC_FL_STATUS = pTIS_TAC_FL_STATUS
    WHERE
        TIS_TAC_CD_TIPO_ACOMODACAO = pTIS_TAC_CD_TIPO_ACOMODACAO;
  end PRC_TIS_TAC_TIPO_ACOMODACAO_U;
 