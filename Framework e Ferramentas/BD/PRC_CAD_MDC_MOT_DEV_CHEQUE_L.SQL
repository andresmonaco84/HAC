--PRC_CAD_MDC_MOTIVO_DEV_CHEQUE_L
create or replace procedure PRC_CAD_MDC_MOT_DEV_CHEQUE_L 
(
     pCAD_MDC_ID IN TB_CAD_MDC_MOTIVO_DEV_CHEQUE.CAD_MDC_ID%type DEFAULT NULL,
     pCAD_MDC_CD_MOTIVO_DEV_CHEQUE IN TB_CAD_MDC_MOTIVO_DEV_CHEQUE.CAD_MDC_CD_MOTIVO_DEV_CHEQUE%type DEFAULT NULL,
     pCAD_MDC_DS_MOTIVO_DEV_CHEQUE IN TB_CAD_MDC_MOTIVO_DEV_CHEQUE.CAD_MDC_DS_MOTIVO_DEV_CHEQUE%type DEFAULT NULL,
     pCAD_MDC_TP_CLASSIFICACAO IN TB_CAD_MDC_MOTIVO_DEV_CHEQUE.CAD_MDC_TP_CLASSIFICACAO%type DEFAULT NULL,
     pCAD_MDC_DT_CRIACAO IN TB_CAD_MDC_MOTIVO_DEV_CHEQUE.CAD_MDC_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_CAD_MDC_MOTIVO_DEV_CHEQUE.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pCAD_MDC_DT_ULTIMA_ATUALIZACAO IN TB_CAD_MDC_MOTIVO_DEV_CHEQUE.CAD_MDC_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ATUALIZ IN TB_CAD_MDC_MOTIVO_DEV_CHEQUE.SEG_USU_ID_USUARIO_ATUALIZ%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_MDC_MOTIVO_DEV_CHEQUE_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_MDC_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_MDC_ID = ' || pCAD_MDC_ID; END IF;
IF pCAD_MDC_CD_MOTIVO_DEV_CHEQUE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_MDC_CD_MOTIVO_DEV_CHEQUE = ' || pCAD_MDC_CD_MOTIVO_DEV_CHEQUE; END IF;
IF pCAD_MDC_DS_MOTIVO_DEV_CHEQUE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_MDC_DS_MOTIVO_DEV_CHEQUE = ' || CHR(39) || pCAD_MDC_DS_MOTIVO_DEV_CHEQUE || CHR(39); END IF;
IF pCAD_MDC_TP_CLASSIFICACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_MDC_TP_CLASSIFICACAO = ' || pCAD_MDC_TP_CLASSIFICACAO; END IF;
IF pCAD_MDC_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_MDC_DT_CRIACAO = ' || CHR(39) || pCAD_MDC_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; END IF;
IF pCAD_MDC_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_MDC_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_MDC_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ATUALIZ = ' || pSEG_USU_ID_USUARIO_ATUALIZ; END IF;
 
   V_SELECT := '
SELECT	
       CAD_MDC_ID,
       CAD_MDC_CD_MOTIVO_DEV_CHEQUE,
       CAD_MDC_DS_MOTIVO_DEV_CHEQUE,
       CAD_MDC_TP_CLASSIFICACAO,
       CAD_MDC_DT_CRIACAO,
       SEG_USU_ID_USUARIO_CRIACAO,
       CAD_MDC_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO_ATUALIZ
FROM TB_CAD_MDC_MOTIVO_DEV_CHEQUE
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_MDC_MOT_DEV_CHEQUE_L;
/
