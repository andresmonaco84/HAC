    pID_PLANOS                         VARCHAR2 DEFAULT NULL,
   -- TESTE                              OUT LONG,
    io_cursor                          OUT PKG_CURSOR.t_cursor) is
  /********************************************************************
  *    Procedure: PRC_INT_REL_PAC_SND
  *
  *    Data Criacao:  19/11/2019           Por: Fabiola
  *
  *    Funcao:
  *
 *******************************************************************/
  v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(3000);
  V_SELECT  varchar2(32000);
  V_WHERESUB  varchar2(3000);
  BEGIN
    V_WHERE := NULL;
    V_WHERESUB:=NULL;

IF pCAD_UNI_ID_UNIDADE IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND ATD.CAD_UNI_ID_UNIDADE = ' || pCAD_UNI_ID_UNIDADE; END IF;
IF pCAD_LAT_ID_LOCAL_ATENDIMENTO IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND ATD.CAD_LAT_ID_LOCAL_ATENDIMENTO = ' || pCAD_LAT_ID_LOCAL_ATENDIMENTO; END IF;
IF pCAD_SET_ID IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND IML_QLE.CAD_SET_ID = ' || pCAD_SET_ID; END IF;
IF pATD_AIC_TP_SITUACAO_PAC IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND AIC.ATD_AIC_TP_SITUACAO_PAC = ' ||CHR(39)|| pATD_AIC_TP_SITUACAO_PAC ||CHR(39); END IF;
IF pATD_AIC_TP_SITUACAO_PAC = 'A' THEN
  V_WHERE := V_WHERE || ' AND INA.ATD_ATE_ID IS NOT NULL '; END IF;
IF pATD_ATE_HR_ATENDIMENTO_INI IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND ATD.ATD_ATE_HR_ATENDIMENTO  BETWEEN ' || pATD_ATE_HR_ATENDIMENTO_INI || ' AND ' || pATD_ATE_HR_ATENDIMENTO_FIM  ; END IF;
IF pFAIXA_ETARIA_INI IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND FLOOR(FLOOR(MONTHS_BETWEEN(DECODE(INA.ATD_INA_DT_ALTA_ADM,NULL,ATD.ATD_ATE_DT_ATENDIMENTO,INA.ATD_INA_DT_ALTA_ADM), PES.CAD_PES_DT_NASCIMENTO)) / 12) BETWEEN '
                     || pFAIXA_ETARIA_INI || ' AND ' || pFAIXA_ETARIA_FIM; END IF;
IF pTIS_CBO_CD_CBOS IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND ATD.TIS_CBO_CD_CBOS = ' ||CHR(39)|| pTIS_CBO_CD_CBOS ||CHR(39); END IF;
IF pCAD_PRO_ID_PROF_EXEC IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND ATD.CAD_PRO_ID_PROF_EXEC = ' || pCAD_PRO_ID_PROF_EXEC; END IF;
IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND PAC.CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
IF pCAD_PLA_ID_PLANO IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND PAC.CAD_PLA_ID_PLANO = ' || pCAD_PLA_ID_PLANO; END IF;
IF pCAD_PRD_ID IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND AIC.CAD_PRD_ID = ' || pCAD_PRD_ID; END IF;
IF pCAD_PES_TP_SEXO IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND PES.CAD_PES_TP_SEXO = ' ||CHR(39)|| pCAD_PES_TP_SEXO ||CHR(39); END IF;
IF pTIS_TAC_CD_TIPO_ACOMODACAO IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND TAC.TIS_TAC_CD_TIPO_ACOMODACAO = ' ||CHR(39)|| pTIS_TAC_CD_TIPO_ACOMODACAO ||CHR(39); END IF;
IF pCAD_QLE_TP_QUARTO_LEITO IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND IML_QLE.CAD_QLE_TP_QUARTO_LEITO = ' ||CHR(39)|| pCAD_QLE_TP_QUARTO_LEITO ||CHR(39); END IF;
IF pTIS_TIN_CD_INTER IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND TIN.TIS_TIN_CD_INTERNACAO = ' ||CHR(39)|| pTIS_TIN_CD_INTER ||CHR(39); END IF;
IF pTIS_TRI_CD_TP_REGINT IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND  TRI.TIS_TRI_CD_TP_REGINTERNACAO = ' ||CHR(39)|| pTIS_TRI_CD_TP_REGINT ||CHR(39); END IF;
IF pCAD_UNI_ID_UNID_PROC  IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND ATD.CAD_UNI_ID_UNID_PROC = ' || pCAD_UNI_ID_UNID_PROC ; END IF;
IF pATD_ATE_DT_ATENDIMENTO_INI IS NOT NULL THEN
  V_WHERE:= V_WHERE || ' AND ATD.ATD_ATE_DT_ATENDIMENTO >= TO_DATE('||CHR(39)||TO_CHAR(pATD_ATE_DT_ATENDIMENTO_INI,'DDMMYYYY')||CHR(39)||','||CHR(39)||'DDMMYYYY'||CHR(39)|| ')' ; END IF;
IF pATD_ATE_DT_ATENDIMENTO_FIM IS NOT NULL THEN
  V_WHERE:= V_WHERE || ' AND ATD.ATD_ATE_DT_ATENDIMENTO <= TO_DATE('||CHR(39)||TO_CHAR(pATD_ATE_DT_ATENDIMENTO_FIM,'DDMMYYYY')||CHR(39)||','||CHR(39)||'DDMMYYYY'||CHR(39)|| ')' ; END IF;
IF pATD_ATE_DT_ATENDIMENTO_INI IS NULL AND pATD_ATE_DT_ATENDIMENTO_FIM IS NULL THEN
   V_WHERE := V_WHERE || ' AND AIC.ATD_AIC_TP_SITUACAO_PAC = '||CHR(39)||'I'||CHR(39); END IF;
IF pAUX_MUN_CD_IBGE IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND FNC_CAD_PES_IBGE_MUNICIPIO_S(PES.CAD_PES_ID_PESSOA,1) = ' ||CHR(39)|| pAUX_MUN_CD_IBGE ||CHR(39); END IF;
IF pAUX_MUN_SG_UF IS NOT NULL THEN
  V_WHERE := V_WHERE || ' AND FNC_CAD_PES_UF_MUNICIPIO_S(PES.CAD_PES_ID_PESSOA,1) = ' ||CHR(39)|| pAUX_MUN_SG_UF ||CHR(39); END IF;
IF pID_PLANOS IS NOT NULL THEN
   V_WHERE := V_WHERE || ' AND PLA.CAD_PLA_ID_PLANO IN ( SELECT * FROM TABLE(FNC_SPLIT(''' || pID_PLANOS || ''' ))) '; END IF;
IF pCAD_UNI_ID_UNIDADE IS NOT NULL THEN
  V_WHERESUB := V_WHERESUB || ' AND ATD2.CAD_UNI_ID_UNIDADE = ' || pCAD_UNI_ID_UNIDADE; END IF;
IF pCAD_LAT_ID_LOCAL_ATENDIMENTO IS NOT NULL THEN
  V_WHERESUB := V_WHERESUB || ' AND ATD2.CAD_LAT_ID_LOCAL_ATENDIMENTO = ' || pCAD_LAT_ID_LOCAL_ATENDIMENTO; END IF;
IF pATD_AIC_TP_SITUACAO_PAC IS NOT NULL THEN
  V_WHERESUB := V_WHERESUB || ' AND AIC2.ATD_AIC_TP_SITUACAO_PAC = ' ||CHR(39)|| pATD_AIC_TP_SITUACAO_PAC ||CHR(39); END IF;
IF pCAD_UNI_ID_UNID_PROC  IS NOT NULL THEN
  V_WHERESUB := V_WHERESUB || ' AND ATD2.CAD_UNI_ID_UNID_PROC = ' || pCAD_UNI_ID_UNID_PROC ; END IF;
IF pATD_ATE_DT_ATENDIMENTO_INI IS NOT NULL THEN
  V_WHERESUB:= V_WHERESUB || ' AND ATD2.ATD_ATE_DT_ATENDIMENTO >= TO_DATE('||CHR(39)||TO_CHAR(pATD_ATE_DT_ATENDIMENTO_INI,'DDMMYYYY')||CHR(39)||','||CHR(39)||'DDMMYYYY'||CHR(39)|| ')' ; END IF;
IF pATD_ATE_DT_ATENDIMENTO_FIM IS NOT NULL THEN
  V_WHERESUB:= V_WHERESUB || ' AND ATD2.ATD_ATE_DT_ATENDIMENTO <= TO_DATE('||CHR(39)||TO_CHAR(pATD_ATE_DT_ATENDIMENTO_FIM,'DDMMYYYY')||CHR(39)||','||CHR(39)||'DDMMYYYY'||CHR(39)|| ')' ; END IF;
IF pATD_ATE_DT_ATENDIMENTO_INI IS NULL AND pATD_ATE_DT_ATENDIMENTO_FIM IS NULL THEN
  V_WHERESUB := V_WHERESUB || ' AND AIC2.ATD_AIC_TP_SITUACAO_PAC = '||CHR(39)||'I'||CHR(39); END IF;

V_SELECT :=
   '
   SELECT      CAD_QLE_NR_QUARTO,
               CAD_QLE_NR_LEITO,
               ATD_ATE_ID,
                DECODE(ATD_IDR_NM_NOME, NULL, PACIENTE, ATD_IDR_NM_NOME) PACIENTE,
               CAD_CNV_CD_HAC_PRESTADOR,
               CAD_CNV_DS_RAZAOSOCIAL,
               CAD_PLA_CD_PLANO_HAC,
               CAD_PLA_NM_NOME_PLANO,
               CAD_PLA_FL_COB_REFEI_ACOMP,
               ATD_PME_DS_ALERGIA,              
               DECODE(ATD_IDR_DT_NASCIMENTO, NULL,CAD_PES_DT_NASCIMENTO , ATD_IDR_DT_NASCIMENTO) CAD_PES_DT_NASCIMENTO,
               
               ATD_PME_ID,ATD_PME_DT_HR_CRIACAO,
               
               ORDEM,
               TIPO_DIETA,
               STATUS_DIETA,
               DESCRICAO_DIETA,
               OBSERVACAO_DIETA,
               DOSE_DIETA,
               VIA_DIETA,
               INTERVALO_DIETA,
               APRAZ_DIETA,
               DTHR_CRIACAO_DIETA,
               DTHR_ULT_ATUALIZ_DIETA,
               DTHR_REALIZ_DIETA,
               IMEDIATO_DIETA
                             

FROM ( SELECT DISTINCT  ATD_PME_DS_ALERGIA,
                       PME.ATD_PME_DT_HR_CRIACAO, PME.ATD_PME_ID,
                       CAD_PLA_FL_COB_REFEI_ACOMP,
                       IML_QLE.ATD_IML_DT_ENTRADA,
                       IML_QLE.ATD_IML_HR_ENTRADA,
                       IDR.ATD_IDR_NM_NOME, IDR.ATD_IDR_DT_NASCIMENTO,
                       
                       DECODE(IML_QLE.CAD_QLE_TP_QUARTO_LEITO,''I'',''INTERNO'','||CHR(39)||'E'||CHR(39)||','||CHR(39)||'EXTRA'||CHR(39)||') CAD_QLE_TP_QUARTO_LEITO,
                       IML_QLE.CAD_QLE_NR_QUARTO,
                       IML_QLE.CAD_QLE_NR_LEITO,
                       TAC.TIS_TAC_DS_TIPO_ACOMODACAO,
                       TAC_AUT.TIS_TAC_DS_TIPO_ACOMODACAO TIS_TAC_DS_TIPO_ACOMODACAO_AUT,
                       ATD.ATD_ATE_ID,
                       DECODE(ATD.ATD_ATE_FL_CARATER_SOLIC,'||CHR(39)||'U'||CHR(39)||','||CHR(39)||'URGENCIA'||CHR(39)||','||CHR(39)||'E'||CHR(39)||','||CHR(39)||'ELETIVA'||CHR(39)||') ATD_ATE_FL_CARATER_SOLIC,
                       UNI.CAD_UNI_DS_UNIDADE UNIDADE,
                       UNI.CAD_UNI_ID_UNIDADE,
                       LAT.CAD_LAT_DS_LOCAL_ATENDIMENTO,
                       SETOR.CAD_SET_DS_SETOR,
                       PAC.CAD_PAC_CD_CREDENCIAL,
                       PAC.CAD_PAC_NR_PRONTUARIO,
                       PES.CAD_PES_NM_PESSOA PACIENTE,
                       PES.CAD_PES_DT_NASCIMENTO,
                       PES.CAD_PES_TP_SEXO,
                       PES.CAD_PES_CD_CNS_SUS,                      
                       CBOS.TIS_CBO_DS_CBOS_HAC,
                       DECODE(AIC.ATD_AIC_TP_SITUACAO_PAC,'||CHR(39)||'I'||CHR(39)||','||CHR(39)||'INTERNADO'||CHR(39)||','||CHR(39)||'A'||CHR(39)||','||CHR(39)||'SAIDA'||CHR(39)||') ATD_AIC_TP_SITUACAO_PAC,
                       DECODE(ATD_ATE_TP_PACIENTE,'||CHR(39)||'I'||CHR(39)||','||CHR(39)||'INTERNO'||CHR(39)||','||CHR(39)||'E'||CHR(39)||','||CHR(39)||'EXTERNO'||CHR(39)||') ATD_ATE_TP_PACIENTE,
                       INA.ATD_INA_DT_ALTA_ADM ATD_INA_DT_ALTA_CLINICA,
                       CNV.CAD_CNV_CD_HAC_PRESTADOR,
                       CNV.CAD_CNV_DS_RAZAOSOCIAL,
                       PLA.CAD_PLA_CD_TIPOPLANO,
                       PLA.CAD_PLA_CD_PLANO_HAC,
                       PLA.CAD_PLA_NM_NOME_PLANO,
                       ATD.ATD_ATE_DT_ATENDIMENTO,
                       ATD.ATD_ATE_HR_ATENDIMENTO,
                       PRO.CAD_PRO_NM_NOME PROFISSIONAL,
                       PRO.CAD_PRO_NR_CONSELHO,
                       PRO.CAD_PRO_SG_UF_CONSELHO,
                       FNC_INT_ULTIMO_CID(ATD.ATD_ATE_ID) CAD_CID_DS_CID10,
                       TIN.TIS_TIN_DS_INTERNACAO,
                       TRI.TIS_TRI_DS_TP_REGINTERNACAO,
                       PRD.CAD_PRD_NM_MNEMONICO,
                       PRD.CAD_PRD_DS_DESCRICAO,
                       SETOR.CAD_SET_NR_ANDAR,
                       FLOOR(FLOOR(MONTHS_BETWEEN(DECODE(INA.ATD_INA_DT_ALTA_ADM,NULL,ATD.ATD_ATE_DT_ATENDIMENTO,INA.ATD_INA_DT_ALTA_ADM), PES.CAD_PES_DT_NASCIMENTO)) / 12) idade,
                       
                       GUI.ATD_GUI_CD_CODIGO,
                       GUI.ATD_GUI_CD_SENHA,
                       CASE WHEN ATD.CAD_LAT_ID_LOCAL_ATENDIMENTO = 46 THEN DECODE(TRIM(ATD.TIS_TAT_CD_TPATENDIMENTO),''6'',''A'',''7'',''I'')
                            ELSE NULL
                       END TIS_TAT_CD_TPATENDIMENTO,
                     
                       USU.SEG_USU_CD_MATRICULA,
                       USU.SEG_USU_DS_NOME,
                       PESRESP.CAD_PES_NM_PESSOA NOME_RESP,
                       PESRESP.CAD_PES_DT_NASCIMENTO DTNASC_RESP,
                      
                       CASE WHEN SETOR.CAD_SET_ID IN (2072,2312,5,140,2472) THEN ''S'' ELSE ''N'' END HOSPITALDIA,
                       
                       DIETA.ordem,
                       DIETA.TIPO_DIETA,
                       DIETA.ATD_MPM_DS_MEDICAMENTO  DESCRICAO_DIETA, 
                       DIETA.ATD_MPM_DS_OBSERVACAO OBSERVACAO_DIETA,
                       DIETA.STATUS_DIETA,  
                       DIETA.ATD_MPM_DOSE_ADM DOSE_DIETA,
                       DIETA.ATD_MPM_DS_VIA   VIA_DIETA, 
                       DIETA.ATD_MPM_DS_INTERVALO  INTERVALO_DIETA,
                       DIETA.ATD_MPM_APRAZAMENTO   APRAZ_DIETA, 
                       DIETA.ATD_MPM_DT_HORA_CRIACAO DTHR_CRIACAO_DIETA,
                       DIETA.ATD_MPM_DT_ULT_ATUALIZ DTHR_ULT_ATUALIZ_DIETA,
                       DIETA.ATD_MPM_DT_HORA_REALIZ DTHR_REALIZ_DIETA,
                       DIETA.ATD_MPM_FL_IMEDIATO IMEDIATO_DIETA
                         
                  FROM TB_ATD_ATE_ATENDIMENTO ATD
                  JOIN TB_CAD_PAC_PACIENTE PAC ON PAC.CAD_PAC_ID_PACIENTE = FNC_BUSCAR_PACIENTE_ATUAL(ATD.ATD_ATE_ID)
                  JOIN TB_ATD_PME_PRESCRICAO_MEDICA PME ON ATD.ATD_ATE_ID = PME.ATD_ATE_ID 
                   AND trunc(PME.ATD_PME_DT_HR_CRIACAO) = ( SELECT MAX(trunc(PME1.ATD_PME_DT_HR_CRIACAO))
                                                       FROM TB_ATD_PME_PRESCRICAO_MEDICA PME1
                                                      WHERE PME1.ATD_ATE_ID = ATD.ATD_ATE_ID AND PME1.ATD_PME_FL_STATUS in (''A'',''D''))
                  LEFT JOIN TB_ATD_DPM_DIETA_PRESC_MED DPM ON DPM.ATD_PME_ID = PME.ATD_PME_ID AND DPM.ATD_DPM_STATUS IN (''A'',''D'',''C'',''S'')

                   left join Tb_Atd_Idr_Int_Dados_Rn IDR ON IDR.ATD_ATE_ID = PME.ATD_ATE_ID and idr.atd_idr_id = pme.atd_idr_id
                                   
                  JOIN TB_CAD_PES_PESSOA PES ON PES.CAD_PES_ID_PESSOA = PAC.CAD_PES_ID_PESSOA
                  JOIN TB_CAD_CNV_CONVENIO CNV ON CNV.CAD_CNV_ID_CONVENIO = PAC.CAD_CNV_ID_CONVENIO
                  JOIN TB_CAD_PLA_PLANO PLA ON PLA.CAD_PLA_ID_PLANO = PAC.CAD_PLA_ID_PLANO
                  JOIN TB_ATD_AIC_ATE_INT_COMPL AIC ON AIC.ATD_ATE_ID = ATD.ATD_ATE_ID
                  LEFT JOIN (SELECT 2 ORDEM,''ENTERAL'' TIPO_DIETA, CASE WHEN MPM.ATD_MPM_STATUS IN (''C'',''S'') THEN ''SUSPENSO'' ELSE '''' END STATUS_DIETA,
                            MPM.ATD_MPM_DS_MEDICAMENTO, MPM.ATD_MPM_DS_DOSE,MPM.ATD_MPM_DOSE_ADM,  MPM.ATD_MPM_DS_VIA, MPM.ATD_MPM_DS_INTERVALO,
                             MPM.ATD_MPM_DS_OBSERVACAO, MPM.ATD_MPM_DT_HORA_CRIACAO, MPM.ATD_MPM_DT_HORA_REALIZ, MPM.ATD_PME_ID, MPM.ATD_MPM_APRAZAMENTO,
                             AIC.ATD_ATE_ID, MPM.ATD_MPM_STATUS, MPM.ATD_MPM_FL_IMEDIATO, MPM.ATD_MPM_DT_ULT_ATUALIZ
                              FROM TB_ATD_MPM_MED_PRESC_MED MPM
                              join tb_atd_pme_prescricao_medica pme on pme.atd_pme_id = mpm.atd_pme_id
                              join tb_atd_aic_ate_int_compl aic on aic.atd_ate_id = pme.atd_ate_id and aic.atd_aic_tp_situacao_pac=''I''
                             WHERE MPM.ATD_MPM_TIPO=''DIE'' AND MPM.ATD_MPM_STATUS IN (''A'',''D'',''C'',''S'')
                            UNION
                             SELECT 1 ORDEM,''ORAL'' TIPO_DIETA, CASE WHEN DPM.ATD_DPM_STATUS IN (''C'',''S'') THEN ''SUSPENSO'' ELSE '''' END STATUS_DIETA,
                             DPM.ATD_DPM_DS_DIETA, NULL ATD_MPM_DS_DOSE, NULL ATD_MPM_DOSE_ADM, NULL ATD_MPM_DS_VIA, NULL ATD_MPM_DS_INTERVALO,
