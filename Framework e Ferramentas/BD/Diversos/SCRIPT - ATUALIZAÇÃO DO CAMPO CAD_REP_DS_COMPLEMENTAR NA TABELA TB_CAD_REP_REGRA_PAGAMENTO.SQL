DECLARE
   CURSOR C1
   IS
      SELECT DISTINCT BCP.CAD_REP_TP_BASE_CALCULO, BCP.REP_BCP_DS_BASE_CALCULO 
        FROM TB_CAD_REP_BCP_BASE_CALCULO BCP;
BEGIN
   FOR F1 IN C1
   LOOP
         UPDATE TB_CAD_REP_REGRA_PAGAMENTO REP 
            SET REP.CAD_REP_DS_COMPLEMENTAR = F1.REP_BCP_DS_BASE_CALCULO
          WHERE REP.CAD_REP_TP_BASE_CALCULO = F1.CAD_REP_TP_BASE_CALCULO;
         COMMIT;
   END LOOP;
END;