 
--PRC_CAD_LOG_ERRO_PROCESSO_L
create or replace procedure PRC_CAD_LOG_ERRO_PROCESSO_L 
(
     pCAD_LOG_DATA_PROCESSO IN TB_CAD_LOG_ERRO_PROCESSO.CAD_LOG_DATA_PROCESSO%type DEFAULT NULL,
     pCAD_LOG_ATE_ATD IN TB_CAD_LOG_ERRO_PROCESSO.CAD_LOG_ATE_ATD%type DEFAULT NULL,
     pCAD_LOG_ATS_ATE IN TB_CAD_LOG_ERRO_PROCESSO.CAD_LOG_ATS_ATE%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_LOG_ERRO_PROCESSO.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_LOG_MSG_ERRO IN TB_CAD_LOG_ERRO_PROCESSO.CAD_LOG_MSG_ERRO%type DEFAULT NULL,
     pCAD_LOG_ORIGEM IN TB_CAD_LOG_ERRO_PROCESSO.CAD_LOG_ORIGEM%type DEFAULT NULL,
     pCAD_LOG_CLASS IN TB_CAD_LOG_ERRO_PROCESSO.CAD_LOG_CLASS%type DEFAULT NULL,
     pCAD_LOG_METODO IN TB_CAD_LOG_ERRO_PROCESSO.CAD_LOG_METODO%type DEFAULT NULL,
     pCAD_LOG_PRD_ID IN TB_CAD_LOG_ERRO_PROCESSO.CAD_LOG_PRD_ID%type DEFAULT NULL,
     pCAD_PRD_NM_MNEMONICO IN TB_CAD_LOG_ERRO_PROCESSO.CAD_PRD_NM_MNEMONICO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_LOG_ERRO_PROCESSO_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
v_cursor PKG_CURSOR.t_cursor;
begin
OPEN v_cursor FOR
SELECT	
       CAD_LOG_DATA_PROCESSO,
       CAD_LOG_ATE_ATD,
       CAD_LOG_ATS_ATE,
       SEG_USU_ID_USUARIO,
       CAD_LOG_MSG_ERRO,
       CAD_LOG_ORIGEM,
       CAD_LOG_CLASS,
       CAD_LOG_METODO,
       CAD_LOG_PRD_ID,
       CAD_PRD_NM_MNEMONICO
FROM TB_CAD_LOG_ERRO_PROCESSO
WHERE
        (pCAD_LOG_DATA_PROCESSO is null OR CAD_LOG_DATA_PROCESSO = pCAD_LOG_DATA_PROCESSO) AND 
        (pCAD_LOG_ATE_ATD is null OR CAD_LOG_ATE_ATD = pCAD_LOG_ATE_ATD) AND 
        (pCAD_LOG_ATS_ATE is null OR CAD_LOG_ATS_ATE = pCAD_LOG_ATS_ATE) AND 
        (pSEG_USU_ID_USUARIO is null OR SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO) AND 
        (pCAD_LOG_MSG_ERRO is null OR CAD_LOG_MSG_ERRO = pCAD_LOG_MSG_ERRO) AND 
        (pCAD_LOG_ORIGEM is null OR CAD_LOG_ORIGEM = pCAD_LOG_ORIGEM) AND 
        (pCAD_LOG_CLASS is null OR CAD_LOG_CLASS = pCAD_LOG_CLASS) AND 
        (pCAD_LOG_METODO is null OR CAD_LOG_METODO = pCAD_LOG_METODO) AND 
        (pCAD_LOG_PRD_ID is null OR CAD_LOG_PRD_ID = pCAD_LOG_PRD_ID) AND 
        (pCAD_PRD_NM_MNEMONICO is null OR CAD_PRD_NM_MNEMONICO = pCAD_PRD_NM_MNEMONICO);          
io_cursor := v_cursor;
end PRC_CAD_LOG_ERRO_PROCESSO_L;
