--PRC_ASS_TCP_TAB_CNV_PORTANES_L
create or replace procedure PRC_ASS_TCP_TAB_CNV_PORTANES_L
(
     pASS_TCP_ID IN TB_ASS_TCP_TAB_CNV_PORTE_ANES.ASS_TCP_ID%type DEFAULT NULL,
     pTIS_MED_CD_TABELAMEDICA IN TB_ASS_TCP_TAB_CNV_PORTE_ANES.TIS_MED_CD_TABELAMEDICA%type DEFAULT NULL,
     pASS_TCP_TP_PORTE IN TB_ASS_TCP_TAB_CNV_PORTE_ANES.ASS_TCP_TP_PORTE%type DEFAULT NULL,
     pCAD_TIH_TP_INDICE_HOSP IN TB_ASS_TCP_TAB_CNV_PORTE_ANES.CAD_TIH_TP_INDICE_HOSP%type DEFAULT NULL,
     pASS_TCP_QT_INDICE_HOSPITALAR IN TB_ASS_TCP_TAB_CNV_PORTE_ANES.ASS_TCP_QT_INDICE_HOSPITALAR%type DEFAULT NULL,
     pASS_TCP_DT_INICIO_VIGENCIA IN TB_ASS_TCP_TAB_CNV_PORTE_ANES.ASS_TCP_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pASS_TCP_DT_FIM_VIGENCIA IN TB_ASS_TCP_TAB_CNV_PORTE_ANES.ASS_TCP_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pASS_TCP_DT_ULTIMA_ATUALIZACAO IN TB_ASS_TCP_TAB_CNV_PORTE_ANES.ASS_TCP_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_TCP_TAB_CNV_PORTE_ANES.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_CNV_ID_CONVENIO IN TB_ASS_TCP_TAB_CNV_PORTE_ANES.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ASS_TCP_TAB_CNV_PORTANES_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
v_cursor PKG_CURSOR.t_cursor;
begin
OPEN v_cursor FOR
SELECT	
       ASS_TCP_ID,
       TIS_MED_CD_TABELAMEDICA,
       ASS_TCP_TP_PORTE,
       CAD_TIH_TP_INDICE_HOSP,
       ASS_TCP_QT_INDICE_HOSPITALAR,
       ASS_TCP_DT_INICIO_VIGENCIA,
       ASS_TCP_DT_FIM_VIGENCIA,
       ASS_TCP_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       CAD_CNV_ID_CONVENIO
FROM TB_ASS_TCP_TAB_CNV_PORTE_ANES
WHERE
        (pASS_TCP_ID is null OR ASS_TCP_ID = pASS_TCP_ID) AND 
        (pTIS_MED_CD_TABELAMEDICA is null OR TIS_MED_CD_TABELAMEDICA = pTIS_MED_CD_TABELAMEDICA) AND 
        (pASS_TCP_TP_PORTE is null OR ASS_TCP_TP_PORTE = pASS_TCP_TP_PORTE) AND 
        (pCAD_TIH_TP_INDICE_HOSP is null OR CAD_TIH_TP_INDICE_HOSP = pCAD_TIH_TP_INDICE_HOSP) AND 
        (pASS_TCP_QT_INDICE_HOSPITALAR is null OR ASS_TCP_QT_INDICE_HOSPITALAR = pASS_TCP_QT_INDICE_HOSPITALAR) AND 
        (pASS_TCP_DT_INICIO_VIGENCIA is null OR ASS_TCP_DT_INICIO_VIGENCIA = pASS_TCP_DT_INICIO_VIGENCIA) AND 
        (pASS_TCP_DT_FIM_VIGENCIA is null OR ASS_TCP_DT_FIM_VIGENCIA = pASS_TCP_DT_FIM_VIGENCIA) AND 
        (pASS_TCP_DT_ULTIMA_ATUALIZACAO is null OR ASS_TCP_DT_ULTIMA_ATUALIZACAO = pASS_TCP_DT_ULTIMA_ATUALIZACAO) AND 
        (pSEG_USU_ID_USUARIO is null OR SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO) AND 
        (pCAD_CNV_ID_CONVENIO is null OR CAD_CNV_ID_CONVENIO = pCAD_CNV_ID_CONVENIO);          
io_cursor := v_cursor;
end PRC_ASS_TCP_TAB_CNV_PORTANES_L;