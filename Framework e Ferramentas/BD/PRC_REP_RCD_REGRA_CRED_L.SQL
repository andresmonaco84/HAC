create or replace procedure PRC_REP_RCD_REGRA_CRED_L
(
     pREP_RCD_ID IN TB_REP_RCD_REGRA_CRED.REP_RCD_ID%type DEFAULT NULL,
     pREP_RCD_TP_CREDENCIA_PROF IN TB_REP_RCD_REGRA_CRED.REP_RCD_TP_CREDENCIA_PROF%type DEFAULT NULL,
     pREP_RCD_PC_CONTA IN TB_REP_RCD_REGRA_CRED.REP_RCD_PC_CONTA%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_REP_RCD_REGRA_CRED.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pREP_RCD_DT_ULTIMA_ATUALIZACAO IN TB_REP_RCD_REGRA_CRED.REP_RCD_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pREP_RCD_DS_DESCRICAO IN TB_REP_RCD_REGRA_CRED.REP_RCD_DS_DESCRICAO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
)
is
/********************************************************************
*    Procedure: PRC_REP_RCD_REGRA_CRED_L
*
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pREP_RCD_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND REP_RCD_ID = ' || pREP_RCD_ID; END IF;
IF pREP_RCD_TP_CREDENCIA_PROF IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND REP_RCD_TP_CREDENCIA_PROF = ' || CHR(39) || pREP_RCD_TP_CREDENCIA_PROF || CHR(39); END IF;
IF pREP_RCD_PC_CONTA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND REP_RCD_PC_CONTA = ' || pREP_RCD_PC_CONTA; END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pREP_RCD_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND REP_RCD_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pREP_RCD_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pREP_RCD_DS_DESCRICAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND REP_RCD_DS_DESCRICAO = ' || CHR(39) || pREP_RCD_DS_DESCRICAO || CHR(39); END IF;

   V_SELECT := '
SELECT
       REP_RCD_ID,
       REP_RCD_TP_CREDENCIA_PROF,
       REP_RCD_PC_CONTA,
       SEG_USU_ID_USUARIO,
       REP_RCD_DT_ULTIMA_ATUALIZACAO,
			 REP_RCD_DS_DESCRICAO
FROM TB_REP_RCD_REGRA_CRED
WHERE null is null  '    ;

OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_REP_RCD_REGRA_CRED_L;
