  --PRC_ATS_ACS_ATEND_CANCE_SADT_S
  create or replace procedure PRC_ATS_ACS_ATEND_CANCE_SADT_S 
(
     pATS_ATE_ID IN TB_ATS_ACS_ATEND_CANCE_SADT.ATS_ATE_ID%type DEFAULT NULL,
     pATS_ATE_IN_INTLIB IN TB_ATS_ACS_ATEND_CANCE_SADT.ATS_ATE_IN_INTLIB%type DEFAULT NULL,
     pATS_ATE_CD_INTLIB IN TB_ATS_ACS_ATEND_CANCE_SADT.ATS_ATE_CD_INTLIB%type DEFAULT NULL,
     pAUX_EPP_CD_ESPECPROC IN TB_ATS_ACS_ATEND_CANCE_SADT.AUX_EPP_CD_ESPECPROC%type DEFAULT NULL,
     pCAD_PRD_ID IN TB_ATS_ACS_ATEND_CANCE_SADT.CAD_PRD_ID%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ATS_ACS_ATEND_CANCE_SADT.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pATS_ACS_DT_ULTIMA_ATUALIZACAO IN TB_ATS_ACS_ATEND_CANCE_SADT.ATS_ACS_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pATS_ACS_DS_JUSTIFICATIVA_CAND IN TB_ATS_ACS_ATEND_CANCE_SADT.ATS_ACS_DS_JUSTIFICATIVA_CAND%type DEFAULT NULL,
     pATS_ACS_DT_CANCELAMENTO IN TB_ATS_ACS_ATEND_CANCE_SADT.ATS_ACS_DT_CANCELAMENTO%type DEFAULT NULL,
     pTIS_MED_CD_TABELAMEDICA IN TB_ATS_APS_ATEN_PROC_SEGMENTO.TIS_MED_CD_TABELAMEDICA%type	DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ATS_ACS_ATEND_CANCE_SADT_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pATS_ATE_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_ATE_ID = ' || pATS_ATE_ID; END IF;
IF pATS_ATE_IN_INTLIB IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_ATE_IN_INTLIB = ' || CHR(39) || pATS_ATE_IN_INTLIB || CHR(39); END IF;
IF pATS_ATE_CD_INTLIB IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_ATE_CD_INTLIB = ' || pATS_ATE_CD_INTLIB; END IF;
IF pAUX_EPP_CD_ESPECPROC IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AUX_EPP_CD_ESPECPROC = ' || CHR(39) || pAUX_EPP_CD_ESPECPROC || CHR(39); END IF;
IF pCAD_PRD_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_ID = ' || pCAD_PRD_ID; END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pATS_ACS_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_ACS_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pATS_ACS_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pATS_ACS_DS_JUSTIFICATIVA_CAND IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_ACS_DS_JUSTIFICATIVA_CAND = ' || CHR(39) || pATS_ACS_DS_JUSTIFICATIVA_CAND || CHR(39); END IF;
IF pATS_ACS_DT_CANCELAMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_ACS_DT_CANCELAMENTO = ' || CHR(39) || pATS_ACS_DT_CANCELAMENTO || CHR(39); END IF;
IF pTIS_MED_CD_TABELAMEDICA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_MED_CD_TABELAMEDICA = ' || CHR(39) || pTIS_MED_CD_TABELAMEDICA || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       ATS_ATE_ID,
       ATS_ATE_IN_INTLIB,
       ATS_ATE_CD_INTLIB,
       AUX_EPP_CD_ESPECPROC,
       CAD_PRD_ID,
       SEG_USU_ID_USUARIO,
       ATS_ACS_DT_ULTIMA_ATUALIZACAO,
       ATS_ACS_DS_JUSTIFICATIVA_CAND,
       ATS_ACS_DT_CANCELAMENTO,
       TIS_MED_CD_TABELAMEDICA
FROM TB_ATS_ACS_ATEND_CANCE_SADT
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ATS_ACS_ATEND_CANCE_SADT_S;