--PRC_CAD_NFM_NOTA_FIS_MAT_L
create or replace procedure PRC_CAD_NFM_NOTA_FIS_MAT_L 
(
     pCAD_NFM_ID IN TB_CAD_NFM_NOTA_FIS_MAT.CAD_NFM_ID%type DEFAULT NULL,
     pCAD_NFM_NR_NF IN TB_CAD_NFM_NOTA_FIS_MAT.CAD_NFM_NR_NF%type DEFAULT NULL,
     pCAD_NFM_DS_SERIE IN TB_CAD_NFM_NOTA_FIS_MAT.CAD_NFM_DS_SERIE%type DEFAULT NULL,
     pCAD_NFM_DT_EMISSAO IN TB_CAD_NFM_NOTA_FIS_MAT.CAD_NFM_DT_EMISSAO%type DEFAULT NULL,
     pCAD_NFM_NM_FORNECEDOR IN TB_CAD_NFM_NOTA_FIS_MAT.CAD_NFM_NM_FORNECEDOR%type DEFAULT NULL,
     pCAD_NFM_NR_CNPJ IN TB_CAD_NFM_NOTA_FIS_MAT.CAD_NFM_NR_CNPJ%type DEFAULT NULL,
     pCAD_NFM_VL_NF IN TB_CAD_NFM_NOTA_FIS_MAT.CAD_NFM_VL_NF%type DEFAULT NULL,
     pCAD_NFM_VL_VALOR IN TB_CAD_NFM_NOTA_FIS_MAT.CAD_NFM_VL_VALOR%type DEFAULT NULL,
     pCAD_PRD_ID IN TB_CAD_NFM_NOTA_FIS_MAT.CAD_PRD_ID%type DEFAULT NULL,
     pCAD_NFM_DS_OBSERVACAO IN TB_CAD_NFM_NOTA_FIS_MAT.CAD_NFM_DS_OBSERVACAO%type DEFAULT NULL,
     pCAD_NFM_DT_ULT_ATUALIZACAO IN TB_CAD_NFM_NOTA_FIS_MAT.CAD_NFM_DT_ULT_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_NFM_NOTA_FIS_MAT.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_NFM_PC_IPI IN TB_CAD_NFM_NOTA_FIS_MAT.CAD_NFM_PC_IPI%type DEFAULT NULL,
     pATD_ATE_ID IN TB_CAD_NFM_NOTA_FIS_MAT.ATD_ATE_ID%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_NFM_NOTA_FIS_MAT_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_NFM_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_NFM_ID = ' || pCAD_NFM_ID; END IF;
IF pCAD_NFM_NR_NF IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_NFM_NR_NF = ' || pCAD_NFM_NR_NF; END IF;
IF pCAD_NFM_DS_SERIE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_NFM_DS_SERIE = ' || CHR(39) || pCAD_NFM_DS_SERIE || CHR(39); END IF;
IF pCAD_NFM_DT_EMISSAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_NFM_DT_EMISSAO = ' || CHR(39) || pCAD_NFM_DT_EMISSAO || CHR(39); END IF;
IF pCAD_NFM_NM_FORNECEDOR IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_NFM_NM_FORNECEDOR = ' || CHR(39) || pCAD_NFM_NM_FORNECEDOR || CHR(39); END IF;
IF pCAD_NFM_NR_CNPJ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_NFM_NR_CNPJ = ' || pCAD_NFM_NR_CNPJ; END IF;
IF pCAD_NFM_VL_NF IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_NFM_VL_NF = ' || pCAD_NFM_VL_NF; END IF;
IF pCAD_NFM_VL_CALCULADO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_NFM_VL_CALCULADO = ' || pCAD_NFM_VL_CALCULADO; END IF;
IF pCAD_PRD_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_ID = ' || pCAD_PRD_ID; END IF;
IF pCAD_NFM_DS_OBSERVACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_NFM_DS_OBSERVACAO = ' || CHR(39) || pCAD_NFM_DS_OBSERVACAO || CHR(39); END IF;
IF pCAD_NFM_DT_ULT_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_NFM_DT_ULT_ATUALIZACAO = ' || CHR(39) || pCAD_NFM_DT_ULT_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pCAD_NFM_PC_IPI IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_NFM_PC_IPI = ' || pCAD_NFM_PC_IPI; END IF;
IF pATD_ATE_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_ATE_ID = ' || pATD_ATE_ID; END IF;
 
   V_SELECT := '
SELECT	
       CAD_NFM_ID,
       CAD_NFM_NR_NF,
       CAD_NFM_DS_SERIE,
       CAD_NFM_DT_EMISSAO,
       CAD_NFM_NM_FORNECEDOR,
       CAD_NFM_NR_CNPJ,
       CAD_NFM_VL_NF,
       CAD_NFM_VL_CALCULADO,
       CAD_PRD_ID,
       CAD_NFM_DS_OBSERVACAO,
       CAD_NFM_DT_ULT_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       CAD_NFM_PC_IPI,
       ATD_ATE_ID
FROM TB_CAD_NFM_NOTA_FIS_MAT
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_NFM_NOTA_FIS_MAT_L;
