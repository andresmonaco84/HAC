--PRC_CAD_VMH_VALOR_MOEDA_HOSP_S
create or replace procedure PRC_CAD_VMH_VALOR_MOEDA_HOSP_S 
(
     pCAD_TIH_TP_INDICE_HOSP IN TB_CAD_VMH_VALOR_MOEDA_HOSP.CAD_TIH_TP_INDICE_HOSP%type DEFAULT NULL,
     pCAD_VMH_VL_MOEDA_HOSPITALAR IN TB_CAD_VMH_VALOR_MOEDA_HOSP.CAD_VMH_VL_MOEDA_HOSPITALAR%type DEFAULT NULL,
     pCAD_VMH_DT_INICIO_VIGENCIA IN TB_CAD_VMH_VALOR_MOEDA_HOSP.CAD_VMH_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pCAD_VMH_DT_FIM_VIGENCIA IN TB_CAD_VMH_VALOR_MOEDA_HOSP.CAD_VMH_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pCAD_VMH_TP_MOEDA IN TB_CAD_VMH_VALOR_MOEDA_HOSP.CAD_VMH_TP_MOEDA%type DEFAULT NULL,
     pCAD_VMH_DT_ULTIMA_ATUALIZACAO IN TB_CAD_VMH_VALOR_MOEDA_HOSP.CAD_VMH_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_VMH_VALOR_MOEDA_HOSP.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_VMH_VALOR_MOEDA_HOSP_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_TIH_TP_INDICE_HOSP IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TIH_TP_INDICE_HOSP = ' || CHR(39) || pCAD_TIH_TP_INDICE_HOSP || CHR(39); END IF;
IF pCAD_VMH_VL_MOEDA_HOSPITALAR IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_VMH_VL_MOEDA_HOSPITALAR = ' || pCAD_VMH_VL_MOEDA_HOSPITALAR; END IF;
IF pCAD_VMH_DT_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_VMH_DT_INICIO_VIGENCIA = ' || CHR(39) || pCAD_VMH_DT_INICIO_VIGENCIA || CHR(39); END IF;
IF pCAD_VMH_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_VMH_DT_FIM_VIGENCIA = ' || CHR(39) || pCAD_VMH_DT_FIM_VIGENCIA || CHR(39); END IF;
IF pCAD_VMH_TP_MOEDA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_VMH_TP_MOEDA = ' || pCAD_VMH_TP_MOEDA; END IF;
IF pCAD_VMH_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_VMH_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_VMH_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       CAD_TIH_TP_INDICE_HOSP,
       CAD_VMH_VL_MOEDA_HOSPITALAR,
       CAD_VMH_DT_INICIO_VIGENCIA,
       CAD_VMH_DT_FIM_VIGENCIA,
       CAD_VMH_TP_MOEDA,
       CAD_VMH_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO
FROM TB_CAD_VMH_VALOR_MOEDA_HOSP
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_VMH_VALOR_MOEDA_HOSP_S;
