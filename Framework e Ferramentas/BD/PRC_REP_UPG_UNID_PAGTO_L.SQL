 
--PRC_REP_UPG_UNID_PAGTO_L
create or replace procedure PRC_REP_UPG_UNID_PAGTO_L 
(
     pCAD_CLC_ID IN TB_REP_UPG_UNID_PAGTO.CAD_CLC_ID%type DEFAULT NULL,
     pCAD_UNI_ID_UNIDADE IN TB_REP_UPG_UNID_PAGTO.CAD_UNI_ID_UNIDADE%type DEFAULT NULL,
     pCAD_UNI_ID_UNIDADE_PAGAMENTO IN TB_REP_UPG_UNID_PAGTO.CAD_UNI_ID_UNIDADE_PAGAMENTO%type DEFAULT NULL,
     pREP_PGM_TP_CREDENCIA_PROF IN TB_REP_UPG_UNID_PAGTO.REP_PGM_TP_CREDENCIA_PROF%type DEFAULT NULL,
     pREP_UPG_DT_INICIO_VIGENCIA IN TB_REP_UPG_UNID_PAGTO.REP_UPG_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pREP_UPG_DT_FIM_VIGENVCIA IN TB_REP_UPG_UNID_PAGTO.REP_UPG_DT_FIM_VIGENVCIA%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_REP_UPG_UNID_PAGTO.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pREP_UPG_DT_CRIACAO IN TB_REP_UPG_UNID_PAGTO.REP_UPG_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ALTERACAO IN TB_REP_UPG_UNID_PAGTO.SEG_USU_ID_USUARIO_ALTERACAO%type DEFAULT NULL,
     pREP_UPG_DT_ALTERACAO IN TB_REP_UPG_UNID_PAGTO.REP_UPG_DT_ALTERACAO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_REP_UPG_UNID_PAGTO_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_CLC_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CLC_ID = ' || pCAD_CLC_ID; END IF;
IF pCAD_UNI_ID_UNIDADE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_UNI_ID_UNIDADE = ' || pCAD_UNI_ID_UNIDADE; END IF;
IF pCAD_UNI_ID_UNIDADE_PAGAMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_UNI_ID_UNIDADE_PAGAMENTO = ' || pCAD_UNI_ID_UNIDADE_PAGAMENTO; END IF;
IF pREP_PGM_TP_CREDENCIA_PROF IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND REP_PGM_TP_CREDENCIA_PROF = ' || CHR(39) || pREP_PGM_TP_CREDENCIA_PROF || CHR(39); END IF;
IF pREP_UPG_DT_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND REP_UPG_DT_INICIO_VIGENCIA = ' || CHR(39) || pREP_UPG_DT_INICIO_VIGENCIA || CHR(39); END IF;
IF pREP_UPG_DT_FIM_VIGENVCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND REP_UPG_DT_FIM_VIGENVCIA = ' || CHR(39) || pREP_UPG_DT_FIM_VIGENVCIA || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pREP_UPG_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND REP_UPG_DT_CRIACAO = ' || CHR(39) || pREP_UPG_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_ALTERACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ALTERACAO = ' || pSEG_USU_ID_USUARIO_ALTERACAO; END IF;
IF pREP_UPG_DT_ALTERACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND REP_UPG_DT_ALTERACAO = ' || CHR(39) || pREP_UPG_DT_ALTERACAO || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       CAD_CLC_ID,
       CAD_UNI_ID_UNIDADE,
       CAD_UNI_ID_UNIDADE_PAGAMENTO,
       REP_PGM_TP_CREDENCIA_PROF,
       REP_UPG_DT_INICIO_VIGENCIA,
       REP_UPG_DT_FIM_VIGENVCIA,
       SEG_USU_ID_USUARIO,
       REP_UPG_DT_CRIACAO,
       SEG_USU_ID_USUARIO_ALTERACAO,
       REP_UPG_DT_ALTERACAO
FROM TB_REP_UPG_UNID_PAGTO
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_REP_UPG_UNID_PAGTO_L;
