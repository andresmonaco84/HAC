create or replace procedure PRC_ASS_CLP_CNV_UND_LOC_PLAV_S
  (
     pASS_CLP_ID IN TB_ASS_CLP_CNV_UND_LOC_PLANO.ASS_CLP_ID%type DEFAULT NULL,
     pCAD_PLA_ID_PLANO IN TB_CAD_PLA_PLANO.CAD_PLA_ID_PLANO%TYPE DEFAULT null,
     pCAD_UNI_ID_UNIDADE IN TB_ASS_CNU_CONVENIO_UNIDADE.CAD_UNI_ID_UNIDADE%type DEFAULT NULL,
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_ASS_CUL_CONV_UNI_LOCATEND.CAD_LAT_ID_LOCAL_ATENDIMENTO%type DEFAULT NULL,
     pCAD_CNV_ID_CONVENIO IN TB_CAD_CNV_CONVENIO.CAD_CNV_ID_CONVENIO%TYPE DEFAULT NULL,
     pASS_CLP_DT_FIM_VIGENCIA IN TB_ASS_CLP_CNV_UND_LOC_PLANO.ASS_CLP_DT_FIM_VIGENCIA%TYPE,
     io_cursor OUT PKG_CURSOR.t_cursor
  )
  is
  /********************************************************************
  *    Procedure: PRC_ASS_CLP_CNV_UND_LOC_PLAN_S
  *
  *    Data Criacao:   26/06/2007   Por: Bruno Costa
  *    Funcao: Verifica associações abaixo
  *
  *
  **********************************************************************/
  v_cursor PKG_CURSOR.t_cursor;
  begin
    OPEN v_cursor FOR
    SELECT
       CLP.ASS_CLP_ID,
       CLP.ASS_CUL_ID,
       CLP.ASS_CLP_DT_INI_VIGENCIA,
       CLP.ASS_CLP_DT_FIM_VIGENCIA,
       CLP.CAD_PLA_ID_PLANO,
       CLP.ASS_CLP_FL_BLOQUEIA_UNIDADE_OK,
       CLP.ASS_CLP_DT_ULTIMA_ATUALIZACAO,
       CLP.SEG_USU_ID_USUARIO
    FROM TB_ASS_CLP_CNV_UND_LOC_PLANO CLP,
         TB_ASS_CUL_CONV_UNI_LOCATEND CUL,
         TB_ASS_CNU_CONVENIO_UNIDADE CNU,
         TB_CAD_CNV_CONVENIO CNV
    WHERE
          CLP.ASS_CUL_ID = CUL.ASS_CUL_ID
    AND   CUL.ASS_CNU_ID = CNU.ASS_CNU_ID
    AND   CNV.CAD_CNV_ID_CONVENIO = CNU.CAD_CNV_ID_CONVENIO
    AND   (pCAD_CNV_ID_CONVENIO IS NULL OR CNV.CAD_CNV_ID_CONVENIO = pCAD_CNV_ID_CONVENIO)
    AND   (pCAD_UNI_ID_UNIDADE IS NULL OR CNU.CAD_UNI_ID_UNIDADE = pCAD_UNI_ID_UNIDADE)
    AND   (pCAD_LAT_ID_LOCAL_ATENDIMENTO IS NULL OR CUL.CAD_LAT_ID_LOCAL_ATENDIMENTO = pCAD_LAT_ID_LOCAL_ATENDIMENTO)
    AND   (pASS_CLP_ID is null OR CLP.ASS_CLP_ID = pASS_CLP_ID)
    AND   (pCAD_PLA_ID_PLANO is null OR CLP.CAD_PLA_ID_PLANO = pCAD_PLA_ID_PLANO)
    AND   (ASS_CLP_DT_FIM_VIGENCIA IS NULL OR ASS_CLP_DT_FIM_VIGENCIA > pASS_CLP_DT_FIM_VIGENCIA);
    io_cursor := v_cursor;
  end PRC_ASS_CLP_CNV_UND_LOC_PLAV_S;
