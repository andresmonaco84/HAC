) 
is
/********************************************************************
*    Procedure: PRC_ASS_CTU_CNV_TAB_UTILIZA_U
* 
*    Data Criacao: 19/2/2010	   Por: Pedro
*    Data Alteracao: 24/08/2010  Por: André S. Monaco
*         Alteracao: Adição do campo TIS_MED_CD_TABELAMEDICA_COBRA
*
*    Funcao: Atualização de registro de Convenio X Tabela Utilizada
*
*******************************************************************/  
begin
UPDATE TB_ASS_CTU_CNV_TAB_UTILIZA
SET	   
        CAD_CNV_ID_CONVENIO = pCAD_CNV_ID_CONVENIO,
        TIS_MED_CD_TABELAMEDICA = pTIS_MED_CD_TABELAMEDICA,
        ASS_CTU_DT_INICIO_VIGENCIA = pASS_CTU_DT_INICIO_VIGENCIA,
        SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO,
        ASS_CTU_DT_FIM_VIGENCIA = pASS_CTU_DT_FIM_VIGENCIA,
        ASS_CTU_DT_ULTIMA_ATUALIZACAO = pASS_CTU_DT_ULTIMA_ATUALIZACAO,
        CAD_TAP_TP_ATRIBUTO = pCAD_TAP_TP_ATRIBUTO,
        TIS_MED_CD_TABELAMEDICA_COBRA = pTIS_MED_CD_TABELAMEDICA_COBRA
WHERE
        ASS_CTU_ID = pASS_CTU_ID;	
