

--PRC_FAT_PGP_PER_GRAUPARPROF_U
create or replace procedure PRC_FAT_PGP_PER_GRAUPARPROF_U
(
     pTIS_GPP_CD_GRAU_PART_PROF IN TB_FAT_PGP_PER_GRAUPARPROF.TIS_GPP_CD_GRAU_PART_PROF%type,
     pFAT_PGP_FL_CIR_SIMULTANEA IN TB_FAT_PGP_PER_GRAUPARPROF.FAT_PGP_FL_CIR_SIMULTANEA%type,
     pFAT_PGP_FL_CIR_BILATERAL IN TB_FAT_PGP_PER_GRAUPARPROF.FAT_PGP_FL_CIR_BILATERAL%type,
     pFAT_PGP_FL_CIR_EQUIPE_DIF IN TB_FAT_PGP_PER_GRAUPARPROF.FAT_PGP_FL_CIR_EQUIPE_DIF%type,
     pFAT_PGP_PC_PARTICIPACAO IN TB_FAT_PGP_PER_GRAUPARPROF.FAT_PGP_PC_PARTICIPACAO%type,
     pFAT_PGP_FL_STATUS IN TB_FAT_PGP_PER_GRAUPARPROF.FAT_PGP_FL_STATUS%type,
     pFAT_PGP_DT_ULTIMA_ATUALIZACAO IN TB_FAT_PGP_PER_GRAUPARPROF.FAT_PGP_DT_ULTIMA_ATUALIZACAO%type,
     pSEG_USU_ID_USUARIO IN TB_FAT_PGP_PER_GRAUPARPROF.SEG_USU_ID_USUARIO%type,
     pTIS_TVA_CD_VIAACESSO IN TB_FAT_PGP_PER_GRAUPARPROF.TIS_TVA_CD_VIAACESSO%type
) 
is
/********************************************************************
*    Procedure: PRC_FAT_PGP_PER_GRAUPARPROF_U
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/  
begin
UPDATE TB_FAT_PGP_PER_GRAUPARPROF
SET	   
        FAT_PGP_FL_STATUS = pFAT_PGP_FL_STATUS,
        FAT_PGP_DT_ULTIMA_ATUALIZACAO = pFAT_PGP_DT_ULTIMA_ATUALIZACAO,
        SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO 
WHERE
        FAT_PGP_FL_CIR_BILATERAL = pFAT_PGP_FL_CIR_BILATERAL
    AND FAT_PGP_FL_CIR_EQUIPE_DIF = pFAT_PGP_FL_CIR_EQUIPE_DIF
    AND FAT_PGP_FL_CIR_SIMULTANEA = pFAT_PGP_FL_CIR_SIMULTANEA
    AND FAT_PGP_PC_PARTICIPACAO = pFAT_PGP_PC_PARTICIPACAO
    AND TIS_GPP_CD_GRAU_PART_PROF = pTIS_GPP_CD_GRAU_PART_PROF
    AND TIS_TVA_CD_VIAACESSO = pTIS_TVA_CD_VIAACESSO;	
end PRC_FAT_PGP_PER_GRAUPARPROF_U;

