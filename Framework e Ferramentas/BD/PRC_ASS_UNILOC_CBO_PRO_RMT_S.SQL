  --PRC_ASS_UNILOC_CBO_PRO_RMT_S
  create or replace procedure PRC_ASS_UNILOC_CBO_PRO_RMT_S 
  (
     pASS_UCP_QTD_AGEND_PERM_CBOS IN TB_ASS_UCP_UNID_LOC_CBO_PROF.ASS_UCP_QTD_AGEND_PERM_CBOS%type DEFAULT NULL,
     pASS_UCP_DS_OBS IN TB_ASS_UCP_UNID_LOC_CBO_PROF.ASS_UCP_DS_OBS%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_UCP_UNID_LOC_CBO_PROF.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pASS_UCP_DT_ULTIMA_ATUALIZACAO IN TB_ASS_UCP_UNID_LOC_CBO_PROF.ASS_UCP_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pCAD_UNI_ID_UNIDADE IN TB_ASS_UCP_UNID_LOC_CBO_PROF.CAD_UNI_ID_UNIDADE%type DEFAULT NULL,
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_ASS_UCP_UNID_LOC_CBO_PROF.CAD_LAT_ID_LOCAL_ATENDIMENTO%type DEFAULT NULL,
     pTIS_CBO_CD_CBOS IN TB_ASS_UCP_UNID_LOC_CBO_PROF.TIS_CBO_CD_CBOS%type DEFAULT NULL,
     pCAD_PRO_ID_PROFISSIONAL IN TB_ASS_UCP_UNID_LOC_CBO_PROF.CAD_PRO_ID_PROFISSIONAL%type DEFAULT NULL,
     pASS_UCP_FL_STATUS IN TB_ASS_UCP_UNID_LOC_CBO_PROF.ASS_UCP_FL_STATUS%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
  ) 
  is
  /********************************************************************
  *    Procedure: PRC_ASS_UNILOC_CBO_PRO_RMT_S
  * 
  *    Data Criacao: 	data da  criação   Por: Nome do Analista
  *    Data Alteracao:	data da alteração  Por: Nome do Analista
  *
  *    Funcao: Descrição da funcionalidade da Stored Procedure
  *
  *******************************************************************/
  v_cursor PKG_CURSOR.t_cursor;
  begin
    OPEN v_cursor FOR
    SELECT	
       ASS_UCP_QTD_AGEND_PERM_CBOS,
       ASS_UCP_DS_OBS,
       SEG_USU_ID_USUARIO,
       ASS_UCP_DT_ULTIMA_ATUALIZACAO,
       CAD_UNI_ID_UNIDADE,
       CAD_LAT_ID_LOCAL_ATENDIMENTO,
       TIS_CBO_CD_CBOS,
       CAD_PRO_ID_PROFISSIONAL,
       ASS_UCP_FL_STATUS
    FROM TB_ASS_UCP_UNID_LOC_CBO_PROF
    WHERE
        (pASS_UCP_QTD_AGEND_PERM_CBOS is null OR ASS_UCP_QTD_AGEND_PERM_CBOS = pASS_UCP_QTD_AGEND_PERM_CBOS) AND 
        (pASS_UCP_DS_OBS is null OR ASS_UCP_DS_OBS = pASS_UCP_DS_OBS) AND 
        (pSEG_USU_ID_USUARIO is null OR SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO) AND 
        (pASS_UCP_DT_ULTIMA_ATUALIZACAO is null OR ASS_UCP_DT_ULTIMA_ATUALIZACAO = pASS_UCP_DT_ULTIMA_ATUALIZACAO) AND 
        (pCAD_UNI_ID_UNIDADE is null OR CAD_UNI_ID_UNIDADE = pCAD_UNI_ID_UNIDADE) AND 
        (pCAD_LAT_ID_LOCAL_ATENDIMENTO is null OR CAD_LAT_ID_LOCAL_ATENDIMENTO = pCAD_LAT_ID_LOCAL_ATENDIMENTO) AND 
        (pTIS_CBO_CD_CBOS is null OR TIS_CBO_CD_CBOS = pTIS_CBO_CD_CBOS) AND 
        (pCAD_PRO_ID_PROFISSIONAL is null OR CAD_PRO_ID_PROFISSIONAL = pCAD_PRO_ID_PROFISSIONAL) AND 
        (pASS_UCP_FL_STATUS is null OR ASS_UCP_FL_STATUS = pASS_UCP_FL_STATUS);          
    io_cursor := v_cursor;
  end PRC_ASS_UNILOC_CBO_PRO_RMT_S;
