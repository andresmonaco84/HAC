--PRC_CAD_PSF_PROF_SERV_FREQ_L
create or replace procedure PRC_CAD_PSF_PROF_SERV_FREQ_L 
(
     pCAD_PSF_ID IN TB_CAD_PSF_PROF_SERV_FREQ.CAD_PSF_ID%type DEFAULT NULL,
     pCAD_PSF_NM_SERVICO_FREQ IN TB_CAD_PSF_PROF_SERV_FREQ.CAD_PSF_NM_SERVICO_FREQ%type DEFAULT NULL,
     pCAD_PSF_NR_TELEFONE IN TB_CAD_PSF_PROF_SERV_FREQ.CAD_PSF_NR_TELEFONE%type DEFAULT NULL,
     pCAD_PRO_ID_PROFISSIONAL IN TB_CAD_PSF_PROF_SERV_FREQ.CAD_PRO_ID_PROFISSIONAL%type DEFAULT NULL,
     pCAD_PSF_DT_ULTIMA_ATUALIZACAO IN TB_CAD_PSF_PROF_SERV_FREQ.CAD_PSF_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_PSF_PROF_SERV_FREQ.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_PSF_PROF_SERV_FREQ_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_PSF_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PSF_ID = ' || pCAD_PSF_ID; END IF;
IF pCAD_PSF_NM_SERVICO_FREQ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PSF_NM_SERVICO_FREQ = ' || CHR(39) || pCAD_PSF_NM_SERVICO_FREQ || CHR(39); END IF;
IF pCAD_PSF_NR_TELEFONE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PSF_NR_TELEFONE = ' || CHR(39) || pCAD_PSF_NR_TELEFONE || CHR(39); END IF;
IF pCAD_PRO_ID_PROFISSIONAL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRO_ID_PROFISSIONAL = ' || pCAD_PRO_ID_PROFISSIONAL; END IF;
IF pCAD_PSF_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PSF_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_PSF_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       CAD_PSF_ID,
       CAD_PSF_NM_SERVICO_FREQ,
       CAD_PSF_NR_TELEFONE,
       CAD_PRO_ID_PROFISSIONAL,
       CAD_PSF_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO
FROM TB_CAD_PSF_PROF_SERV_FREQ
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_PSF_PROF_SERV_FREQ_L;
