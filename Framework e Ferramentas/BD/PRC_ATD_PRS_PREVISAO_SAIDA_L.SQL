create or replace procedure PRC_ATD_PRS_PREVISAO_SAIDA_L
(
     pATD_PRS_ID IN TB_ATD_PRS_PREVISAO_SAIDA.ATD_PRS_ID%type DEFAULT NULL,
     pATD_ATE_ID IN TB_ATD_PRS_PREVISAO_SAIDA.ATD_ATE_ID%type DEFAULT NULL,
     pATD_PRS_DT_SAIDA IN TB_ATD_PRS_PREVISAO_SAIDA.ATD_PRS_DT_SAIDA%type DEFAULT NULL,
     pATD_PRS_DT_SAIDA_UTI IN TB_ATD_PRS_PREVISAO_SAIDA.ATD_PRS_DT_SAIDA_UTI%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ATD_PRS_PREVISAO_SAIDA.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_PRO_NR_CONSELHO IN TB_ATD_PRS_PREVISAO_SAIDA.CAD_PRO_NR_CONSELHO%type DEFAULT NULL,
     pATD_PRS_DT_CRIACAO IN TB_ATD_PRS_PREVISAO_SAIDA.ATD_PRS_DT_CRIACAO%type DEFAULT NULL,
   --  pATD_PRS_FL_CONFIRMACAO IN TB_ATD_PRS_PREVISAO_SAIDA.ATD_PRS_FL_CONFIRMACAO%type DEFAULT NULL,
   --  pSEG_USU_ID_USUARIO_CONF IN TB_ATD_PRS_PREVISAO_SAIDA.SEG_USU_ID_USUARIO_CONF%type DEFAULT NULL,
   --  pCAD_PRO_NR_CONSELHO_CONF IN TB_ATD_PRS_PREVISAO_SAIDA.CAD_PRO_NR_CONSELHO_CONF%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
)
is
/********************************************************************
*    Procedure: PRC_ATD_PRS_PREVISAO_SAIDA_L
*
*    Data Criacao: 	data da  cria??o   Por: Nome do Analista
*    Data Alteracao:	data da altera??o  Por: Nome do Analista
*
*    Funcao: Descri??o da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
 IF pATD_PRS_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PRS_ID = ' || pATD_PRS_ID; END IF;
IF pATD_ATE_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_ATE_ID = ' || pATD_ATE_ID; END IF;
IF pATD_PRS_DT_SAIDA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PRS_DT_SAIDA = ' || CHR(39) || pATD_PRS_DT_SAIDA || CHR(39); END IF;
IF pATD_PRS_DT_SAIDA_UTI IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PRS_DT_SAIDA_UTI = ' || CHR(39) || pATD_PRS_DT_SAIDA_UTI || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pCAD_PRO_NR_CONSELHO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRO_NR_CONSELHO = ' || CHR(39) || pCAD_PRO_NR_CONSELHO || CHR(39); END IF;
IF pATD_PRS_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PRS_DT_CRIACAO = ' || CHR(39) || pATD_PRS_DT_CRIACAO || CHR(39); END IF;

--IF pATD_PRS_FL_CONFIRMACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PRS_FL_CONFIRMACAO = ' || CHR(39) || pATD_PRS_DT_CRIACAO || CHR(39); END IF;
--IF pSEG_USU_ID_USUARIO_CONF IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CONF = ' || pATD_PRS_DT_CRIACAO; END IF;
--IF pCAD_PRO_NR_CONSELHO_CONF IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRO_NR_CONSELHO_CONF = ' || CHR(39) || pATD_PRS_DT_CRIACAO || CHR(39); END IF;

   V_SELECT := '
SELECT
       ATD_PRS_ID,
       ATD_ATE_ID,
       ATD_PRS_DT_SAIDA,
       ATD_PRS_DT_SAIDA_UTI,
       SEG_USU_ID_USUARIO,
       CAD_PRO_NR_CONSELHO,
       ATD_PRS_DT_CRIACAO--,
    --   ATD_PRS_FL_CONFIRMACAO,
     --  SEG_USU_ID_USUARIO_CONF,
     --  CAD_PRO_NR_CONSELHO_CONF
FROM TB_ATD_PRS_PREVISAO_SAIDA
WHERE null is null  '    ;

OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ATD_PRS_PREVISAO_SAIDA_L;
