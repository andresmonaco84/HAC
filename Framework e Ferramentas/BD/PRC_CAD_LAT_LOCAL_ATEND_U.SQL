CREATE OR REPLACE PROCEDURE "PRC_CAD_LAT_LOCAL_ATEND_U" (
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_CAD_LAT_LOCAL_ATENDIMENTO.CAD_LAT_ID_LOCAL_ATENDIMENTO%type,
     pCAD_LAT_DS_LOCAL_ATENDIMENTO IN TB_CAD_LAT_LOCAL_ATENDIMENTO.CAD_LAT_DS_LOCAL_ATENDIMENTO%type,
     pCAD_LAT_FL_ATIVO_OK IN TB_CAD_LAT_LOCAL_ATENDIMENTO.CAD_LAT_FL_ATIVO_OK%type,
     pSEG_USU_ID_USUARIO IN TB_CAD_LAT_LOCAL_ATENDIMENTO.SEG_USU_ID_USUARIO%type,
     pCAD_LAT_CD_LOCAL_ATENDIMENTO IN TB_CAD_LAT_LOCAL_ATENDIMENTO.CAD_LAT_CD_LOCAL_ATENDIMENTO%type,
     pCAD_LAT_FL_PERMITE_ATEND IN TB_CAD_LAT_LOCAL_ATENDIMENTO.CAD_LAT_FL_PERMITE_ATEND%type
  )
  is
  /********************************************************************
  *    Procedure: PRC_CAD_LAT_LOCAL_ATEND_U
  *
  *    Data Criacao: 	23/04/2007   Por: Andrea Cazuca
  *    Data Alteracao:	data da alteração  Por: Nome do Analista
  *
  *    Funcao: Atualiza os Locais de Atendimentos
  *
  *    Data Alteracao: 25/03/2010  Por: Davi Silvestre M. dos Reis
  *    Alteracao: Inclusao do campo CAD_LAT_FL_PERMITE_ATEND (tarefa 8107)
  *
  *******************************************************************/
  begin
    UPDATE TB_CAD_LAT_LOCAL_ATENDIMENTO
    SET
        CAD_LAT_DS_LOCAL_ATENDIMENTO = UPPER(pCAD_LAT_DS_LOCAL_ATENDIMENTO),
        CAD_LAT_FL_ATIVO_OK = pCAD_LAT_FL_ATIVO_OK,
        CAD_LAT_DT_ULTIMA_ATUALIZACAO = sysdate,
        SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO,
        CAD_LAT_CD_LOCAL_ATENDIMENTO = UPPER(pCAD_LAT_CD_LOCAL_ATENDIMENTO),
        CAD_LAT_FL_PERMITE_ATEND = UPPER(pCAD_LAT_FL_PERMITE_ATEND)       
    WHERE
        CAD_LAT_ID_LOCAL_ATENDIMENTO = pCAD_LAT_ID_LOCAL_ATENDIMENTO;
  end PRC_CAD_LAT_LOCAL_ATEND_U;
/
