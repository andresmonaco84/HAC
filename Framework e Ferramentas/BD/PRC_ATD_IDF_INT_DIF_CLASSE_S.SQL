--PRC_ATD_IDF_INT_DIF_CLASSE_S
create or replace procedure PRC_ATD_IDF_INT_DIF_CLASSE_S 
(
     pATD_IDF_ID IN TB_ATD_IDF_INT_DIF_CLASSE.ATD_IDF_ID%type DEFAULT NULL,
     pATD_ATE_ID IN TB_ATD_IDF_INT_DIF_CLASSE.ATD_ATE_ID%type DEFAULT NULL,
     pATD_IML_ID IN TB_ATD_IDF_INT_DIF_CLASSE.ATD_IML_ID%type DEFAULT NULL,
     pATD_IDF_DS_DIF_CLASSE IN TB_ATD_IDF_INT_DIF_CLASSE.ATD_IDF_DS_DIF_CLASSE%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_DIF_CLASSE IN TB_ATD_IDF_INT_DIF_CLASSE.SEG_USU_ID_USUARIO_DIF_CLASSE%type DEFAULT NULL,
     pATD_IDF_DT_DIF_CLASSE IN TB_ATD_IDF_INT_DIF_CLASSE.ATD_IDF_DT_DIF_CLASSE%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ATD_IDF_INT_DIF_CLASSE.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pATD_INC_DT_ULTIMA_ATUALIZACAO IN TB_ATD_IDF_INT_DIF_CLASSE.ATD_INC_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ATD_IDF_INT_DIF_CLASSE_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pATD_IDF_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_IDF_ID = ' || pATD_IDF_ID; END IF;
IF pATD_ATE_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_ATE_ID = ' || pATD_ATE_ID; END IF;
IF pATD_IML_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_IML_ID = ' || pATD_IML_ID; END IF;
IF pATD_IDF_DS_DIF_CLASSE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_IDF_DS_DIF_CLASSE = ' || CHR(39) || pATD_IDF_DS_DIF_CLASSE || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_DIF_CLASSE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_DIF_CLASSE = ' || pSEG_USU_ID_USUARIO_DIF_CLASSE; END IF;
IF pATD_IDF_DT_DIF_CLASSE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_IDF_DT_DIF_CLASSE = ' || CHR(39) || pATD_IDF_DT_DIF_CLASSE || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pATD_INC_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_INC_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pATD_INC_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       ATD_IDF_ID,
       ATD_ATE_ID,
       ATD_IML_ID,
       ATD_IDF_DS_DIF_CLASSE,
       SEG_USU_ID_USUARIO_DIF_CLASSE,
       ATD_IDF_DT_DIF_CLASSE,
       SEG_USU_ID_USUARIO,
       ATD_INC_DT_ULTIMA_ATUALIZACAO
FROM TB_ATD_IDF_INT_DIF_CLASSE
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ATD_IDF_INT_DIF_CLASSE_S;
