--PRC_CAD_PEC_PERCLME_R_L
create or replace procedure PRC_CAD_PEC_PERCLME_R_L 
(
     pCAD_PEC_ID IN TB_CAD_PEC_PERCLME.CAD_PEC_ID%type DEFAULT NULL,
     pCAD_CME_CLASSIF_MED IN TB_CAD_PEC_PERCLME.CAD_CME_CLASSIF_MED%type DEFAULT NULL,
     pCAD_PEC_PC_CLASSIF_MED IN TB_CAD_PEC_PERCLME.CAD_PEC_PC_CLASSIF_MED%type DEFAULT NULL,
     pCAD_PEC_PC_ICMS IN TB_CAD_PEC_PERCLME.CAD_PEC_PC_ICMS%type DEFAULT NULL,
     pCAD_PEC_DT_INICIO_VIGENCIA IN TB_CAD_PEC_PERCLME.CAD_PEC_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pCAD_PEC_DT_FIM_VIGENCIA IN TB_CAD_PEC_PERCLME.CAD_PEC_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pCAD_PEC_DT_ULTIMA_ATUALIZACAO IN TB_CAD_PEC_PERCLME.CAD_PEC_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_PEC_PERCLME.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_PEC_PERCLME_R_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_PEC_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PEC_ID = ' || pCAD_PEC_ID; END IF;
IF pCAD_CME_CLASSIF_MED IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CME_CLASSIF_MED = ' || pCAD_CME_CLASSIF_MED; END IF;
IF pCAD_PEC_PC_CLASSIF_MED IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PEC_PC_CLASSIF_MED = ' || pCAD_PEC_PC_CLASSIF_MED; END IF;
IF pCAD_PEC_PC_ICMS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PEC_PC_ICMS = ' || pCAD_PEC_PC_ICMS; END IF;
IF pCAD_PEC_DT_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PEC_DT_INICIO_VIGENCIA = ' || CHR(39) || pCAD_PEC_DT_INICIO_VIGENCIA || CHR(39); END IF;
IF pCAD_PEC_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PEC_DT_FIM_VIGENCIA = ' || CHR(39) || pCAD_PEC_DT_FIM_VIGENCIA || CHR(39); END IF;
IF pCAD_PEC_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PEC_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_PEC_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       CAD_PEC_ID,
       CAD_CME_CLASSIF_MED,
       CAD_PEC_PC_CLASSIF_MED,
       CAD_PEC_PC_ICMS,
       CAD_PEC_DT_INICIO_VIGENCIA,
       CAD_PEC_DT_FIM_VIGENCIA,
       CAD_PEC_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO
FROM TB_CAD_PEC_PERCLME
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_PEC_PERCLME_R_L;
