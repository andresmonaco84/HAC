--PRC_ASS_PRC_PROCED_CBOS_L
create or replace procedure PRC_ASS_PRC_PROCED_CBOS_L 
(
     pTIS_CBO_CD_CBOS_HAC IN TB_ASS_PRC_PROCED_CBOS.TIS_CBO_CD_CBOS_HAC%type DEFAULT NULL,
     pTIS_CBO_DS_CBOS_HAC IN TB_ASS_PRC_PROCED_CBOS.TIS_CBO_DS_CBOS_HAC%type DEFAULT NULL,
     pAUX_EPP_CD_ESPECPROC IN TB_ASS_PRC_PROCED_CBOS.AUX_EPP_CD_ESPECPROC%type DEFAULT NULL,
     pAUX_GPC_CD_GRUPOPROC IN TB_ASS_PRC_PROCED_CBOS.AUX_GPC_CD_GRUPOPROC%type DEFAULT NULL,
     pTIS_MED_CD_TABELAMEDICA IN TB_ASS_PRC_PROCED_CBOS.TIS_MED_CD_TABELAMEDICA%type DEFAULT NULL,
     pCAD_PRD_CD_CODIGO IN TB_ASS_PRC_PROCED_CBOS.CAD_PRD_CD_CODIGO%type DEFAULT NULL,
     pCAD_PRD_DS_DESCRICAO IN TB_ASS_PRC_PROCED_CBOS.CAD_PRD_DS_DESCRICAO%type DEFAULT NULL,
     pCAD_PRD_NM_MNEMONICO IN TB_ASS_PRC_PROCED_CBOS.CAD_PRD_NM_MNEMONICO%type DEFAULT NULL,
     pASS_PRC_FL_STATUS IN TB_ASS_PRC_PROCED_CBOS.ASS_PRC_FL_STATUS%type DEFAULT NULL,
     pASS_PRC_DT_INICIO_VIGENCIA IN TB_ASS_PRC_PROCED_CBOS.ASS_PRC_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pASS_PRC_DT_ULTIMA_ATUALIZACAO IN TB_ASS_PRC_PROCED_CBOS.ASS_PRC_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_DS_LOGIN IN TB_ASS_PRC_PROCED_CBOS.SEG_USU_DS_LOGIN%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ASS_PRC_PROCED_CBOS_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pTIS_CBO_CD_CBOS_HAC IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_CBO_CD_CBOS_HAC = ' || CHR(39) || pTIS_CBO_CD_CBOS_HAC || CHR(39); END IF;
IF pTIS_CBO_DS_CBOS_HAC IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_CBO_DS_CBOS_HAC = ' || CHR(39) || pTIS_CBO_DS_CBOS_HAC || CHR(39); END IF;
IF pAUX_EPP_CD_ESPECPROC IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AUX_EPP_CD_ESPECPROC = ' || CHR(39) || pAUX_EPP_CD_ESPECPROC || CHR(39); END IF;
IF pAUX_GPC_CD_GRUPOPROC IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AUX_GPC_CD_GRUPOPROC = ' || CHR(39) || pAUX_GPC_CD_GRUPOPROC || CHR(39); END IF;
IF pTIS_MED_CD_TABELAMEDICA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_MED_CD_TABELAMEDICA = ' || CHR(39) || pTIS_MED_CD_TABELAMEDICA || CHR(39); END IF;
IF pCAD_PRD_CD_CODIGO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_CD_CODIGO = ' || CHR(39) || pCAD_PRD_CD_CODIGO || CHR(39); END IF;
IF pCAD_PRD_DS_DESCRICAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_DS_DESCRICAO = ' || CHR(39) || pCAD_PRD_DS_DESCRICAO || CHR(39); END IF;
IF pCAD_PRD_NM_MNEMONICO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_NM_MNEMONICO = ' || CHR(39) || pCAD_PRD_NM_MNEMONICO || CHR(39); END IF;
IF pASS_PRC_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PRC_FL_STATUS = ' || CHR(39) || pASS_PRC_FL_STATUS || CHR(39); END IF;
IF pASS_PRC_DT_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PRC_DT_INICIO_VIGENCIA = ' || CHR(39) || pASS_PRC_DT_INICIO_VIGENCIA || CHR(39); END IF;
IF pASS_PRC_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PRC_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pASS_PRC_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_DS_LOGIN IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_DS_LOGIN = ' || CHR(39) || pSEG_USU_DS_LOGIN || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       TIS_CBO_CD_CBOS_HAC,
       TIS_CBO_DS_CBOS_HAC,
       AUX_EPP_CD_ESPECPROC,
       AUX_GPC_CD_GRUPOPROC,
       TIS_MED_CD_TABELAMEDICA,
       CAD_PRD_CD_CODIGO,
       CAD_PRD_DS_DESCRICAO,
       CAD_PRD_NM_MNEMONICO,
       ASS_PRC_FL_STATUS,
       ASS_PRC_DT_INICIO_VIGENCIA,
       ASS_PRC_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_DS_LOGIN
FROM TB_ASS_PRC_PROCED_CBOS
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ASS_PRC_PROCED_CBOS_L;
