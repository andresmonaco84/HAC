--PRC_CAD_UMC_UNI_MED_CONSUMO_L
create or replace procedure PRC_CAD_UMC_UNI_MED_CONSUMO_L 
(
     pCAD_UMC_CD_MEDIDA_CONSUMO IN TB_CAD_UMC_UNI_MED_CONSUMO.CAD_UMC_CD_MEDIDA_CONSUMO%type DEFAULT NULL,
     pCAD_UMC_DS_MEDIDA_CONSUMO IN TB_CAD_UMC_UNI_MED_CONSUMO.CAD_UMC_DS_MEDIDA_CONSUMO%type DEFAULT NULL,
     pCAD_UMC_FL_STATUS IN TB_CAD_UMC_UNI_MED_CONSUMO.CAD_UMC_FL_STATUS%type DEFAULT NULL,
     pCAD_UML_DT_ULTIMA_ATUALIZACAO IN TB_CAD_UMC_UNI_MED_CONSUMO.CAD_UML_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_UMC_UNI_MED_CONSUMO.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_UMC_UNI_MED_CONSUMO_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_UMC_CD_MEDIDA_CONSUMO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_UMC_CD_MEDIDA_CONSUMO = ' || CHR(39) || pCAD_UMC_CD_MEDIDA_CONSUMO || CHR(39); END IF;
IF pCAD_UMC_DS_MEDIDA_CONSUMO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_UMC_DS_MEDIDA_CONSUMO = ' || CHR(39) || pCAD_UMC_DS_MEDIDA_CONSUMO || CHR(39); END IF;
IF pCAD_UMC_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_UMC_FL_STATUS = ' || CHR(39) || pCAD_UMC_FL_STATUS || CHR(39); END IF;
IF pCAD_UML_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_UML_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_UML_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       CAD_UMC_CD_MEDIDA_CONSUMO,
       CAD_UMC_DS_MEDIDA_CONSUMO,
       CAD_UMC_FL_STATUS,
       CAD_UML_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO
FROM TB_CAD_UMC_UNI_MED_CONSUMO
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_UMC_UNI_MED_CONSUMO_L;
