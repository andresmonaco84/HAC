  --PRC_INT_ASS_CUT_S
  create or replace procedure PRC_INT_ASS_CUT_S 
  (
     pASS_CUT_ID IN TB_ASS_CUT_CONV_UNI_TPACOMOD.ASS_CUT_ID%type DEFAULT NULL,
     pASS_CUT_DT_INI_VIGENCIA IN TB_ASS_CUT_CONV_UNI_TPACOMOD.ASS_CUT_DT_INI_VIGENCIA%type DEFAULT NULL,
     pASS_CUT_DS_MOT_FIM_VIGENCIA IN TB_ASS_CUT_CONV_UNI_TPACOMOD.ASS_CUT_DS_MOTIVO_FIM_VIGENCIA%type DEFAULT NULL,
     pASS_CUT_DT_FIM_VIGENCIA IN TB_ASS_CUT_CONV_UNI_TPACOMOD.ASS_CUT_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pASS_CUT_DT_ULT_ATUALIZACAO IN TB_ASS_CUT_CONV_UNI_TPACOMOD.ASS_CUT_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_CUT_CONV_UNI_TPACOMOD.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pASS_UTA_ID_UNID_TPACOMOD IN TB_ASS_CUT_CONV_UNI_TPACOMOD.ASS_UTA_ID_UNID_TPACOMOD%type DEFAULT NULL,
     pASS_CNU_ID IN TB_ASS_CUT_CONV_UNI_TPACOMOD.ASS_CNU_ID%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
  ) 
  is
  /********************************************************************
  *    Procedure: PRC_INT_ASS_CUT_S
  * 
  *    Data Criacao: 	data da  criação   Por: Nome do Analista
  *    Data Alteracao:	data da alteração  Por: Nome do Analista
  *
  *    Funcao: Descrição da funcionalidade da Stored Procedure
  *
  *******************************************************************/
  v_cursor PKG_CURSOR.t_cursor;
  begin
    OPEN v_cursor FOR
    SELECT	
       ASS_CUT_ID,
       ASS_CUT_DT_INI_VIGENCIA,
       ASS_CUT_DS_MOTIVO_FIM_VIGENCIA,
       ASS_CUT_DT_FIM_VIGENCIA,
       ASS_CUT_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       ASS_UTA_ID_UNID_TPACOMOD,
       ASS_CNU_ID
    FROM TB_ASS_CUT_CONV_UNI_TPACOMOD
    WHERE
        (pASS_CUT_ID is null OR ASS_CUT_ID = pASS_CUT_ID) AND 
        (pASS_CUT_DT_INI_VIGENCIA is null OR ASS_CUT_DT_INI_VIGENCIA = pASS_CUT_DT_INI_VIGENCIA) AND 
        (pASS_CUT_DS_MOT_FIM_VIGENCIA is null OR ASS_CUT_DS_MOTIVO_FIM_VIGENCIA = pASS_CUT_DS_MOT_FIM_VIGENCIA) AND 
        (pASS_CUT_DT_FIM_VIGENCIA is null OR ASS_CUT_DT_FIM_VIGENCIA = pASS_CUT_DT_FIM_VIGENCIA) AND 
        (pASS_CUT_DT_ULT_ATUALIZACAO is null OR ASS_CUT_DT_ULTIMA_ATUALIZACAO = pASS_CUT_DT_ULT_ATUALIZACAO) AND 
        (pSEG_USU_ID_USUARIO is null OR SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO) AND 
        (pASS_UTA_ID_UNID_TPACOMOD is null OR ASS_UTA_ID_UNID_TPACOMOD = pASS_UTA_ID_UNID_TPACOMOD) AND 
        (pASS_CNU_ID is null OR ASS_CNU_ID = pASS_CNU_ID);          
    io_cursor := v_cursor;
  end PRC_INT_ASS_CUT_S;
