     pCAD_FEX_DT_ULTIMA_ATUALIZ IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_DT_ULTIMA_ATUALIZ%type,
     pCAD_FEX_DS_OBSERVACAO IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_DS_OBSERVACAO%type default NULL,
     pCAD_FEX_INICIO_VIGENCIA IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_INICIO_VIGENCIA%type,
     pCAD_FEX_FIM_VIGENCIA IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_FIM_VIGENCIA%type default NULL	
) 
is
/********************************************************************
*    Procedure: PRC_CAD_FEX_FORMULARIO_EXPED_I
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/  
	  SEQ NUMBER;
begin
	    
SELECT NVL(MAX(FEX.CAD_FEX_ID),0)+1 INTO SEQ FROM TB_CAD_FEX_FORMULARIO_EXPED FEX  ;

INSERT INTO TB_CAD_FEX_FORMULARIO_EXPED
(
       CAD_FEX_ID,
       CAD_FEX_NOME_FANTASIA,
       CAD_FEX_RAZAO_SOCIAL,
       CAD_FEX_PESSOA_CONTATO,
       CAD_FEX_TELEFONE,
       CAD_FEX_EMAIL,
       CAD_FEX_NUMERO_FORMULARIO,
       SEG_USU_ID_USUARIO_CRIACAO,
       CAD_FEX_DT_CRIACAO,
       SEG_USU_ID_USUARIO_ATUALIZ,
       CAD_FEX_DT_ULTIMA_ATUALIZ,
       CAD_FEX_DS_OBSERVACAO,
       CAD_FEX_INICIO_VIGENCIA,
       CAD_FEX_FIM_VIGENCIA
)
VALUES
(
	     SEQ,
	     pCAD_FEX_NOME_FANTASIA,
	     pCAD_FEX_RAZAO_SOCIAL,
	     pCAD_FEX_PESSOA_CONTATO,
	     pCAD_FEX_TELEFONE,
	     pCAD_FEX_EMAIL,
	     pCAD_FEX_NUMERO_FORMULARIO,
	     pSEG_USU_ID_USUARIO_CRIACAO,
	     pCAD_FEX_DT_CRIACAO,
	     pSEG_USU_ID_USUARIO_ATUALIZ,
	     pCAD_FEX_DT_ULTIMA_ATUALIZ,
	     pCAD_FEX_DS_OBSERVACAO,
	     pCAD_FEX_INICIO_VIGENCIA,
	     pCAD_FEX_FIM_VIGENCIA
);
	
