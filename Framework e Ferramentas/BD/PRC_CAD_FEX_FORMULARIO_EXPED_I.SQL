     pCAD_FEX_PESSOA_CONTATO IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_PESSOA_CONTATO%type default NULL,
     pCAD_FEX_TELEFONE IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_TELEFONE%type default NULL,
     pCAD_FEX_EMAIL IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_EMAIL%type default NULL,
     pCAD_FEX_NUMERO_FORMULARIO IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_NUMERO_FORMULARIO%type,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_CAD_FEX_FORMULARIO_EXPED.SEG_USU_ID_USUARIO_CRIACAO%type,
     pCAD_FEX_DT_CRIACAO IN TB_CAD_FEX_FORMULARIO_EXPED.CAD_FEX_DT_CRIACAO%type,
