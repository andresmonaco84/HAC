CREATE OR REPLACE PROCEDURE "PRC_CAD_CLC_CLINICA_CREDENC_I"
  (
     pNewIdt OUT integer,
     pCAD_CLC_DT_INI_VIGENCIA IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_CLC_DT_INI_VIGENCIA%type,
     pCAD_CLC_DT_FIM_VIGENCIA IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_CLC_DT_FIM_VIGENCIA%type default NULL,
     pCAD_PES_ID_PESSOA IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_PES_ID_PESSOA%type,
     pCAD_CLC_CD_CLINICA IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_CLC_CD_CLINICA%type,
     pCAD_CLC_FL_STATUS IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_CLC_FL_STATUS%type,
     pSEG_USU_ID_USUARIO IN TB_CAD_CLC_CLINICA_CREDENCIADA.SEG_USU_ID_USUARIO%type,
     pCAD_CLC_DS_RESUMIDA IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_CLC_DS_RESUMIDA%type default NULL,
     pCAD_CLC_FL_MOVIMENTAPACINT_OK IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_CLC_FL_MOVIMENTAPACINT_OK%type default NULL,
     pCAD_CLC_FL_UTILIZA_LIBERACAO IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_CLC_FL_UTILIZA_LIBERACAO%type default NULL,
     pCAD_CLC_DS_DESCRICAO IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_CLC_DS_DESCRICAO%type default NULL,
     pCAD_CLC_CT_EMPRESA IN TB_CAD_CLC_CLINICA_CREDENCIADA.CAD_CLC_CT_EMPRESA%type default NULL
  )
  is
  /********************************************************************
  *    Procedure: PRC_CAD_CLC_CLINICA_CREDENC_I
  *
  *    Data Criacao:  24/01/2008         Por: Davi S. M. dos Reis
  *    Data Alteracao: data da alteracao  Por: Nome do Analista
  *
  *    Funcao: Incluir clinica credenciada
  *
  *    Data Alteracao: 04/08/2011  Por: Davi S. M. dos Reis
  *    Alteração: Inclusao dos campos CAD_CLC_FL_UTILIZA_LIBERACAO e CAD_CLC_DS_DESCRICAO
  *
  *******************************************************************/
    lIdtRetorno integer;

  begin
    SELECT SEQ_CAD_CLC_01.NextVal INTO lIdtRetorno FROM DUAL;

    INSERT INTO TB_CAD_CLC_CLINICA_CREDENCIADA
    (
       CAD_CLC_ID,
       CAD_CLC_DT_INI_VIGENCIA,
       CAD_CLC_DT_FIM_VIGENCIA,
       CAD_PES_ID_PESSOA,
       CAD_CLC_CD_CLINICA,
       CAD_CLC_FL_STATUS,
       CAD_CLC_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       CAD_CLC_DS_RESUMIDA,
       CAD_CLC_FL_MOVIMENTAPACINT_OK,
       CAD_CLC_FL_UTILIZA_LIBERACAO,
       CAD_CLC_DS_DESCRICAO,
       CAD_CLC_CT_EMPRESA
    )
    VALUES
    (
      lIdtRetorno,
      pCAD_CLC_DT_INI_VIGENCIA,
      pCAD_CLC_DT_FIM_VIGENCIA,
      pCAD_PES_ID_PESSOA,
      pCAD_CLC_CD_CLINICA,
      pCAD_CLC_FL_STATUS,
      sysdate,
      pSEG_USU_ID_USUARIO,
      UPPER(pCAD_CLC_DS_RESUMIDA),
       pCAD_CLC_FL_MOVIMENTAPACINT_OK,
       pCAD_CLC_FL_UTILIZA_LIBERACAO,
       UPPER(pCAD_CLC_DS_DESCRICAO),
       pCAD_CLC_CT_EMPRESA
    );
    pNewIdt := lIdtRetorno;

  end PRC_CAD_CLC_CLINICA_CREDENC_I;
 