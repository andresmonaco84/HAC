--PRC_ASS_ULS_UN_LO_SE_USU_R_L
create or replace procedure PRC_ASS_ULS_UN_LO_SE_USU_R_L 
(
     pASS_ULS_ID IN TB_ASS_ULS_UNID_LOC_SET_USU.ASS_ULS_ID%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_ULS_UNID_LOC_SET_USU.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_UNI_ID_UNIDADE IN TB_ASS_ULS_UNID_LOC_SET_USU.CAD_UNI_ID_UNIDADE%type DEFAULT NULL,
     pCAD_SET_ID IN TB_ASS_ULS_UNID_LOC_SET_USU.CAD_SET_ID%type DEFAULT NULL,
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_ASS_ULS_UNID_LOC_SET_USU.CAD_LAT_ID_LOCAL_ATENDIMENTO%type DEFAULT NULL,
     pASS_ULS_FL_STATUS IN TB_ASS_ULS_UNID_LOC_SET_USU.ASS_ULS_FL_STATUS%type DEFAULT NULL,
     pASS_DT_ULTIMA_ATUALIZACAO IN TB_ASS_ULS_UNID_LOC_SET_USU.ASS_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pASS_ULS_ID_USUARIO IN TB_ASS_ULS_UNID_LOC_SET_USU.ASS_ULS_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ASS_ULS_UN_LO_SE_USU_R_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pASS_ULS_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_ULS_ID = ' || pASS_ULS_ID; END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pCAD_UNI_ID_UNIDADE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_UNI_ID_UNIDADE = ' || pCAD_UNI_ID_UNIDADE; END IF;
IF pCAD_SET_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_SET_ID = ' || pCAD_SET_ID; END IF;
IF pCAD_LAT_ID_LOCAL_ATENDIMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_LAT_ID_LOCAL_ATENDIMENTO = ' || pCAD_LAT_ID_LOCAL_ATENDIMENTO; END IF;
IF pASS_ULS_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_ULS_FL_STATUS = ' || CHR(39) || pASS_ULS_FL_STATUS || CHR(39); END IF;
IF pASS_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pASS_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pASS_ULS_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_ULS_ID_USUARIO = ' || pASS_ULS_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       ASS_ULS_ID,
       SEG_USU_ID_USUARIO,
       CAD_UNI_ID_UNIDADE,
       CAD_SET_ID,
       CAD_LAT_ID_LOCAL_ATENDIMENTO,
       ASS_ULS_FL_STATUS,
       ASS_DT_ULTIMA_ATUALIZACAO,
       ASS_ULS_ID_USUARIO
FROM TB_ASS_ULS_UNID_LOC_SET_USU
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ASS_ULS_UN_LO_SE_USU_R_L;
