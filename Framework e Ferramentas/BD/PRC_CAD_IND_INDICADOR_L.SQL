create or replace procedure PRC_CAD_IND_INDICADOR_L
(
     pCAD_IND_ID IN TB_CAD_IND_INDICADOR.CAD_IND_ID%type DEFAULT NULL,
     pCAD_IND_DS_CODIGO IN TB_CAD_IND_INDICADOR.CAD_IND_DS_CODIGO%type DEFAULT NULL,
     pCAD_IND_DS_DESCRICAO IN TB_CAD_IND_INDICADOR.CAD_IND_DS_DESCRICAO%type DEFAULT NULL,
     pCAD_IND_DS_GRUPO IN TB_CAD_IND_INDICADOR.CAD_IND_DS_GRUPO%type DEFAULT NULL,
     pCAD_IND_DS_TIPOUSUARIO IN TB_CAD_IND_INDICADOR.CAD_IND_DS_TIPOUSUARIO%type,
     pCAD_UNI_ID_UNIDADE IN TB_CAD_IND_INDICADOR.CAD_UNI_ID_UNIDADE%type DEFAULT NULL,
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_CAD_IND_INDICADOR.CAD_LAT_ID_LOCAL_ATENDIMENTO%type DEFAULT NULL,
     pCAD_SET_ID IN TB_CAD_IND_INDICADOR.CAD_SET_ID%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_IND_INDICADOR.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_IND_DT_ULTIMA_ATUALIZACAO IN TB_CAD_IND_INDICADOR.CAD_IND_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pCAD_IND_DS_PERIODO IN TB_CAD_IND_INDICADOR.CAD_IND_DS_PERIODO%type DEFAULT NULL,
     pCAD_IND_DS_TIPO_INFO IN TB_CAD_IND_INDICADOR.CAD_IND_DS_TIPO_INFO%type DEFAULT NULL,
     pCAD_IND_VL_MIN IN TB_CAD_IND_INDICADOR.CAD_IND_VL_MIN%type DEFAULT NULL,
     pCAD_IND_VL_MAX IN TB_CAD_IND_INDICADOR.CAD_IND_VL_MAX%type DEFAULT NULL,
          pCAD_IND_DS_OPCOES IN TB_CAD_IND_INDICADOR.CAD_IND_DS_OPCOES%type default null,
     io_cursor OUT PKG_CURSOR.t_cursor
)
is
/********************************************************************
*    Procedure: PRC_CAD_IND_INDICADOR_L
*
*    Data Criacao:   data da  criação   Por: Nome do Analista
*    Data Alteracao:  data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_IND_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_IND_ID = ' || pCAD_IND_ID; END IF;
IF pCAD_IND_DS_CODIGO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_IND_DS_CODIGO = ' || CHR(39) || pCAD_IND_DS_CODIGO || CHR(39); END IF;
IF pCAD_IND_DS_DESCRICAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_IND_DS_DESCRICAO = ' || CHR(39) || pCAD_IND_DS_DESCRICAO || CHR(39); END IF;
IF pCAD_IND_DS_GRUPO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_IND_DS_GRUPO = ' || CHR(39) || pCAD_IND_DS_GRUPO || CHR(39); END IF;
IF pCAD_IND_DS_TIPOUSUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_IND_DS_TIPOUSUARIO = ' || CHR(39) || pCAD_IND_DS_TIPOUSUARIO || CHR(39); END IF;
IF pCAD_UNI_ID_UNIDADE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_UNI_ID_UNIDADE = ' || pCAD_UNI_ID_UNIDADE; END IF;
IF pCAD_LAT_ID_LOCAL_ATENDIMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_LAT_ID_LOCAL_ATENDIMENTO = ' || pCAD_LAT_ID_LOCAL_ATENDIMENTO; END IF;
IF pCAD_SET_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_SET_ID = ' || pCAD_SET_ID; END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pCAD_IND_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_IND_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_IND_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pCAD_IND_DS_PERIODO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_IND_DS_PERIODO = ' || CHR(39) || pCAD_IND_DS_PERIODO || CHR(39); END IF;
IF pCAD_IND_DS_TIPO_INFO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_IND_DS_TIPO_INFO = ' || CHR(39) || pCAD_IND_DS_TIPO_INFO || CHR(39); END IF;
IF pCAD_IND_VL_MIN IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_IND_VL_MIN = ' || pCAD_IND_VL_MIN; END IF;
IF pCAD_IND_VL_MAX IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_IND_VL_MAX = ' || pCAD_IND_VL_MAX; END IF;
IF pCAD_IND_DS_OPCOES IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_IND_DS_OPCOES = ' || CHR(39) || pCAD_IND_DS_OPCOES || CHR(39); END IF;

   V_SELECT := '
SELECT
       CAD_IND_ID,
       CAD_IND_DS_CODIGO,
       CAD_IND_DS_DESCRICAO,
       CAD_IND_DS_GRUPO,
       CAD_IND_DS_TIPOUSUARIO,
       CAD_UNI_ID_UNIDADE,
       CAD_LAT_ID_LOCAL_ATENDIMENTO,
       CAD_SET_ID,
       SEG_USU_ID_USUARIO,
       CAD_IND_DT_ULTIMA_ATUALIZACAO,
       CAD_IND_DS_PERIODO,
       CAD_IND_DS_TIPO_INFO,
       CAD_IND_VL_MIN,
       CAD_IND_VL_MAX,
       CAD_IND_DS_OPCOES
       
FROM TB_CAD_IND_INDICADOR
WHERE null is null  '    ;

OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_IND_INDICADOR_L;
