create or replace procedure PRC_CAD_TAP_TP_ATRIB_PRODUTO_S 
(
     pCAD_TAP_TP_ATRIBUTO IN TB_CAD_TAP_TP_ATRIB_PRODUTO.CAD_TAP_TP_ATRIBUTO%type DEFAULT NULL,
     pCAD_TAP_DS_ATRIBUTO IN TB_CAD_TAP_TP_ATRIB_PRODUTO.CAD_TAP_DS_ATRIBUTO%type DEFAULT NULL,
     pCAD_TAP_FL_STATUS IN TB_CAD_TAP_TP_ATRIB_PRODUTO.CAD_TAP_FL_STATUS%type DEFAULT NULL,
     pCAD_TAP_DT_ULTIMA_ATUALIZACAO IN TB_CAD_TAP_TP_ATRIB_PRODUTO.CAD_TAP_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_TAP_TP_ATRIB_PRODUTO.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pTIS_CDE_CD_CODIGO_DESPESA IN TB_CAD_TAP_TP_ATRIB_PRODUTO.TIS_CDE_CD_CODIGO_DESPESA%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_TAP_TP_ATRIB_PRODUTO_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_TAP_TP_ATRIBUTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TAP_TP_ATRIBUTO = ' || CHR(39) || pCAD_TAP_TP_ATRIBUTO || CHR(39); END IF;
IF pCAD_TAP_DS_ATRIBUTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TAP_DS_ATRIBUTO = ' || CHR(39) || pCAD_TAP_DS_ATRIBUTO || CHR(39); END IF;
IF pCAD_TAP_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TAP_FL_STATUS = ' || CHR(39) || pCAD_TAP_FL_STATUS || CHR(39); END IF;
IF pCAD_TAP_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TAP_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_TAP_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pTIS_CDE_CD_CODIGO_DESPESA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_CDE_CD_CODIGO_DESPESA = ' || CHR(39) || pTIS_CDE_CD_CODIGO_DESPESA || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       CAD_TAP_TP_ATRIBUTO,
       CAD_TAP_DS_ATRIBUTO,
       CAD_TAP_FL_STATUS,
       CAD_TAP_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       TIS_CDE_CD_CODIGO_DESPESA
FROM TB_CAD_TAP_TP_ATRIB_PRODUTO
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_TAP_TP_ATRIB_PRODUTO_S;
