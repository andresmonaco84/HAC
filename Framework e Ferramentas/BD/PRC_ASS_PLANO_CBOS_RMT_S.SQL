--PRC_ASS_PLANO_CBOS_RMT_S
create or replace procedure PRC_ASS_PLANO_CBOS_RMT_S 
(
     pCAD_PLA_ID_PLANO IN TB_ASS_PLC_PLANO_CBOS.CAD_PLA_ID_PLANO%type DEFAULT NULL,
     pTIS_CBO_CD_CBOS IN TB_ASS_PLC_PLANO_CBOS.TIS_CBO_CD_CBOS%type DEFAULT NULL,
     pASS_PLC_FL_STATUS IN TB_ASS_PLC_PLANO_CBOS.ASS_PLC_FL_STATUS%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_PLC_PLANO_CBOS.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pASS_PLC_DT_ULTIMA_ATUALIZACAO IN TB_ASS_PLC_PLANO_CBOS.ASS_PLC_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pASS_PLC_FL_ENCAMINHA_OK IN TB_ASS_PLC_PLANO_CBOS.ASS_PLC_FL_ENCAMINHA_OK%type DEFAULT NULL,
     pASS_PLC_FL_EXIGE_SENHA_OK IN TB_ASS_PLC_PLANO_CBOS.ASS_PLC_FL_EXIGE_SENHA_OK%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ASS_PLANO_CBOS_RMT_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_PLA_ID_PLANO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PLA_ID_PLANO = ' || pCAD_PLA_ID_PLANO; END IF;
IF pTIS_CBO_CD_CBOS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_CBO_CD_CBOS = ' || CHR(39) || pTIS_CBO_CD_CBOS || CHR(39); END IF;
IF pASS_PLC_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PLC_FL_STATUS = ' || CHR(39) || pASS_PLC_FL_STATUS || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pASS_PLC_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PLC_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pASS_PLC_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pASS_PLC_FL_ENCAMINHA_OK IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PLC_FL_ENCAMINHA_OK = ' || CHR(39) || pASS_PLC_FL_ENCAMINHA_OK || CHR(39); END IF;
IF pASS_PLC_FL_EXIGE_SENHA_OK IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PLC_FL_EXIGE_SENHA_OK = ' || CHR(39) || pASS_PLC_FL_EXIGE_SENHA_OK || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       CAD_PLA_ID_PLANO,
       TIS_CBO_CD_CBOS,
       ASS_PLC_FL_STATUS,
       SEG_USU_ID_USUARIO,
       ASS_PLC_DT_ULTIMA_ATUALIZACAO,
       ASS_PLC_FL_ENCAMINHA_OK,
       ASS_PLC_FL_EXIGE_SENHA_OK
FROM TB_ASS_PLC_PLANO_CBOS
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ASS_PLANO_CBOS_RMT_S;