--PRC_AGS_ORS_ORIENTACAO_SADT_S
create or replace procedure PRC_AGS_ORS_ORIENTACAO_SADT_S 
(
     pAGS_ORS_ID IN TB_AGS_ORS_ORIENTACAO_SADT.AGS_ORS_ID%type DEFAULT NULL,
     pAGS_ORS_DS_ORIENTACAO_SADT IN TB_AGS_ORS_ORIENTACAO_SADT.AGS_ORS_DS_ORIENTACAO_SADT%type DEFAULT NULL,
     pAGS_ORS_TP_FINALIDADE IN TB_AGS_ORS_ORIENTACAO_SADT.AGS_ORS_TP_FINALIDADE%type DEFAULT NULL,
     pAGS_ORS_DT_ULTIMA_ATUALIZACAO IN TB_AGS_ORS_ORIENTACAO_SADT.AGS_ORS_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_AGS_ORS_ORIENTACAO_SADT.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_AGS_ORS_ORIENTACAO_SADT_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pAGS_ORS_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AGS_ORS_ID = ' || pAGS_ORS_ID; END IF;
IF pAGS_ORS_DS_ORIENTACAO_SADT IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AGS_ORS_DS_ORIENTACAO_SADT = ' || CHR(39) || pAGS_ORS_DS_ORIENTACAO_SADT || CHR(39); END IF;
IF pAGS_ORS_TP_FINALIDADE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AGS_ORS_TP_FINALIDADE = ' || CHR(39) || pAGS_ORS_TP_FINALIDADE || CHR(39); END IF;
IF pAGS_ORS_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AGS_ORS_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pAGS_ORS_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       AGS_ORS_ID,
       AGS_ORS_DS_ORIENTACAO_SADT,
       AGS_ORS_TP_FINALIDADE,
       AGS_ORS_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO
FROM TB_AGS_ORS_ORIENTACAO_SADT
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_AGS_ORS_ORIENTACAO_SADT_S;
