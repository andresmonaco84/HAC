--PRC_CAD_PSO_PROF_SOLIC_S
create or replace procedure PRC_CAD_PSO_PROF_SOLIC_S
(
     pTIS_CPR_CD_CONSELHOPROF IN TB_CAD_PSO_PROF_SOLICITANTE.TIS_CPR_CD_CONSELHOPROF%type DEFAULT NULL,
     pCAD_PSO_CD_CONSELHO IN TB_CAD_PSO_PROF_SOLICITANTE.CAD_PSO_CD_CONSELHO%type DEFAULT NULL,
     pCAD_PSO_SG_UF_CONSELHO IN TB_CAD_PSO_PROF_SOLICITANTE.CAD_PSO_SG_UF_CONSELHO%type DEFAULT NULL,
     pCAD_PSO_FL_ATIVO_OK IN TB_CAD_PSO_PROF_SOLICITANTE.CAD_PSO_FL_ATIVO_OK%type DEFAULT NULL,
     pCAD_PSO_NM_PROFISSIONAL IN TB_CAD_PSO_PROF_SOLICITANTE.CAD_PSO_NM_PROFISSIONAL%type DEFAULT NULL,
     pCAD_PSO_DT_ULTIMA_ATUALIZACAO IN TB_CAD_PSO_PROF_SOLICITANTE.CAD_PSO_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_PSO_PROF_SOLICITANTE.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_PSO_PROF_SOLIC_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pTIS_CPR_CD_CONSELHOPROF IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_CPR_CD_CONSELHOPROF = ' || CHR(39) || pTIS_CPR_CD_CONSELHOPROF || CHR(39); END IF;
IF pCAD_PSO_CD_CONSELHO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PSO_CD_CONSELHO = ' || CHR(39) || pCAD_PSO_CD_CONSELHO || CHR(39); END IF;
IF pCAD_PSO_SG_UF_CONSELHO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PSO_SG_UF_CONSELHO = ' || CHR(39) || pCAD_PSO_SG_UF_CONSELHO || CHR(39); END IF;
IF pCAD_PSO_FL_ATIVO_OK IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PSO_FL_ATIVO_OK = ' || CHR(39) || pCAD_PSO_FL_ATIVO_OK || CHR(39); END IF;
IF pCAD_PSO_NM_PROFISSIONAL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PSO_NM_PROFISSIONAL LIKE UPPER(' || CHR(39) || pCAD_PSO_NM_PROFISSIONAL || CHR(39)||')'; END IF;
IF pCAD_PSO_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PSO_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_PSO_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       TIS_CPR_CD_CONSELHOPROF,
       CAD_PSO_CD_CONSELHO,
       CAD_PSO_SG_UF_CONSELHO,
       CAD_PSO_FL_ATIVO_OK,
       CAD_PSO_NM_PROFISSIONAL,
       CAD_PSO_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO
FROM TB_CAD_PSO_PROF_SOLICITANTE
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE || 
  'ORDER BY CAD_PSO_NM_PROFISSIONAL, CAD_PSO_SG_UF_CONSELHO, TIS_CPR_CD_CONSELHOPROF, CAD_PSO_CD_CONSELHO';
  io_cursor := v_cursor;
end PRC_CAD_PSO_PROF_SOLIC_S;