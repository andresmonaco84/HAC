--PRC_COB_PAG_PAGAMENTO_GUIA_L
create or replace procedure PRC_COB_PAG_PAGAMENTO_GUIA_L 
(
     pCOB_PAG_ID IN TB_COB_PAG_PAGAMENTO_GUIA.COB_PAG_ID%type DEFAULT NULL,
     pATD_ATE_ID IN TB_COB_PAG_PAGAMENTO_GUIA.ATD_ATE_ID%type DEFAULT NULL,
     pFAT_COC_ID IN TB_COB_PAG_PAGAMENTO_GUIA.FAT_COC_ID%type DEFAULT NULL,
     pFAT_CCP_ID IN TB_COB_PAG_PAGAMENTO_GUIA.FAT_CCP_ID%type DEFAULT NULL,
     pATD_GUI_CD_CODIGO IN TB_COB_PAG_PAGAMENTO_GUIA.ATD_GUI_CD_CODIGO%type DEFAULT NULL,
     pFAT_NOF_ID IN TB_COB_PAG_PAGAMENTO_GUIA.FAT_NOF_ID%type DEFAULT NULL,
     pCOB_PAG_DT_PAGAMENTO IN TB_COB_PAG_PAGAMENTO_GUIA.COB_PAG_DT_PAGAMENTO%type DEFAULT NULL,
     pCOB_PAG_VL_PAGAMENTO IN TB_COB_PAG_PAGAMENTO_GUIA.COB_PAG_VL_PAGAMENTO%type DEFAULT NULL,
     pCOB_PAG_DT_CRIACAO IN TB_COB_PAG_PAGAMENTO_GUIA.COB_PAG_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_COB_PAG_PAGAMENTO_GUIA.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pCOB_PAG_DT_ULTIMA_ATUALIZACAO IN TB_COB_PAG_PAGAMENTO_GUIA.COB_PAG_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ATUALIZ IN TB_COB_PAG_PAGAMENTO_GUIA.SEG_USU_ID_USUARIO_ATUALIZ%type DEFAULT NULL,
     pCAD_PAC_ID_PACIENTE IN TB_COB_PAG_PAGAMENTO_GUIA.CAD_PAC_ID_PACIENTE%type DEFAULT NULL,
     pATD_GUI_DT_VALIDADE IN TB_COB_PAG_PAGAMENTO_GUIA.ATD_GUI_DT_VALIDADE%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_COB_PAG_PAGAMENTO_GUIA_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCOB_PAG_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_PAG_ID = ' || pCOB_PAG_ID; END IF;
IF pATD_ATE_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_ATE_ID = ' || pATD_ATE_ID; END IF;
IF pFAT_COC_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_COC_ID = ' || pFAT_COC_ID; END IF;
IF pFAT_CCP_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_CCP_ID = ' || pFAT_CCP_ID; END IF;
IF pATD_GUI_CD_CODIGO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_GUI_CD_CODIGO = ' || CHR(39) || pATD_GUI_CD_CODIGO || CHR(39); END IF;
IF pFAT_NOF_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_NOF_ID = ' || pFAT_NOF_ID; END IF;
IF pCOB_PAG_DT_PAGAMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_PAG_DT_PAGAMENTO = ' || CHR(39) || pCOB_PAG_DT_PAGAMENTO || CHR(39); END IF;
IF pCOB_PAG_VL_PAGAMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_PAG_VL_PAGAMENTO = ' || pCOB_PAG_VL_PAGAMENTO; END IF;
IF pCOB_PAG_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_PAG_DT_CRIACAO = ' || CHR(39) || pCOB_PAG_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; END IF;
IF pCOB_PAG_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_PAG_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCOB_PAG_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ATUALIZ = ' || pSEG_USU_ID_USUARIO_ATUALIZ; END IF;
IF pCAD_PAC_ID_PACIENTE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PAC_ID_PACIENTE = ' || pCAD_PAC_ID_PACIENTE; END IF;
IF pATD_GUI_DT_VALIDADE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_GUI_DT_VALIDADE = ' || CHR(39) || pATD_GUI_DT_VALIDADE || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       COB_PAG_ID,
       ATD_ATE_ID,
       FAT_COC_ID,
       FAT_CCP_ID,
       ATD_GUI_CD_CODIGO,
       FAT_NOF_ID,
       COB_PAG_DT_PAGAMENTO,
       COB_PAG_VL_PAGAMENTO,
       COB_PAG_DT_CRIACAO,
       SEG_USU_ID_USUARIO_CRIACAO,
       COB_PAG_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO_ATUALIZ,
       CAD_PAC_ID_PACIENTE,
       ATD_GUI_DT_VALIDADE
FROM TB_COB_PAG_PAGAMENTO_GUIA
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_COB_PAG_PAGAMENTO_GUIA_L;
/
