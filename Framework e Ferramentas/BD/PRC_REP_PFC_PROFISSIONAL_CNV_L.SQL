     pCAD_CNV_ID_CONVENIO IN TB_REP_PFC_PROFISSIONAL_CNV.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pCAD_PRO_ID_PROFISSIONAL IN TB_REP_PFC_PROFISSIONAL_CNV.CAD_PRO_ID_PROFISSIONAL%type DEFAULT NULL,
     pCAD_UNI_ID_UNIDADE IN TB_REP_PFC_PROFISSIONAL_CNV.CAD_UNI_ID_UNIDADE%type DEFAULT NULL,
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_REP_PFC_PROFISSIONAL_CNV.CAD_LAT_ID_LOCAL_ATENDIMENTO%type DEFAULT NULL,
     pCAD_TAP_TP_ATRIBUTO IN TB_REP_PFC_PROFISSIONAL_CNV.CAD_TAP_TP_ATRIBUTO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_REP_PFC_PROFISSIONAL_CNV.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ATUA IN TB_REP_PFC_PROFISSIONAL_CNV.SEG_USU_ID_USUARIO_ATUA%type DEFAULT NULL,
     pREP_PFC_DT_CRIACAO IN TB_REP_PFC_PROFISSIONAL_CNV.REP_PFC_DT_CRIACAO%type DEFAULT NULL,
     pREP_PFC_DT_ULTIMA_ATUALIZACAO IN TB_REP_PFC_PROFISSIONAL_CNV.REP_PFC_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pCAD_CPR_TP_CREDENCIA_PROF IN TB_REP_PFC_PROFISSIONAL_CNV.CAD_CPR_TP_CREDENCIA_PROF%type DEFAULT NULL,
     pREP_PFC_PC_CONTA IN TB_REP_PFC_PROFISSIONAL_CNV.REP_PFC_PC_CONTA%type DEFAULT NULL,
     pREP_PFC_DT_INICIO_VIGENCIA IN TB_REP_PFC_PROFISSIONAL_CNV.REP_PFC_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pREP_PFC_DT_FIM_VIGENCIA IN TB_REP_PFC_PROFISSIONAL_CNV.REP_PFC_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pREP_PFC_ID IN TB_REP_PFC_PROFISSIONAL_CNV.REP_PFC_ID%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_REP_PFC_PROFISSIONAL_CNV_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
IF pCAD_PRO_ID_PROFISSIONAL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRO_ID_PROFISSIONAL = ' || pCAD_PRO_ID_PROFISSIONAL; END IF;
IF pCAD_UNI_ID_UNIDADE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_UNI_ID_UNIDADE = ' || pCAD_UNI_ID_UNIDADE; END IF;
IF pCAD_LAT_ID_LOCAL_ATENDIMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_LAT_ID_LOCAL_ATENDIMENTO = ' || pCAD_LAT_ID_LOCAL_ATENDIMENTO; END IF;
IF pCAD_TAP_TP_ATRIBUTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TAP_TP_ATRIBUTO = ' || CHR(39) || pCAD_TAP_TP_ATRIBUTO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; END IF;
IF pSEG_USU_ID_USUARIO_ATUA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ATUA = ' || pSEG_USU_ID_USUARIO_ATUA; END IF;
