IF pREP_PFC_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND REP_PFC_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pREP_PFC_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pCAD_CPR_TP_CREDENCIA_PROF IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CPR_TP_CREDENCIA_PROF = ' || CHR(39) || pCAD_CPR_TP_CREDENCIA_PROF || CHR(39); END IF;
IF pREP_PFC_PC_CONTA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND REP_PFC_PC_CONTA = ' || pREP_PFC_PC_CONTA; END IF;
IF pREP_PFC_DT_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND REP_PFC_DT_INICIO_VIGENCIA = ' || CHR(39) || pREP_PFC_DT_INICIO_VIGENCIA || CHR(39); END IF;
IF pREP_PFC_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND REP_PFC_DT_FIM_VIGENCIA = ' || CHR(39) || pREP_PFC_DT_FIM_VIGENCIA || CHR(39); END IF;
IF pREP_PFC_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND REP_PFC_ID = ' || pREP_PFC_ID; END IF;
 
   V_SELECT := '
SELECT	
       CAD_CNV_ID_CONVENIO,
       CAD_PRO_ID_PROFISSIONAL,
       CAD_UNI_ID_UNIDADE,
       CAD_LAT_ID_LOCAL_ATENDIMENTO,
       CAD_TAP_TP_ATRIBUTO,
       SEG_USU_ID_USUARIO_CRIACAO,
       SEG_USU_ID_USUARIO_ATUALIZACAO,
       REP_PFC_DT_CRIACAO,
       REP_PFC_DT_ULTIMA_ATUALIZACAO,
       CAD_CPR_TP_CREDENCIA_PROF,
       REP_PFC_PC_CONTA,
       REP_PFC_DT_INICIO_VIGENCIA,
       REP_PFC_DT_FIM_VIGENCIA,
       REP_PFC_ID
FROM TB_REP_PFC_PROFISSIONAL_CNV
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_REP_PFC_PROFISSIONAL_CNV_L;
