--PRC_ASS_ULO_UNID_LOCAL_R_L
create or replace procedure PRC_ASS_ULO_UNID_LOCAL_R_L 
(
     pCAD_UNI_ID_UNIDADE IN TB_ASS_ULO_UNID_LOCAL.CAD_UNI_ID_UNIDADE%type DEFAULT NULL,
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_ASS_ULO_UNID_LOCAL.CAD_LAT_ID_LOCAL_ATENDIMENTO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_ULO_UNID_LOCAL.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pASS_ULO_DT_ULTIMA_ATUALIZACAO IN TB_ASS_ULO_UNID_LOCAL.ASS_ULO_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pASS_ULO_FL_STATUS IN TB_ASS_ULO_UNID_LOCAL.ASS_ULO_FL_STATUS%type DEFAULT NULL,
     pASS_ULO_FL_LIBERA_AGENDA_OK IN TB_ASS_ULO_UNID_LOCAL.ASS_ULO_FL_LIBERA_AGENDA_OK%type DEFAULT NULL,
     pASS_ULO_HR_INICIAL_ATEND IN TB_ASS_ULO_UNID_LOCAL.ASS_ULO_HR_INICIAL_ATEND%type DEFAULT NULL,
     pASS_ULO_HR_FINAL_ATEND IN TB_ASS_ULO_UNID_LOCAL.ASS_ULO_HR_FINAL_ATEND%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ASS_ULO_UNID_LOCAL_R_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_UNI_ID_UNIDADE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_UNI_ID_UNIDADE = ' || pCAD_UNI_ID_UNIDADE; END IF;
IF pCAD_LAT_ID_LOCAL_ATENDIMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_LAT_ID_LOCAL_ATENDIMENTO = ' || pCAD_LAT_ID_LOCAL_ATENDIMENTO; END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pASS_ULO_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_ULO_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pASS_ULO_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pASS_ULO_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_ULO_FL_STATUS = ' || CHR(39) || pASS_ULO_FL_STATUS || CHR(39); END IF;
IF pASS_ULO_FL_LIBERA_AGENDA_OK IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_ULO_FL_LIBERA_AGENDA_OK = ' || CHR(39) || pASS_ULO_FL_LIBERA_AGENDA_OK || CHR(39); END IF;
IF pASS_ULO_HR_INICIAL_ATEND IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_ULO_HR_INICIAL_ATEND = ' || pASS_ULO_HR_INICIAL_ATEND; END IF;
IF pASS_ULO_HR_FINAL_ATEND IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_ULO_HR_FINAL_ATEND = ' || pASS_ULO_HR_FINAL_ATEND; END IF;
 
   V_SELECT := '
SELECT	
       CAD_UNI_ID_UNIDADE,
       CAD_LAT_ID_LOCAL_ATENDIMENTO,
       SEG_USU_ID_USUARIO,
       ASS_ULO_DT_ULTIMA_ATUALIZACAO,
       ASS_ULO_FL_STATUS,
       ASS_ULO_FL_LIBERA_AGENDA_OK,
       ASS_ULO_HR_INICIAL_ATEND,
       ASS_ULO_HR_FINAL_ATEND
FROM TB_ASS_ULO_UNID_LOCAL
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ASS_ULO_UNID_LOCAL_R_L;
