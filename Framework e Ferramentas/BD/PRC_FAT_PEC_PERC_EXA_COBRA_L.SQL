
--PRC_FAT_PEC_PERC_EXA_COBRA_L
create or replace procedure PRC_FAT_PEC_PERC_EXA_COBRA_L 
(
     pAUX_EPP_CD_ESPECPROC IN TB_FAT_PEC_PERC_EXA_COBRA.AUX_EPP_CD_ESPECPROC%type DEFAULT NULL,
     pFAT_PEC_DS_DESCRICAO IN TB_FAT_PEC_PERC_EXA_COBRA.FAT_PEC_DS_DESCRICAO%type DEFAULT NULL,
     pFAT_PEC_PC_COBRADO_EXA IN TB_FAT_PEC_PERC_EXA_COBRA.FAT_PEC_PC_COBRADO_EXA%type DEFAULT NULL,
     pCAD_CNV_ID_CONVENIO IN TB_FAT_PEC_PERC_EXA_COBRA.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pFAT_PEC_DT_ULTIMA_ATUALIZACAO IN TB_FAT_PEC_PERC_EXA_COBRA.FAT_PEC_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_FAT_PEC_PERC_EXA_COBRA.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pFAT_PEC_CD_CODIGO IN TB_FAT_PEC_PERC_EXA_COBRA.FAT_PEC_CD_CODIGO%type DEFAULT NULL,
     pFAT_PEC_ID IN TB_FAT_PEC_PERC_EXA_COBRA.FAT_PEC_ID%type DEFAULT NULL,
     pTIS_MED_CD_TABELAMEDICA IN TB_FAT_PEC_PERC_EXA_COBRA.TIS_MED_CD_TABELAMEDICA%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_FAT_PEC_PERC_EXA_COBRA_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pAUX_EPP_CD_ESPECPROC IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AUX_EPP_CD_ESPECPROC = ' || CHR(39) || pAUX_EPP_CD_ESPECPROC || CHR(39); END IF;
IF pFAT_PEC_DS_DESCRICAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_PEC_DS_DESCRICAO = ' || CHR(39) || pFAT_PEC_DS_DESCRICAO || CHR(39); END IF;
IF pFAT_PEC_PC_COBRADO_EXA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_PEC_PC_COBRADO_EXA = ' || pFAT_PEC_PC_COBRADO_EXA; END IF;
IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
IF pFAT_PEC_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_PEC_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pFAT_PEC_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pFAT_PEC_CD_CODIGO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_PEC_CD_CODIGO = ' || pFAT_PEC_CD_CODIGO; END IF;
IF pFAT_PEC_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_PEC_ID = ' || pFAT_PEC_ID; END IF;
IF pTIS_MED_CD_TABELAMEDICA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_MED_CD_TABELAMEDICA = ' || CHR(39) || pTIS_MED_CD_TABELAMEDICA || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       AUX_EPP_CD_ESPECPROC,
       FAT_PEC_DS_DESCRICAO,
       FAT_PEC_PC_COBRADO_EXA,
       CAD_CNV_ID_CONVENIO,
       FAT_PEC_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       FAT_PEC_CD_CODIGO,
       FAT_PEC_ID,
       TIS_MED_CD_TABELAMEDICA
FROM TB_FAT_PEC_PERC_EXA_COBRA
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_FAT_PEC_PERC_EXA_COBRA_L;
