
--PRC_FAT_TCP_TP_COMANDA_PROD_L
create or replace procedure PRC_FAT_TCP_TP_COMANDA_PROD_L 
(
     pFAT_TCP_ID IN TB_FAT_TCP_TP_COMANDA_PROD.FAT_TCP_ID%type DEFAULT NULL,
     pFAT_TCO_ID IN TB_FAT_TCP_TP_COMANDA_PROD.FAT_TCO_ID%type DEFAULT NULL,
     pCAD_PRD_ID IN TB_FAT_TCP_TP_COMANDA_PROD.CAD_PRD_ID%type DEFAULT NULL,
     pFAT_TCP_DT_ULTIMA_ATUALIZACAO IN TB_FAT_TCP_TP_COMANDA_PROD.FAT_TCP_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pFAT_TCP_FL_STATUS IN TB_FAT_TCP_TP_COMANDA_PROD.FAT_TCP_FL_STATUS%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_FAT_TCP_TP_COMANDA_PROD.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_FAT_TCP_TP_COMANDA_PROD_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pFAT_TCP_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_TCP_ID = ' || pFAT_TCP_ID; END IF;
IF pFAT_TCO_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_TCO_ID = ' || pFAT_TCO_ID; END IF;
IF pCAD_PRD_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_ID = ' || pCAD_PRD_ID; END IF;
IF pFAT_TCP_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_TCP_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pFAT_TCP_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pFAT_TCP_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_TCP_FL_STATUS = ' || CHR(39) || pFAT_TCP_FL_STATUS || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       FAT_TCP_ID,
       FAT_TCO_ID,
       CAD_PRD_ID,
       FAT_TCP_DT_ULTIMA_ATUALIZACAO,
       FAT_TCP_FL_STATUS,
       SEG_USU_ID_USUARIO
FROM TB_FAT_TCP_TP_COMANDA_PROD
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_FAT_TCP_TP_COMANDA_PROD_L;
