--PRC_ASS_CPA_CONVENIO_PACOTE_L
create or replace procedure PRC_ASS_CPA_CONVENIO_PACOTE_L 
(
     pASS_CPA_ID IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_ID%type DEFAULT NULL,
     pCAD_CNV_ID_CONVENIO IN TB_ASS_CPA_CONVENIO_PACOTE.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pCAD_PLA_ID_PLANO IN TB_ASS_CPA_CONVENIO_PACOTE.CAD_PLA_ID_PLANO%type DEFAULT NULL,
     pCAD_PRD_ID IN TB_ASS_CPA_CONVENIO_PACOTE.CAD_PRD_ID%type DEFAULT NULL,
     pASS_CPA_FL_STATUS IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_FL_STATUS%type DEFAULT NULL,
     pASS_CPA_DT_INICIO_VIGENCIA IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pASS_CPA_DT_FIM_VIGENCIA IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pASS_CPA_FL_LANC_PAC_ABERTO IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_FL_LANC_PAC_ABERTO%type DEFAULT NULL,
     pASS_CPA_DS_OBSERVACAO IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_DS_OBSERVACAO%type DEFAULT NULL,
     pASS_CPA_DT_ULTIMA_ATUALIZACAO IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_CPA_CONVENIO_PACOTE.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pASS_CPA_VL_PACOTE_ESPECIAL IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_VL_PACOTE_ESPECIAL%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ASS_CPA_CONVENIO_PACOTE_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pASS_CPA_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CPA_ID = ' || pASS_CPA_ID; END IF;
IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
IF pCAD_PLA_ID_PLANO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PLA_ID_PLANO = ' || pCAD_PLA_ID_PLANO; END IF;
IF pCAD_PRD_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_ID = ' || pCAD_PRD_ID; END IF;
IF pASS_CPA_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CPA_FL_STATUS = ' || CHR(39) || pASS_CPA_FL_STATUS || CHR(39); END IF;
IF pASS_CPA_DT_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CPA_DT_INICIO_VIGENCIA = ' || CHR(39) || pASS_CPA_DT_INICIO_VIGENCIA || CHR(39); END IF;
IF pASS_CPA_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CPA_DT_FIM_VIGENCIA = ' || CHR(39) || pASS_CPA_DT_FIM_VIGENCIA || CHR(39); END IF;
IF pASS_CPA_FL_LANC_PAC_ABERTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CPA_FL_LANC_PAC_ABERTO = ' || CHR(39) || pASS_CPA_FL_LANC_PAC_ABERTO || CHR(39); END IF;
IF pASS_CPA_DS_OBSERVACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CPA_DS_OBSERVACAO = ' || CHR(39) || pASS_CPA_DS_OBSERVACAO || CHR(39); END IF;
IF pASS_CPA_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CPA_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pASS_CPA_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pASS_CPA_VL_PACOTE_ESPECIAL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CPA_VL_PACOTE_ESPECIAL = ' || pASS_CPA_VL_PACOTE_ESPECIAL; END IF;
 
   V_SELECT := '
SELECT	
       ASS_CPA_ID,
       CAD_CNV_ID_CONVENIO,
       CAD_PLA_ID_PLANO,
       CAD_PRD_ID,
       ASS_CPA_FL_STATUS,
       ASS_CPA_DT_INICIO_VIGENCIA,
       ASS_CPA_DT_FIM_VIGENCIA,
       ASS_CPA_FL_LANC_PAC_ABERTO,
       ASS_CPA_DS_OBSERVACAO,
       ASS_CPA_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       ASS_CPA_VL_PACOTE_ESPECIAL
FROM TB_ASS_CPA_CONVENIO_PACOTE
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ASS_CPA_CONVENIO_PACOTE_L;
