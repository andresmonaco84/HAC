--PRC_ASS_PSP_PLANO_SETOR_PRD_S
create or replace procedure PRC_ASS_PSP_PLANO_SETOR_PRD_S 
(
     pCAD_CNV_ID_CONVENIO IN TB_ASS_PSP_PLANO_SETOR_PRD.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pCAD_PLA_ID_PLANO IN TB_ASS_PSP_PLANO_SETOR_PRD.CAD_PLA_ID_PLANO%type DEFAULT NULL,
     pCAD_UNI_ID_UNIDADE IN TB_ASS_PSP_PLANO_SETOR_PRD.CAD_UNI_ID_UNIDADE%type DEFAULT NULL,
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_ASS_PSP_PLANO_SETOR_PRD.CAD_LAT_ID_LOCAL_ATENDIMENTO%type DEFAULT NULL,
     pCAD_SET_ID IN TB_ASS_PSP_PLANO_SETOR_PRD.CAD_SET_ID%type DEFAULT NULL,
     pCAD_PRD_ID IN TB_ASS_PSP_PLANO_SETOR_PRD.CAD_PRD_ID%type DEFAULT NULL,
     pASS_PSP_FL_ATIVO_OK IN TB_ASS_PSP_PLANO_SETOR_PRD.ASS_PSP_FL_ATIVO_OK%type DEFAULT NULL,
     pASS_PSP_DT_ULTIMA_ATUALIZACAO IN TB_ASS_PSP_PLANO_SETOR_PRD.ASS_PSP_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_PSP_PLANO_SETOR_PRD.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ASS_PSP_PLANO_SETOR_PRD_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
IF pCAD_PLA_ID_PLANO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PLA_ID_PLANO = ' || pCAD_PLA_ID_PLANO; END IF;
IF pCAD_UNI_ID_UNIDADE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_UNI_ID_UNIDADE = ' || pCAD_UNI_ID_UNIDADE; END IF;
IF pCAD_LAT_ID_LOCAL_ATENDIMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_LAT_ID_LOCAL_ATENDIMENTO = ' || pCAD_LAT_ID_LOCAL_ATENDIMENTO; END IF;
IF pCAD_SET_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_SET_ID = ' || pCAD_SET_ID; END IF;
IF pCAD_PRD_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_ID = ' || pCAD_PRD_ID; END IF;
IF pASS_PSP_FL_ATIVO_OK IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PSP_FL_ATIVO_OK = ' || CHR(39) || pASS_PSP_FL_ATIVO_OK || CHR(39); END IF;
IF pASS_PSP_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PSP_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pASS_PSP_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       CAD_CNV_ID_CONVENIO,
       CAD_PLA_ID_PLANO,
       CAD_UNI_ID_UNIDADE,
       CAD_LAT_ID_LOCAL_ATENDIMENTO,
       CAD_SET_ID,
       CAD_PRD_ID,
       ASS_PSP_FL_ATIVO_OK,
       ASS_PSP_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO
FROM TB_ASS_PSP_PLANO_SETOR_PRD
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ASS_PSP_PLANO_SETOR_PRD_S;
