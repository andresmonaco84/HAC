  --PRC_ASS_CPA_CONVENIO_PACOTE_S
  create or replace procedure PRC_ASS_CPA_CONVENIO_PACOTE_S 
  (
     pASS_CPA_ID IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_ID%type DEFAULT NULL,
     pCAD_CNV_ID_CONVENIO IN TB_ASS_CPA_CONVENIO_PACOTE.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pCAD_PLA_ID_PLANO IN TB_ASS_CPA_CONVENIO_PACOTE.CAD_PLA_ID_PLANO%type DEFAULT NULL,
     pCAD_PRD_ID IN TB_ASS_CPA_CONVENIO_PACOTE.CAD_PRD_ID%type DEFAULT NULL,
     pASS_CPA_FL_STATUS IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_FL_STATUS%type DEFAULT NULL,
     pASS_CPA_DT_INICIO_VIGENCIA IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pASS_CPA_DT_FIM_VIGENCIA IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pASS_CPA_FL_LANC_PAC_ABERTO IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_FL_LANC_PAC_ABERTO%type DEFAULT NULL,
     pASS_CPA_DS_OBSERVACAO IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_DS_OBSERVACAO%type DEFAULT NULL,
     pASS_CPA_DT_ULTIMA_ATUALIZACAO IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_CPA_CONVENIO_PACOTE.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pASS_CPA_VL_PACOTE_ESPECIAL IN TB_ASS_CPA_CONVENIO_PACOTE.ASS_CPA_VL_PACOTE_ESPECIAL%TYPE DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
  ) 
  is
  /********************************************************************
  *    Procedure: PRC_ASS_CPA_CONVENIO_PACOTE_S
  * 
  *    Data Criacao: 	data da  criação   Por: Nome do Analista
  *    Data Alteracao:	08/04/2010  Por: Pedro
  *
  *    Funcao: Descrição da funcionalidade da Stored Procedure
  *
  *******************************************************************/
  v_cursor PKG_CURSOR.t_cursor;
  begin
    OPEN v_cursor FOR
    SELECT	
       ASS_CPA_ID,
       CAD_CNV_ID_CONVENIO,
       CAD_PLA_ID_PLANO,
       CAD_PRD_ID,
       ASS_CPA_FL_STATUS,
       ASS_CPA_DT_INICIO_VIGENCIA,
       ASS_CPA_DT_FIM_VIGENCIA,
       ASS_CPA_FL_LANC_PAC_ABERTO,
       ASS_CPA_DS_OBSERVACAO,
       ASS_CPA_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       ASS_CPA_VL_PACOTE_ESPECIAL
    FROM TB_ASS_CPA_CONVENIO_PACOTE
    WHERE
        (pASS_CPA_ID is null OR ASS_CPA_ID = pASS_CPA_ID) AND 
        (pCAD_CNV_ID_CONVENIO is null OR CAD_CNV_ID_CONVENIO = pCAD_CNV_ID_CONVENIO) AND 
        (pCAD_PLA_ID_PLANO is null OR CAD_PLA_ID_PLANO = pCAD_PLA_ID_PLANO) AND 
        (pCAD_PRD_ID is null OR CAD_PRD_ID = pCAD_PRD_ID) AND 
        (pASS_CPA_FL_STATUS is null OR ASS_CPA_FL_STATUS = pASS_CPA_FL_STATUS) AND 
        (pASS_CPA_DT_INICIO_VIGENCIA is null OR ASS_CPA_DT_INICIO_VIGENCIA = pASS_CPA_DT_INICIO_VIGENCIA) AND 
        (pASS_CPA_DT_FIM_VIGENCIA is null OR ASS_CPA_DT_FIM_VIGENCIA = pASS_CPA_DT_FIM_VIGENCIA) AND 
        (pASS_CPA_FL_LANC_PAC_ABERTO is null OR ASS_CPA_FL_LANC_PAC_ABERTO = pASS_CPA_FL_LANC_PAC_ABERTO) AND 
        (pASS_CPA_DS_OBSERVACAO is null OR ASS_CPA_DS_OBSERVACAO = pASS_CPA_DS_OBSERVACAO) AND 
        (pASS_CPA_DT_ULTIMA_ATUALIZACAO is null OR ASS_CPA_DT_ULTIMA_ATUALIZACAO = pASS_CPA_DT_ULTIMA_ATUALIZACAO) AND 
        (pSEG_USU_ID_USUARIO is null OR SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO) AND 
        (pASS_CPA_VL_PACOTE_ESPECIAL IS NULL OR ASS_CPA_VL_PACOTE_ESPECIAL = pASS_CPA_VL_PACOTE_ESPECIAL);          
    io_cursor := v_cursor;
  end PRC_ASS_CPA_CONVENIO_PACOTE_S;
