create or replace procedure PRC_ATD_CIR_CIRURGIA_REAL_L
(
     pATD_CIR_ID IN TB_ATD_CIR_CIRURGIA_REALIZADA.ATD_CIR_ID%type DEFAULT NULL,
     pATD_ATE_ID IN TB_ATD_CIR_CIRURGIA_REALIZADA.ATD_ATE_ID%type DEFAULT NULL,
     pATD_CIR_DT_INICIO_REALIZ IN TB_ATD_CIR_CIRURGIA_REALIZADA.ATD_CIR_DT_INICIO_REALIZ%type DEFAULT NULL,
     pATD_CIR_DT_FIM_REALIZ IN TB_ATD_CIR_CIRURGIA_REALIZADA.ATD_CIR_DT_FIM_REALIZ%type DEFAULT NULL,
     pATD_CIR_HR_INICIO_REALIZ IN TB_ATD_CIR_CIRURGIA_REALIZADA.ATD_CIR_HR_INICIO_REALIZ%type DEFAULT NULL,
     pATD_CIR_HR_FIM_REALIZ IN TB_ATD_CIR_CIRURGIA_REALIZADA.ATD_CIR_HR_FIM_REALIZ%type DEFAULT NULL,
     pSEG_USU_ID_USU_CRIACAO IN TB_ATD_CIR_CIRURGIA_REALIZADA.SEG_USU_ID_USU_CRIACAO%type DEFAULT NULL,
     pATD_CIR_DT_CRIACAO IN TB_ATD_CIR_CIRURGIA_REALIZADA.ATD_CIR_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USU_ULT_ATUALIZ IN TB_ATD_CIR_CIRURGIA_REALIZADA.SEG_USU_ID_USU_ULT_ATUALIZ%type DEFAULT NULL,
     pATD_CIR_DT_ULT_ATUALIZ IN TB_ATD_CIR_CIRURGIA_REALIZADA.ATD_CIR_DT_ULT_ATUALIZ%type DEFAULT NULL,
     pATD_CIR_FL_SITUACAO IN TB_ATD_CIR_CIRURGIA_REALIZADA.ATD_CIR_FL_SITUACAO%type DEFAULT NULL,
     pAGE_SAU_ID IN TB_ATD_CIR_CIRURGIA_REALIZADA.AGE_SAU_ID%type DEFAULT NULL,
     pCAD_PRO_ID_CIRURGIAO IN TB_ATD_CIR_CIRURGIA_REALIZADA.CAD_PRO_ID_CIRURGIAO%type DEFAULT NULL,
	 pATD_CIR_MOTIVO_CANCELAMENTO IN TB_ATD_CIR_CIRURGIA_REALIZADA.ATD_CIR_MOTIVO_CANCELAMENTO%type,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ATD_CIR_CIRURGIA_REAL_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pATD_CIR_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_CIR_ID = ' || pATD_CIR_ID; END IF;
IF pATD_ATE_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_ATE_ID = ' || pATD_ATE_ID; END IF;
IF pATD_CIR_DT_INICIO_REALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_CIR_DT_INICIO_REALIZ = ' || CHR(39) || pATD_CIR_DT_INICIO_REALIZ || CHR(39); END IF;
IF pATD_CIR_DT_FIM_REALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_CIR_DT_FIM_REALIZ = ' || CHR(39) || pATD_CIR_DT_FIM_REALIZ || CHR(39); END IF;
IF pATD_CIR_HR_INICIO_REALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_CIR_HR_INICIO_REALIZ = ' || pATD_CIR_HR_INICIO_REALIZ; END IF;
IF pATD_CIR_HR_FIM_REALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_CIR_HR_FIM_REALIZ = ' || pATD_CIR_HR_FIM_REALIZ; END IF;
IF pSEG_USU_ID_USU_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USU_CRIACAO = ' || pSEG_USU_ID_USU_CRIACAO; END IF;
IF pATD_CIR_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_CIR_DT_CRIACAO = ' || CHR(39) || pATD_CIR_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USU_ULT_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USU_ULT_ATUALIZ = ' || pSEG_USU_ID_USU_ULT_ATUALIZ; END IF;
IF pATD_CIR_DT_ULT_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_CIR_DT_ULT_ATUALIZ = ' || CHR(39) || pATD_CIR_DT_ULT_ATUALIZ || CHR(39); END IF;
IF pATD_CIR_FL_SITUACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_CIR_FL_SITUACAO = ' || CHR(39) || pATD_CIR_FL_SITUACAO || CHR(39); END IF;
IF pAGE_SAU_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AGE_SAU_ID = ' || pAGE_SAU_ID; END IF; 
IF pCAD_PRO_ID_CIRURGIAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRO_ID_CIRURGIAO = ' || pCAD_PRO_ID_CIRURGIAO; END IF;
IF pATD_CIR_MOTIVO_CANCELAMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_CIR_MOTIVO_CANCELAMENTO = ' || pATD_CIR_MOTIVO_CANCELAMENTO; END IF;

   V_SELECT := '
SELECT	
       ATD_CIR_ID,
       ATD_ATE_ID,
       ATD_CIR_DT_INICIO_REALIZ,
       ATD_CIR_DT_FIM_REALIZ,
       ATD_CIR_HR_INICIO_REALIZ,
       ATD_CIR_HR_FIM_REALIZ,
       SEG_USU_ID_USU_CRIACAO,
       ATD_CIR_DT_CRIACAO,
       SEG_USU_ID_USU_ULT_ATUALIZ,
       ATD_CIR_DT_ULT_ATUALIZ,
       ATD_CIR_FL_SITUACAO,
       AGE_SAU_ID,
       CAD_PRO_ID_CIRURGIAO,
	   ATD_CIR_MOTIVO_CANCELAMENTO
FROM TB_ATD_CIR_CIRURGIA_REALIZADA
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ATD_CIR_CIRURGIA_REAL_L;