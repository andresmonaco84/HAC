CREATE OR REPLACE PROCEDURE "PRC_INT_ATD_INA_INT_ALTA_S"
  (
     pATD_ATE_ID IN TB_ATD_INA_INT_ALTA.ATD_ATE_ID%type DEFAULT NULL,
     pATD_INA_DT_ALTA_CLINICA IN TB_ATD_INA_INT_ALTA.ATD_INA_DT_ALTA_CLINICA%type DEFAULT NULL,
     pATD_INA_DT_ALTA_ADM IN TB_ATD_INA_INT_ALTA.ATD_INA_DT_ALTA_ADM%type DEFAULT NULL,
     pATD_INA_HR_ALTA_ADM IN TB_ATD_INA_INT_ALTA.ATD_INA_HR_ALTA_ADM%type DEFAULT NULL,
     pTIS_MSI_CD_MOTIVOSAIDAINT IN TB_ATD_INA_INT_ALTA.TIS_MSI_CD_MOTIVOSAIDAINT%type DEFAULT NULL,
     pATD_INA_DT_ULTIMA_ATUALIZACAO IN TB_ATD_INA_INT_ALTA.ATD_INA_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ATD_INA_INT_ALTA.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pATD_INA_HR_ALTA_CLINICA IN TB_ATD_INA_INT_ALTA.ATD_INA_HR_ALTA_CLINICA%type DEFAULT NULL,
     pCAD_PRO_ID_PROFISSIONAL IN TB_ATD_INA_INT_ALTA.CAD_PRO_ID_PROFISSIONAL%type DEFAULT NULL,
     pTIS_CBO_CD_CBOS IN TB_ATD_INA_INT_ALTA.TIS_CBO_CD_CBOS%type DEFAULT NULL,
     pATD_INA_ID IN TB_ATD_INA_INT_ALTA.ATD_INA_ID%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ALTA IN TB_ATD_INA_INT_ALTA.SEG_USU_ID_USUARIO_ALTA%type DEFAULT NULL,
     pATD_INA_DS_OBSERVACAO IN TB_ATD_INA_INT_ALTA.ATD_INA_DS_OBSERVACAO%type default NULL	,

     pATD_INA_CD_MOTIVO_PERM_LEITO IN TB_ATD_INA_INT_ALTA.ATD_INA_CD_MOTIVO_PERM_LEITO%type default NULL,
     pATD_INA_DS_RECEITUARIO IN TB_ATD_INA_INT_ALTA.ATD_INA_DS_RECEITUARIO%TYPE DEFAULT NULL,
          
     io_cursor OUT PKG_CURSOR.t_cursor
  )
  is
  /********************************************************************
  *    Procedure: PRC_INT_ATD_INA_INT_ALTA_S
  *
  *    Data Criacao:  data da  criac?o   Por: Nome do Analista
  *    Data Alteracao: data da alterac?o  Por: Nome do Analista
  *
  *    Funcao: Descric?o da funcionalidade da Stored Procedure
  *
  *******************************************************************/
  v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pATD_ATE_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_ATE_ID = ' || pATD_ATE_ID; END IF;
IF pATD_INA_DT_ALTA_CLINICA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TRUNC(ATD_INA_DT_ALTA_CLINICA) = ' || CHR(39) || TRUNC(pATD_INA_DT_ALTA_CLINICA) || CHR(39); END IF;
IF pATD_INA_DT_ALTA_ADM IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TRUNC(ATD_INA_DT_ALTA_ADM) = ' || CHR(39) || TRUNC(pATD_INA_DT_ALTA_ADM) || CHR(39); END IF;
IF pATD_INA_HR_ALTA_ADM IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_INA_HR_ALTA_ADM = ' || pATD_INA_HR_ALTA_ADM; END IF;
IF pTIS_MSI_CD_MOTIVOSAIDAINT IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_MSI_CD_MOTIVOSAIDAINT = ' || CHR(39) || pTIS_MSI_CD_MOTIVOSAIDAINT || CHR(39); END IF;
IF pATD_INA_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_INA_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pATD_INA_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pATD_INA_HR_ALTA_CLINICA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_INA_HR_ALTA_CLINICA = ' || pATD_INA_HR_ALTA_CLINICA; END IF;
IF pCAD_PRO_ID_PROFISSIONAL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRO_ID_PROFISSIONAL = ' || pCAD_PRO_ID_PROFISSIONAL; END IF;
IF pTIS_CBO_CD_CBOS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_CBO_CD_CBOS = ' || CHR(39) || pTIS_CBO_CD_CBOS || CHR(39); END IF;
IF pATD_INA_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_INA_ID = ' || pATD_INA_ID; END IF;
IF pSEG_USU_ID_USUARIO_ALTA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ALTA = ' || pSEG_USU_ID_USUARIO_ALTA; END IF;
IF pATD_INA_DS_OBSERVACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_INA_DS_OBSERVACAO = ' || CHR(39) || pATD_INA_DS_OBSERVACAO || CHR(39); END IF;

IF pATD_INA_CD_MOTIVO_PERM_LEITO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_INA_CD_MOTIVO_PERM_LEITO = ' || CHR(39) || pATD_INA_CD_MOTIVO_PERM_LEITO || CHR(39); END IF;
IF pATD_INA_DS_RECEITUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_INA_DS_RECEITUARIO = ' || CHR(39) || pATD_INA_DS_RECEITUARIO || CHR(39); END IF;

   V_SELECT := '
SELECT
       ATD_ATE_ID,
       TRUNC(ATD_INA_DT_ALTA_CLINICA) ATD_INA_DT_ALTA_CLINICA,
       TRUNC(ATD_INA_DT_ALTA_ADM) ATD_INA_DT_ALTA_ADM,
       ATD_INA_HR_ALTA_ADM,
       TIS_MSI_CD_MOTIVOSAIDAINT,
       ATD_INA_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       ATD_INA_HR_ALTA_CLINICA,
       CAD_PRO_ID_PROFISSIONAL,
       TIS_CBO_CD_CBOS,
       ATD_INA_ID,
       SEG_USU_ID_USUARIO_ALTA,
       ATD_INA_DS_OBSERVACAO,
       ATD_INA_CD_MOTIVO_PERM_LEITO,
       ATD_INA_DS_RECEITUARIO
FROM TB_ATD_INA_INT_ALTA
WHERE 1=1  '    ;
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
  end PRC_INT_ATD_INA_INT_ALTA_S;
