

--PRC_CAD_REA_RESTRICAO_ATEND_U
create or replace procedure PRC_CAD_REA_RESTRICAO_ATEND_U
(
     pCAD_PAC_ID_PACIENTE IN TB_CAD_REA_RESTRICAO_ATEND.CAD_PAC_ID_PACIENTE%type,
     pCAD_REA_DT_INICIO_VIGENCIA IN TB_CAD_REA_RESTRICAO_ATEND.CAD_REA_DT_INICIO_VIGENCIA%type,
     pCAD_REA_DT_FIM_VIGENCIA IN TB_CAD_REA_RESTRICAO_ATEND.CAD_REA_DT_FIM_VIGENCIA%type default NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_CAD_REA_RESTRICAO_ATEND.SEG_USU_ID_USUARIO_CRIACAO%type,
     pCAD_REA_DT_CRIACAO IN TB_CAD_REA_RESTRICAO_ATEND.CAD_REA_DT_CRIACAO%type,
     pSEG_USU_ID_USUARIO_ATUALIZ IN TB_CAD_REA_RESTRICAO_ATEND.SEG_USU_ID_USUARIO_ATUALIZ%type,
     pCAD_REA_DT_ULTIMA_ATUALIZ IN TB_CAD_REA_RESTRICAO_ATEND.CAD_REA_DT_ULTIMA_ATUALIZ%type,
     pCAD_PES_ID_PESSOA_RESPONSAVEL IN TB_CAD_REA_RESTRICAO_ATEND.CAD_PES_ID_PESSOA_RESPONSAVEL%type,
     pATD_ATE_ID IN TB_CAD_REA_RESTRICAO_ATEND.ATD_ATE_ID%type,
     pFAT_CCP_ID IN TB_CAD_REA_RESTRICAO_ATEND.FAT_CCP_ID%type,
     pCAD_REA_DS_OBSERVACAO IN TB_CAD_REA_RESTRICAO_ATEND.CAD_REA_DS_OBSERVACAO%type default NULL,
     pFAT_NOF_ID IN TB_CAD_REA_RESTRICAO_ATEND.FAT_NOF_ID%type default NULL
) 
is
/********************************************************************
*    Procedure: PRC_CAD_REA_RESTRICAO_ATEND_U
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/  
begin
UPDATE TB_CAD_REA_RESTRICAO_ATEND
SET	   
        CAD_REA_DT_FIM_VIGENCIA = pCAD_REA_DT_FIM_VIGENCIA,
        SEG_USU_ID_USUARIO_CRIACAO = pSEG_USU_ID_USUARIO_CRIACAO,
        CAD_REA_DT_CRIACAO = pCAD_REA_DT_CRIACAO,
        SEG_USU_ID_USUARIO_ATUALIZ = pSEG_USU_ID_USUARIO_ATUALIZ,
        CAD_REA_DT_ULTIMA_ATUALIZ = pCAD_REA_DT_ULTIMA_ATUALIZ,
        CAD_REA_DS_OBSERVACAO = pCAD_REA_DS_OBSERVACAO,
        FAT_NOF_ID = pFAT_NOF_ID 
WHERE
        ATD_ATE_ID = pATD_ATE_ID
    AND CAD_PAC_ID_PACIENTE = pCAD_PAC_ID_PACIENTE
    AND CAD_PES_ID_PESSOA_RESPONSAVEL = pCAD_PES_ID_PESSOA_RESPONSAVEL
    AND CAD_REA_DT_INICIO_VIGENCIA = pCAD_REA_DT_INICIO_VIGENCIA
    AND FAT_CCP_ID = pFAT_CCP_ID;	
end PRC_CAD_REA_RESTRICAO_ATEND_U;

