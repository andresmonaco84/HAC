create or replace procedure PRC_ATD_IEM_PRD_L
(
     PATD_IEM_DS_GRUPO IN TB_ATD_IEM_EVOL_MULTI.ATD_IEM_DS_GRUPO%type default NULL  ,
     io_cursor OUT PKG_CURSOR.t_cursor
)
is
 v_cursor PKG_CURSOR.t_cursor;

begin


OPEN v_cursor FOR

--FISIO - UTI
  SELECT PRD.CAD_PRD_ID, PRD.CAD_PRD_CD_CODIGO, PRD.CAD_PRD_DS_DESCRICAO, PRD.CAD_PRD_FL_STATUS, PRD.CAD_PRD_NM_MNEMONICO, PRD.AUX_EPP_CD_ESPECPROC
  FROM TB_CAD_PRD_PRODUTO PRD 
  WHERE PRD.CAD_PRD_CD_CODIGO IN ('20103379','20203012','20203047','20103093','20103271','20103700','20103662','20103670','20103530', '20103514','20103441','20103450')
  AND PRD.TIS_MED_CD_TABELAMEDICA = '16'
  AND (PATD_IEM_DS_GRUPO = 'FISIOTERAPEUTA')
  
   
;

  io_cursor := v_cursor;
end PRC_ATD_IEM_PRD_L;
