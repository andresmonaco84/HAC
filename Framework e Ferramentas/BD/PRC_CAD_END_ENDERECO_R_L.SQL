--PRC_CAD_END_ENDERECO_R_L
create or replace procedure PRC_CAD_END_ENDERECO_R_L 
(
     pCAD_END_ID_ENDERECO IN TB_CAD_END_ENDERECO.CAD_END_ID_ENDERECO%type DEFAULT NULL,
     pCAD_END_NM_LOGRADOURO IN TB_CAD_END_ENDERECO.CAD_END_NM_LOGRADOURO%type DEFAULT NULL,
     pCAD_END_NM_BAIRRO IN TB_CAD_END_ENDERECO.CAD_END_NM_BAIRRO%type DEFAULT NULL,
     pCAD_END_SG_UF IN TB_CAD_END_ENDERECO.CAD_END_SG_UF%type DEFAULT NULL,
     pCAD_END_CD_CEP IN TB_CAD_END_ENDERECO.CAD_END_CD_CEP%type DEFAULT NULL,
     pCAD_END_DS_NUMERO IN TB_CAD_END_ENDERECO.CAD_END_DS_NUMERO%type DEFAULT NULL,
     pCAD_END_DT_ULTIMA_ATUALIZACAO IN TB_CAD_END_ENDERECO.CAD_END_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pCAD_END_DS_COMPLEMENTO IN TB_CAD_END_ENDERECO.CAD_END_DS_COMPLEMENTO%type DEFAULT NULL,
     pAUX_MUN_CD_IBGE IN TB_CAD_END_ENDERECO.AUX_MUN_CD_IBGE%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_END_ENDERECO.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pAUX_TTE_CD_TP_TEL_END IN TB_CAD_END_ENDERECO.AUX_TTE_CD_TP_TEL_END%type DEFAULT NULL,
     pTIS_TLG_CD_TPLOGRADOURO IN TB_CAD_END_ENDERECO.TIS_TLG_CD_TPLOGRADOURO%type DEFAULT NULL,
     pCAD_PES_ID_PESSOA IN TB_CAD_END_ENDERECO.CAD_PES_ID_PESSOA%type DEFAULT NULL,
     pCAD_END_NM_ENDERECO_NF IN TB_CAD_END_ENDERECO.CAD_END_NM_ENDERECO_NF%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_END_ENDERECO_R_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_END_ID_ENDERECO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_END_ID_ENDERECO = ' || pCAD_END_ID_ENDERECO; END IF;
IF pCAD_END_NM_LOGRADOURO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_END_NM_LOGRADOURO = ' || CHR(39) || pCAD_END_NM_LOGRADOURO || CHR(39); END IF;
IF pCAD_END_NM_BAIRRO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_END_NM_BAIRRO = ' || CHR(39) || pCAD_END_NM_BAIRRO || CHR(39); END IF;
IF pCAD_END_SG_UF IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_END_SG_UF = ' || CHR(39) || pCAD_END_SG_UF || CHR(39); END IF;
IF pCAD_END_CD_CEP IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_END_CD_CEP = ' || CHR(39) || pCAD_END_CD_CEP || CHR(39); END IF;
IF pCAD_END_DS_NUMERO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_END_DS_NUMERO = ' || CHR(39) || pCAD_END_DS_NUMERO || CHR(39); END IF;
IF pCAD_END_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_END_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_END_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pCAD_END_DS_COMPLEMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_END_DS_COMPLEMENTO = ' || CHR(39) || pCAD_END_DS_COMPLEMENTO || CHR(39); END IF;
IF pAUX_MUN_CD_IBGE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AUX_MUN_CD_IBGE = ' || CHR(39) || pAUX_MUN_CD_IBGE || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pAUX_TTE_CD_TP_TEL_END IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AUX_TTE_CD_TP_TEL_END = ' || pAUX_TTE_CD_TP_TEL_END; END IF;
IF pTIS_TLG_CD_TPLOGRADOURO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_TLG_CD_TPLOGRADOURO = ' || CHR(39) || pTIS_TLG_CD_TPLOGRADOURO || CHR(39); END IF;
IF pCAD_PES_ID_PESSOA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PES_ID_PESSOA = ' || pCAD_PES_ID_PESSOA; END IF;
IF pCAD_END_NM_ENDERECO_NF IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_END_NM_ENDERECO_NF = ' || CHR(39) || pCAD_END_NM_ENDERECO_NF || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       CAD_END_ID_ENDERECO,
       CAD_END_NM_LOGRADOURO,
       CAD_END_NM_BAIRRO,
       CAD_END_SG_UF,
       CAD_END_CD_CEP,
       CAD_END_DS_NUMERO,
       CAD_END_DT_ULTIMA_ATUALIZACAO,
       CAD_END_DS_COMPLEMENTO,
       AUX_MUN_CD_IBGE,
       SEG_USU_ID_USUARIO,
       AUX_TTE_CD_TP_TEL_END,
       TIS_TLG_CD_TPLOGRADOURO,
       CAD_PES_ID_PESSOA,
       CAD_END_NM_ENDERECO_NF
FROM TB_CAD_END_ENDERECO
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_END_ENDERECO_R_L;
