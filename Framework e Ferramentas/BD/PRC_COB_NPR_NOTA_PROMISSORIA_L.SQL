--PRC_COB_NPR_NOTA_PROMISSORIA_L
create or replace procedure PRC_COB_NPR_NOTA_PROMISSORIA_L 
(
     pCOB_NPR_ID IN TB_COB_NPR_NOTA_PROMISSORIA.COB_NPR_ID%type DEFAULT NULL,
     pCOB_NPR_NR_NOTA_PROMISSORIA IN TB_COB_NPR_NOTA_PROMISSORIA.COB_NPR_NR_NOTA_PROMISSORIA%type DEFAULT NULL,
     pCOB_NPR_QT_TOTAL IN TB_COB_NPR_NOTA_PROMISSORIA.COB_NPR_QT_TOTAL%type DEFAULT NULL,
     pCOB_NPR_DT_VENCIMENTO IN TB_COB_NPR_NOTA_PROMISSORIA.COB_NPR_DT_VENCIMENTO%type DEFAULT NULL,
     pAUX_MUN_CD_IBGE_PRACA IN TB_COB_NPR_NOTA_PROMISSORIA.AUX_MUN_CD_IBGE_PRACA%type DEFAULT NULL,
     pCOB_NPR_VL_NOTA_PROMISSORIA IN TB_COB_NPR_NOTA_PROMISSORIA.COB_NPR_VL_NOTA_PROMISSORIA%type DEFAULT NULL,
     pCOB_NPR_DT_EMISSAO IN TB_COB_NPR_NOTA_PROMISSORIA.COB_NPR_DT_EMISSAO%type DEFAULT NULL,
     pAUX_MUN_CD_IBGE_LOCAL_EMISSAO IN TB_COB_NPR_NOTA_PROMISSORIA.AUX_MUN_CD_IBGE_LOCAL_EMISSAO%type DEFAULT NULL,
     pCAD_PES_ID_PESSOA IN TB_COB_NPR_NOTA_PROMISSORIA.CAD_PES_ID_PESSOA%type DEFAULT NULL,
     pCOB_NPR_DT_CRIACAO IN TB_COB_NPR_NOTA_PROMISSORIA.COB_NPR_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_COB_NPR_NOTA_PROMISSORIA.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pCOB_NPR_DT_ULTIMA_ATUALIZACAO IN TB_COB_NPR_NOTA_PROMISSORIA.COB_NPR_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ATUALIZ IN TB_COB_NPR_NOTA_PROMISSORIA.SEG_USU_ID_USUARIO_ATUALIZ%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_COB_NPR_NOTA_PROMISSORIA_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCOB_NPR_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_NPR_ID = ' || pCOB_NPR_ID; END IF;
IF pCOB_NPR_NR_NOTA_PROMISSORIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_NPR_NR_NOTA_PROMISSORIA = ' || pCOB_NPR_NR_NOTA_PROMISSORIA; END IF;
IF pCOB_NPR_QT_TOTAL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_NPR_QT_TOTAL = ' || pCOB_NPR_QT_TOTAL; END IF;
IF pCOB_NPR_DT_VENCIMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_NPR_DT_VENCIMENTO = ' || CHR(39) || pCOB_NPR_DT_VENCIMENTO || CHR(39); END IF;
IF pAUX_MUN_CD_IBGE_PRACA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AUX_MUN_CD_IBGE_PRACA = ' || CHR(39) || pAUX_MUN_CD_IBGE_PRACA || CHR(39); END IF;
IF pCOB_NPR_VL_NOTA_PROMISSORIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_NPR_VL_NOTA_PROMISSORIA = ' || pCOB_NPR_VL_NOTA_PROMISSORIA; END IF;
IF pCOB_NPR_DT_EMISSAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_NPR_DT_EMISSAO = ' || CHR(39) || pCOB_NPR_DT_EMISSAO || CHR(39); END IF;
IF pAUX_MUN_CD_IBGE_LOCAL_EMISSAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AUX_MUN_CD_IBGE_LOCAL_EMISSAO = ' || CHR(39) || pAUX_MUN_CD_IBGE_LOCAL_EMISSAO || CHR(39); END IF;
IF pCAD_PES_ID_PESSOA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PES_ID_PESSOA = ' || pCAD_PES_ID_PESSOA; END IF;
IF pCOB_NPR_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_NPR_DT_CRIACAO = ' || CHR(39) || pCOB_NPR_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; END IF;
IF pCOB_NPR_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_NPR_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCOB_NPR_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ATUALIZ = ' || pSEG_USU_ID_USUARIO_ATUALIZ; END IF;
 
   V_SELECT := '
SELECT	
       COB_NPR_ID,
       COB_NPR_NR_NOTA_PROMISSORIA,
       COB_NPR_QT_TOTAL,
       COB_NPR_DT_VENCIMENTO,
       AUX_MUN_CD_IBGE_PRACA,
       COB_NPR_VL_NOTA_PROMISSORIA,
       COB_NPR_DT_EMISSAO,
       AUX_MUN_CD_IBGE_LOCAL_EMISSAO,
       CAD_PES_ID_PESSOA,
       COB_NPR_DT_CRIACAO,
       SEG_USU_ID_USUARIO_CRIACAO,
       COB_NPR_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO_ATUALIZ
FROM TB_COB_NPR_NOTA_PROMISSORIA
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_COB_NPR_NOTA_PROMISSORIA_L;
/
