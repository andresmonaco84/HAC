create or replace procedure PRC_TIS_GPP_GRAU_PART_PROF_L
(
     pTIS_GPP_CD_GRAU_PART_PROF IN TB_TIS_GPP_GRAU_PART_PROF.TIS_GPP_CD_GRAU_PART_PROF%type DEFAULT NULL,
     pTIS_GPP_DS_GRAU_PART_PROF IN TB_TIS_GPP_GRAU_PART_PROF.TIS_GPP_DS_GRAU_PART_PROF%type DEFAULT NULL,
     pTIS_GPP_PC_GRAU_PART_PROF IN TB_TIS_GPP_GRAU_PART_PROF.TIS_GPP_PC_GRAU_PART_PROF%type DEFAULT NULL,
     pTIS_GPP_DT_ULTIMA_ATUALIZACAO IN TB_TIS_GPP_GRAU_PART_PROF.TIS_GPP_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_TIS_GPP_GRAU_PART_PROF.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pTIS_GPP_FL_STATUS IN TB_TIS_GPP_GRAU_PART_PROF.TIS_GPP_FL_STATUS%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
)
is
/********************************************************************
*    Procedure: PRC_TIS_GPP_GRAU_PART_PROF_L
*
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
	IF pTIS_GPP_CD_GRAU_PART_PROF IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_GPP_CD_GRAU_PART_PROF = ' || CHR(39) || pTIS_GPP_CD_GRAU_PART_PROF || CHR(39); END IF;
	IF pTIS_GPP_DS_GRAU_PART_PROF IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_GPP_DS_GRAU_PART_PROF = ' || CHR(39) || pTIS_GPP_DS_GRAU_PART_PROF || CHR(39); END IF;
	IF pTIS_GPP_PC_GRAU_PART_PROF IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_GPP_PC_GRAU_PART_PROF = ' || pTIS_GPP_PC_GRAU_PART_PROF; END IF;
	IF pTIS_GPP_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_GPP_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pTIS_GPP_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
	IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
  IF pTIS_GPP_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_GPP_FL_STATUS = ' || CHR(39) || pTIS_GPP_FL_STATUS || CHR(39); END IF;  

  V_SELECT := '
	SELECT TIS_GPP_CD_GRAU_PART_PROF,
		   TIS_GPP_DS_GRAU_PART_PROF,
		   TIS_GPP_PC_GRAU_PART_PROF,
		   TIS_GPP_DT_ULTIMA_ATUALIZACAO,
		   SEG_USU_ID_USUARIO,
		   TIS_GPP_FL_STATUS,
		   TIS_GPP_DT_CRIACAO,
		   SEG_USU_ID_USUARIO_CRIACAO
	  FROM TB_TIS_GPP_GRAU_PART_PROF
	 WHERE null is null  ';

OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_TIS_GPP_GRAU_PART_PROF_L;