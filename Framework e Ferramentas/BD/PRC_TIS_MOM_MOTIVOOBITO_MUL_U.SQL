create or replace procedure PRC_TIS_MOM_MOTIVOOBITO_MUL_U
  (
     pTIS_MOM_CD_MOTIVOOBITO IN TB_TIS_MOM_MOTIVOOBITO_MULHER.TIS_MOM_CD_MOTIVOOBITO%type,
     pTIS_MOM_DS_MOTIVOOBITO IN TB_TIS_MOM_MOTIVOOBITO_MULHER.TIS_MOM_DS_MOTIVOOBITO%type,
  pTIS_MOM_FL_STATUS   IN TB_TIS_MOM_MOTIVOOBITO_MULHER.TIS_MOM_FL_STATUS%type,
   pTIS_MOM_DT_CRIACAO IN TB_TIS_MOM_MOTIVOOBITO_MULHER.TIS_MOM_DT_CRIACAO%TYPE DEFAULT NULL,     
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_TIS_MOM_MOTIVOOBITO_MULHER.SEG_USU_ID_USUARIO_CRIACAO%TYPE DEFAULT NULL,
     pTIS_MOM_DT_ULT_ATUALIZACAO IN TB_TIS_MOM_MOTIVOOBITO_MULHER.TIS_MOM_DT_ULT_ATUALIZACAO%TYPE DEFAULT NULL,
     pSEG_USU_ID_USU_ULT_ATUALIZ IN TB_TIS_MOM_MOTIVOOBITO_MULHER.SEG_USU_ID_USU_ULT_ATUALIZ%TYPE DEFAULT NULL
 
  )
  is
  /********************************************************************
  *    Procedure: PRC_TIS_MOM_MOTIVOOBITO_MUL_U
  *
  *    Data Criacao:  12/03/2007   Por: Cristiane Gomes da Silva
  *    Data Alteracao: 02/05/2007   Por: Cristiane Gomes da Silva
  *
  *    Data Alterac?o: 25/05/2007   Por: Silmara
  *    Alterac?o:Foi alterado os parametros para gravar em maiusculo
  *    Funcao: Altera Motivo de Obito em Mulher - TISS
  *
  *******************************************************************/
  begin
    UPDATE TB_TIS_MOM_MOTIVOOBITO_MULHER
    SET
        TIS_MOM_DS_MOTIVOOBITO = UPPER(pTIS_MOM_DS_MOTIVOOBITO),
        TIS_MOM_FL_STATUS = pTIS_MOM_FL_STATUS,
   TIS_MOM_DT_ULT_ATUALIZACAO  = SYSDATE,
        SEG_USU_ID_USU_ULT_ATUALIZ = pSEG_USU_ID_USU_ULT_ATUALIZ
    WHERE
        TIS_MOM_CD_MOTIVOOBITO = pTIS_MOM_CD_MOTIVOOBITO;
  end PRC_TIS_MOM_MOTIVOOBITO_MUL_U;
 