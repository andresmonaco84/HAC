CREATE OR REPLACE PROCEDURE PRC_CAD_CTS_FECHA_PERIODO_U(PCAD_CTS_MES_FECHAMENTO        IN TB_CAD_CTS_FECHA_PERIODO.CAD_CTS_MES_FECHAMENTO%TYPE,
                                                        PCAD_CTS_ANO_FECHAMENTO        IN TB_CAD_CTS_FECHA_PERIODO.CAD_CTS_ANO_FECHAMENTO%TYPE,
                                                        PCAD_CTS_STATUS                IN TB_CAD_CTS_FECHA_PERIODO.CAD_CTS_STATUS%TYPE,
                                                        PCAD_CTS_DT_ULTIMA_ATUALIZACAO IN TB_CAD_CTS_FECHA_PERIODO.CAD_CTS_DT_ULTIMA_ATUALIZACAO%TYPE DEFAULT NULL,
                                                        PSEG_USU_ID_USUARIO            IN TB_CAD_CTS_FECHA_PERIODO.SEG_USU_ID_USUARIO%TYPE DEFAULT NULL) IS
  /********************************************************************
  *    PROCEDURE: PRC_CAD_CTS_FECHA_PERIODO_U
  *
  *    DATA CRIACAO:  DATA DA  CRIA??O   POR: NOME DO ANALISTA
  *    DATA ALTERACAO:  DATA DA ALTERA??O  POR: NOME DO ANALISTA
  *
  *    FUNCAO: DESCRI??O DA FUNCIONALIDADE DA STORED PROCEDURE
  *
  *******************************************************************/
BEGIN
  UPDATE TB_CAD_CTS_FECHA_PERIODO
     SET CAD_CTS_DT_ULTIMA_ATUALIZACAO = PCAD_CTS_DT_ULTIMA_ATUALIZACAO,
         SEG_USU_ID_USUARIO            = PSEG_USU_ID_USUARIO,
         CAD_CTS_STATUS                = PCAD_CTS_STATUS
   WHERE CAD_CTS_ANO_FECHAMENTO = PCAD_CTS_ANO_FECHAMENTO
     AND CAD_CTS_MES_FECHAMENTO = PCAD_CTS_MES_FECHAMENTO;
END PRC_CAD_CTS_FECHA_PERIODO_U;
