  /********************************************************************
  *    Procedure: PRC_CAD_PES_PESSOA_RMT_U
  *
  *    Data Alteracao:  05/01/2012  Por: Pedro
  *    Alteracao: Adic?o doS campoS pCODCFO pCAD_PES_CD_CNS_SUS
  *
  *    Funcao: Descric?o da funcionalidade da Stored Procedure
  *
  *******************************************************************/
  estadoCivil VARCHAR2(2);
  BEGIN
  IF pCAD_PES_CD_ESTADOCIVIL not in (1,2,3,4,5,6) THEN
  CASE
   WHEN pCAD_PES_CD_ESTADOCIVIL = 'S' THEN
      estadoCivil := '1';
   WHEN pCAD_PES_CD_ESTADOCIVIL = 'C' THEN
      estadoCivil := '2';
   WHEN pCAD_PES_CD_ESTADOCIVIL = 'D' THEN
      estadoCivil := '4';
   WHEN pCAD_PES_CD_ESTADOCIVIL = 'V' THEN
      estadoCivil := '5';
   WHEN pCAD_PES_CD_ESTADOCIVIL = 'U' THEN
      estadoCivil := '6';
   ELSE estadoCivil := NULL;
  END CASE;
  ELSE
  estadoCivil := pCAD_PES_CD_ESTADOCIVIL;
  END IF;
    UPDATE TB_CAD_PES_PESSOA
    SET
        CAD_PES_NR_CNPJ_CPF = pCAD_PES_NR_CNPJ_CPF,
        CAD_PES_FL_JURIDICA_OK = pCAD_PES_FL_JURIDICA_OK,
        CAD_PES_NM_PESSOA = pCAD_PES_NM_PESSOA,
        CAD_PES_DT_ULTIMA_ATUALIZACAO = pCAD_PES_DT_ULTIMA_ATUALIZACAO,
        SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO,
        CAD_PES_DT_NASCIMENTO = TRUNC(pCAD_PES_DT_NASCIMENTO),
        CAD_PES_NR_RG = pCAD_PES_NR_RG,
        CAD_PES_NM_RAZAOSOCIAL = pCAD_PES_NM_RAZAOSOCIAL,
        CAD_PES_CD_INSCR_ESTAD = pCAD_PES_CD_INSCR_ESTAD,
        CAD_PES_CD_INSCR_MUNIC = pCAD_PES_CD_INSCR_MUNIC,
        CAD_PES_TP_SEXO = pCAD_PES_TP_SEXO,
        CAD_PES_CD_ESTADOCIVIL = estadoCivil,
        CAD_PES_NM_NOMEMAE = DECODE(pCAD_PES_NM_NOMEMAE, NULL,
                                                                  DECODE(CAD_PES_NM_NOMEMAE, NULL,
                                                                                             '.', trim(UPPER(CAD_PES_NM_NOMEMAE))), pCAD_PES_NM_NOMEMAE),
/*        CAD_PES_NM_NOMEMAE = DECODE(pCAD_PES_NM_NOMEMAE, NULL,
                                                                  DECODE(CAD_PES_NM_NOMEMAE, NULL,
                                                                                             'M?E DE ' || trim(upper(pCAD_PES_NM_PESSOA)), trim(UPPER(CAD_PES_NM_NOMEMAE))), pCAD_PES_NM_NOMEMAE),
*/
        CAD_PES_CD_ORGAOEMISSORRG = pCAD_PES_CD_ORGAOEMISSORRG,
        CAD_PES_DT_EXPEDICAORG = pCAD_PES_DT_EXPEDICAORG,
        CAD_PES_TP_SANGUINEO = pCAD_PES_TP_SANGUINEO,
        CAD_PES_FL_SANGPOSITIVO_OK = pCAD_PES_FL_SANGPOSITIVO_OK,
        AUX_PRF_CD_CODIGO = pAUX_PRF_CD_CODIGO,
        AUX_NAC_CD_CODIGO = pAUX_NAC_CD_CODIGO,
        CAD_PES_FL_DOADORORGAOS_OK = pCAD_PES_FL_DOADORORGAOS_OK,
        CAD_PES_NM_NATURALIDADE = pCAD_PES_NM_NATURALIDADE,
        AUX_ESC_CD_CODIGO = pAUX_ESC_CD_CODIGO,
        CAD_PES_NM_NOMEPAI = pCAD_PES_NM_NOMEPAI,
        CAD_PES_NM_CONJUGE = pCAD_PES_NM_CONJUGE,
        AUX_UFE_SG_UF = pAUX_UFE_SG_UF,
        CODCFO  = pCODCFO,
        CAD_PES_CD_CNS_SUS = pCAD_PES_CD_CNS_SUS,
        CAD_PES_NOME_SOUNDEX = FNC_PES_SOUNDEX_NOME(pCAD_PES_NM_PESSOA,' '),
        CAD_PES_FL_ATIVO_OK = nvl(pCAD_PES_FL_ATIVO_OK,'S'),
        CAD_PES_NM_RN = pCAD_PES_NM_RN,
        CAD_PES_DT_NASC_RN = TRUNC(pCAD_PES_DT_NASC_RN),
        CAD_PES_TP_SEXO_RN = pCAD_PES_TP_SEXO_RN,
        CAD_PES_FL_PRE_ATENDIMENTO = pCAD_PES_FL_PRE_ATENDIMENTO,
        CAD_PES_FL_EMAIL_INFORMADO = pCAD_PES_FL_EMAIL_INFORMADO,
        CAD_PES_DT_DESBLOQUEIO_CPF = pCAD_PES_DT_DESBLOQUEIO_CPF,
        SEG_USU_ID_USUARIO_DESB_CPF = pSEG_USU_ID_USUARIO_DESB_CPF,
        CAD_PES_FL_CUIDADOINTEGRAL = pCAD_PES_FL_CUIDADOINTEGRAL,
        CAD_PES_FL_ISOLAMENTO = pCAD_PES_FL_ISOLAMENTO

    WHERE
        CAD_PES_ID_PESSOA = pCAD_PES_ID_PESSOA;
  end PRC_CAD_PES_PESSOA_RMT_U;
