--PRC_ATS_APS_ATEN_PROC_SEG_S
create or replace procedure PRC_ATS_APS_ATEN_PROC_SEG_S 
(
     pATS_ATE_CD_INTLIB IN TB_ATS_APS_ATEN_PROC_SEGMENTO.ATS_ATE_CD_INTLIB%type DEFAULT NULL,
     pATS_ATE_IN_INTLIB IN TB_ATS_APS_ATEN_PROC_SEGMENTO.ATS_ATE_IN_INTLIB%type DEFAULT NULL,
     pATS_ATE_ID IN TB_ATS_APS_ATEN_PROC_SEGMENTO.ATS_ATE_ID%type DEFAULT NULL,
     pAUX_EPP_CD_ESPECPROC IN TB_ATS_APS_ATEN_PROC_SEGMENTO.AUX_EPP_CD_ESPECPROC%type DEFAULT NULL,
     pCAD_PRD_ID IN TB_ATS_APS_ATEN_PROC_SEGMENTO.CAD_PRD_ID%type DEFAULT NULL,
     pATS_APS_ID IN TB_ATS_APS_ATEN_PROC_SEGMENTO.ATS_APS_ID%type DEFAULT NULL,
     pATS_CPS_CD_SEGMENTO IN TB_ATS_APS_ATEN_PROC_SEGMENTO.ATS_CPS_CD_SEGMENTO%type DEFAULT NULL,
     pATS_APS_DT_ULTIMA_ATUALIZACAO IN TB_ATS_APS_ATEN_PROC_SEGMENTO.ATS_APS_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ATS_APS_ATEN_PROC_SEGMENTO.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pATS_APS_FL_STATUS IN TB_ATS_APS_ATEN_PROC_SEGMENTO.ATS_APS_FL_STATUS%type DEFAULT NULL,
     pCAD_PRD_ID_APS IN TB_ATS_APS_ATEN_PROC_SEGMENTO.CAD_PRD_ID_APS%type DEFAULT NULL,
     pTIS_MED_CD_TABELAMEDICA IN TB_ATS_APS_ATEN_PROC_SEGMENTO.TIS_MED_CD_TABELAMEDICA%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ATS_APS_ATEN_PROC_SEG_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pATS_ATE_CD_INTLIB IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_ATE_CD_INTLIB = ' || pATS_ATE_CD_INTLIB; END IF;
IF pATS_ATE_IN_INTLIB IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_ATE_IN_INTLIB = ' || CHR(39) || pATS_ATE_IN_INTLIB || CHR(39); END IF;
IF pATS_ATE_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_ATE_ID = ' || pATS_ATE_ID; END IF;
IF pAUX_EPP_CD_ESPECPROC IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AUX_EPP_CD_ESPECPROC = ' || CHR(39) || pAUX_EPP_CD_ESPECPROC || CHR(39); END IF;
IF pCAD_PRD_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_ID = ' || pCAD_PRD_ID; END IF;
IF pATS_APS_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_APS_ID = ' || pATS_APS_ID; END IF;
IF pATS_CPS_CD_SEGMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_CPS_CD_SEGMENTO = ' || CHR(39) || pATS_CPS_CD_SEGMENTO || CHR(39); END IF;
IF pATS_APS_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_APS_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pATS_APS_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pATS_APS_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_APS_FL_STATUS = ' || CHR(39) || pATS_APS_FL_STATUS || CHR(39); END IF;
IF pCAD_PRD_ID_APS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_ID_APS = ' || pCAD_PRD_ID_APS; END IF;
IF pTIS_MED_CD_TABELAMEDICA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_MED_CD_TABELAMEDICA = ' || CHR(39) || pTIS_MED_CD_TABELAMEDICA || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       ATS_ATE_CD_INTLIB,
       ATS_ATE_IN_INTLIB,
       ATS_ATE_ID,
       AUX_EPP_CD_ESPECPROC,
       CAD_PRD_ID,
       ATS_APS_ID,
       ATS_CPS_CD_SEGMENTO,
       ATS_APS_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       ATS_APS_FL_STATUS,
       CAD_PRD_ID_APS,
       TIS_MED_CD_TABELAMEDICA
FROM TB_ATS_APS_ATEN_PROC_SEGMENTO
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ATS_APS_ATEN_PROC_SEG_S;
