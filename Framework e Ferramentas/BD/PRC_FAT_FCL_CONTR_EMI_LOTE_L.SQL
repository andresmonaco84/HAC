--PRC_FAT_FCL_CONTR_EMI_LOTE_L
create or replace procedure PRC_FAT_FCL_CONTR_EMI_LOTE_L 
(
     pFAT_FCL_NR_SEQ_LOTE IN TB_FAT_FCL_CONTR_EMI_LOTE.FAT_FCL_NR_SEQ_LOTE%type DEFAULT NULL,
     pFAT_COC_ID IN TB_FAT_FCL_CONTR_EMI_LOTE.FAT_COC_ID%type DEFAULT NULL,
     pFAT_CCP_ID IN TB_FAT_FCL_CONTR_EMI_LOTE.FAT_CCP_ID%type DEFAULT NULL,
     pFAT_FCL_DT_ULTIMA_ATUALIZACAO IN TB_FAT_FCL_CONTR_EMI_LOTE.FAT_FCL_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_FAT_FCL_CONTR_EMI_LOTE.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pATD_ATE_ID IN TB_FAT_FCL_CONTR_EMI_LOTE.ATD_ATE_ID%type DEFAULT NULL,
     pCAD_PAC_ID_PACIENTE IN TB_FAT_FCL_CONTR_EMI_LOTE.CAD_PAC_ID_PACIENTE%type DEFAULT NULL,
     pFAT_FCL_NR_SEQ_IMPRIME IN TB_FAT_FCL_CONTR_EMI_LOTE.FAT_FCL_NR_SEQ_IMPRIME%TYPE DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_FAT_FCL_CONTR_EMI_LOTE_L
*
*    Data Criacao: 	data da  criação   Por: Pedro
*    Data Alteracao:	20/9/2010  Por: Pedro
*    Alteracao: FAT_FCL_NR_SEQ_IMPRIME
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pFAT_FCL_NR_SEQ_LOTE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_FCL_NR_SEQ_LOTE = ' || pFAT_FCL_NR_SEQ_LOTE; END IF;
  IF pFAT_COC_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_COC_ID = ' || pFAT_COC_ID; END IF;
  IF pFAT_CCP_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_CCP_ID = ' || pFAT_CCP_ID; END IF;
  IF pFAT_FCL_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_FCL_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pFAT_FCL_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
  IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
  IF pATD_ATE_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_ATE_ID = ' || pATD_ATE_ID; END IF;
  IF pCAD_PAC_ID_PACIENTE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PAC_ID_PACIENTE = ' || pCAD_PAC_ID_PACIENTE; END IF;
  IF pFAT_FCL_NR_SEQ_IMPRIME IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_FCL_NR_SEQ_IMPRIME = ' || pFAT_FCL_NR_SEQ_IMPRIME; END IF;
  V_SELECT:=
  'SELECT	
       FAT_FCL_NR_SEQ_LOTE,
       FAT_COC_ID,
       FAT_CCP_ID,
       FAT_FCL_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       ATD_ATE_ID,
       CAD_PAC_ID_PACIENTE,
       FAT_FCL_NR_SEQ_IMPRIME
FROM TB_FAT_FCL_CONTR_EMI_LOTE
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_FAT_FCL_CONTR_EMI_LOTE_L;

