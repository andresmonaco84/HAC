     pATD_CPM_DS_OBSERVACAO IN TB_ATD_CPM_CUID_ESP_PRESC_MED.ATD_CPM_DS_OBSERVACAO%type DEFAULT NULL,
     pATD_CPM_DT_HORA_CRIACAO IN TB_ATD_CPM_CUID_ESP_PRESC_MED.ATD_CPM_DT_HORA_CRIACAO%type DEFAULT NULL,
     pATD_CPM_ID_USU_CRIACAO IN TB_ATD_CPM_CUID_ESP_PRESC_MED.ATD_CPM_ID_USU_CRIACAO%type DEFAULT NULL,
     pATD_CPM_DT_ULT_ATUALIZ IN TB_ATD_CPM_CUID_ESP_PRESC_MED.ATD_CPM_DT_ULT_ATUALIZ%type DEFAULT NULL,
     pATD_CPM_ID_USU_ULT_ATUALIZ IN TB_ATD_CPM_CUID_ESP_PRESC_MED.ATD_CPM_ID_USU_ULT_ATUALIZ%type DEFAULT NULL,
          pATD_CPM_STATUS IN TB_ATD_CPM_CUID_ESP_PRESC_MED.ATD_CPM_STATUS%type DEFAULT NULL,
                         pATD_CPM_FL_IMPRESSO IN TB_ATD_CPM_CUID_ESP_PRESC_MED.ATD_CPM_FL_IMPRESSO%type DEFAULT NULL,
               pATD_CPM_TP_CUIDADO  IN TB_ATD_CPM_CUID_ESP_PRESC_MED.ATD_CPM_TP_CUIDADO %type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
)
is
/********************************************************************
*    Procedure: PRC_ATD_CPM_CUID_ESP_PRESC_MED_L
*
*    Data Criacao: 	data da  cria??o   Por: Nome do Analista
*    Data Alteracao:	data da altera??o  Por: Nome do Analista
*
*    Funcao: Descri??o da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pATD_CPM_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_CPM_ID = ' || pATD_CPM_ID; END IF;
IF pATD_PME_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PME_ID = ' || pATD_PME_ID; END IF;
IF pATD_CPM_DS_CUIDADO_ESPEC IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_CPM_DS_CUIDADO_ESPEC = ' || CHR(39) || pATD_CPM_DS_CUIDADO_ESPEC || CHR(39); END IF;
IF pATD_CPM_DS_OBSERVACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_CPM_DS_OBSERVACAO = ' || CHR(39) || pATD_CPM_DS_OBSERVACAO || CHR(39); END IF;
IF pATD_CPM_DT_HORA_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_CPM_DT_HORA_CRIACAO = ' || CHR(39) || pATD_CPM_DT_HORA_CRIACAO || CHR(39); END IF;
IF pATD_CPM_ID_USU_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_CPM_ID_USU_CRIACAO = ' || pATD_CPM_ID_USU_CRIACAO; END IF;
