 
--PRC_CAD_HTR_HORA_TRAB_L
create or replace procedure PRC_CAD_HTR_HORA_TRAB_L 
(
     pCAD_HTR_ID IN TB_CAD_HTR_HORA_TRAB.CAD_HTR_ID%type DEFAULT NULL,
     pCAD_CLC_ID IN TB_CAD_HTR_HORA_TRAB.CAD_CLC_ID%type DEFAULT NULL,
     pCAD_UNI_ID_UNIDADE IN TB_CAD_HTR_HORA_TRAB.CAD_UNI_ID_UNIDADE%type DEFAULT NULL,
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_CAD_HTR_HORA_TRAB.CAD_LAT_ID_LOCAL_ATENDIMENTO%type DEFAULT NULL,
     pCAD_PRO_ID_PROFISSIONAL IN TB_CAD_HTR_HORA_TRAB.CAD_PRO_ID_PROFISSIONAL%type DEFAULT NULL,
     pTIS_CBO_CD_CBOS IN TB_CAD_HTR_HORA_TRAB.TIS_CBO_CD_CBOS%type DEFAULT NULL,
     pCAD_SET_ID IN TB_CAD_HTR_HORA_TRAB.CAD_SET_ID%type DEFAULT NULL,
     pCAD_HTR_DT_INICIO_VIGENCIA IN TB_CAD_HTR_HORA_TRAB.CAD_HTR_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pCAD_HTR_DT_FIM_VIGENCIA IN TB_CAD_HTR_HORA_TRAB.CAD_HTR_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pCAD_HTR_QT_HORA_SEMANA IN TB_CAD_HTR_HORA_TRAB.CAD_HTR_QT_HORA_SEMANA%type DEFAULT NULL,
     pCAD_HTR_VL_FATOR_MULTI_SEMANA IN TB_CAD_HTR_HORA_TRAB.CAD_HTR_VL_FATOR_MULTI_SEMANA%type DEFAULT NULL,
     pCAD_HTR_QT_HORA_FIM_SEMANA IN TB_CAD_HTR_HORA_TRAB.CAD_HTR_QT_HORA_FIM_SEMANA%type DEFAULT NULL,
     pCAD_HTR_VL_FATOR_MULTI_FIMSEM IN TB_CAD_HTR_HORA_TRAB.CAD_HTR_VL_FATOR_MULTI_FIMSEM%type DEFAULT NULL,
     pCAD_HTR_DT_ULTIMA_ATUALIZACAO IN TB_CAD_HTR_HORA_TRAB.CAD_HTR_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_HTR_HORA_TRAB.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_HTR_HORA_TRAB_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_HTR_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HTR_ID = ' || pCAD_HTR_ID; END IF;
IF pCAD_CLC_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CLC_ID = ' || pCAD_CLC_ID; END IF;
IF pCAD_UNI_ID_UNIDADE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_UNI_ID_UNIDADE = ' || pCAD_UNI_ID_UNIDADE; END IF;
IF pCAD_LAT_ID_LOCAL_ATENDIMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_LAT_ID_LOCAL_ATENDIMENTO = ' || pCAD_LAT_ID_LOCAL_ATENDIMENTO; END IF;
IF pCAD_PRO_ID_PROFISSIONAL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRO_ID_PROFISSIONAL = ' || pCAD_PRO_ID_PROFISSIONAL; END IF;
IF pTIS_CBO_CD_CBOS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_CBO_CD_CBOS = ' || CHR(39) || pTIS_CBO_CD_CBOS || CHR(39); END IF;
IF pCAD_SET_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_SET_ID = ' || pCAD_SET_ID; END IF;
IF pCAD_HTR_DT_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HTR_DT_INICIO_VIGENCIA = ' || CHR(39) || pCAD_HTR_DT_INICIO_VIGENCIA || CHR(39); END IF;
IF pCAD_HTR_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HTR_DT_FIM_VIGENCIA = ' || CHR(39) || pCAD_HTR_DT_FIM_VIGENCIA || CHR(39); END IF;
IF pCAD_HTR_QT_HORA_SEMANA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HTR_QT_HORA_SEMANA = ' || pCAD_HTR_QT_HORA_SEMANA; END IF;
IF pCAD_HTR_VL_FATOR_MULTI_SEMANA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HTR_VL_FATOR_MULTI_SEMANA = ' || pCAD_HTR_VL_FATOR_MULTI_SEMANA; END IF;
IF pCAD_HTR_QT_HORA_FIM_SEMANA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HTR_QT_HORA_FIM_SEMANA = ' || pCAD_HTR_QT_HORA_FIM_SEMANA; END IF;
IF pCAD_HTR_VL_FATOR_MULTI_FIMSEM IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HTR_VL_FATOR_MULTI_FIMSEM = ' || pCAD_HTR_VL_FATOR_MULTI_FIMSEM; END IF;
IF pCAD_HTR_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_HTR_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_HTR_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       CAD_HTR_ID,
       CAD_CLC_ID,
       CAD_UNI_ID_UNIDADE,
       CAD_LAT_ID_LOCAL_ATENDIMENTO,
       CAD_PRO_ID_PROFISSIONAL,
       TIS_CBO_CD_CBOS,
       CAD_SET_ID,
       CAD_HTR_DT_INICIO_VIGENCIA,
       CAD_HTR_DT_FIM_VIGENCIA,
       CAD_HTR_QT_HORA_SEMANA,
       CAD_HTR_VL_FATOR_MULTI_SEMANA,
       CAD_HTR_QT_HORA_FIM_SEMANA,
       CAD_HTR_VL_FATOR_MULTI_FIMSEM,
       CAD_HTR_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO
FROM TB_CAD_HTR_HORA_TRAB
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_HTR_HORA_TRAB_L;
