--PRC_COB_DCH_DEVOLUCAO_CHEQUE_L
create or replace procedure PRC_COB_DCH_DEVOLUCAO_CHEQUE_L 
(
     pCOB_DCH_ID IN TB_COB_DCH_DEVOLUCAO_CHEQUE.COB_DCH_ID%type DEFAULT NULL,
     pCOB_CHQ_ID IN TB_COB_DCH_DEVOLUCAO_CHEQUE.COB_CHQ_ID%type DEFAULT NULL,
     pCOB_DCH_DT_DEVOLUCAO IN TB_COB_DCH_DEVOLUCAO_CHEQUE.COB_DCH_DT_DEVOLUCAO%type DEFAULT NULL,
     pCAD_MDC_ID IN TB_COB_DCH_DEVOLUCAO_CHEQUE.CAD_MDC_ID%type DEFAULT NULL,
     pCOB_DCH_DT_REAPRESENTACAO IN TB_COB_DCH_DEVOLUCAO_CHEQUE.COB_DCH_DT_REAPRESENTACAO%type DEFAULT NULL,
     pASS_BCT_ID IN TB_COB_DCH_DEVOLUCAO_CHEQUE.ASS_BCT_ID%type DEFAULT NULL,
     pCOB_DCH_DT_CRIACAO IN TB_COB_DCH_DEVOLUCAO_CHEQUE.COB_DCH_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_COB_DCH_DEVOLUCAO_CHEQUE.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pCOB_DCH_DT_ULTIMA_ATUALIZACAO IN TB_COB_DCH_DEVOLUCAO_CHEQUE.COB_DCH_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ATUALIZ IN TB_COB_DCH_DEVOLUCAO_CHEQUE.SEG_USU_ID_USUARIO_ATUALIZ%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_COB_DCH_DEVOLUCAO_CHEQUE_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCOB_DCH_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_DCH_ID = ' || pCOB_DCH_ID; END IF;
IF pCOB_CHQ_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_CHQ_ID = ' || pCOB_CHQ_ID; END IF;
IF pCOB_DCH_DT_DEVOLUCAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_DCH_DT_DEVOLUCAO = ' || CHR(39) || pCOB_DCH_DT_DEVOLUCAO || CHR(39); END IF;
IF pCAD_MDC_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_MDC_ID = ' || pCAD_MDC_ID; END IF;
IF pCOB_DCH_DT_REAPRESENTACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_DCH_DT_REAPRESENTACAO = ' || CHR(39) || pCOB_DCH_DT_REAPRESENTACAO || CHR(39); END IF;
IF pASS_BCT_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_BCT_ID = ' || pASS_BCT_ID; END IF;
IF pCOB_DCH_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_DCH_DT_CRIACAO = ' || CHR(39) || pCOB_DCH_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; END IF;
IF pCOB_DCH_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_DCH_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCOB_DCH_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ATUALIZ = ' || pSEG_USU_ID_USUARIO_ATUALIZ; END IF;
 
   V_SELECT := '
SELECT	
       COB_DCH_ID,
       COB_CHQ_ID,
       COB_DCH_DT_DEVOLUCAO,
       CAD_MDC_ID,
       COB_DCH_DT_REAPRESENTACAO,
       ASS_BCT_ID,
       COB_DCH_DT_CRIACAO,
       SEG_USU_ID_USUARIO_CRIACAO,
       COB_DCH_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO_ATUALIZ
FROM TB_COB_DCH_DEVOLUCAO_CHEQUE
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_COB_DCH_DEVOLUCAO_CHEQUE_L;
/
