--PRC_COB_CHQ_CHEQUE_L
create or replace procedure PRC_COB_CHQ_CHEQUE_L 
(
     pCOB_CHQ_ID IN TB_COB_CHQ_CHEQUE.COB_CHQ_ID%type DEFAULT NULL,
     pCAD_BAN_ID IN TB_COB_CHQ_CHEQUE.CAD_BAN_ID%type DEFAULT NULL,
     pCOB_CHQ_CD_AGENCIA IN TB_COB_CHQ_CHEQUE.COB_CHQ_CD_AGENCIA%type DEFAULT NULL,
     pCOB_CHQ_DV_AGENCIA IN TB_COB_CHQ_CHEQUE.COB_CHQ_DV_AGENCIA%type DEFAULT NULL,
     pCOB_CHQ_NR_CONTA IN TB_COB_CHQ_CHEQUE.COB_CHQ_NR_CONTA%type DEFAULT NULL,
     pCOB_CHQ_DV_CONTA IN TB_COB_CHQ_CHEQUE.COB_CHQ_DV_CONTA%type DEFAULT NULL,
     pCOB_CHQ_NR_CHEQUE IN TB_COB_CHQ_CHEQUE.COB_CHQ_NR_CHEQUE%type DEFAULT NULL,
     pCAD_PES_ID_PESSOA IN TB_COB_CHQ_CHEQUE.CAD_PES_ID_PESSOA%type DEFAULT NULL,
     pCOB_CHQ_DT_CHEQUE IN TB_COB_CHQ_CHEQUE.COB_CHQ_DT_CHEQUE%type DEFAULT NULL,
     pCOB_CHQ_VL_CHEQUE IN TB_COB_CHQ_CHEQUE.COB_CHQ_VL_CHEQUE%type DEFAULT NULL,
     pCOB_CHQ_DT_CRIACAO IN TB_COB_CHQ_CHEQUE.COB_CHQ_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_COB_CHQ_CHEQUE.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pCOB_CHQ_DT_ULTIMA_ATUALIZACAO IN TB_COB_CHQ_CHEQUE.COB_CHQ_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_ATUALIZ IN TB_COB_CHQ_CHEQUE.SEG_USU_ID_USUARIO_ATUALIZ%type DEFAULT NULL,
     pCOB_SCH_ID IN TB_COB_CHQ_CHEQUE.COB_SCH_ID%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_COB_CHQ_CHEQUE_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCOB_CHQ_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_CHQ_ID = ' || pCOB_CHQ_ID; END IF;
IF pCAD_BAN_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_BAN_ID = ' || pCAD_BAN_ID; END IF;
IF pCOB_CHQ_CD_AGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_CHQ_CD_AGENCIA = ' || pCOB_CHQ_CD_AGENCIA; END IF;
IF pCOB_CHQ_DV_AGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_CHQ_DV_AGENCIA = ' || pCOB_CHQ_DV_AGENCIA; END IF;
IF pCOB_CHQ_NR_CONTA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_CHQ_NR_CONTA = ' || pCOB_CHQ_NR_CONTA; END IF;
IF pCOB_CHQ_DV_CONTA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_CHQ_DV_CONTA = ' || pCOB_CHQ_DV_CONTA; END IF;
IF pCOB_CHQ_NR_CHEQUE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_CHQ_NR_CHEQUE = ' || pCOB_CHQ_NR_CHEQUE; END IF;
IF pCAD_PES_ID_PESSOA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PES_ID_PESSOA = ' || pCAD_PES_ID_PESSOA; END IF;
IF pCOB_CHQ_DT_CHEQUE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_CHQ_DT_CHEQUE = ' || CHR(39) || pCOB_CHQ_DT_CHEQUE || CHR(39); END IF;
IF pCOB_CHQ_VL_CHEQUE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_CHQ_VL_CHEQUE = ' || pCOB_CHQ_VL_CHEQUE; END IF;
IF pCOB_CHQ_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_CHQ_DT_CRIACAO = ' || CHR(39) || pCOB_CHQ_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; END IF;
IF pCOB_CHQ_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_CHQ_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCOB_CHQ_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_ATUALIZ = ' || pSEG_USU_ID_USUARIO_ATUALIZ; END IF;
IF pCOB_SCH_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND COB_SCH_ID = ' || pCOB_SCH_ID; END IF;
 
   V_SELECT := '
SELECT	
       COB_CHQ_ID,
       CAD_BAN_ID,
       COB_CHQ_CD_AGENCIA,
       COB_CHQ_DV_AGENCIA,
       COB_CHQ_NR_CONTA,
       COB_CHQ_DV_CONTA,
       COB_CHQ_NR_CHEQUE,
       CAD_PES_ID_PESSOA,
       COB_CHQ_DT_CHEQUE,
       COB_CHQ_VL_CHEQUE,
       COB_CHQ_DT_CRIACAO,
       SEG_USU_ID_USUARIO_CRIACAO,
       COB_CHQ_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO_ATUALIZ,
       COB_SCH_ID
FROM TB_COB_CHQ_CHEQUE
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_COB_CHQ_CHEQUE_L;
/
