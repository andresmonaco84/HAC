  --PRC_ASS_CPE_CONV_PROD_EQUIVALE_L
  create or replace procedure PRC_ASS_CPE_CONV_PROD_EQ_L 
  (
     pASS_CPE_ID IN TB_ASS_CPE_CONV_PROD_EQUIVALE.ASS_CPE_ID%type DEFAULT NULL,
     pCAD_CNV_ID_CONVENIO IN TB_ASS_CPE_CONV_PROD_EQUIVALE.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pCAD_LAT_ID_LOCAL_ATENDIMENTO IN TB_ASS_CPE_CONV_PROD_EQUIVALE.CAD_LAT_ID_LOCAL_ATENDIMENTO%type DEFAULT NULL,
     pCAD_PRD_ID_ORIGEM IN TB_ASS_CPE_CONV_PROD_EQUIVALE.CAD_PRD_ID_ORIGEM%type DEFAULT NULL,
     pCAD_PRD_ID_DESTINO IN TB_ASS_CPE_CONV_PROD_EQUIVALE.CAD_PRD_ID_DESTINO%type DEFAULT NULL,
     pCAD_CPE_DT_INICIO_VIGENCIA IN TB_ASS_CPE_CONV_PROD_EQUIVALE.CAD_CPE_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pCAD_CPE_DT_FIM_VIGENCIA IN TB_ASS_CPE_CONV_PROD_EQUIVALE.CAD_CPE_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pCAD_CPE_DT_ULTIMA_ATUALIZACAO IN TB_ASS_CPE_CONV_PROD_EQUIVALE.CAD_CPE_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ASS_CPE_CONV_PROD_EQUIVALE.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
  ) 
 is
/********************************************************************
*    Procedure: PRC_ASS_CPE_CONV_PROD_EQ_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pASS_CPE_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_CPE_ID = ' || pASS_CPE_ID; END IF;
IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
IF pCAD_LAT_ID_LOCAL_ATENDIMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_LAT_ID_LOCAL_ATENDIMENTO = ' || pCAD_LAT_ID_LOCAL_ATENDIMENTO; END IF;
IF pCAD_PRD_ID_ORIGEM IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_ID_ORIGEM = ' || pCAD_PRD_ID_ORIGEM; END IF;
IF pCAD_PRD_ID_DESTINO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_ID_DESTINO = ' || pCAD_PRD_ID_DESTINO; END IF;
IF pCAD_CPE_DT_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CPE_DT_INICIO_VIGENCIA = ' || CHR(39) || pCAD_CPE_DT_INICIO_VIGENCIA || CHR(39); END IF;
IF pCAD_CPE_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CPE_DT_FIM_VIGENCIA = ' || CHR(39) || pCAD_CPE_DT_FIM_VIGENCIA || CHR(39); END IF;
IF pCAD_CPE_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CPE_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_CPE_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       ASS_CPE_ID,
       CAD_CNV_ID_CONVENIO,
       CAD_LAT_ID_LOCAL_ATENDIMENTO,
       CAD_PRD_ID_ORIGEM,
       CAD_PRD_ID_DESTINO,
       CAD_CPE_DT_INICIO_VIGENCIA,
       CAD_CPE_DT_FIM_VIGENCIA,
       CAD_CPE_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO
FROM TB_ASS_CPE_CONV_PROD_EQUIVALE
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
  end PRC_ASS_CPE_CONV_PROD_EQ_L;

