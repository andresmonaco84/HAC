--PRC_CAD_TEL_TELEFONE_RMT_S
create or replace procedure PRC_CAD_TEL_TELEFONE_RMT_S 
(
     pCAD_TEL_ID_TELEFONE IN TB_CAD_TEL_TELEFONE.CAD_TEL_ID_TELEFONE%type DEFAULT NULL,
     pCAD_TEL_NR_NUM_TEL IN TB_CAD_TEL_TELEFONE.CAD_TEL_NR_NUM_TEL%type DEFAULT NULL,
     pCAD_TEL_NR_RAMAL_TEL IN TB_CAD_TEL_TELEFONE.CAD_TEL_NR_RAMAL_TEL%type DEFAULT NULL,
     pCAD_TEL_NM_CONTATO IN TB_CAD_TEL_TELEFONE.CAD_TEL_NM_CONTATO%type DEFAULT NULL,
     pCAD_TEL_DT_ULTIMA_ATUALIZACAO IN TB_CAD_TEL_TELEFONE.CAD_TEL_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_TEL_TELEFONE.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pAUX_TTE_CD_TP_TEL_END IN TB_CAD_TEL_TELEFONE.AUX_TTE_CD_TP_TEL_END%type DEFAULT NULL,
     pCAD_PES_ID_PESSOA IN TB_CAD_TEL_TELEFONE.CAD_PES_ID_PESSOA%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_TEL_TELEFONE_RMT_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_TEL_ID_TELEFONE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TEL_ID_TELEFONE = ' || pCAD_TEL_ID_TELEFONE; END IF;
IF pCAD_TEL_NR_NUM_TEL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TEL_NR_NUM_TEL = ' || CHR(39) || pCAD_TEL_NR_NUM_TEL || CHR(39); END IF;
IF pCAD_TEL_NR_RAMAL_TEL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TEL_NR_RAMAL_TEL = ' || pCAD_TEL_NR_RAMAL_TEL; END IF;
IF pCAD_TEL_NM_CONTATO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TEL_NM_CONTATO = ' || CHR(39) || pCAD_TEL_NM_CONTATO || CHR(39); END IF;
IF pCAD_TEL_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_TEL_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_TEL_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pAUX_TTE_CD_TP_TEL_END IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AUX_TTE_CD_TP_TEL_END = ' || pAUX_TTE_CD_TP_TEL_END; END IF;
IF pCAD_PES_ID_PESSOA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PES_ID_PESSOA = ' || pCAD_PES_ID_PESSOA; END IF;
 
   V_SELECT := '
SELECT	
       CAD_TEL_ID_TELEFONE,
       CAD_TEL_NR_NUM_TEL,
       CAD_TEL_NR_RAMAL_TEL,
       CAD_TEL_NM_CONTATO,
       CAD_TEL_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       AUX_TTE_CD_TP_TEL_END,
       CAD_PES_ID_PESSOA
FROM TB_CAD_TEL_TELEFONE
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_TEL_TELEFONE_RMT_S;

