--PRC_TIS_MED_TABELAMEDICA_R_L
create or replace procedure PRC_TIS_MED_TABELAMEDICA_R_L 
(
     pTIS_MED_CD_TABELAMEDICA IN TB_TIS_MED_TABELAMEDICA.TIS_MED_CD_TABELAMEDICA%type DEFAULT NULL,
     pTIS_MED_DS_TABELAMEDICA IN TB_TIS_MED_TABELAMEDICA.TIS_MED_DS_TABELAMEDICA%type DEFAULT NULL,
     pTIS_MED_DT_INICIO_VIGENCIA IN TB_TIS_MED_TABELAMEDICA.TIS_MED_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pTIS_MED_DT_FIM_VIGENCIA IN TB_TIS_MED_TABELAMEDICA.TIS_MED_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pTIS_MED_FL_STATUS IN TB_TIS_MED_TABELAMEDICA.TIS_MED_FL_STATUS%type DEFAULT NULL,
     pTIS_MED_FL_UTILIZA_LANCAMENTO IN TB_TIS_MED_TABELAMEDICA.TIS_MED_FL_UTILIZA_LANCAMENTO%type DEFAULT NULL,
     pTIS_MED_DT_ULTIMA_ATUALIZACAO IN TB_TIS_MED_TABELAMEDICA.TIS_MED_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_TIS_MED_TABELAMEDICA.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_TIS_MED_TABELAMEDICA_R_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pTIS_MED_CD_TABELAMEDICA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_MED_CD_TABELAMEDICA = ' || CHR(39) || pTIS_MED_CD_TABELAMEDICA || CHR(39); END IF;
  IF pTIS_MED_DS_TABELAMEDICA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_MED_DS_TABELAMEDICA = ' || CHR(39) || pTIS_MED_DS_TABELAMEDICA || CHR(39); END IF;
  IF pTIS_MED_DT_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_MED_DT_INICIO_VIGENCIA = ' || CHR(39) || pTIS_MED_DT_INICIO_VIGENCIA || CHR(39); END IF;
  IF pTIS_MED_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_MED_DT_FIM_VIGENCIA = ' || CHR(39) || pTIS_MED_DT_FIM_VIGENCIA || CHR(39); END IF;
  IF pTIS_MED_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_MED_FL_STATUS = ' || CHR(39) || pTIS_MED_FL_STATUS || CHR(39); END IF;
  IF pTIS_MED_FL_UTILIZA_LANCAMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_MED_FL_UTILIZA_LANCAMENTO = ' || CHR(39) || pTIS_MED_FL_UTILIZA_LANCAMENTO || CHR(39); END IF;
  IF pTIS_MED_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_MED_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pTIS_MED_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
  IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       TIS_MED_CD_TABELAMEDICA,
       TIS_MED_DS_TABELAMEDICA,
       TIS_MED_DT_INICIO_VIGENCIA,
       TIS_MED_DT_FIM_VIGENCIA,
       TIS_MED_FL_STATUS,
       TIS_MED_FL_UTILIZA_LANCAMENTO,
       TIS_MED_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       TIS_MED_FL_UTILIZA_REPASSE
FROM TB_TIS_MED_TABELAMEDICA
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_TIS_MED_TABELAMEDICA_R_L;
