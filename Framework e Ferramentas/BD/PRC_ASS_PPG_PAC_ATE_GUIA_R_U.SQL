

--PRC_ASS_PPG_PAC_ATE_GUIA_R_U
create or replace procedure PRC_ASS_PPG_PAC_ATE_GUIA_R_U
(
     pSEG_USU_ID_USUARIO IN TB_ASS_PPG_PAC_ATE_PROC_GUIA.SEG_USU_ID_USUARIO%type,
     pASS_PPG_DT_ULTIMA_ATUALIZACAO IN TB_ASS_PPG_PAC_ATE_PROC_GUIA.ASS_PPG_DT_ULTIMA_ATUALIZACAO%type,
     pATE_GUI_CD_CODIGO IN TB_ASS_PPG_PAC_ATE_PROC_GUIA.ATE_GUI_CD_CODIGO%type,
     pATE_GUI_DT_VALIDADE IN TB_ASS_PPG_PAC_ATE_PROC_GUIA.ATE_GUI_DT_VALIDADE%type,
     pATD_ATE_ID IN TB_ASS_PPG_PAC_ATE_PROC_GUIA.ATD_ATE_ID%type,
     pCAD_PRD_ID IN TB_ASS_PPG_PAC_ATE_PROC_GUIA.CAD_PRD_ID%type,
     pCAD_PAC_ID_PACIENTE IN TB_ASS_PPG_PAC_ATE_PROC_GUIA.CAD_PAC_ID_PACIENTE%type,
     pASS_PPG_CD_SENHA IN TB_ASS_PPG_PAC_ATE_PROC_GUIA.ASS_PPG_CD_SENHA%type default NULL,
     pASS_PPG_CD_SENHA_AUTORIZADOR IN TB_ASS_PPG_PAC_ATE_PROC_GUIA.ASS_PPG_CD_SENHA_AUTORIZADOR%type default NULL,
     pASS_PPG_DT_VALIDADE_SENHA IN TB_ASS_PPG_PAC_ATE_PROC_GUIA.ASS_PPG_DT_VALIDADE_SENHA%type default NULL,
     pASS_PAP_ID IN TB_ASS_PPG_PAC_ATE_PROC_GUIA.ASS_PAP_ID%type,
     pASS_PPG_DT_EMISSAOGUIA IN TB_ASS_PPG_PAC_ATE_PROC_GUIA.ASS_PPG_DT_EMISSAOGUIA%type default NULL,
     pASS_PPG_DT_AUTORIZGUIA IN TB_ASS_PPG_PAC_ATE_PROC_GUIA.ASS_PPG_DT_AUTORIZGUIA%type default NULL
) 
is
/********************************************************************
*    Procedure: PRC_ASS_PPG_PAC_ATE_GUIA_R_U
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/  
begin
UPDATE TB_ASS_PPG_PAC_ATE_PROC_GUIA
SET	   
        SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO,
        ASS_PPG_DT_ULTIMA_ATUALIZACAO = pASS_PPG_DT_ULTIMA_ATUALIZACAO,
        ASS_PPG_CD_SENHA = pASS_PPG_CD_SENHA,
        ASS_PPG_CD_SENHA_AUTORIZADOR = pASS_PPG_CD_SENHA_AUTORIZADOR,
        ASS_PPG_DT_VALIDADE_SENHA = pASS_PPG_DT_VALIDADE_SENHA,
        ASS_PPG_DT_EMISSAOGUIA = pASS_PPG_DT_EMISSAOGUIA,
        ASS_PPG_DT_AUTORIZGUIA = pASS_PPG_DT_AUTORIZGUIA 
WHERE
        ASS_PAP_ID = pASS_PAP_ID
    AND ATD_ATE_ID = pATD_ATE_ID
    AND ATE_GUI_CD_CODIGO = pATE_GUI_CD_CODIGO
    AND ATE_GUI_DT_VALIDADE = pATE_GUI_DT_VALIDADE
    AND CAD_PAC_ID_PACIENTE = pCAD_PAC_ID_PACIENTE
    AND CAD_PRD_ID = pCAD_PRD_ID;	
end PRC_ASS_PPG_PAC_ATE_GUIA_R_U;

