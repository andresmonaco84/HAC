--PRC_ATD_PCI_PROCED_CIRURGIA_L
create or replace procedure PRC_ATD_PCI_PROCED_CIRURGIA_L 
(
     pATD_PCI_ID IN TB_ATD_PCI_PROCED_CIRURGIA.ATD_PCI_ID%type DEFAULT NULL,
     pATD_CIR_ID IN TB_ATD_PCI_PROCED_CIRURGIA.ATD_CIR_ID%type DEFAULT NULL,
     pTIS_MED_CD_TABELAMEDICA IN TB_ATD_PCI_PROCED_CIRURGIA.TIS_MED_CD_TABELAMEDICA%type DEFAULT NULL,
     pAUX_EPP_CD_ESPECPROC IN TB_ATD_PCI_PROCED_CIRURGIA.AUX_EPP_CD_ESPECPROC%type DEFAULT NULL,
     pAUX_GPC_CD_GRUPOPROC IN TB_ATD_PCI_PROCED_CIRURGIA.AUX_GPC_CD_GRUPOPROC%type DEFAULT NULL,
     pCAD_PRD_ID IN TB_ATD_PCI_PROCED_CIRURGIA.CAD_PRD_ID%type DEFAULT NULL,
     pSEG_USU_ID_USU_CRIACAO IN TB_ATD_PCI_PROCED_CIRURGIA.SEG_USU_ID_USU_CRIACAO%type DEFAULT NULL,
     pATD_PCI_DT_CRIACAO IN TB_ATD_PCI_PROCED_CIRURGIA.ATD_PCI_DT_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USU_ULT_ATUALIZ IN TB_ATD_PCI_PROCED_CIRURGIA.SEG_USU_ID_USU_ULT_ATUALIZ%type DEFAULT NULL,
     pATD_PCI_DT_ULT_ATUALIZ IN TB_ATD_PCI_PROCED_CIRURGIA.ATD_PCI_DT_ULT_ATUALIZ%type DEFAULT NULL,
     pCAD_PRD_TP_PORTE IN TB_ATD_PCI_PROCED_CIRURGIA.CAD_PRD_TP_PORTE%type DEFAULT NULL,
     pATD_PCI_FL_SITUACAO IN TB_ATD_PCI_PROCED_CIRURGIA.ATD_PCI_FL_SITUACAO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ATD_PCI_PROCED_CIRURGIA_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pATD_PCI_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PCI_ID = ' || pATD_PCI_ID; END IF;
IF pATD_CIR_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_CIR_ID = ' || pATD_CIR_ID; END IF;
IF pTIS_MED_CD_TABELAMEDICA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_MED_CD_TABELAMEDICA = ' || CHR(39) || pTIS_MED_CD_TABELAMEDICA || CHR(39); END IF;
IF pAUX_EPP_CD_ESPECPROC IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AUX_EPP_CD_ESPECPROC = ' || CHR(39) || pAUX_EPP_CD_ESPECPROC || CHR(39); END IF;
IF pAUX_GPC_CD_GRUPOPROC IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AUX_GPC_CD_GRUPOPROC = ' || CHR(39) || pAUX_GPC_CD_GRUPOPROC || CHR(39); END IF;
IF pCAD_PRD_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_ID = ' || pCAD_PRD_ID; END IF;
IF pSEG_USU_ID_USU_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USU_CRIACAO = ' || pSEG_USU_ID_USU_CRIACAO; END IF;
IF pATD_PCI_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PCI_DT_CRIACAO = ' || CHR(39) || pATD_PCI_DT_CRIACAO || CHR(39); END IF;
IF pSEG_USU_ID_USU_ULT_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USU_ULT_ATUALIZ = ' || pSEG_USU_ID_USU_ULT_ATUALIZ; END IF;
IF pATD_PCI_DT_ULT_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PCI_DT_ULT_ATUALIZ = ' || CHR(39) || pATD_PCI_DT_ULT_ATUALIZ || CHR(39); END IF;
IF pCAD_PRD_TP_PORTE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_TP_PORTE = ' || pCAD_PRD_TP_PORTE; END IF;
IF pATD_PCI_FL_SITUACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PCI_FL_SITUACAO = ' || CHR(39) || pATD_PCI_FL_SITUACAO || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       ATD_PCI_ID,
       ATD_CIR_ID,
       TIS_MED_CD_TABELAMEDICA,
       AUX_EPP_CD_ESPECPROC,
       AUX_GPC_CD_GRUPOPROC,
       CAD_PRD_ID,
       SEG_USU_ID_USU_CRIACAO,
       ATD_PCI_DT_CRIACAO,
       SEG_USU_ID_USU_ULT_ATUALIZ,
       ATD_PCI_DT_ULT_ATUALIZ,
       CAD_PRD_TP_PORTE,
       ATD_PCI_FL_SITUACAO
FROM TB_ATD_PCI_PROCED_CIRURGIA
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ATD_PCI_PROCED_CIRURGIA_L;
