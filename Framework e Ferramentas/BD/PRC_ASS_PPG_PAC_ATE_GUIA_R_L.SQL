        (pASS_PPG_DT_VALIDADE_SENHA is null OR ASS_PPG_DT_VALIDADE_SENHA = pASS_PPG_DT_VALIDADE_SENHA) AND 
        (pASS_PAP_ID is null OR ASS_PAP_ID = pASS_PAP_ID) AND 
        (pASS_PPG_DT_EMISSAOGUIA is null OR ASS_PPG_DT_EMISSAOGUIA = pASS_PPG_DT_EMISSAOGUIA) AND 
