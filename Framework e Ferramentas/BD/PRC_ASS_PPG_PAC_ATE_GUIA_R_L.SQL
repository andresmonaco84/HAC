v_cursor PKG_CURSOR.t_cursor;
begin
OPEN v_cursor FOR
SELECT	
       SEG_USU_ID_USUARIO,
       ASS_PPG_DT_ULTIMA_ATUALIZACAO,
       ATE_GUI_CD_CODIGO,
       ATE_GUI_DT_VALIDADE,
       ATD_ATE_ID,
