CREATE OR REPLACE PROCEDURE PRC_ASS_PCB_PROF_CBOS_RMT_U
(
     pASS_PCB_DS_OBS IN TB_ASS_PCB_PROFISSIONAL_CBOS.ASS_PCB_DS_OBS%type default NULL,
     pASS_PCB_FL_STATUS IN TB_ASS_PCB_PROFISSIONAL_CBOS.ASS_PCB_FL_STATUS%type default NULL,
     pTIS_CBO_CD_CBOS IN TB_ASS_PCB_PROFISSIONAL_CBOS.TIS_CBO_CD_CBOS%type,
     pCAD_PRO_ID_PROFISSIONAL IN TB_ASS_PCB_PROFISSIONAL_CBOS.CAD_PRO_ID_PROFISSIONAL%type,
     pSEG_USU_ID_USUARIO IN TB_ASS_PCB_PROFISSIONAL_CBOS.SEG_USU_ID_USUARIO%type,
     pASS_PCB_DT_ULTIMA_ATUALIZACAO IN TB_ASS_PCB_PROFISSIONAL_CBOS.ASS_PCB_DT_ULTIMA_ATUALIZACAO%type,
     pASS_PCB_FL_ESPECIALISTA IN TB_ASS_PCB_PROFISSIONAL_CBOS.ASS_PCB_FL_ESPECIALISTA%type default NULL,
     pCAD_CFM_ID_CFM IN TB_ASS_PCB_PROFISSIONAL_CBOS.CAD_CFM_ID_CFM%type default NULL
)
is
/********************************************************************
*    Procedure: PRC_ASS_PCB_PROF_CBOS_RMT_U
*
*    Data Criacao:  15/07/2010   Por: André Souza Monaco
*    Data Alteracao:   Por:
*
*    Funcao: Atualiza especialidade de profissional
*
*******************************************************************/
begin
UPDATE TB_ASS_PCB_PROFISSIONAL_CBOS
SET
        ASS_PCB_DS_OBS = pASS_PCB_DS_OBS,
        ASS_PCB_FL_STATUS = pASS_PCB_FL_STATUS,
        SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO,
        ASS_PCB_DT_ULTIMA_ATUALIZACAO = pASS_PCB_DT_ULTIMA_ATUALIZACAO,
        ASS_PCB_FL_ESPECIALISTA = pASS_PCB_FL_ESPECIALISTA,
        CAD_CFM_ID_CFM = pCAD_CFM_ID_CFM
WHERE
        CAD_PRO_ID_PROFISSIONAL = pCAD_PRO_ID_PROFISSIONAL
    AND TIS_CBO_CD_CBOS = pTIS_CBO_CD_CBOS;
end PRC_ASS_PCB_PROF_CBOS_RMT_U;

 
