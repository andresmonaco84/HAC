--PRC_CAD_DAC_DIARIA_AUTOM_CNV_L
create or replace procedure PRC_CAD_DAC_DIARIA_AUTOM_CNV_L 
(
     pCAD_DAC_ID IN TB_CAD_DAC_DIARIA_AUTOM_CNV.CAD_DAC_ID%type DEFAULT NULL,
     pCAD_CNV_ID_CONVENIO IN TB_CAD_DAC_DIARIA_AUTOM_CNV.CAD_CNV_ID_CONVENIO%type DEFAULT NULL,
     pCAD_PLA_ID_PLANO IN TB_CAD_DAC_DIARIA_AUTOM_CNV.CAD_PLA_ID_PLANO%type DEFAULT NULL,
     pTIS_TAC_CD_TIPO_ACOMODACAO IN TB_CAD_DAC_DIARIA_AUTOM_CNV.TIS_TAC_CD_TIPO_ACOMODACAO%type DEFAULT NULL,
     pCAD_PRD_ID IN TB_CAD_DAC_DIARIA_AUTOM_CNV.CAD_PRD_ID%type DEFAULT NULL,
     pCAD_SPL_ID IN TB_CAD_DAC_DIARIA_AUTOM_CNV.CAD_SPL_ID%type DEFAULT NULL,
     pCAD_SET_ID IN TB_CAD_DAC_DIARIA_AUTOM_CNV.CAD_SET_ID%type DEFAULT NULL,
     pCAD_DAC_DT_INICIO_VIGENCIA IN TB_CAD_DAC_DIARIA_AUTOM_CNV.CAD_DAC_DT_INICIO_VIGENCIA%type DEFAULT NULL,
     pCAD_DAC_DT_FIM_VIGENCIA IN TB_CAD_DAC_DIARIA_AUTOM_CNV.CAD_DAC_DT_FIM_VIGENCIA%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_CAD_DAC_DIARIA_AUTOM_CNV.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_DAC_DT_ULTIMA_ATUALIZACAO IN TB_CAD_DAC_DIARIA_AUTOM_CNV.CAD_DAC_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pCAD_DAC_FL_CONSIDERAOBITO IN TB_CAD_DAC_DIARIA_AUTOM_CNV.CAD_DAC_FL_CONSIDERAOBITO%type DEFAULT NULL,
     pCAD_DAC_HR_OBITO_MAIOR IN TB_CAD_DAC_DIARIA_AUTOM_CNV.CAD_DAC_HR_OBITO_MAIOR%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_CAD_DAC_DIARIA_AUTOM_CNV_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_DAC_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_DAC_ID = ' || pCAD_DAC_ID; END IF;
IF pCAD_CNV_ID_CONVENIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_CNV_ID_CONVENIO = ' || pCAD_CNV_ID_CONVENIO; END IF;
IF pCAD_PLA_ID_PLANO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PLA_ID_PLANO = ' || pCAD_PLA_ID_PLANO; END IF;
IF pTIS_TAC_CD_TIPO_ACOMODACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND TIS_TAC_CD_TIPO_ACOMODACAO = ' || CHR(39) || pTIS_TAC_CD_TIPO_ACOMODACAO || CHR(39); END IF;
IF pCAD_PRD_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_ID = ' || pCAD_PRD_ID; END IF;
IF pCAD_SPL_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_SPL_ID = ' || pCAD_SPL_ID; END IF;
IF pCAD_SET_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_SET_ID = ' || pCAD_SET_ID; END IF;
IF pCAD_DAC_DT_INICIO_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_DAC_DT_INICIO_VIGENCIA = ' || CHR(39) || pCAD_DAC_DT_INICIO_VIGENCIA || CHR(39); END IF;
IF pCAD_DAC_DT_FIM_VIGENCIA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_DAC_DT_FIM_VIGENCIA = ' || CHR(39) || pCAD_DAC_DT_FIM_VIGENCIA || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pCAD_DAC_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_DAC_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pCAD_DAC_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pCAD_DAC_FL_CONSIDERAOBITO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_DAC_FL_CONSIDERAOBITO = ' || CHR(39) || pCAD_DAC_FL_CONSIDERAOBITO || CHR(39); END IF;
IF pCAD_DAC_HR_OBITO_MAIOR IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_DAC_HR_OBITO_MAIOR = ' || pCAD_DAC_HR_OBITO_MAIOR; END IF;
 
   V_SELECT := '
SELECT	
       CAD_DAC_ID,
       CAD_CNV_ID_CONVENIO,
       CAD_PLA_ID_PLANO,
       TIS_TAC_CD_TIPO_ACOMODACAO,
       CAD_PRD_ID,
       CAD_SPL_ID,
       CAD_SET_ID,
       CAD_DAC_DT_INICIO_VIGENCIA,
       CAD_DAC_DT_FIM_VIGENCIA,
       SEG_USU_ID_USUARIO,
       CAD_DAC_DT_ULTIMA_ATUALIZACAO,
       CAD_DAC_FL_CONSIDERAOBITO,
       CAD_DAC_HR_OBITO_MAIOR
FROM TB_CAD_DAC_DIARIA_AUTOM_CNV
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_CAD_DAC_DIARIA_AUTOM_CNV_L;
