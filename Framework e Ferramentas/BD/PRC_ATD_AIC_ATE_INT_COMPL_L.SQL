create or replace procedure PRC_ATD_AIC_ATE_INT_COMPL_L
(
     pATD_ATE_ID IN TB_ATD_AIC_ATE_INT_COMPL.ATD_ATE_ID%type DEFAULT NULL,
     pTIS_TIN_CD_INTERNACAO IN TB_ATD_AIC_ATE_INT_COMPL.TIS_TIN_CD_INTERNACAO%type DEFAULT NULL,
     pCAD_PRO_ID_PROF_ADM IN TB_ATD_AIC_ATE_INT_COMPL.CAD_PRO_ID_PROF_ADM%type DEFAULT NULL,
     pATD_AIC_DS_CONTATO IN TB_ATD_AIC_ATE_INT_COMPL.ATD_AIC_DS_CONTATO%type DEFAULT NULL,
     pATD_AIC_DS_PROCEDIMENTO IN TB_ATD_AIC_ATE_INT_COMPL.ATD_AIC_DS_PROCEDIMENTO%type DEFAULT NULL,
     pTIS_TRI_CD_REGINTENNACAO IN TB_ATD_AIC_ATE_INT_COMPL.TIS_TRI_CD_REGINTENNACAO%type DEFAULT NULL,
     pATD_AIC_QT_DIARIA_SOLICITADA IN TB_ATD_AIC_ATE_INT_COMPL.ATD_AIC_QT_DIARIA_SOLICITADA%type DEFAULT NULL,
     pATD_AIC_DS_EMPRESA IN TB_ATD_AIC_ATE_INT_COMPL.ATD_AIC_DS_EMPRESA%type DEFAULT NULL,
     pATD_AIC_DT_ULTIMA_ATUALIZACAO IN TB_ATD_AIC_ATE_INT_COMPL.ATD_AIC_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_ATD_AIC_ATE_INT_COMPL.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pATD_AIC_TP_SITUACAO_PAC IN TB_ATD_AIC_ATE_INT_COMPL.ATD_AIC_TP_SITUACAO_PAC%type DEFAULT NULL,
     pCAD_PRD_ID IN TB_ATD_AIC_ATE_INT_COMPL.CAD_PRD_ID%type DEFAULT NULL,
     pATD_AIC_FL_PAC_ESTADOGRAVE IN TB_ATD_AIC_ATE_INT_COMPL.ATD_AIC_FL_PAC_ESTADOGRAVE%type DEFAULT NULL,
          pATD_ATE_ID_ORIGEM IN TB_ATD_AIC_ATE_INT_COMPL.ATD_ATE_ID%type default NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
)
is
/********************************************************************
*    Procedure: PRC_ATD_AIC_ATE_INT_COMPL_L
*
*    Data Criacao:  data da  criação   Por: Nome do Analista
*    Data Alteracao: data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
v_cursor PKG_CURSOR.t_cursor;
begin
OPEN v_cursor FOR
SELECT
       ATD_ATE_ID,
       TIS_TIN_CD_INTERNACAO,
       CAD_PRO_ID_PROF_ADM,
       ATD_AIC_DS_CONTATO,
       ATD_AIC_DS_PROCEDIMENTO,
       TIS_TRI_CD_REGINTENNACAO,
       ATD_AIC_QT_DIARIA_SOLICITADA,
       ATD_AIC_DS_EMPRESA,
       ATD_AIC_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       ATD_AIC_TP_SITUACAO_PAC,
       CAD_PRD_ID,
       ATD_AIC_FL_PAC_ESTADOGRAVE,
       ATD_ATE_ID_ORIGEM
       
FROM TB_ATD_AIC_ATE_INT_COMPL
WHERE
        (pATD_ATE_ID is null OR ATD_ATE_ID = pATD_ATE_ID) AND
        (pTIS_TIN_CD_INTERNACAO is null OR TIS_TIN_CD_INTERNACAO = pTIS_TIN_CD_INTERNACAO) AND
        (pCAD_PRO_ID_PROF_ADM is null OR CAD_PRO_ID_PROF_ADM = pCAD_PRO_ID_PROF_ADM) AND
        (pATD_AIC_DS_CONTATO is null OR ATD_AIC_DS_CONTATO = pATD_AIC_DS_CONTATO) AND
        (pATD_AIC_DS_PROCEDIMENTO is null OR ATD_AIC_DS_PROCEDIMENTO = pATD_AIC_DS_PROCEDIMENTO) AND
        (pTIS_TRI_CD_REGINTENNACAO is null OR TIS_TRI_CD_REGINTENNACAO = pTIS_TRI_CD_REGINTENNACAO) AND
        (pATD_AIC_QT_DIARIA_SOLICITADA is null OR ATD_AIC_QT_DIARIA_SOLICITADA = pATD_AIC_QT_DIARIA_SOLICITADA) AND
        (pATD_AIC_DS_EMPRESA is null OR ATD_AIC_DS_EMPRESA = pATD_AIC_DS_EMPRESA) AND
        (pATD_AIC_DT_ULTIMA_ATUALIZACAO is null OR ATD_AIC_DT_ULTIMA_ATUALIZACAO = pATD_AIC_DT_ULTIMA_ATUALIZACAO) AND
        (pSEG_USU_ID_USUARIO is null OR SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO) AND
        (pATD_AIC_TP_SITUACAO_PAC is null OR ATD_AIC_TP_SITUACAO_PAC = pATD_AIC_TP_SITUACAO_PAC) AND
        (pCAD_PRD_ID is null OR CAD_PRD_ID = pCAD_PRD_ID) AND
        (pATD_AIC_FL_PAC_ESTADOGRAVE is null OR ATD_AIC_FL_PAC_ESTADOGRAVE = pATD_AIC_FL_PAC_ESTADOGRAVE) AND 
        (pATD_ATE_ID_ORIGEM IS NULL OR ATD_ATE_ID_ORIGEM = pATD_ATE_ID_ORIGEM)
        ;
io_cursor := v_cursor;
end PRC_ATD_AIC_ATE_INT_COMPL_L;
 