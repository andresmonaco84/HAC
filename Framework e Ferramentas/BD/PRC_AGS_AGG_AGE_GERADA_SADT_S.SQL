is
/********************************************************************
*    Procedure: PRC_AGS_AGG_AGE_GERADA_SADT_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pAGS_AGG_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AGS_AGG_ID = ' || pAGS_AGG_ID; END IF;
IF pAGS_ESM_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AGS_ESM_ID = ' || pAGS_ESM_ID; END IF;
IF pAGS_ESF_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AGS_ESF_ID = ' || pAGS_ESF_ID; END IF;
IF pAGS_AGG_DT_AGENDA_GERADA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AGS_AGG_DT_AGENDA_GERADA = ' || CHR(39) || pAGS_AGG_DT_AGENDA_GERADA || CHR(39); END IF;
IF pAGS_AGG_HR_AGENDA_GERADA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AGS_AGG_HR_AGENDA_GERADA = ' || pAGS_AGG_HR_AGENDA_GERADA; END IF;
IF pAGS_AGG_TP_HORARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AGS_AGG_TP_HORARIO = ' || CHR(39) || pAGS_AGG_TP_HORARIO || CHR(39); END IF;
IF pAGS_AGG_FL_STATUS_HORARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AGS_AGG_FL_STATUS_HORARIO = ' || CHR(39) || pAGS_AGG_FL_STATUS_HORARIO || CHR(39); END IF;
IF pAGS_AGG_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND AGS_AGG_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pAGS_AGG_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pASS_EHR_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_EHR_ID = ' || pASS_EHR_ID; END IF;
 
   V_SELECT := '
SELECT	
       AGS_AGG_ID,
       AGS_ESM_ID,
       AGS_ESF_ID,
       AGS_AGG_DT_AGENDA_GERADA,
       AGS_AGG_HR_AGENDA_GERADA,
       AGS_AGG_TP_HORARIO,
       AGS_AGG_FL_STATUS_HORARIO,
       AGS_AGG_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       ASS_EHR_ID,
	   AGS_AGG_DS_BLOQUEIO
FROM TB_AGS_AGG_AGE_GERADA_SADT
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_AGS_AGG_AGE_GERADA_SADT_S;
