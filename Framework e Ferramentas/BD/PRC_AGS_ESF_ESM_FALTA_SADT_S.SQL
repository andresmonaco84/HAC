 
  --PRC_AGS_ESF_ESCALA_FALTA_SADT_S
  create or replace procedure PRC_AGS_ESF_ESM_FALTA_SADT_S 
  (
     pAGS_ESF_ID IN TB_AGS_ESF_ESCALA_FALTA_SADT.AGS_ESF_ID%type DEFAULT NULL,
     pAGS_ESF_DT_INI_FALTA IN TB_AGS_ESF_ESCALA_FALTA_SADT.AGS_ESF_DT_INI_FALTA%type DEFAULT NULL,
     pAGS_ESF_DT_FIM_FALTA IN TB_AGS_ESF_ESCALA_FALTA_SADT.AGS_ESF_DT_FIM_FALTA%type DEFAULT NULL,
     pAGS_ESF_DS_OBSERVACAO IN TB_AGS_ESF_ESCALA_FALTA_SADT.AGS_ESF_DS_OBSERVACAO%type DEFAULT NULL,
     pAGS_ESF_FL_SUBSTITUTO_OK IN TB_AGS_ESF_ESCALA_FALTA_SADT.AGS_ESF_FL_SUBSTITUTO_OK%type DEFAULT NULL,
     pCAD_PRO_ID_PROFISSIONAL_SUBST IN TB_AGS_ESF_ESCALA_FALTA_SADT.CAD_PRO_ID_PROFISSIONAL_SUBST%type DEFAULT NULL,
     pAGS_ESM_ID IN TB_AGS_ESF_ESCALA_FALTA_SADT.AGS_ESM_ID%type DEFAULT NULL,
     pAGS_ESF_DT_ULTIMA_ATUALIZACAO IN TB_AGS_ESF_ESCALA_FALTA_SADT.AGS_ESF_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_AGS_ESF_ESCALA_FALTA_SADT.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
  ) 
  is
  /********************************************************************
  *    Procedure: PRC_AGS_ESF_ESCALA_FALTA_SADT_S
  * 
  *    Data Criacao: 	data da  criação   Por: Nome do Analista
  *    Data Alteracao:	data da alteração  Por: Nome do Analista
  *
  *    Funcao: Descrição da funcionalidade da Stored Procedure
  *
  *******************************************************************/
  v_cursor PKG_CURSOR.t_cursor;
  begin
    OPEN v_cursor FOR
    SELECT	
       AGS_ESF_ID,
       AGS_ESF_DT_INI_FALTA,
       AGS_ESF_DT_FIM_FALTA,
       AGS_ESF_DS_OBSERVACAO,
       AGS_ESF_FL_SUBSTITUTO_OK,
       CAD_PRO_ID_PROFISSIONAL_SUBST,
       AGS_ESM_ID,
       AGS_ESF_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO
    FROM TB_AGS_ESF_ESCALA_FALTA_SADT
    WHERE
        (pAGS_ESF_ID is null OR AGS_ESF_ID = pAGS_ESF_ID) AND 
        (pAGS_ESF_DT_INI_FALTA is null OR AGS_ESF_DT_INI_FALTA = pAGS_ESF_DT_INI_FALTA) AND 
        (pAGS_ESF_DT_FIM_FALTA is null OR AGS_ESF_DT_FIM_FALTA = pAGS_ESF_DT_FIM_FALTA) AND 
        (pAGS_ESF_DS_OBSERVACAO is null OR AGS_ESF_DS_OBSERVACAO = pAGS_ESF_DS_OBSERVACAO) AND 
        (pAGS_ESF_FL_SUBSTITUTO_OK is null OR AGS_ESF_FL_SUBSTITUTO_OK = pAGS_ESF_FL_SUBSTITUTO_OK) AND 
        (pCAD_PRO_ID_PROFISSIONAL_SUBST is null OR CAD_PRO_ID_PROFISSIONAL_SUBST = pCAD_PRO_ID_PROFISSIONAL_SUBST) AND 
        (pAGS_ESM_ID is null OR AGS_ESM_ID = pAGS_ESM_ID) AND 
        (pAGS_ESF_DT_ULTIMA_ATUALIZACAO is null OR AGS_ESF_DT_ULTIMA_ATUALIZACAO = pAGS_ESF_DT_ULTIMA_ATUALIZACAO) AND 
        (pSEG_USU_ID_USUARIO is null OR SEG_USU_ID_USUARIO = pSEG_USU_ID_USUARIO);          
    io_cursor := v_cursor;
  end PRC_AGS_ESF_ESM_FALTA_SADT_S;
