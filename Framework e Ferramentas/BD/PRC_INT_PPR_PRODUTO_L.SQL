 
--PRC_INT_PPR_PRODUTO_L
create or replace procedure PRC_INT_PPR_PRODUTO_L 
(
     pINT_PPR_ID IN TB_INT_PPR_PRODUTO.INT_PPR_ID%type DEFAULT NULL,
     pINT_PPR_DS_DESCRICAO IN TB_INT_PPR_PRODUTO.INT_PPR_DS_DESCRICAO%type DEFAULT NULL,
     pINT_PPR_CD_COR IN TB_INT_PPR_PRODUTO.INT_PPR_CD_COR%type DEFAULT NULL,
     pINT_PPR_DS_PRINCIPIOATIVO IN TB_INT_PPR_PRODUTO.INT_PPR_DS_PRINCIPIOATIVO%type DEFAULT NULL,
     pINT_PPR_DS_IRRITANTE IN TB_INT_PPR_PRODUTO.INT_PPR_DS_IRRITANTE%type DEFAULT NULL,
     pINT_PPR_DS_PH IN TB_INT_PPR_PRODUTO.INT_PPR_DS_PH%type DEFAULT NULL,
     pINT_PPR_DS_REFBIB IN TB_INT_PPR_PRODUTO.INT_PPR_DS_REFBIB%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_INT_PPR_PRODUTO_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pINT_PPR_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND INT_PPR_ID = ' || pINT_PPR_ID; END IF;
IF pINT_PPR_DS_DESCRICAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND INT_PPR_DS_DESCRICAO = ' || CHR(39) || pINT_PPR_DS_DESCRICAO || CHR(39); END IF;
IF pINT_PPR_CD_COR IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND INT_PPR_CD_COR = ' || CHR(39) || pINT_PPR_CD_COR || CHR(39); END IF;
IF pINT_PPR_DS_PRINCIPIOATIVO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND INT_PPR_DS_PRINCIPIOATIVO = ' || CHR(39) || pINT_PPR_DS_PRINCIPIOATIVO || CHR(39); END IF;
IF pINT_PPR_DS_IRRITANTE IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND INT_PPR_DS_IRRITANTE = ' || CHR(39) || pINT_PPR_DS_IRRITANTE || CHR(39); END IF;
IF pINT_PPR_DS_PH IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND INT_PPR_DS_PH = ' || CHR(39) || pINT_PPR_DS_PH || CHR(39); END IF;
IF pINT_PPR_DS_REFBIB IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND INT_PPR_DS_REFBIB = ' || CHR(39) || pINT_PPR_DS_REFBIB || CHR(39); END IF;
 
   V_SELECT := '
SELECT	
       INT_PPR_ID,
       INT_PPR_DS_DESCRICAO,
       INT_PPR_CD_COR,
       INT_PPR_DS_PRINCIPIOATIVO,
       INT_PPR_DS_IRRITANTE,
       INT_PPR_DS_PH,
       INT_PPR_DS_REFBIB
FROM TB_INT_PPR_PRODUTO
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_INT_PPR_PRODUTO_L;
