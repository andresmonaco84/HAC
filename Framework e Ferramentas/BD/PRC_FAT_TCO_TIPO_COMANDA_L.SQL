 
--PRC_FAT_TCO_TIPO_COMANDA_L
create or replace procedure PRC_FAT_TCO_TIPO_COMANDA_L 
(
     pFAT_TCO_ID IN TB_FAT_TCO_TIPO_COMANDA.FAT_TCO_ID%type DEFAULT NULL,
     pFAT_TCO_DS_COMANDA IN TB_FAT_TCO_TIPO_COMANDA.FAT_TCO_DS_COMANDA%type DEFAULT NULL,
     pFAT_TCO_FL_STATUS IN TB_FAT_TCO_TIPO_COMANDA.FAT_TCO_FL_STATUS%type DEFAULT NULL,
     pFAT_TCO_FL_VERIFICA_ITENS IN TB_FAT_TCO_TIPO_COMANDA.FAT_TCO_FL_VERIFICA_ITENS%type DEFAULT NULL,
     pFAT_TCO_QT_MAX_LANCAMENTO IN TB_FAT_TCO_TIPO_COMANDA.FAT_TCO_QT_MAX_LANCAMENTO%type DEFAULT NULL,
     pFAT_TCO_DT_ULTIMA_ATUALIZACAO IN TB_FAT_TCO_TIPO_COMANDA.FAT_TCO_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO IN TB_FAT_TCO_TIPO_COMANDA.SEG_USU_ID_USUARIO%type DEFAULT NULL,
     pCAD_PRO_ID_PROFISSIONAL IN TB_FAT_TCO_TIPO_COMANDA.CAD_PRO_ID_PROFISSIONAL%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_FAT_TCO_TIPO_COMANDA_L
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pFAT_TCO_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_TCO_ID = ' || pFAT_TCO_ID; END IF;
IF pFAT_TCO_DS_COMANDA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_TCO_DS_COMANDA = ' || CHR(39) || pFAT_TCO_DS_COMANDA || CHR(39); END IF;
IF pFAT_TCO_FL_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_TCO_FL_STATUS = ' || CHR(39) || pFAT_TCO_FL_STATUS || CHR(39); END IF;
IF pFAT_TCO_FL_VERIFICA_ITENS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_TCO_FL_VERIFICA_ITENS = ' || CHR(39) || pFAT_TCO_FL_VERIFICA_ITENS || CHR(39); END IF;
IF pFAT_TCO_QT_MAX_LANCAMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_TCO_QT_MAX_LANCAMENTO = ' || pFAT_TCO_QT_MAX_LANCAMENTO; END IF;
IF pFAT_TCO_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND FAT_TCO_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pFAT_TCO_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO = ' || pSEG_USU_ID_USUARIO; END IF;
IF pCAD_PRO_ID_PROFISSIONAL IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRO_ID_PROFISSIONAL = ' || pCAD_PRO_ID_PROFISSIONAL; END IF;
 
   V_SELECT := '
SELECT	
       FAT_TCO_ID,
       FAT_TCO_DS_COMANDA,
       FAT_TCO_FL_STATUS,
       FAT_TCO_FL_VERIFICA_ITENS,
       FAT_TCO_QT_MAX_LANCAMENTO,
       FAT_TCO_DT_ULTIMA_ATUALIZACAO,
       SEG_USU_ID_USUARIO,
       CAD_PRO_ID_PROFISSIONAL
FROM TB_FAT_TCO_TIPO_COMANDA
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_FAT_TCO_TIPO_COMANDA_L;
