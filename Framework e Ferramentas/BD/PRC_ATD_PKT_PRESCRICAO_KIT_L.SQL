create or replace procedure PRC_ATD_PKT_PRESCRICAO_KIT_L
(
     pATD_PKT_ID IN TB_ATD_PKT_PRESCRICAO_KIT.ATD_PKT_ID%type DEFAULT NULL,
     pATD_PKT_KIT_STATUS IN TB_ATD_PKT_PRESCRICAO_KIT.ATD_PKT_KIT_STATUS%type DEFAULT NULL,
     pATD_PKT_DESCRICAO IN TB_ATD_PKT_PRESCRICAO_KIT.ATD_PKT_DESCRICAO%type DEFAULT NULL,
     pATD_PKT_ID_USU_ULT_ATUALIZ IN TB_ATD_PKT_PRESCRICAO_KIT.ATD_PKT_ID_USU_ULT_ATUALIZ%type DEFAULT NULL,
     pATD_PKT_DT_ULT_ATUALIZ IN TB_ATD_PKT_PRESCRICAO_KIT.ATD_PKT_DT_ULT_ATUALIZ%type DEFAULT NULL,
     pCAD_SET_ID IN TB_ATD_PKT_PRESCRICAO_KIT.CAD_SET_ID%type DEFAULT NULL,

     pATD_PKT_FL_INT_ALA IN TB_ATD_PKT_PRESCRICAO_KIT.ATD_PKT_FL_INT_ALA%type DEFAULT NULL,
     pATD_PKT_FL_INT_UTI IN TB_ATD_PKT_PRESCRICAO_KIT.ATD_PKT_FL_INT_UTI%type DEFAULT NULL,
     pATD_PKT_FL_INT_ADULTO IN TB_ATD_PKT_PRESCRICAO_KIT.ATD_PKT_FL_INT_ADULTO%type DEFAULT NULL,
     pATD_PKT_FL_INT_NEO IN TB_ATD_PKT_PRESCRICAO_KIT.ATD_PKT_FL_INT_NEO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
)
is
/********************************************************************
*    Procedure: PRC_ATD_PKT_PRESCRICAO_KIT_L
*
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pATD_PKT_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PKT_ID = ' || pATD_PKT_ID; END IF;
IF pATD_PKT_KIT_STATUS IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PKT_KIT_STATUS = ' || pATD_PKT_KIT_STATUS; END IF;
IF pATD_PKT_DESCRICAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PKT_DESCRICAO = ' || CHR(39) || pATD_PKT_DESCRICAO || CHR(39); END IF;
IF pATD_PKT_ID_USU_ULT_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PKT_ID_USU_ULT_ATUALIZ = ' || pATD_PKT_ID_USU_ULT_ATUALIZ; END IF;
IF pATD_PKT_DT_ULT_ATUALIZ IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PKT_DT_ULT_ATUALIZ = ' || CHR(39) || pATD_PKT_DT_ULT_ATUALIZ || CHR(39); END IF;
IF pCAD_SET_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_SET_ID = ' || pCAD_SET_ID; END IF;

IF pATD_PKT_FL_INT_ALA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PKT_FL_INT_ALA = ' || pATD_PKT_FL_INT_ALA; END IF;
IF pATD_PKT_FL_INT_UTI IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PKT_FL_INT_UTI = ' || pATD_PKT_FL_INT_UTI; END IF;
IF pATD_PKT_FL_INT_ADULTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PKT_FL_INT_ADULTO = ' || pATD_PKT_FL_INT_ADULTO; END IF;
IF pATD_PKT_FL_INT_NEO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATD_PKT_FL_INT_NEO = ' || pATD_PKT_FL_INT_NEO; END IF;

   V_SELECT := '
SELECT
       ATD_PKT_ID,
       ATD_PKT_KIT_STATUS,
       ATD_PKT_DESCRICAO,
       ATD_PKT_ID_USU_ULT_ATUALIZ,
       ATD_PKT_DT_ULT_ATUALIZ,
       CAD_SET_ID,

       ATD_PKT_FL_INT_ALA,
       ATD_PKT_FL_INT_UTI,
       ATD_PKT_FL_INT_ADULTO,
       ATD_PKT_FL_INT_NEO
FROM TB_ATD_PKT_PRESCRICAO_KIT
WHERE null is null  '    ;

OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ATD_PKT_PRESCRICAO_KIT_L;
