--PRC_ATS_PSA_PROC_SEGM_ASSOC_S
create or replace procedure PRC_ATS_PSA_PROC_SEGM_ASSOC_S 
(
     pCAD_PRD_ID IN TB_ATS_PSA_PROC_SEGM_ASSOC.CAD_PRD_ID%type DEFAULT NULL,
     pATS_PSA_QT_INI_SEG IN TB_ATS_PSA_PROC_SEGM_ASSOC.ATS_PSA_QT_INI_SEG%type DEFAULT NULL,
     pATS_PSA_QT_FIM_SEG IN TB_ATS_PSA_PROC_SEGM_ASSOC.ATS_PSA_QT_FIM_SEG%type DEFAULT NULL,
     pCAD_PRD_ID_PSA IN TB_ATS_PSA_PROC_SEGM_ASSOC.CAD_PRD_ID_PSA%type DEFAULT NULL,
     pATS_CPS_CD_SEGMENTO IN TB_ATS_PSA_PROC_SEGM_ASSOC.ATS_CPS_CD_SEGMENTO%type DEFAULT NULL,
     pATS_PSA_QT_VEZES_PROC IN TB_ATS_PSA_PROC_SEGM_ASSOC.ATS_PSA_QT_VEZES_PROC%type DEFAULT NULL,
     pATS_PSA_DT_ULTIMA_ATUALIZACAO IN TB_ATS_PSA_PROC_SEGM_ASSOC.ATS_PSA_DT_ULTIMA_ATUALIZACAO%type DEFAULT NULL,
     pCAD_SEG_ID_USUARIO IN TB_ATS_PSA_PROC_SEGM_ASSOC.CAD_SEG_ID_USUARIO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ATS_PSA_PROC_SEGM_ASSOC_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pCAD_PRD_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_ID = ' || pCAD_PRD_ID; END IF;
IF pATS_PSA_QT_INI_SEG IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_PSA_QT_INI_SEG = ' || pATS_PSA_QT_INI_SEG; END IF;
IF pATS_PSA_QT_FIM_SEG IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_PSA_QT_FIM_SEG = ' || pATS_PSA_QT_FIM_SEG; END IF;
IF pCAD_PRD_ID_PSA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PRD_ID_PSA = ' || pCAD_PRD_ID_PSA; END IF;
IF pATS_CPS_CD_SEGMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_CPS_CD_SEGMENTO = ' || CHR(39) || pATS_CPS_CD_SEGMENTO || CHR(39); END IF;
IF pATS_PSA_QT_VEZES_PROC IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_PSA_QT_VEZES_PROC = ' || pATS_PSA_QT_VEZES_PROC; END IF;
IF pATS_PSA_DT_ULTIMA_ATUALIZACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ATS_PSA_DT_ULTIMA_ATUALIZACAO = ' || CHR(39) || pATS_PSA_DT_ULTIMA_ATUALIZACAO || CHR(39); END IF;
IF pCAD_SEG_ID_USUARIO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_SEG_ID_USUARIO = ' || pCAD_SEG_ID_USUARIO; END IF;
 
   V_SELECT := '
SELECT	
       CAD_PRD_ID,
       ATS_PSA_QT_INI_SEG,
       ATS_PSA_QT_FIM_SEG,
       CAD_PRD_ID_PSA,
       ATS_CPS_CD_SEGMENTO,
       ATS_PSA_QT_VEZES_PROC,
       ATS_PSA_DT_ULTIMA_ATUALIZACAO,
       CAD_SEG_ID_USUARIO
FROM TB_ATS_PSA_PROC_SEGM_ASSOC
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ATS_PSA_PROC_SEGM_ASSOC_S;
