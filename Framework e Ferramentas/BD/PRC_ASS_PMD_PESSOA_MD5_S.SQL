--PRC_ASS_PMD_PESSOA_MD5_S
create or replace procedure PRC_ASS_PMD_PESSOA_MD5_S 
(
     pASS_PMD_ID IN TB_ASS_PMD_PESSOA_MD5.ASS_PMD_ID%type DEFAULT NULL,
     pASS_PMD_CD_MD5 IN TB_ASS_PMD_PESSOA_MD5.ASS_PMD_CD_MD5%type DEFAULT NULL,
     pCAD_PES_ID_PESSOA IN TB_ASS_PMD_PESSOA_MD5.CAD_PES_ID_PESSOA%type DEFAULT NULL,
     pASS_PMD_NM_PESSOA IN TB_ASS_PMD_PESSOA_MD5.ASS_PMD_NM_PESSOA%type DEFAULT NULL,
     pASS_PMD_DT_NASCIMENTO IN TB_ASS_PMD_PESSOA_MD5.ASS_PMD_DT_NASCIMENTO%type DEFAULT NULL,
     pASS_PMD_TP_SEXO IN TB_ASS_PMD_PESSOA_MD5.ASS_PMD_TP_SEXO%type DEFAULT NULL,
     pASS_PMD_DT_CRIACAO IN TB_ASS_PMD_PESSOA_MD5.ASS_PMD_DT_CRIACAO%type DEFAULT NULL,
     pASS_PMD_DT_DESATIVACAO IN TB_ASS_PMD_PESSOA_MD5.ASS_PMD_DT_DESATIVACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_CRIACAO IN TB_ASS_PMD_PESSOA_MD5.SEG_USU_ID_USUARIO_CRIACAO%type DEFAULT NULL,
     pSEG_USU_ID_USUARIO_DESATIV IN TB_ASS_PMD_PESSOA_MD5.SEG_USU_ID_USUARIO_DESATIVACAO%type DEFAULT NULL,
     io_cursor OUT PKG_CURSOR.t_cursor
) 
is
/********************************************************************
*    Procedure: PRC_ASS_PMD_PESSOA_MD5_S
* 
*    Data Criacao: 	data da  criação   Por: Nome do Analista
*    Data Alteracao:	data da alteração  Por: Nome do Analista
*
*    Funcao: Descrição da funcionalidade da Stored Procedure
*
*******************************************************************/
 v_cursor PKG_CURSOR.t_cursor;
  V_WHERE  varchar2(5000);
  V_SELECT  varchar2(5000);
begin
  V_WHERE := NULL;
  IF pASS_PMD_ID IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PMD_ID = ' || pASS_PMD_ID; END IF;
IF pASS_PMD_CD_MD5 IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PMD_CD_MD5 = ' || CHR(39) || pASS_PMD_CD_MD5 || CHR(39); END IF;
IF pCAD_PES_ID_PESSOA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND CAD_PES_ID_PESSOA = ' || pCAD_PES_ID_PESSOA; END IF;
IF pASS_PMD_NM_PESSOA IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PMD_NM_PESSOA = ' || CHR(39) || pASS_PMD_NM_PESSOA || CHR(39); END IF;
IF pASS_PMD_DT_NASCIMENTO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PMD_DT_NASCIMENTO = ' || CHR(39) || pASS_PMD_DT_NASCIMENTO || CHR(39); END IF;
IF pASS_PMD_TP_SEXO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PMD_TP_SEXO = ' || CHR(39) || pASS_PMD_TP_SEXO || CHR(39); END IF;
IF pASS_PMD_DT_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PMD_DT_CRIACAO = ' || CHR(39) || pASS_PMD_DT_CRIACAO || CHR(39); END IF;
IF pASS_PMD_DT_DESATIVACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND ASS_PMD_DT_DESATIVACAO = ' || CHR(39) || pASS_PMD_DT_DESATIVACAO || CHR(39); END IF;
IF pSEG_USU_ID_USUARIO_CRIACAO IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_CRIACAO = ' || pSEG_USU_ID_USUARIO_CRIACAO; END IF;
IF pSEG_USU_ID_USUARIO_DESATIV IS NOT NULL THEN V_WHERE:= V_WHERE || ' AND SEG_USU_ID_USUARIO_DESATIVACAO = ' || pSEG_USU_ID_USUARIO_DESATIV; END IF;
 
   V_SELECT := '
SELECT	
       ASS_PMD_ID,
       ASS_PMD_CD_MD5,
       CAD_PES_ID_PESSOA,
       ASS_PMD_NM_PESSOA,
       ASS_PMD_DT_NASCIMENTO,
       ASS_PMD_TP_SEXO,
       ASS_PMD_DT_CRIACAO,
       ASS_PMD_DT_DESATIVACAO,
       SEG_USU_ID_USUARIO_CRIACAO,
       SEG_USU_ID_USUARIO_DESATIVACAO
FROM TB_ASS_PMD_PESSOA_MD5
WHERE null is null  '    ;
       
OPEN v_cursor FOR
  V_SELECT || V_WHERE ;
  io_cursor := v_cursor;
end PRC_ASS_PMD_PESSOA_MD5_S;
